// ─── отображение типа Konda в строку (для сигнатур/hover) ────────────────────

static void тип_в_строку(ТипТокена тип, int беззнаковый, int указатели,
                         int срез, int массив, const Токен *имя_типа,
                         char *out, size_t cap)
{
    char базовый[96];
    switch (тип) {
        case ТОКЕН_ЦЕЛОЕ8:  snprintf(базовый, sizeof(базовый), "%sцелое8",  беззнаковый ? "неподписанный " : ""); break;
        case ТОКЕН_ЦЕЛОЕ16: snprintf(базовый, sizeof(базовый), "%sцелое16", беззнаковый ? "неподписанный " : ""); break;
        case ТОКЕН_ЦЕЛОЕ32: snprintf(базовый, sizeof(базовый), "%sцелое32", беззнаковый ? "неподписанный " : ""); break;
        case ТОКЕН_ЦЕЛОЕ64: snprintf(базовый, sizeof(базовый), "%sцелое64", беззнаковый ? "неподписанный " : ""); break;
        case ТОКЕН_БАЙТ:         snprintf(базовый, sizeof(базовый), "байт"); break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ: snprintf(базовый, sizeof(базовый), "вещественное"); break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ64: snprintf(базовый, sizeof(базовый), "вещественное64"); break;
        case ТОКЕН_ТИП_СИМВОЛ:   snprintf(базовый, sizeof(базовый), "символ"); break;
        case ТОКЕН_ЛОГИЧЕСКИЙ:   snprintf(базовый, sizeof(базовый), "логический"); break;
        case ТОКЕН_НИЧЕГО:       snprintf(базовый, sizeof(базовый), "ничего"); break;
        case ТОКЕН_СТРУКТУРА:
        case ТОКЕН_ПЕРЕЧИСЛЕНИЕ:
            if (имя_типа && имя_типа->длина > 0)
                snprintf(базовый, sizeof(базовый), "%.*s",
                         (int)имя_типа->длина, имя_типа->начало);
            else
                snprintf(базовый, sizeof(базовый), "тип");
            break;
        default: snprintf(базовый, sizeof(базовый), "нечто"); break;
    }
    char звёзды[9] = {0};
    for (int i = 0; i < указатели && i < 8; ++i) звёзды[i] = '*';
    if (срез)
        snprintf(out, cap, "срез<%s>", базовый);
    else
        snprintf(out, cap, "%s%s%s", базовый, звёзды, массив ? "[]" : "");
}

// Строит человекочитаемую сигнатуру функции по её узлу.
static void сигнатура_функции(const Узел *ф, char *out, size_t cap)
{
    char возврат[128];
    тип_в_строку(ф->тип_данных, ф->беззнаковый, 0, ф->срез, 0,
                 &ф->имя_типа, возврат, sizeof(возврат));
    size_t всего = узел_число_детей(ф);
    size_t тело = всего > 0 ? всего - 1 : 0;  // последний ребёнок — тело

    char параметры[512] = {0};
    size_t поз = 0;
    int первый = 1;
    for (size_t i = 0; i < тело; ++i) {
        const Узел *п = узел_ребёнок(ф, i);
        if (!п || п->тип != УЗЕЛ_ПАРАМЕТР) continue;
        char т[128];
        тип_в_строку(п->тип_данных, п->беззнаковый, п->указатели, п->срез,
                     п->массив, &п->имя_типа, т, sizeof(т));
        int напис = snprintf(параметры + поз, sizeof(параметры) - поз,
                             "%s%s%s", первый ? "" : ", ",
                             п->владеет ? "владеет " : "", т);
        if (напис > 0) поз += (size_t)напис;
        if (поз >= sizeof(параметры)) { поз = sizeof(параметры) - 1; break; }
        первый = 0;
    }

    const char *имя = ф->это_main ? "точка_входа" : nullptr;
    if (имя)
        snprintf(out, cap, "%s %s(%s)", возврат, имя, параметры);
    else
        snprintf(out, cap, "%s %.*s(%s)", возврат,
                 (int)ф->токен.длина, ф->токен.начало, параметры);
}

// ─── сбор функций и типов из AST ─────────────────────────────────────────────

static void добавить_функцию_ide(КдРезультат *р, const Узел *ф)
{
    КдФункция *нов = realloc(р->функции, (р->число_функций + 1) * sizeof(КдФункция));
    if (!нов) return;
    р->функции = нов;
    КдФункция *к = &р->функции[р->число_функций++];
    memset(к, 0, sizeof(*к));
    const char *имя = ф->это_main ? "точка_входа" : ф->токен.начало;
    size_t дл = ф->это_main ? strlen("точка_входа") : ф->токен.длина;
    if (дл >= sizeof(к->имя)) дл = sizeof(к->имя) - 1;
    memcpy(к->имя, имя, дл);
    к->имя[дл] = '\0';
    сигнатура_функции(ф, к->сигнатура, sizeof(к->сигнатура));
    к->вид = КД_ФУНКЦИЯ_KONDA;
    к->число_параметров = -1;   // точная арность для Konda-функций не требуется
    к->вариативная = 0;
    к->строка = ф->токен.строка;
    к->колонка = ф->токен.позиция_в_строке;
}

static void добавить_внешние_ide(КдРезультат *р, const ТаблицаВнешних *т)
{
    size_t n = внешние_число(т);
    for (size_t i = 0; i < n; ++i) {
        const ВнешняяФункция *вф = внешние_получить(т, i);
        КдФункция *нов = realloc(р->функции, (р->число_функций + 1) * sizeof(КдФункция));
        if (!нов) return;
        р->функции = нов;
        КдФункция *к = &р->функции[р->число_функций++];
        memset(к, 0, sizeof(*к));
        snprintf(к->имя, sizeof(к->имя), "%s", вф->имя);
        if (вф->число_параметров < 0)
            snprintf(к->сигнатура, sizeof(к->сигнатура), "%s(…) [C]", вф->имя);
        else if (вф->переменное_число)
            snprintf(к->сигнатура, sizeof(к->сигнатура), "%s(%d+, …) [C]",
                     вф->имя, вф->число_параметров);
        else
            snprintf(к->сигнатура, sizeof(к->сигнатура), "%s(%d параметр(ов)) [C]",
                     вф->имя, вф->число_параметров);
        к->вид = КД_ФУНКЦИЯ_ВНЕШНЯЯ;
        к->число_параметров = вф->число_параметров;
        к->вариативная = вф->переменное_число;
        к->строка = 0;   // позиция определения во внешнем заголовке не отслеживается
        к->колонка = 0;
    }
}

static void добавить_тип_ide(КдРезультат *р, const Узел *узел, int вид)
{
    КдТип *нов = realloc(р->типы, (р->число_типов + 1) * sizeof(КдТип));
    if (!нов) return;
    р->типы = нов;
    КдТип *к = &р->типы[р->число_типов++];
    memset(к, 0, sizeof(*к));
    size_t дл = узел->токен.длина < sizeof(к->имя) - 1 ? узел->токен.длина : sizeof(к->имя) - 1;
    memcpy(к->имя, узел->токен.начало, дл);
    к->имя[дл] = '\0';
    к->вид = вид;
    к->строка = узел->токен.строка;
    к->колонка = узел->токен.позиция_в_строке;
}

static void добавить_диагностику(КдРезультат *р, int уровень,
                                 int строка, int колонка, const char *текст)
{
    КдДиагностика *нов = realloc(р->диагностики,
        (р->число_диагностик + 1) * sizeof(КдДиагностика));
    if (!нов) return;
    р->диагностики = нов;
    КдДиагностика *д = &р->диагностики[р->число_диагностик++];
    memset(д, 0, sizeof(*д));
    д->уровень = уровень;
    д->строка = строка;
    д->колонка = колонка;
    snprintf(д->сообщение, sizeof(д->сообщение), "%s", текст);
}

// ─── публичный вход ──────────────────────────────────────────────────────────

КдРезультат *konda_анализ(const char *источник, size_t длина, const char *база_путей)
{
    КдРезультат *р = calloc(1, sizeof(КдРезультат));
    if (!р) return nullptr;

    // Токены и AST держат указатели в исходный буфер — копируем его во владение
    // и держим до конца анализа.
    char *буфер = malloc(длина + 1);
    if (!буфер) return р;
    memcpy(буфер, источник, длина);
    буфер[длина] = '\0';

    МассивТокенов токены = токенизировать_код(длина, буфер, 0);

    Диагностика диаг = {0};
    Узел *корень = разобрать(&токены, &диаг);
    if (!корень) {
        добавить_диагностику(р, КД_ОШИБКА, диаг.строка, диаг.колонка, диаг.сообщение);
        массив_токенов_освободить(&токены);
        free(буфер);
        return р;
    }

    // Внешние функции из подключённых заголовков.
    ТаблицаВнешних *внешние = внешние_создать();
    if (внешние) собрать_внешние_функции(корень, база_путей, внешние);

    // Семантика: батч-режим — собираем ВСЕ ошибки (для подсветки всех сразу) и
    // предупреждения. Внешние вызовы проверяются по таблице заголовков.
    ДинМассив предупреждения, ошибки;
    дин_массив_инициализировать(&предупреждения, sizeof(Диагностика));
    дин_массив_инициализировать(&ошибки, sizeof(Диагностика));
    Диагностика сдиаг = {0};
    анализировать_полный(корень, &сдиаг, внешние, &предупреждения, &ошибки);

    // Все семантические ошибки — в диагностики.
    for (size_t i = 0; i < ошибки.длина; ++i) {
        Диагностика *д = дин_массив_получить(&ошибки, i);
        добавить_диагностику(р, КД_ОШИБКА, д->строка, д->колонка, д->сообщение);
    }
    // Предупреждения.
    for (size_t i = 0; i < предупреждения.длина; ++i) {
        Диагностика *д = дин_массив_получить(&предупреждения, i);
        добавить_диагностику(р, КД_ПРЕДУПРЕЖДЕНИЕ, д->строка, д->колонка, д->сообщение);
    }
    дин_массив_освободить(&предупреждения);
    дин_массив_освободить(&ошибки);

    // Символы: функции (Konda) и типы.
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        const Узел *узел = узел_ребёнок(корень, i);
        if (!узел) continue;
        if (узел->тип == УЗЕЛ_ФУНКЦИЯ) добавить_функцию_ide(р, узел);
        else if (узел->тип == УЗЕЛ_СТРУКТУРА) добавить_тип_ide(р, узел, КД_ТИП_СТРУКТУРА);
        else if (узел->тип == УЗЕЛ_ПЕРЕЧИСЛЕНИЕ) добавить_тип_ide(р, узел, КД_ТИП_ПЕРЕЧИСЛЕНИЕ);
    }
    // Внешние функции (для автодополнения C-вызовов).
    if (внешние) добавить_внешние_ide(р, внешние);

    внешние_освободить(внешние);
    освободить_узел(корень);
    массив_токенов_освободить(&токены);
    free(буфер);
    return р;
}

void konda_освободить_результат(КдРезультат *результат)
{
    if (!результат) return;
    free(результат->диагностики);
    free(результат->функции);
    free(результат->типы);
    free(результат);
}
