#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "аст.h"

#define МАКС_ИМЯ 128
#define МАКС_СВЯЗЕЙ_МАССИВОВ 32
#define МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ 64

typedef struct {
    char массив[МАКС_ИМЯ];
    char длина[МАКС_ИМЯ];
} СвязьМассива;

typedef struct {
    char переменная[МАКС_ИМЯ];
    long минимальная_длина;
    int известна;
} ОграничениеДлины;

typedef struct {
    Диагностика *диаг;
    СвязьМассива связи[МАКС_СВЯЗЕЙ_МАССИВОВ];
    size_t число_связей;
    ОграничениеДлины ограничения[МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ];
    size_t глубина_ограничений;
} Анализатор;

static int байты_равны(const char *a, size_t la, const char *b, size_t lb)
{
    return la == lb && memcmp(a, b, la) == 0;
}

static int токен_текст_равен(const Токен *t, const char *текст)
{
    return байты_равны(t->начало, t->длина, текст, strlen(текст));
}

static void ошибка_семантики(Анализатор *а, const Токен *t, const char *текст)
{
    if (а->диаг->ошибка) {
        return;
    }
    а->диаг->ошибка = 1;
    а->диаг->строка = t ? t->строка : 0;
    а->диаг->колонка = t ? t->позиция_в_строке : 0;
    snprintf(а->диаг->сообщение, sizeof(а->диаг->сообщение), "%s", текст);
}

static void связь_добавить(Анализатор *а, const char *массив, size_t lm, const char *длина, size_t ld)
{
    if (а->число_связей >= МАКС_СВЯЗЕЙ_МАССИВОВ) {
        return;
    }
    СвязьМассива *s = &а->связи[а->число_связей++];
    size_t n = lm < МАКС_ИМЯ - 1 ? lm : МАКС_ИМЯ - 1;
    memcpy(s->массив, массив, n);
    s->массив[n] = '\0';
    n = ld < МАКС_ИМЯ - 1 ? ld : МАКС_ИМЯ - 1;
    memcpy(s->длина, длина, n);
    s->длина[n] = '\0';
}

static void ограничения_сохранить(Анализатор *а)
{
    if (а->глубина_ограничений >= МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ) {
        return;
    }
    а->ограничения[а->глубина_ограничений++] = (ОграничениеДлины){0};
}

static void ограничения_восстановить(Анализатор *а)
{
    if (а->глубина_ограничений > 0) {
        а->глубина_ограничений--;
    }
}

static void ограничение_установить(Анализатор *а, const char *переменная, size_t длина_имени, long минимальная_длина)
{
    if (а->глубина_ограничений == 0) {
        return;
    }
    ОграничениеДлины *o = &а->ограничения[а->глубина_ограничений - 1];
    size_t n = длина_имени < МАКС_ИМЯ - 1 ? длина_имени : МАКС_ИМЯ - 1;
    memcpy(o->переменная, переменная, n);
    o->переменная[n] = '\0';
    o->минимальная_длина = минимальная_длина;
    o->известна = 1;
}

static long минимальная_длина_для(const Анализатор *а, const char *имя, size_t длина_имени)
{
    for (size_t i = а->глубина_ограничений; i > 0; --i) {
        const ОграничениеДлины *o = &а->ограничения[i - 1];
        if (o->известна && байты_равны(o->переменная, strlen(o->переменная), имя, длина_имени)) {
            return o->минимальная_длина;
        }
    }
    return 0;
}

static int известна_длина_массива(const Анализатор *а, const char *имя_массива, size_t длина_имени, long *минимальная_длина)
{
    const char *имя_длины = nullptr;
    for (size_t i = 0; i < а->число_связей; ++i) {
        if (байты_равны(а->связи[i].массив, strlen(а->связи[i].массив), имя_массива, длина_имени)) {
            имя_длины = а->связи[i].длина;
            break;
        }
    }
    if (!имя_длины) {
        return 0;
    }
    long m = минимальная_длина_для(а, имя_длины, strlen(имя_длины));
    if (m <= 0) {
        return 0;
    }
    *минимальная_длина = m;
    return 1;
}

static void проверить_доступ_к_массиву(Анализатор *а, const Токен *t, const char *имя, size_t длина_имени, long индекс)
{
    long минимум = 0;
    if (!известна_длина_массива(а, имя, длина_имени, &минимум)) {
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "доступ к массиву «%.*s»: длина неизвестна на этапе компиляции",
                 (int)длина_имени, имя);
        ошибка_семантики(а, t, msg);
        return;
    }
    if (индекс < 0 || индекс >= минимум) {
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "индекс %ld вне границ: при текущих условиях длина не меньше %ld",
                 индекс, минимум);
        ошибка_семантики(а, t, msg);
    }
}

static void анализ_выражения(Анализатор *а, Узел *узел)
{
    if (!узел || а->диаг->ошибка) {
        return;
    }
    switch (узел->тип) {
        case УЗЕЛ_ИНДЕКС: {
            Узел *индекс = узел_ребёнок(узел, 0);
            if (!индекс || индекс->тип != УЗЕЛ_ЧИСЛО) {
                ошибка_семантики(а, &узел->токен,
                    "индекс массива должен быть числовым литералом для проверки границ");
                return;
            }
            long idx = strtol(индекс->токен.начало, nullptr, 10);
            проверить_доступ_к_массиву(а, &индекс->токен, узел->токен.начало, узел->токен.длина, idx);
            break;
        }
        case УЗЕЛ_БИНОП:
        case УЗЕЛ_ВЫЗОВ:
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                анализ_выражения(а, узел_ребёнок(узел, i));
            }
            break;
        default:
            break;
    }
}

// Если условие имеет вид «идентификатор > N» или «идентификатор >= N»,
// задаёт минимальную длину для переменной в текущей ветке.
static void установить_ограничение_из_условия(Анализатор *а, Узел *условие)
{
    if (условие->тип != УЗЕЛ_БИНОП || условие->токен.тип != ТОКЕН_СТРЕЛКА_ВПРАВО) {
        return;
    }
    Узел *лево = узел_ребёнок(условие, 0);
    Узел *право = узел_ребёнок(условие, 1);
    if (!лево || !право || лево->тип != УЗЕЛ_ИДЕНТИФИКАТОР || право->тип != УЗЕЛ_ЧИСЛО) {
        return;
    }
    long порог = strtol(право->токен.начало, nullptr, 10);
    long минимум = токен_текст_равен(&условие->токен, ">=") ? порог : порог + 1;
    ограничение_установить(а, лево->токен.начало, лево->токен.длина, минимум);
}

static void анализ_блока(Анализатор *а, Узел *блок);

static void анализ_если(Анализатор *а, Узел *узел)
{
    Узел *условие = узел_ребёнок(узел, 0);
    Узел *тогда = узел_ребёнок(узел, 1);
    Узел *иначе = узел_ребёнок(узел, 2);

    ограничения_сохранить(а);
    установить_ограничение_из_условия(а, условие);
    анализ_выражения(а, условие);
    анализ_блока(а, тогда);
    ограничения_восстановить(а);

    if (иначе) {
        ограничения_сохранить(а);
        анализ_блока(а, иначе);
        ограничения_восстановить(а);
    }
}

static void анализ_оператора(Анализатор *а, Узел *узел)
{
    if (!узел || а->диаг->ошибка) {
        return;
    }
    switch (узел->тип) {
        case УЗЕЛ_ЕСЛИ:
            анализ_если(а, узел);
            break;
        case УЗЕЛ_ОБЪЯВЛЕНИЕ:
            if (узел_число_детей(узел) > 0) {
                анализ_выражения(а, узел_ребёнок(узел, 0));
            }
            break;
        case УЗЕЛ_ВЫЗОВ:
            анализ_выражения(а, узел);
            break;
        case УЗЕЛ_БЛОК:
            анализ_блока(а, узел);
            break;
        default:
            break;
    }
}

static void анализ_блока(Анализатор *а, Узел *блок)
{
    if (!блок) {
        return;
    }
    for (size_t i = 0; i < узел_число_детей(блок) && !а->диаг->ошибка; ++i) {
        анализ_оператора(а, узел_ребёнок(блок, i));
    }
}

static void анализ_функции(Анализатор *а, Узел *функция)
{
    if (функция->это_main) {
        связь_добавить(а, "аргументы", strlen("аргументы"),
                       "количество_аргументов", strlen("количество_аргументов"));
    }
    // Тело — последний ребёнок функции.
    size_t n = узел_число_детей(функция);
    if (n > 0) {
        Узел *тело = узел_ребёнок(функция, n - 1);
        if (тело && тело->тип == УЗЕЛ_БЛОК) {
            анализ_блока(а, тело);
        }
    }
}

int анализировать(Узел *корень, Диагностика *диаг)
{
    Анализатор а = { .диаг = диаг };
    for (size_t i = 0; i < узел_число_детей(корень) && !диаг->ошибка; ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ФУНКЦИЯ) {
            анализ_функции(&а, узел);
        }
    }
    return !диаг->ошибка;
}
