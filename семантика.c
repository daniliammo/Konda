static int тип_арифметический(ИнфоТипа т)
{
    if (т.срез || т.указатели > 0) return 0;
    switch (т.тип) {
        case ТОКЕН_ЦЕЛОЕ8: case ТОКЕН_ЦЕЛОЕ16:
        case ТОКЕН_ЦЕЛОЕ32: case ТОКЕН_ЦЕЛОЕ64:
        case ТОКЕН_БАЙТ: case ТОКЕН_ВЕЩЕСТВЕННОЕ: case ТОКЕН_ВЕЩЕСТВЕННОЕ64:
        case ТОКЕН_ЛОГИЧЕСКИЙ:    // bool в C — целочисленный тип; арифметика/условия
        case ТОКЕН_ПЕРЕЧИСЛЕНИЕ:  // enum в C — целое; в выражениях ведёт себя как int
            return 1;
        default: return 0;
    }
}

static int тип_целочисленный(ИнфоТипа т)
{
    if (т.срез || т.указатели > 0) return 0;
    switch (т.тип) {
        case ТОКЕН_ЦЕЛОЕ8: case ТОКЕН_ЦЕЛОЕ16:
        case ТОКЕН_ЦЕЛОЕ32: case ТОКЕН_ЦЕЛОЕ64:
        case ТОКЕН_БАЙТ: case ТОКЕН_ПЕРЕЧИСЛЕНИЕ: return 1;
        default: return 0;
    }
}

static int тип_указатель(ИнфоТипа т)
{
    if (т.срез) return 0;
    return т.указатели > 0 || т.тип == ТОКЕН_НУЛ_УКАЗАТЕЛЬ;
}

static int тип_скалярный(ИнфоТипа т)
{
    return тип_арифметический(т) || тип_указатель(т);
}

// Человекочитаемое описание типа для сообщений об ошибках.
static void описание_типа(ИнфоТипа т, char *out, size_t cap)
{
    const char *база;
    switch (т.тип) {
        case ТОКЕН_ЦЕЛОЕ8:       база = т.беззнаковый ? "неподписанный целое8"  : "целое8";  break;
        case ТОКЕН_ЦЕЛОЕ16:      база = т.беззнаковый ? "неподписанный целое16" : "целое16"; break;
        case ТОКЕН_ЦЕЛОЕ32:      база = т.беззнаковый ? "неподписанный целое32" : "целое32"; break;
        case ТОКЕН_ЦЕЛОЕ64:      база = т.беззнаковый ? "неподписанный целое64" : "целое64"; break;
        case ТОКЕН_БАЙТ:         база = "байт";         break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ: база = "вещественное"; break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ64: база = "вещественное64"; break;
        case ТОКЕН_ТИП_СИМВОЛ:  база = "символ";        break;
        case ТОКЕН_ЛОГИЧЕСКИЙ:  база = "логический";    break;
        case ТОКЕН_НИЧЕГО:      база = "ничего";        break;
        case ТОКЕН_СТРУКТУРА:    база = "структура";    break;
        case ТОКЕН_ПЕРЕЧИСЛЕНИЕ: база = "перечисление"; break;
        case ТОКЕН_НУЛ_УКАЗАТЕЛЬ: snprintf(out, cap, "нуль"); return;
        case ТОКЕН_НЕИЗВЕСТНЫЙ:  snprintf(out, cap, "<неизвестный>"); return;
        default:                 база = "?"; break;
    }
    if (т.срез) {
        snprintf(out, cap, "срез<%s>", база);
    } else if (т.указатели == 0) {
        snprintf(out, cap, "%s", база);
    } else {
        char звёзды[16] = {0};
        int n = т.указатели < 8 ? т.указатели : 8;
        for (int i = 0; i < n; ++i) звёзды[i] = '*';
        snprintf(out, cap, "%s%s", база, звёзды);
    }
}

// Совместимость типов при присваивании: цель ← источник.
// Правила как в C (без явного приведения):
//   - оба арифметические → OK (неявные числовые преобразования)
//   - цель = указатель, источник = нуль → OK
//   - оба указатели одного базового типа и уровня → OK
//   - указатель ← арифметика → ОШИБКА
//   - арифметика ← указатель → ОШИБКА
//   - указатели разных типов → ОШИБКА
static int совместимы_для_присваивания(ИнфоТипа цель, ИнфоТипа источник)
{
    // Срезы: цель-срез принимает только срез. Источник с неизвестным типом
    // элемента (результат «выделить») совместим с любым срезом.
    if (цель.срез || источник.срез) {
        if (!цель.срез || !источник.срез)
            return 0;  // срез ↔ не-срез несовместимы
        if (источник.тип == ТОКЕН_НЕИЗВЕСТНЫЙ || цель.тип == ТОКЕН_НЕИЗВЕСТНЫЙ)
            return 1;  // выделить(...) либо неизвестный элемент
        return цель.тип == источник.тип && цель.беззнаковый == источник.беззнаковый;
    }

    // Структуры: совместимы только одинаковые структуры той же косвенности.
    if (цель.тип == ТОКЕН_СТРУКТУРА || источник.тип == ТОКЕН_СТРУКТУРА) {
        return цель.тип == ТОКЕН_СТРУКТУРА && источник.тип == ТОКЕН_СТРУКТУРА
            && цель.индекс_структуры == источник.индекс_структуры
            && цель.указатели == источник.указатели;
    }

    // Перечисления: enum ← enum того же типа (или его вариант) — OK; enum → целое
    // (расширение, как в C) — OK; целое → enum запрещено (нужен явный вариант),
    // как и смешение разных перечислений.
    if (цель.тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ || источник.тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) {
        if (цель.тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ && источник.тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ)
            return цель.индекс_структуры == источник.индекс_структуры;
        if (источник.тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ && тип_целочисленный(цель))
            return 1;  // enum → целое
        return 0;       // целое → enum и прочее — запрещено
    }

    if (цель.тип == ТОКЕН_НЕИЗВЕСТНЫЙ || источник.тип == ТОКЕН_НЕИЗВЕСТНЫЙ)
        return 1;  // неизвестный тип — не проверяем

    int ц_ук = тип_указатель(цель);
    int и_ук = тип_указатель(источник);

    // nullptr → любой указатель
    if (ц_ук && источник.тип == ТОКЕН_НУЛ_УКАЗАТЕЛЬ)
        return 1;

    // оба арифметические
    if (!ц_ук && !и_ук)
        return 1;

    // указатель ← арифметика  или  арифметика ← указатель
    if (ц_ук != и_ук)
        return 0;

    // оба указатели: проверяем базовый тип и уровень косвенности
    return цель.тип == источник.тип && цель.указатели == источник.указатели;
}

// Размер типа в байтах (для статической проверки переполнения «выделить»).
// Указатель — 8 байт (LP64). 0 — неизвестно/непроверяемо.
static long размер_типа(ИнфоТипа т)
{
    if (т.срез) return 0;
    if (т.указатели > 0) return 8;
    switch (т.тип) {
        case ТОКЕН_ЦЕЛОЕ8: case ТОКЕН_БАЙТ: case ТОКЕН_ТИП_СИМВОЛ:
        case ТОКЕН_ЛОГИЧЕСКИЙ: return 1;
        case ТОКЕН_ЦЕЛОЕ16: return 2;
        case ТОКЕН_ЦЕЛОЕ32: case ТОКЕН_ВЕЩЕСТВЕННОЕ:
        case ТОКЕН_ПЕРЕЧИСЛЕНИЕ: return 4;
        case ТОКЕН_ЦЕЛОЕ64: case ТОКЕН_ВЕЩЕСТВЕННОЕ64: return 8;
        default: return 0;
    }
}

// Разрядность целого типа в битах (для проверки сдвигов). 0 — неизвестно.
static int ширина_бит(ИнфоТипа т)
{
    if (т.срез || т.указатели > 0) return 0;
    switch (т.тип) {
        case ТОКЕН_ЦЕЛОЕ8: case ТОКЕН_БАЙТ: case ТОКЕН_ТИП_СИМВОЛ: return 8;
        case ТОКЕН_ЦЕЛОЕ16: return 16;
        case ТОКЕН_ЦЕЛОЕ32: case ТОКЕН_ПЕРЕЧИСЛЕНИЕ: return 32;
        case ТОКЕН_ЦЕЛОЕ64: return 64;
        default: return 0;
    }
}

static int вычислить_константу(Анализатор *а, Узел *узел, long *из);

// Неявное сужение целого: цель уже источника по разрядности, и источник не
// сворачивается в константу (для констант диапазон уже проверяется отдельно).
// Такое присваивание тихо теряет данные в C — требуем явного «как<тип>(…)».
// Перечисления исключены (свои правила enum↔целое).
static int сужение_запрещено(Анализатор *а, ИнфоТипа цель, Узел *инит, ИнфоТипа ист)
{
    if (!инит) return 0;
    if (цель.тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ || ист.тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) return 0;
    if (!тип_целочисленный(цель) || !тип_целочисленный(ист)) return 0;
    int шц = ширина_бит(цель), ши = ширина_бит(ист);
    if (шц == 0 || ши == 0 || шц >= ши) return 0;  // расширение/равно — допустимо
    long _к;
    if (вычислить_константу(а, инит, &_к)) return 0;  // константа — проверена диапазоном
    return 1;
}

// Тип результата смешанной арифметики двух чисел — «старший» операнд, как в C
// (usual arithmetic conversions, упрощённо). Вещественное доминирует над целым;
// при равной разрядности беззнаковый доминирует над знаковым (правило C).
// Раньше возвращался левый операнд, из-за чего «целое32 в = целое8 + целое64»
// типизировался как целое8 → присваивание целое64-суммы в целое32 молча
// усекалось. Теперь результат шире → такое присваивание ловится как сужение.
static ИнфоТипа объединить_арифметику(ИнфоТипа л, ИнфоТипа п)
{
    // Вещественное доминирует над целым; двойная точность — над одинарной.
    if (л.тип == ТОКЕН_ВЕЩЕСТВЕННОЕ64 || п.тип == ТОКЕН_ВЕЩЕСТВЕННОЕ64) {
        ИнфоТипа р = { ТОКЕН_ВЕЩЕСТВЕННОЕ64, 0, 0, 0, -1 };
        return р;
    }
    if (л.тип == ТОКЕН_ВЕЩЕСТВЕННОЕ || п.тип == ТОКЕН_ВЕЩЕСТВЕННОЕ) {
        ИнфоТипа р = { ТОКЕН_ВЕЩЕСТВЕННОЕ, 0, 0, 0, -1 };
        return р;
    }
    int шл = ширина_бит(л), шп = ширина_бит(п);
    if (шл == 0) шл = 32;  // перечисление/неизвестное — ранг целого32
    if (шп == 0) шп = 32;
    if (шл > шп) return л;
    if (шп > шл) return п;
    return л.беззнаковый ? л : п;  // равная разрядность: беззнаковый доминирует
}

// Совместимость операндов бинарного оператора. Возвращает 1 и кладёт
// тип результата в *рез при успехе; 0 при несовместимости.
static int тип_бинопа(ИнфоТипа л, ИнфоТипа п,
                       const Токен *оп, ИнфоТипа *рез)
{
    int л_ук = тип_указатель(л);
    int п_ук = тип_указатель(п);
    int л_ар = тип_арифметический(л);
    int п_ар = тип_арифметический(п);

    int сложение = токен_текст_равен(оп, "+");
    int вычит    = токен_текст_равен(оп, "-");
    int сравнение = токен_текст_равен(оп, ">")  || токен_текст_равен(оп, "<")
                 || токен_текст_равен(оп, ">=") || токен_текст_равен(оп, "==")
                 || токен_текст_равен(оп, "<=") || токен_текст_равен(оп, "!=");
    int логическое = токен_текст_равен(оп, "&&") || токен_текст_равен(оп, "||");
    int побитовое = токен_текст_равен(оп, "&") || токен_текст_равен(оп, "|")
                 || токен_текст_равен(оп, "^");
    int сдвиг     = токен_текст_равен(оп, "<<") || токен_текст_равен(оп, ">>");

    // Логическое «и»/«или» (&&/||): операнды — любые истинностные (арифметика
    // или указатель), результат — целое32 (0/1), как в C.
    if (логическое) {
        if (тип_скалярный(л) && тип_скалярный(п)) {
            *рез = ТИП_ЦЕЛОЕ32_V;
            return 1;
        }
        return 0;  // срез/структура в логическом операторе — ошибка
    }

    // Побитовые «& | ^» и сдвиги «<< >>»: только целочисленные операнды (не
    // float/указатель/срез — как в C). Нулевая рантайм-цена. Результат сдвига —
    // тип левого операнда; побитового — «старший» из двух (обычные преобразования).
    if (побитовое || сдвиг) {
        if (тип_целочисленный(л) && тип_целочисленный(п)) {
            *рез = сдвиг ? л : объединить_арифметику(л, п);
            return 1;
        }
        return 0;  // побитовые/сдвиг над нецелым — ошибка
    }

    // Сравнение: допускаем арифметику и указатели одного типа, результат int.
    if (сравнение) {
        if ((л_ук && п_ук) &&
            (л.тип != п.тип || л.указатели != п.указатели) &&
            п.тип != ТОКЕН_НУЛ_УКАЗАТЕЛЬ && л.тип != ТОКЕН_НУЛ_УКАЗАТЕЛЬ) {
            return 0;  // сравнение указателей разных типов
        }
        *рез = ТИП_ЦЕЛОЕ32_V;
        return 1;
    }

    // Оба арифметические — стандартные числовые преобразования (как в C):
    // результат — «старший» (более широкий) операнд.
    if (л_ар && п_ар) {
        *рез = объединить_арифметику(л, п);
        return 1;
    }

    // Арифметика указателей: ptr ± integer → ptr.
    if ((сложение || вычит) && л_ук && п_ар) {
        *рез = л;
        return 1;
    }
    if (сложение && л_ар && п_ук) {
        *рез = п;
        return 1;
    }

    // ptr - ptr → ptrdiff_t (целое32).
    if (вычит && л_ук && п_ук) {
        if (л.тип != п.тип || л.указатели != п.указатели)
            return 0;  // разные типы указателей
        *рез = ТИП_ЦЕЛОЕ32_V;
        return 1;
    }

    return 0;  // всё остальное: ptr OP ptr, ptr * int, и т.д.
}

