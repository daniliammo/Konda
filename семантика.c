// Сигнатура функции совпадает с типом-указателем (возврат + параметры)?
static int сигнатура_совпадает_с_типфункцией(const СигнатураФункции *с,
                                             const ОписаниеТипфункции *т)
{
    if (с->тип_возврата != т->тип_возврата) return 0;
    if (с->указатели_возврата != т->указатели_возврата) return 0;
    if (с->беззнаковый != т->беззнаковый_возврата) return 0;
    if (с->число_параметров != т->число_параметров) return 0;
    for (size_t i = 0; i < с->число_параметров; ++i) {
        if (с->параметры[i].тип != т->параметры[i].тип) return 0;
        if (с->параметры[i].указатели != т->параметры[i].указатели) return 0;
        if (с->параметры[i].срез != т->параметры[i].срез) return 0;
        if (с->параметры[i].беззнаковый != т->параметры[i].беззнаковый) return 0;
    }
    return 1;
}

