// Структуре по индексу нужна деструкция? Да, если у неё есть собственный
// деструктор ИЛИ хотя бы одно поле-структура (по значению) транзитивно требует
// деструкции. Поле-структура по значению может ссылаться только на уже
// объявленную структуру (найти_индекс_структуры ищет среди зарегистрированных
// ранее), поэтому цепочка полей — DAG по порядку объявления и не может
// зациклиться: настоящий предел глубины — число различных структур в
// программе. Раньше здесь стоял фиксированный «глубина > 16» — для более
// глубокой (но валидной) вложенности деструктор молча пропускался в кодогене
// без единой диагностики (тихая утечка ресурса). Бонус: раз цикл структурно
// невозможен, при превышении границы (не должно происходить) — не «нет
// деструкции», а явная ошибка компиляции, чтобы баг не маскировался утечкой.
static int структура_индекс_нужна_деструкция(Анализатор *а, int индекс,
                                              int глубина)
{
    if (индекс < 0 || (size_t)индекс >= а->число_структур) return 0;
    if ((size_t)глубина > а->число_структур) {
        // Структурно недостижимо: поле-структура по значению может ссылаться
        // только на уже объявленную структуру, значит цикла быть не может, а
        // цепочка не длиннее числа структур в программе. Если всё же сюда
        // попали — внутренняя ошибка анализатора; явная диагностика лучше
        // тихого «деструкции нет» (утечка ресурса без единого сообщения).
        char msg[256];
        snprintf(msg, sizeof(msg),
            "не удалось определить необходимость деструкции структуры «%s»: "
            "цепочка вложенных полей длиннее числа объявленных структур",
            а->структуры[индекс].имя);
        ошибка_семантики(а, nullptr, msg);
        return 0;
    }
    const ОписаниеСтруктуры *с = &а->структуры[индекс];
    if (с->есть_деструктор) return 1;
    for (size_t i = 0; i < с->число_полей; ++i) {
        const ПолеСтруктуры *п = &с->поля[i];
        // Поле-срез — сама структура владеет его памятью (free при выходе из
        // области), это тоже «нужна деструкция», как собственный деструктор.
        if (п->срез) return 1;
        if (п->тип == ТОКЕН_СТРУКТУРА && п->указатели == 0
            && структура_индекс_нужна_деструкция(а, п->индекс_структуры, глубина + 1))
            return 1;
    }
    return 0;
}

