static int байты_равны(const char *a, size_t la, const char *b, size_t lb)
{
    return la == lb && memcmp(a, b, la) == 0;
}

static int токен_текст_равен(const Токен *t, const char *текст)
{
    return байты_равны(t->начало, t->длина, текст, strlen(текст));
}

// Индекс структуры по имени-токену; -1 если не найдена.
static int найти_индекс_структуры(const Анализатор *а, const Токен *имя)
{
    for (size_t i = 0; i < а->число_структур; ++i) {
        if (байты_равны(а->структуры[i].имя, strlen(а->структуры[i].имя),
                        имя->начало, имя->длина)) {
            return (int)i;
        }
    }
    return -1;
}

// Индекс типа-указателя на функцию по имени-токену; -1 если не найден.
static int найти_индекс_типфункции(const Анализатор *а, const Токен *имя)
{
    for (size_t i = 0; i < а->число_типовфункций; ++i) {
        if (байты_равны(а->типыфункций[i].имя, strlen(а->типыфункций[i].имя),
                        имя->начало, имя->длина)) {
            return (int)i;
        }
    }
    return -1;
}

static СигнатураФункции *найти_сигнатуру_по_имени(Анализатор *а,
                                                  const char *начало, size_t длина)
{
    for (size_t i = 0; i < а->число_функций; ++i)
        if (байты_равны(а->функции[i].имя, strlen(а->функции[i].имя), начало, длина))
            return &а->функции[i];
    return nullptr;
}

// Сигнатура функции совпадает с типом-указателем (возврат + параметры)?
static int сигнатура_совпадает_с_типфункцией(const СигнатураФункции *с,
                                             const ОписаниеТипфункции *т)
{
    if (с->тип_возврата != т->тип_возврата) return 0;
    if (с->указатели_возврата != т->указатели_возврата) return 0;
    if (с->беззнаковый != т->беззнаковый_возврата) return 0;
    if (с->число_параметров != т->число_параметров) return 0;
    for (size_t i = 0; i < с->число_параметров; ++i) {
        if (с->параметры[i].тип != т->параметры[i].тип) return 0;
        if (с->параметры[i].указатели != т->параметры[i].указатели) return 0;
        if (с->параметры[i].срез != т->параметры[i].срез) return 0;
        if (с->параметры[i].беззнаковый != т->параметры[i].беззнаковый) return 0;
    }
    return 1;
}

