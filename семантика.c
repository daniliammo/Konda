// «п» в условии сравнения с «нуль»? Возвращает символ указателя, если условие
// имеет вид «п <оп> нуль» или «нуль <оп> п» (п — идентификатор-указатель).
static Символ *указатель_в_сравнении_с_нулём(Анализатор *а, Узел *условие)
{
    if (!условие || условие->тип != УЗЕЛ_БИНОП) return nullptr;
    Узел *л = узел_ребёнок(условие, 0);
    Узел *п = узел_ребёнок(условие, 1);
    Узел *пер = nullptr;
    if (л && л->тип == УЗЕЛ_ИДЕНТИФИКАТОР && п && п->тип == УЗЕЛ_НУЛЬ) пер = л;
    else if (п && п->тип == УЗЕЛ_ИДЕНТИФИКАТОР && л && л->тип == УЗЕЛ_НУЛЬ) пер = п;
    if (!пер) return nullptr;
    return найти_символ(а, пер->токен.начало, пер->токен.длина);
}

static void установить_ограничение_из_условия(Анализатор *а, Узел *условие)
{
    // Null-narrowing (разворот «возможно»): «если (п != нуль)» доказывает, что в
    // then-ветви «п» ненулевой → в этой ветви его можно разыменовать. Снимок пути
    // (анализ_если) восстановит может_быть_нуль для else/после if.
    if (условие && условие->тип == УЗЕЛ_БИНОП
        && условие->токен.тип == ТОКЕН_ВОСКЛИЦАТЕЛЬНЫЙ_ЗНАК
        && условие->токен.длина == 2) {
        Символ *с = указатель_в_сравнении_с_нулём(а, условие);
        if (с) с->может_быть_нуль = 0;
    }
    if (условие->тип != УЗЕЛ_БИНОП || условие->токен.тип != ТОКЕН_СТРЕЛКА_ВПРАВО)
        return;
    Узел *лево = узел_ребёнок(условие, 0);
    Узел *право = узел_ребёнок(условие, 1);
    if (!лево || !право
        || лево->тип != УЗЕЛ_ИДЕНТИФИКАТОР
        || право->тип != УЗЕЛ_ЧИСЛО)
        return;
    long порог = strtol(право->токен.начало, nullptr, 10);
    long минимум = токен_текст_равен(&условие->токен, ">=") ? порог : порог + 1;
    ограничение_установить(а, лево->токен.начало, лево->токен.длина, минимум);
}

static void проверить_условие(Анализатор *а, Узел *условие)
{
    ИнфоТипа тип = типизировать_выражение(а, условие);
    if (а->диаг->ошибка) return;
    if (тип.тип != ТОКЕН_НЕИЗВЕСТНЫЙ && !тип_скалярный(тип)) {
        char тб[64];
        описание_типа(тип, тб, sizeof(тб));
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "условие должно быть скалярным типом, получен «%s»", тб);
        ошибка_семантики(а, &условие->токен, msg);
    }
}

// Присваивается ли имени (или объявляется заново) где-либо в поддереве?
// Консервативно: затенение тем же именем тоже считается изменением.
static int имя_изменяется_в(Узел *узел, const char *имя, size_t длина_имени)
{
    if (!узел) return 0;
    if (узел->тип == УЗЕЛ_ПРИСВАИВАНИЕ) {
        Узел *цель = узел_ребёнок(узел, 0);
        if (цель && цель->тип == УЗЕЛ_ИДЕНТИФИКАТОР
            && байты_равны(цель->токен.начало, цель->токен.длина, имя, длина_имени))
            return 1;
    }
    if (узел->тип == УЗЕЛ_ОБЪЯВЛЕНИЕ
        && байты_равны(узел->токен.начало, узел->токен.длина, имя, длина_имени))
        return 1;
    for (size_t i = 0; i < узел_число_детей(узел); ++i)
        if (имя_изменяется_в(узел_ребёнок(узел, i), имя, длина_имени))
            return 1;
    return 0;
}

// Распознаёт канонический цикл «для (T и = c0; и < длина(с); и = и + ш)»,
// где c0 ≥ 0 и ш > 0 — константы, а тело не меняет ни «и», ни «с». В этом
// случае любой доступ «с[и]» доказанно в границах [0, длина): добавляет пару
// (с, и) в стек безопасных. Возвращает 1, если пара добавлена.
static int распознать_безопасный_цикл(Анализатор *а, Узел *иниц,
                                       Узел *условие, Узел *шаг, Узел *тело)
{
    if (а->число_безопасных >= МАКС_БЕЗОПАСНЫХ) return 0;

    // Инициализация: «T и = c0», c0 — константа ≥ 0.
    if (!иниц || иниц->тип != УЗЕЛ_ОБЪЯВЛЕНИЕ || иниц->срез || иниц->массив
        || узел_число_детей(иниц) != 1)
        return 0;
    long c0;
    if (!вычислить_константу(а, узел_ребёнок(иниц, 0), &c0) || c0 < 0)
        return 0;
    const char *и_имя = иниц->токен.начало;
    size_t и_длина = иниц->токен.длина;

    // Условие: «и < длина(с)» (режим среза) или «и < N» (режим предела).
    if (!условие || условие->тип != УЗЕЛ_БИНОП
        || !токен_текст_равен(&условие->токен, "<"))
        return 0;
    Узел *лево = узел_ребёнок(условие, 0);
    Узел *право = узел_ребёнок(условие, 1);
    if (!лево || лево->тип != УЗЕЛ_ИДЕНТИФИКАТОР
        || !байты_равны(лево->токен.начало, лево->токен.длина, и_имя, и_длина))
        return 0;

    const char *с_имя = nullptr;
    size_t с_длина = 0;
    int есть_срез = 0;
    long предел = 0;
    int есть_предел = 0;
    const char *п_имя = nullptr; size_t п_длина = 0;
    int есть_перем = 0;

    if (право && право->тип == УЗЕЛ_ВЫЗОВ
        && токен_текст_равен(&право->токен, "длина")
        && узел_число_детей(право) == 1) {
        Узел *аргумент = узел_ребёнок(право, 0);
        if (!аргумент || аргумент->тип != УЗЕЛ_ИДЕНТИФИКАТОР)
            return 0;
        с_имя = аргумент->токен.начало;
        с_длина = аргумент->токен.длина;
        есть_срез = 1;
    } else if (вычислить_константу(а, право, &предел) && предел > 0) {
        есть_предел = 1;
    } else if (право && право->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
        // «и < н»: сработает только для срезов, созданных как «выделить(н)»
        // (проверяется на доступе). Иначе о связи длины и «н» ничего не знаем.
        п_имя = право->токен.начало;
        п_длина = право->токен.длина;
        есть_перем = 1;
    } else {
        return 0;
    }

    // Шаг: «и = и + ш», ш — константа > 0.
    if (!шаг || шаг->тип != УЗЕЛ_ПРИСВАИВАНИЕ)
        return 0;
    Узел *цель = узел_ребёнок(шаг, 0);
    Узел *выраж = узел_ребёнок(шаг, 1);
    if (!цель || цель->тип != УЗЕЛ_ИДЕНТИФИКАТОР
        || !байты_равны(цель->токен.начало, цель->токен.длина, и_имя, и_длина))
        return 0;
    if (!выраж || выраж->тип != УЗЕЛ_БИНОП || !токен_текст_равен(&выраж->токен, "+"))
        return 0;
    Узел *ш_лево = узел_ребёнок(выраж, 0);
    Узел *ш_право = узел_ребёнок(выраж, 1);
    if (!ш_лево || ш_лево->тип != УЗЕЛ_ИДЕНТИФИКАТОР
        || !байты_равны(ш_лево->токен.начало, ш_лево->токен.длина, и_имя, и_длина))
        return 0;
    long ш;
    if (!вычислить_константу(а, ш_право, &ш) || ш <= 0)
        return 0;

    // Тело не должно менять индекс (иначе граница не гарантирована).
    if (имя_изменяется_в(тело, и_имя, и_длина))
        return 0;
    // В режиме среза тело не должно переназначать сам срез (иначе его длина,
    // а с ней и верхняя граница, могут измениться).
    if (есть_срез && имя_изменяется_в(тело, с_имя, с_длина))
        return 0;
    // Режим переменной: «н» не должна меняться в теле, иначе верхняя граница в
    // момент доступа уже другая.
    if (есть_перем && имя_изменяется_в(тело, п_имя, п_длина))
        return 0;

    а->безопасные[а->число_безопасных++] = (БезопаснаяПара){
        .индекс = { и_имя, и_длина },
        .срез = { с_имя, с_длина },
        .есть_срез = есть_срез,
        .предел = предел,
        .есть_предел = есть_предел,
        .перем = { п_имя, п_длина },
        .есть_перем = есть_перем,
    };
    return 1;
}

// Условие «если (и < длина(с))» или «если (и < N)» с БЕЗЗНАКОВЫМ индексом «и»
// доказывает и ∈ [0, верх) в then-ветви: и ≥ 0 — из беззнаковости, и < верх —
// из условия. Пока анализируется then-блок, добавляет безопасную пару (доступ
// «с[и]» напрямую, без рантайм-проверки границ — устранение проверки без потери
// безопасности). Возвращает 1, если пара добавлена (снять после блока). Индекс
// (и в режиме среза — сам срез) не должен меняться в теле, иначе верхняя граница
// в момент доступа не гарантирована.
static int распознать_безопасное_условие(Анализатор *а, Узел *условие, Узел *тело)
{
    if (а->число_безопасных >= МАКС_БЕЗОПАСНЫХ) return 0;
    if (!условие || условие->тип != УЗЕЛ_БИНОП
        || !токен_текст_равен(&условие->токен, "<"))
        return 0;
    Узел *лево = узел_ребёнок(условие, 0);
    Узел *право = узел_ребёнок(условие, 1);
    if (!лево || лево->тип != УЗЕЛ_ИДЕНТИФИКАТОР) return 0;
    // Беззнаковый индекс: гарантирует и ≥ 0 (для знакового отрицательное «и»
    // прошло бы «и < верх», но было бы вне границ снизу).
    Символ *ис = найти_символ(а, лево->токен.начало, лево->токен.длина);
    if (!ис || !ис->беззнаковый || ис->срез || ис->указатели) return 0;
    const char *и_имя = лево->токен.начало;
    size_t и_длина = лево->токен.длина;

    const char *с_имя = nullptr; size_t с_длина = 0;
    int есть_срез = 0; long предел = 0; int есть_предел = 0;
    if (право && право->тип == УЗЕЛ_ВЫЗОВ
        && токен_текст_равен(&право->токен, "длина")
        && узел_число_детей(право) == 1) {
        Узел *аргумент = узел_ребёнок(право, 0);
        if (!аргумент || аргумент->тип != УЗЕЛ_ИДЕНТИФИКАТОР) return 0;
        с_имя = аргумент->токен.начало; с_длина = аргумент->токен.длина;
        есть_срез = 1;
    } else if (вычислить_константу(а, право, &предел) && предел > 0) {
        есть_предел = 1;
    } else {
        return 0;
    }
    if (имя_изменяется_в(тело, и_имя, и_длина)) return 0;
    if (есть_срез && имя_изменяется_в(тело, с_имя, с_длина)) return 0;

    а->безопасные[а->число_безопасных++] = (БезопаснаяПара){
        .индекс = { и_имя, и_длина },
        .срез = { с_имя, с_длина },
        .есть_срез = есть_срез,
        .предел = предел,
        .есть_предел = есть_предел,
    };
    return 1;
}

// ─── путь-чувствительные флаги (definite assignment + нулевость) ─────────────
// Снимок флагов «инициализирована»/«может_быть_нуль» для каждого слота таблицы
// символов. Сливается на стыке ветвей: переменная инициализирована после if,
// только если она инициализирована на ВСЕХ путях (AND); указатель может быть
// нулём, если он нулевой хотя бы на ОДНОМ пути (OR).
typedef struct { int иниц; int нуль; int безоп; } ФлагиПути;
typedef struct {
    ФлагиПути эл[МАКС_СИМВОЛОВ];
    size_t число;
} СнимокПути;

static void снять_снимок_пути(const Анализатор *а, СнимокПути *с)
{
    с->число = а->число_символов;
    for (size_t i = 0; i < а->число_символов; ++i) {
        с->эл[i].иниц = а->символы[i].инициализирована;
        с->эл[i].нуль = а->символы[i].может_быть_нуль;
        с->эл[i].безоп = а->символы[i].безопасен_для_возврата;
    }
}

static void восстановить_снимок_пути(Анализатор *а, const СнимокПути *с)
{
    size_t n = с->число < а->число_символов ? с->число : а->число_символов;
    for (size_t i = 0; i < n; ++i) {
        а->символы[i].инициализирована = с->эл[i].иниц;
        а->символы[i].может_быть_нуль = с->эл[i].нуль;
        а->символы[i].безопасен_для_возврата = с->эл[i].безоп;
    }
}

// Слияние двух ветвей if. Ветвь-sink (заканчивается «вернуть») не вливается в
// fallthrough — берём состояние только из не-sink ветви. «база» используется,
// если обе ветви sink (код после if недостижим, состояние неважно).
static void слить_снимки_пути(Анализатор *а, int т_sink, int и_sink,
                               const СнимокПути *тогда, const СнимокПути *иначе,
                               const СнимокПути *база)
{
    for (size_t i = 0; i < а->число_символов; ++i) {
        int иниц, нуль, безоп;
        if (т_sink && и_sink) {
            иниц = база->эл[i].иниц;  нуль = база->эл[i].нуль;  безоп = база->эл[i].безоп;
        } else if (т_sink) {
            иниц = иначе->эл[i].иниц; нуль = иначе->эл[i].нуль; безоп = иначе->эл[i].безоп;
        } else if (и_sink) {
            иниц = тогда->эл[i].иниц; нуль = тогда->эл[i].нуль; безоп = тогда->эл[i].безоп;
        } else {
            иниц = тогда->эл[i].иниц && иначе->эл[i].иниц;  // init на всех путях
            нуль = тогда->эл[i].нуль || иначе->эл[i].нуль;  // нуль на любом пути
            безоп = тогда->эл[i].безоп && иначе->эл[i].безоп; // безопасен на ВСЕХ путях
        }
        а->символы[i].инициализирована = иниц;
        а->символы[i].может_быть_нуль = нуль;
        а->символы[i].безопасен_для_возврата = безоп;
    }
}

// Слияние для цикла: тело может не выполниться ни разу. Поэтому инициализацию
// внутри тела НЕ засчитываем как гарантированную (берём состояние до цикла), а
// зануление указателя в теле, наоборот, учитываем (мог стать нулём).
static void слить_цикл_путь(Анализатор *а, const СнимокПути *до,
                             const СнимокПути *тело)
{
    for (size_t i = 0; i < а->число_символов; ++i) {
        а->символы[i].инициализирована = до->эл[i].иниц;
        а->символы[i].может_быть_нуль = до->эл[i].нуль || тело->эл[i].нуль;
        // Указатель безопасен для возврата после цикла, только если безопасен и
        // до цикла, и после тела (тело могло переприсвоить его небезопасным).
        а->символы[i].безопасен_для_возврата =
            до->эл[i].безоп && тело->эл[i].безоп;
    }
}

static void анализ_если(Анализатор *а, Узел *узел)
{
    Узел *условие = узел_ребёнок(узел, 0);
    Узел *тогда   = узел_ребёнок(узел, 1);
    Узел *иначе   = узел_ребёнок(узел, 2);

    // Snapshot владения перед ветвлением: каждая ветвь работает со своей
    // копией, потом сливаем по правилу «во всех ветвях одинаково — иначе
    // ошибка». Это работает без рантайма: если итог одинаков, autofree
    // получает однозначное состояние на выходе из if; иначе мы отказываем.
    СостояниеВладения до, после_тогда, после_иначе;
    СнимокПути пдо, после_тогда_п, после_иначе_п;
    снять_состояние_владения(а, &до);
    снять_снимок_пути(а, &пдо);

    ограничения_сохранить(а);
    установить_ограничение_из_условия(а, условие);
    проверить_условие(а, условие);
    // Устранение проверки границ: «если (и < длина(с))» с беззнаковым «и»
    // доказывает и ∈ [0, длина(с)) в then-ветви → доступ «с[и]» прямой.
    int снять_безопасную = распознать_безопасное_условие(а, условие, тогда);
    анализ_блока(а, тогда);
    if (снять_безопасную) а->число_безопасных--;
    ограничения_восстановить(а);
    снять_состояние_владения(а, &после_тогда);
    снять_снимок_пути(а, &после_тогда_п);
    int тогда_sink = блок_завершается_возвратом(тогда);

    int иначе_sink = 0;
    if (иначе) {
        восстановить_состояние_владения(а, &до);
        восстановить_снимок_пути(а, &пдо);
        // Null-narrowing симметрично: «если (п == нуль) { … } иначе { … }» —
        // в else-ветви «п» доказанно ненулевой (его можно разыменовать).
        if (условие && условие->тип == УЗЕЛ_БИНОП
            && условие->токен.тип == ТОКЕН_РАВНО && условие->токен.длина == 2) {
            Символ *с = указатель_в_сравнении_с_нулём(а, условие);
            if (с) с->может_быть_нуль = 0;
        }
        ограничения_сохранить(а);
        анализ_блока(а, иначе);
        ограничения_восстановить(а);
        снять_состояние_владения(а, &после_иначе);
        снять_снимок_пути(а, &после_иначе_п);
        иначе_sink = блок_завершается_возвратом(иначе);
    } else {
        // Нет ветви «иначе» — для слияния её эквивалент = состояние до if
        // (ветвь «не вошли в if» не делает ничего с владением).
        после_иначе = до;
        после_иначе_п = пдо;
    }

    // Sink: ветвь с безусловным «вернуть» не вливается в fallthrough.
    // Заменяем её снимок на «до», чтобы merge видел: «эта ветвь как будто не
    // меняла владения для тех, кто продолжает после if». autofree кодогена
    // на это не сломается: drop перед «вернуть» уже вставлен внутри ветви, а
    // в outer-scope-выход в этой ветви управление просто не попадает.
    СостояниеВладения для_слияния_тогда = тогда_sink ? до : после_тогда;
    СостояниеВладения для_слияния_иначе = иначе_sink ? до : после_иначе;

    // Восстанавливаем «до», чтобы слияние писало в чистый базовый снимок.
    восстановить_состояние_владения(а, &до);
    слить_состояния_владения(а, &до, &для_слияния_тогда, &для_слияния_иначе);

    // Путь-чувствительное слияние definite-assignment и нулевости указателей.
    слить_снимки_пути(а, тогда_sink, иначе_sink,
                      &после_тогда_п, &после_иначе_п, &пдо);
}

// Цикл «для (T и = c0; и < предел; …)» доказуемо выполняется хотя бы раз?
// Тело гарантированно исполняется, если входное условие истинно при и = c0:
// предел — константа (или длина среза с известной длиной) и c0 < предел
// (для «<») либо c0 <= предел (для «<=»). Тогда инициализацию/ненулевость,
// установленные в теле, можно засчитать и после цикла (снимаем консервативность).
static int цикл_для_выполнится(Анализатор *а, Узел *иниц, Узел *условие)
{
    if (!иниц || иниц->тип != УЗЕЛ_ОБЪЯВЛЕНИЕ || узел_число_детей(иниц) != 1)
        return 0;
    long c0;
    if (!вычислить_константу(а, узел_ребёнок(иниц, 0), &c0))
        return 0;
    if (!условие || условие->тип != УЗЕЛ_БИНОП)
        return 0;
    int меньше = токен_текст_равен(&условие->токен, "<");
    int меньше_равно = токен_текст_равен(&условие->токен, "<=");
    if (!меньше && !меньше_равно)
        return 0;
    Узел *лево = узел_ребёнок(условие, 0);
    Узел *право = узел_ребёнок(условие, 1);
    if (!лево || лево->тип != УЗЕЛ_ИДЕНТИФИКАТОР
        || !байты_равны(лево->токен.начало, лево->токен.длина,
                        иниц->токен.начало, иниц->токен.длина))
        return 0;
    long предел;
    if (вычислить_константу(а, право, &предел))
        return меньше ? (c0 < предел) : (c0 <= предел);
    // «и < длина(с)» с известной на этапе компиляции длиной среза.
    if (право && право->тип == УЗЕЛ_ВЫЗОВ
        && токен_текст_равен(&право->токен, "длина")
        && узел_число_детей(право) == 1) {
        Узел *арг = узел_ребёнок(право, 0);
        if (арг && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
            Символ *с = найти_символ(а, арг->токен.начало, арг->токен.длина);
            if (с && с->длина_известна)
                return меньше ? (c0 < с->длина) : (c0 <= с->длина);
        }
    }
    return 0;
}

// «выбор выр { ВАРИАНТ { … } … }» — match по перечислению с проверкой
// исчерпываемости: выражение должно быть перечислением, каждая ветка —
// существующий вариант этого enum без повторов, и покрыты все варианты (или
// есть ветка «_»). Путь-чувствительное слияние ветвей — как у if, обобщённое
// на N ветвей (init на всех ветвях, нуль — на любой).
static void анализ_выбор(Анализатор *а, Узел *узел)
{
    Узел *выр = узел_ребёнок(узел, 0);
    ИнфоТипа т = типизировать_выражение(а, выр);
    if (а->диаг->ошибка) return;
    if (т.тип != ТОКЕН_ПЕРЕЧИСЛЕНИЕ || т.индекс_структуры < 0
        || (size_t)т.индекс_структуры >= а->число_перечислений) {
        ошибка_семантики(а, &выр->токен,
            "«выбор» применим только к выражению-перечислению");
        return;
    }
    const ОписаниеПеречисления *п = &а->перечисления[т.индекс_структуры];

    int покрыт[МАКС_ВАРИАНТОВ] = {0};
    int есть_умолчание = 0;

    СнимокПути пдо;
    снять_снимок_пути(а, &пдо);
    СнимокПути acc;
    acc.число = а->число_символов;
    for (size_t i = 0; i < а->число_символов; ++i) { acc.эл[i].иниц = 1; acc.эл[i].нуль = 0; }
    int есть_живая = 0;

    for (size_t b = 1; b < узел_число_детей(узел); ++b) {
        Узел *ветка = узел_ребёнок(узел, b);
        if (!ветка || ветка->тип != УЗЕЛ_ВЕТКА_ВЫБОРА) continue;

        int нашли = -1;
        size_t число_привязок = узел_число_детей(ветка) - 1;  // все, кроме блока
        if (байты_равны(ветка->токен.начало, ветка->токен.длина, "_", 1)) {
            есть_умолчание = 1;
            if (число_привязок > 0) {
                ошибка_семантики(а, &ветка->токен,
                    "ветка «_» не может привязывать полезную нагрузку");
                return;
            }
        } else {
            for (size_t j = 0; j < п->число_вариантов; ++j) {
                if (байты_равны(п->варианты[j].имя, strlen(п->варианты[j].имя),
                                ветка->токен.начало, ветка->токен.длина)) {
                    нашли = (int)j; break;
                }
            }
            if (нашли < 0) {
                ошибка_семантики(а, &ветка->токен,
                    "вариант не принадлежит перечислению выбираемого выражения");
                return;
            }
            if (покрыт[нашли]) {
                ошибка_семантики(а, &ветка->токен,
                    "дублирующаяся ветка варианта в «выбор»");
                return;
            }
            покрыт[нашли] = 1;
            // Привязка полезной нагрузки: число имён должно совпадать с числом
            // полей варианта (либо 0 — нагрузка игнорируется).
            const ВариантПеречисления *в = &п->варианты[нашли];
            if (число_привязок > 0 && число_привязок != в->число_полей) {
                char msg[256];
                snprintf(msg, sizeof msg,
                    "вариант «%s» несёт %zu поле(й), привязано %zu",
                    в->имя, в->число_полей, число_привязок);
                ошибка_семантики(а, &ветка->токен, msg);
                return;
            }
        }

        восстановить_снимок_пути(а, &пдо);
        Узел *блок_ветки = узел_последний_ребёнок(ветка);
        // Область для привязок нагрузки: объявляем b1,b2,… с типами полей
        // варианта; видны только в блоке этой ветки.
        войти_в_область(а);
        if (нашли >= 0 && число_привязок > 0) {
            const ВариантПеречисления *в = &п->варианты[нашли];
            for (size_t k = 0; k < число_привязок; ++k) {
                Узел *имя_узел = узел_ребёнок(ветка, k);
                const ПолеВарианта *пв = &в->поля[k];
                объявить_символ(а, имя_узел->токен.начало, имя_узел->токен.длина,
                    пв->тип, пв->указатели, 0, пв->беззнаковый, 0, 0, 0,
                    &имя_узел->токен, 0, 0);
                Символ *с = найти_символ(а, имя_узел->токен.начало,
                                         имя_узел->токен.длина);
                if (с) с->индекс_структуры = пв->индекс_структуры;
            }
        }
        анализ_блока(а, блок_ветки);
        выйти_из_области(а);
        if (а->диаг->ошибка) return;
        if (!блок_завершается_возвратом(блок_ветки)) {
            есть_живая = 1;
            for (size_t i = 0; i < а->число_символов; ++i) {
                acc.эл[i].иниц &= а->символы[i].инициализирована;
                acc.эл[i].нуль |= а->символы[i].может_быть_нуль;
            }
        }
    }

    // Исчерпываемость: без «_» должны быть покрыты все варианты.
    if (!есть_умолчание) {
        for (size_t j = 0; j < п->число_вариантов; ++j) {
            if (!покрыт[j]) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                    "«выбор» не исчерпывающий: не обработан вариант «%s» "
                    "(добавьте ветку или «_»)", п->варианты[j].имя);
                ошибка_семантики(а, &выр->токен, msg);
                return;
            }
        }
    }

    // Слияние ветвей: применяем накопленное состояние живых ветвей.
    восстановить_снимок_пути(а, &пдо);
    if (есть_живая) {
        for (size_t i = 0; i < а->число_символов; ++i) {
            а->символы[i].инициализирована = acc.эл[i].иниц;
            а->символы[i].может_быть_нуль = acc.эл[i].нуль;
        }
    }
}

static void анализ_пока(Анализатор *а, Узел *узел)
{
    Узел *условие = узел_ребёнок(узел, 0);
    Узел *тело    = узел_ребёнок(узел, 1);

    ограничения_сохранить(а);
    установить_ограничение_из_условия(а, условие);
    проверить_условие(а, условие);
    а->глубина_цикла++;
    СнимокПути до_тела;
    снять_снимок_пути(а, &до_тела);
    анализ_блока(а, тело);
    СнимокПути после_тела;
    снять_снимок_пути(а, &после_тела);
    слить_цикл_путь(а, &до_тела, &после_тела);
    а->глубина_цикла--;
    ограничения_восстановить(а);
}

// ─── «параллельно для»: параллелизм данных без разделяемого состояния ────────
//
// Диапазон цикла дробится на непересекающиеся куски по потокам. Гонок нет ПО
// ПОСТРОЕНИЮ, если каждая итерация пишет только «в свой» элемент: чтение общих
// данных безопасно (никто их не меняет), а запись наружу — уже гонка.
//
// Отсюда правила (все статические, нулевая рантайм-цена):
//   * писать можно только в «база[и]» по переменной цикла, либо в переменные,
//     объявленные ВНУТРИ тела (они у каждой итерации свои);
//   * «прервать»/«продолжить»/«вернуть» из тела запрещены (у кусков нет общего
//     порядка, и выход из середины параллельного диапазона не имеет смысла);
//   * вызывать можно только ЧИСТЫЕ функции (чистота выводится — см.
//     «вывести_чистоту»): чистая не пишет наружу, значит гонки не создаст;
//   * писать во внешнюю переменную можно только как в аккумулятор редукции
//     («редукция(итог)»): у каждого куска он свой, свёртка — после join.

// Копирует текст токена в буфер имени (для диагностики).
static void скопировать_имя_токена(char *куда, const Токен *т)
{
    size_t n = т->длина < МАКС_ИМЯ - 1 ? т->длина : МАКС_ИМЯ - 1;
    memcpy(куда, т->начало, n);
    куда[n] = '\0';
}

// Складывает объяснение, ПОЧЕМУ функция не чистая: без причины диагностика
// («нельзя вызывать») не подсказывает, что чинить. «в» == nullptr — сигнатуры
// нет вовсе (внешняя/libc).
static void объяснить_нечистоту(const СигнатураФункции *в, const Токен *вызов,
                                 char *буфер, size_t размер)
{
    char имя[МАКС_ИМЯ];
    скопировать_имя_токена(имя, вызов);
    const char *что = в ? в->имя_эффекта : "";
    char почему[320];
    switch (в ? в->вид_эффекта : ЭФФЕКТ_ВНЕШНЯЯ) {
        case ЭФФЕКТ_ССЫЛОЧНЫЙ_ПАРАМЕТР:
            snprintf(почему, sizeof почему,
                "имеет параметр «%s» с «изменяемый»/«вывод» — пишет в кадр "
                "вызывающего", что);
            break;
        case ЭФФЕКТ_УКАЗАТЕЛЬ_ПАРАМЕТР:
            snprintf(почему, sizeof почему,
                "принимает указатель «%s» — может писать в чужую память", что);
            break;
        case ЭФФЕКТ_ВЛАДЕЕТ_ПАРАМЕТР:
            snprintf(почему, sizeof почему,
                "принимает «владеет»-параметр «%s» — потребляет срез, а он общий "
                "у всех кусков", что);
            break;
        case ЭФФЕКТ_ЗАПИСЬ_В_СРЕЗ:
            snprintf(почему, sizeof почему,
                "пишет в срез-параметр «%s» — это общие данные", что);
            break;
        case ЭФФЕКТ_ПОТОК:
            snprintf(почему, sizeof почему, "сама запускает потоки");
            break;
        case ЭФФЕКТ_ВЫДЕЛЕНИЕ:
            snprintf(почему, sizeof почему,
                "выделяет память («%s»)", что[0] ? что : "выделить");
            break;
        case ЭФФЕКТ_НЕБЕЗОПАСНО:
            snprintf(почему, sizeof почему,
                "содержит «небезопасно» — там возможен сырой указатель");
            break;
        case ЭФФЕКТ_ВЫЗОВ_НЕЧИСТОЙ:
            snprintf(почему, sizeof почему,
                "вызывает нечистую «%s»", что);
            break;
        default:
            snprintf(почему, sizeof почему,
                "внешняя — её эффекты транспилятору неизвестны");
            break;
    }
    snprintf(буфер, размер,
        "в «параллельно для» можно вызывать только чистые функции, а «%s» %s; "
        "это могло бы быть гонкой", имя, почему);
}

// Операция редукции обязана быть ассоциативной И коммутативной: куски
// сворачиваются в произвольном порядке. «-» не годится — свёртка частичных
// разностей потребовала бы другой операции, а не той же.
static int операция_редукции(ТипТокена т)
{
    return т == ТОКЕН_ПЛЮС || т == ТОКЕН_ЗВЕЗДА || т == ТОКЕН_ПРЯМАЯ_ЧЕРТА
        || т == ТОКЕН_СТРЕЛКА_ВВЕРХ || т == ТОКЕН_АМПЕРСАНД;
}

// Имя объявлено внутри тела цикла (тогда оно у каждой итерации своё)?
static int объявлено_в_теле(const Узел *тело, const Токен *имя)
{
    if (!тело) return 0;
    if (тело->тип == УЗЕЛ_ОБЪЯВЛЕНИЕ && тело->токен.длина == имя->длина
        && memcmp(тело->токен.начало, имя->начало, имя->длина) == 0)
        return 1;
    for (size_t i = 0; i < узел_число_детей(тело); ++i)
        if (объявлено_в_теле(узел_ребёнок(тело, i), имя)) return 1;
    return 0;
}

static int токены_равны_(const Токен *а, const Токен *б)
{
    return а->длина == б->длина && memcmp(а->начало, б->начало, а->длина) == 0;
}

// Имя упоминается в поддереве? Нужно, чтобы запретить чтение частичного
// аккумулятора: его значение зависит от раскладки кусков по потокам.
static int упоминается_имя(const Узел *у, const Токен *имя)
{
    if (!у) return 0;
    if (у->тип == УЗЕЛ_ИДЕНТИФИКАТОР && токены_равны_(&у->токен, имя)) return 1;
    // У «с[и]» старой формы имя базы лежит в ТОКЕНЕ узла-индекса.
    if (у->тип == УЗЕЛ_ИНДЕКС && узел_число_детей(у) == 1
        && токены_равны_(&у->токен, имя)) return 1;
    for (size_t i = 0; i < узел_число_детей(у); ++i)
        if (упоминается_имя(узел_ребёнок(у, i), имя)) return 1;
    return 0;
}

// Оператор вида «итог = итог <оп> выражение» (или «итог = выражение <оп> итог»)?
// Возвращает операцию через «оп» и подвыражение-вклад через «вклад».
// Только такая форма даёт корректную свёртку: у куска свой аккумулятор,
// стартующий с единицы операции, и порядок слияния кусков не важен.
static int канонический_оператор_редукции(Анализатор *а, Узел *у,
                                           const Токен *цель, ТипТокена *оп,
                                           Узел **вклад)
{
    Узел *знач = узел_число_детей(у) > 1 ? узел_ребёнок(у, 1) : nullptr;
    if (!знач || знач->тип != УЗЕЛ_БИНОП || !операция_редукции(знач->токен.тип)) {
        ошибка_семантики(а, &у->токен,
            "аккумулятор редукции меняется только как «имя = имя <оп> выражение», "
            "где <оп> — одна из «+ * | ^ &» (ассоциативных и коммутативных): "
            "куски сворачиваются в произвольном порядке");
        return 0;
    }
    Узел *л = узел_ребёнок(знач, 0);
    Узел *п = узел_ребёнок(знач, 1);
    Узел *сам = nullptr;
    Узел *другой = nullptr;
    if (л && л->тип == УЗЕЛ_ИДЕНТИФИКАТОР && токены_равны_(&л->токен, цель)) {
        сам = л; другой = п;
    } else if (п && п->тип == УЗЕЛ_ИДЕНТИФИКАТОР
               && токены_равны_(&п->токен, цель)) {
        сам = п; другой = л;
    }
    if (!сам) {
        ошибка_семантики(а, &у->токен,
            "в редукции одним из операндов обязан быть сам аккумулятор: "
            "«имя = имя <оп> выражение»");
        return 0;
    }
    if (упоминается_имя(другой, цель)) {
        ошибка_семантики(а, &у->токен,
            "аккумулятор редукции упоминается в правой части дважды: его частичное "
            "значение зависит от раскладки кусков по потокам (недетерминировано)");
        return 0;
    }
    *оп = знач->токен.тип;
    *вклад = другой;
    return 1;
}

// Проверяет тело «параллельно для» на отсутствие гонок и запрещённых выходов.
// «цикл» — узел «параллельно для» (несёт сведения о редукции).
static void проверить_параллельное_тело(Анализатор *а, Узел *у, Узел *цикл,
                                         Узел *тело, const Токен *перем_цикла)
{
    if (!у) return;
    // Любое упоминание аккумулятора ВНЕ канонического оператора редукции — это
    // чтение частичной суммы куска; она зависит от того, как диапазон лёг на
    // потоки, то есть недетерминирована. Канонический оператор до сюда не
    // доходит: он разбирается в УЗЕЛ_ПРИСВАИВАНИЕ и возвращается сразу.
    if (цикл->редукция && у->тип == УЗЕЛ_ИДЕНТИФИКАТОР
        && токены_равны_(&у->токен, &цикл->редукция_имя)) {
        ошибка_семантики(а, &у->токен,
            "аккумулятор редукции читается вне «имя = имя <оп> выражение»: у каждого "
            "куска он свой и виден только частично — результат зависел бы от числа "
            "ядер (недетерминированно); используйте его после цикла");
        return;
    }
    switch (у->тип) {
        case УЗЕЛ_ПРЕРВАТЬ:
        case УЗЕЛ_ПРОДОЛЖИТЬ:
        case УЗЕЛ_ВОЗВРАТ:
            ошибка_семантики(а, &у->токен,
                "«прервать»/«продолжить»/«вернуть» запрещены в «параллельно для»: "
                "итерации идут кусками в разных потоках, общего порядка у них нет");
            return;
        case УЗЕЛ_ВЫЗОВ:
            // «длина(с)» — встроенная и чистая. Остальные пускаем, если чистота
            // ВЫВЕДЕНА: чистая функция не пишет наружу, значит гонки создать не
            // может. Нечистую отвергаем, называя причину и позицию эффекта.
            if (!токен_текст_равен(&у->токен, "длина")) {
                const СигнатураФункции *в =
                    найти_сигнатуру_по_имени(а, у->токен.начало, у->токен.длина);
                if (!в || !в->чистая) {
                    char msg[768];
                    объяснить_нечистоту(в, &у->токен, msg, sizeof msg);
                    ошибка_семантики(а, &у->токен, msg);
                    return;
                }
            }
            break;
        case УЗЕЛ_ПРИСВАИВАНИЕ: {
            Узел *цель = узел_ребёнок(у, 0);
            if (цель && цель->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                // Аккумулятор редукции — ЕДИНСТВЕННОЕ исключение из запрета
                // писать наружу: у каждого куска он свой (в его кадре), общей
                // ячейки нет, а свёртка идёт после join в одном потоке.
                if (цикл->редукция
                    && токены_равны_(&цель->токен, &цикл->редукция_имя)) {
                    ТипТокена оп;
                    Узел *вклад = nullptr;
                    if (!канонический_оператор_редукции(а, у, &цикл->редукция_имя,
                                                         &оп, &вклад))
                        return;
                    if (цикл->редукция_оп != ТОКЕН_НЕИЗВЕСТНЫЙ
                        && цикл->редукция_оп != оп) {
                        ошибка_семантики(а, &у->токен,
                            "в одной редукции смешаны разные операции: свёртка "
                            "кусков стала бы неоднозначной");
                        return;
                    }
                    цикл->редукция_оп = оп;
                    // В сам аккумулятор не спускаемся (иначе сработает запрет
                    // чтения); проверяем только вклад.
                    проверить_параллельное_тело(а, вклад, цикл, тело, перем_цикла);
                    return;
                }
                // Переменная, объявленная в теле, — своя у каждой итерации.
                if (!объявлено_в_теле(тело, &цель->токен)) {
                    ошибка_семантики(а, &цель->токен,
                        "запись во внешнюю переменную из «параллельно для» — гонка: "
                        "потоки писали бы в одну ячейку; пишите в «база[и]», "
                        "объявите переменную внутри тела или заведите редукцию "
                        "«редукция(имя)»");
                    return;
                }
            } else if (цель && цель->тип == УЗЕЛ_ИНДЕКС) {
                // Писать можно только «база[и]» — по переменной цикла: такие
                // элементы у разных итераций разные, значит куски не пересекаются.
                Узел *инд = узел_число_детей(цель) == 2 ? узел_ребёнок(цель, 1)
                                                        : узел_ребёнок(цель, 0);
                if (!инд || инд->тип != УЗЕЛ_ИДЕНТИФИКАТОР
                    || !токены_равны_(&инд->токен, перем_цикла)) {
                    ошибка_семантики(а, &цель->токен,
                        "в «параллельно для» писать можно только в «база[и]» по "
                        "переменной цикла: иначе непересечение кусков не доказать "
                        "(возможна гонка)");
                    return;
                }
            } else if (цель) {
                ошибка_семантики(а, &цель->токен,
                    "недопустимая цель записи в «параллельно для»");
                return;
            }
            break;
        }
        default: break;
    }
    for (size_t i = 0; i < узел_число_детей(у); ++i)
        проверить_параллельное_тело(а, узел_ребёнок(у, i), цикл, тело, перем_цикла);
}

// Цель «редукция(имя)» пригодна? Проверяем ДО тела, чтобы ошибка была одна.
static int проверить_цель_редукции(Анализатор *а, Узел *цикл,
                                    const Токен *перем_цикла)
{
    const Токен *имя = &цикл->редукция_имя;
    if (токены_равны_(имя, перем_цикла)) {
        ошибка_семантики(а, имя,
            "переменная цикла не может быть аккумулятором редукции: её значение "
            "задаёт разбиение на куски");
        return 0;
    }
    Символ *с = найти_символ(а, имя->начало, имя->длина);
    if (!с) {
        ошибка_семантики(а, имя,
            "аккумулятор редукции должен быть объявлен до цикла");
        return 0;
    }
    if (с->неизменяемый) {
        ошибка_семантики(а, имя,
            "аккумулятор редукции не может быть «конст»: цикл его меняет");
        return 0;
    }
    ИнфоТипа т = { с->тип, с->указатели, с->беззнаковый, с->срез,
                   с->индекс_структуры };
    if (!тип_целочисленный(т) || с->массив) {
        // Вещественная редукция дала бы разный ответ на разном числе ядер:
        // сложение/умножение float не ассоциативно. Предсказуемость важнее —
        // как и в побитовых операциях, отвергаем нецелые.
        ошибка_семантики(а, имя,
            "редукция поддержана только для целочисленных типов: у вещественных "
            "сложение и умножение не ассоциативны, поэтому результат зависел бы от "
            "числа ядер и раскладки кусков (недетерминированно)");
        return 0;
    }
    return 1;
}

// Собирает внешние переменные, которые читает тело, как дети-УЗЕЛ_ЗАХВАТ.
// Кодогену они нужны, чтобы построить кадр потока: своей таблицы символов у
// него нет, а типы известны только здесь.
static void захватить_имя(Анализатор *а, Узел *цикл, Узел *тело,
                           const Токен *имя, const Токен *перем_цикла)
{
    if (токены_равны_(имя, перем_цикла) || объявлено_в_теле(тело, имя)) return;
    for (size_t i = 4; i < узел_число_детей(цикл); ++i)   // уже захвачено?
        if (токены_равны_(&узел_ребёнок(цикл, i)->токен, имя)) return;
    Символ *с = найти_символ(а, имя->начало, имя->длина);
    if (!с) return;                                        // не переменная
    Узел *з = узел_создать(УЗЕЛ_ЗАХВАТ);
    з->токен = *имя;
    з->тип_данных = с->тип;
    з->беззнаковый = с->беззнаковый;
    з->указатели = с->указатели;
    з->срез = с->срез;
    з->имя_типа = *имя;
    // Аккумулятор редукции захватывается как обычная переменная (тип, поле кадра
    // и подстановка «_к->имя» работают даром), но в кадр кладётся ЕДИНИЦА
    // операции, а не копия внешнего значения, и после join идёт свёртка.
    if (цикл->редукция && токены_равны_(имя, &цикл->редукция_имя)) {
        з->редукция = 1;
        з->редукция_оп = цикл->редукция_оп;
    }
    узел_добавить_ребёнка(цикл, з);
}

static void собрать_захваты(Анализатор *а, Узел *цикл, Узел *у, Узел *тело,
                             const Токен *перем_цикла)
{
    if (!у) return;
    if (у->тип == УЗЕЛ_ИДЕНТИФИКАТОР)
        захватить_имя(а, цикл, тело, &у->токен, перем_цикла);
    // У «с[и]» старой формы имя базы лежит в ТОКЕНЕ узла-индекса, а не в
    // дочернем идентификаторе, — иначе сам срез не был бы захвачен.
    if (у->тип == УЗЕЛ_ИНДЕКС && узел_число_детей(у) == 1)
        захватить_имя(а, цикл, тело, &у->токен, перем_цикла);
    for (size_t i = 0; i < узел_число_детей(у); ++i)
        собрать_захваты(а, цикл, узел_ребёнок(у, i), тело, перем_цикла);
}

static void анализ_для(Анализатор *а, Узел *узел)
{
    Узел *инициализация = узел_ребёнок(узел, 0);
    Узел *условие       = узел_ребёнок(узел, 1);
    Узел *шаг           = узел_ребёнок(узел, 2);
    Узел *тело          = узел_ребёнок(узел, 3);

    войти_в_область(а);
    анализ_оператора(а, инициализация);
    ограничения_сохранить(а);
    установить_ограничение_из_условия(а, условие);
    проверить_условие(а, условие);
    типизировать_выражение(а, шаг);

    // «параллельно для» требует канонической формы: только у неё известно, что
    // «и» пробегает диапазон ровно по разу, — на этом держится непересечение
    // кусков. Проверяем ДО анализа тела, чтобы ошибка была одна и понятная.
    if (узел->параллельно) {
        if (!инициализация || инициализация->тип != УЗЕЛ_ОБЪЯВЛЕНИЕ) {
            ошибка_семантики(а, &узел->токен,
                "«параллельно для» требует канонической формы: "
                "«параллельно для (целое32 и = 0; и < предел; и = и + 1)»");
        } else if (узел->редукция
                   && !проверить_цель_редукции(а, узел, &инициализация->токен)) {
            // Ошибка уже выдана: тело не проверяем, чтобы не сыпать вторичным.
        } else {
            // Можно ли в шиме сравнивать счётчик с границей куска в ЕГО типе?
            // Обычный «для» и так сравнивает в типе счётчика (`и < длина(с)` →
            // `int32 < int32`), а шим хранит границы куска как long и тем самым
            // отклоняется: под -fwrapv компилятор обязан допустить перенос
            // счётчика, число итераций становится недоказуемым и SIMD пропадает.
            // Приводить границу к типу счётчика можно, только если она не шире —
            // иначе (напр. `целое32 и < целое64 предел`) C расширяет сравнение до
            // 64 бит, и сужение изменило бы поведение.
            ИнфоТипа тс = { инициализация->тип_данных, инициализация->указатели,
                            инициализация->беззнаковый, инициализация->срез, -1 };
            int ш_счётчика = ширина_бит(тс);
            int ш_границы = 0;
            if (условие && условие->тип == УЗЕЛ_БИНОП
                && узел_число_детей(условие) == 2)
                ш_границы = ширина_бит(типизировать_выражение(а,
                                            узел_ребёнок(условие, 1)));
            узел->граница_в_типе_счётчика =
                ш_счётчика > 0 && ш_границы > 0 && ш_границы <= ш_счётчика;
            узел->редукция_оп = ТОКЕН_НЕИЗВЕСТНЫЙ;
            проверить_параллельное_тело(а, тело, узел, тело,
                                        &инициализация->токен);
            if (!а->диаг->ошибка)
                собрать_захваты(а, узел, тело, тело, &инициализация->токен);
        }
    }

    // Если это канонический цикл по срезу/массиву, доступы по индексу цикла
    // внутри тела безопасны — анализ тела проставит им безопасный_индекс.
    int безопасный = распознать_безопасный_цикл(а, инициализация, условие, шаг, тело);
    int выполнится = цикл_для_выполнится(а, инициализация, условие);
    а->глубина_цикла++;
    СнимокПути до_тела;
    снять_снимок_пути(а, &до_тела);
    анализ_блока(а, тело);
    // Доказуемо ≥1 итерация: состояние после цикла = состояние конца тела
    // (последняя итерация), оставляем как есть. Иначе — консервативное слияние
    // с «тело могло не выполниться».
    if (!выполнится) {
        СнимокПути после_тела;
        снять_снимок_пути(а, &после_тела);
        слить_цикл_путь(а, &до_тела, &после_тела);
    }
    а->глубина_цикла--;
    if (безопасный) а->число_безопасных--;

    ограничения_восстановить(а);
    выйти_из_области(а);
}

// foreach: «для_каждого (имя из коллекция) { … }».
// Семантика:
//   1. Типизировать коллекцию: срез<T> или массив T[N].
//   2. Завести «имя» в области тела с типом T (копия элемента).
//   3. Тело анализируется как тело цикла (глубина_цикла++, чтобы move
//      исходника из внешней области был запрещён, как в обычном «для»).
// На узле УЗЕЛ_ДЛЯ_КАЖДОГО сохраняем элементный тип — пригодится кодогену
// (он генерит C-цикл с прямым доступом, не зная типа коллекции отдельно).
static void анализ_для_каждого(Анализатор *а, Узел *узел)
{
    Узел *коллекция = узел_ребёнок(узел, 0);
    Узел *тело      = узел_ребёнок(узел, 1);

    ИнфоТипа тип_к = типизировать_выражение(а, коллекция);
    if (а->диаг->ошибка) return;

    int это_срез = тип_к.срез;
    int это_массив = 0;
    long длина_массива = 0;

    // Массив-переменная: тип идёт не через ИнфоТипа (массив там не отражается),
    // а через символ исходного идентификатора (как в авто-конверсии массива в
    // срез при вызове функции).
    if (!это_срез && коллекция->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
        Символ *с = найти_символ(а, коллекция->токен.начало, коллекция->токен.длина);
        if (с && с->массив && с->длина_известна) {
            это_массив = 1;
            длина_массива = с->длина;
        }
    }

    if (!это_срез && !это_массив) {
        ошибка_семантики(а, &коллекция->токен,
            "«для_каждого» работает только со срезом или массивом фикс. размера");
        return;
    }

    // Метаданные элемента сохраняем на самом узле foreach — для кодогена.
    узел->тип_данных = тип_к.тип;
    узел->беззнаковый = тип_к.беззнаковый;
    узел->указатели = тип_к.указатели;
    узел->срез = это_срез;
    узел->массив = это_массив;
    узел->размер_массива = длина_массива;

    // Тело — со своей областью видимости. Имя элемента (узел->токен) живёт
    // только внутри неё.
    войти_в_область(а);

    // Длина элемента известна, только если он сам массив — а вложенные
    // массивы пока не поддерживаются, так что длина_известна = 0.
    объявить_символ(а,
        узел->токен.начало, узел->токен.длина,
        тип_к.тип, тип_к.указатели, /*массив=*/0,
        тип_к.беззнаковый, /*срез=*/0,
        /*длина_известна=*/0, /*длина=*/0,
        &узел->токен,
        /*значение_известно=*/0, /*значение=*/0);

    // Доказуемо ≥1 итерация: массив фикс. размера (его размер — положительный
    // литерал, всегда > 0) или срез с известной длиной > 0. Тогда тело
    // гарантированно исполняется → снимаем консервативность после цикла.
    int выполнится = 0;
    if (это_массив && длина_массива > 0) {
        выполнится = 1;
    } else if (это_срез && коллекция->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
        Символ *с = найти_символ(а, коллекция->токен.начало, коллекция->токен.длина);
        if (с && с->длина_известна && с->длина > 0) выполнится = 1;
    }

    а->глубина_цикла++;
    СнимокПути до_тела;
    снять_снимок_пути(а, &до_тела);
    анализ_блока(а, тело);
    if (!выполнится) {
        СнимокПути после_тела;
        снять_снимок_пути(а, &после_тела);
        слить_цикл_путь(а, &до_тела, &после_тела);
    }
    а->глубина_цикла--;

    выйти_из_области(а);
}

static void анализ_оператора(Анализатор *а, Узел *узел)
{
    if (!узел || а->диаг->ошибка) return;

    switch (узел->тип) {
        case УЗЕЛ_ЕСЛИ:
            анализ_если(а, узел);
            break;

        case УЗЕЛ_ВЫБОР:
            анализ_выбор(а, узел);
            break;

        case УЗЕЛ_ПОКА:
            анализ_пока(а, узел);
            break;

        case УЗЕЛ_ДЛЯ:
            анализ_для(а, узел);
            break;

        case УЗЕЛ_ДЛЯ_КАЖДОГО:
            анализ_для_каждого(а, узел);
            break;

        case УЗЕЛ_ПРЕРВАТЬ:
            if (а->глубина_цикла == 0)
                ошибка_семантики(а, &узел->токен,
                    "«прервать» вне цикла");
            break;

        case УЗЕЛ_ПРОДОЛЖИТЬ:
            if (а->глубина_цикла == 0)
                ошибка_семантики(а, &узел->токен,
                    "«продолжить» вне цикла");
            break;

        case УЗЕЛ_ВОЗВРАТ: {
            // Все «вывод»-параметры обязаны быть присвоены до этого возврата.
            проверить_вывод_присвоен(а, &узел->токен);
            // Функция «ничего» (void) не возвращает значение: «вернуть выр» в ней —
            // ошибка (в C это несовместимость типа возврата).
            if (а->тип_возврата == ТОКЕН_НИЧЕГО && узел_число_детей(узел) > 0) {
                ошибка_семантики(а, &узел->токен,
                    "функция типа «ничего» не может возвращать значение");
                break;
            }
            if (узел_число_детей(узел) > 0) {
                Узел *выр = узел_ребёнок(узел, 0);
                ИнфоТипа тип_выр = типизировать_выражение(а, выр);
                if (а->диаг->ошибка) break;

                ИнфоТипа тип_рет = {
                    а->тип_возврата,
                    а->указатели_возврата,
                    а->беззнаковый_возврата,
                    а->срез_возврата,
                    а->индекс_структуры_возврата
                };
                if (тип_рет.тип != ТОКЕН_НЕИЗВЕСТНЫЙ
                    && тип_выр.тип != ТОКЕН_НЕИЗВЕСТНЫЙ
                    && !совместимы_для_присваивания(тип_рет, тип_выр)) {
                    char тф[64], тв[64];
                    описание_типа(тип_рет, тф, sizeof(тф));
                    описание_типа(тип_выр, тв, sizeof(тв));
                    char msg[256];
                    snprintf(msg, sizeof(msg),
                             "несовместимый тип возврата: функция возвращает «%s», выражение «%s»",
                             тф, тв);
                    ошибка_семантики(а, &выр->токен, msg);
                    break;
                }

                // Безопасность указательного возврата: вернуть можно только
                // указатель на память, которая переживёт возврат и принадлежит
                // вызывающему/статике (borrow) — параметр, «аргументы», строковый
                // литерал, «нуль» или их индексация. Возврат указателя на локальные
                // данные, результата C-функции или неизвестного происхождения —
                // ошибка: иначе висячий указатель или ручное управление памятью.
                // Проверяем ВСЕГДА (в т.ч. при неизвестном типе выражения —
                // напр. прямой «вернуть strchr(…)»: тип результата C-функции
                // неизвестен, но происхождение всё равно небезопасно).
                if (а->указатели_возврата > 0 && !а->срез_возврата
                    && !выражение_безопасно_для_возврата(а, выр)) {
                    ошибка_семантики(а, &выр->токен,
                        "нельзя вернуть этот указатель: его память не переживёт "
                        "возврат (локальные данные / результат C-функции / "
                        "неизвестное происхождение). Безопасно вернуть можно "
                        "указатель-параметр, «аргументы», строковый литерал или «нуль»");
                    break;
                }

                // Возврат среза = передача владения вызывающему. Возвращать можно
                // только то, чем владеем: заимствованный (параметр) срез вернуть
                // нельзя — иначе вызывающий получил бы чужое владение (double-free).
                if (а->срез_возврата && выр->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                    Символ *с = найти_символ(а, выр->токен.начало, выр->токен.длина);
                    if (с && с->срез && !с->владеет_срезом) {
                        char msg[256];
                        snprintf(msg, sizeof(msg),
                            "нельзя вернуть заимствованный срез «%.*s»: "
                            "функция не владеет им",
                            (int)выр->токен.длина, выр->токен.начало);
                        ошибка_семантики(а, &выр->токен, msg);
                        break;
                    }
                    // Владение уходит наружу — источник больше не используется.
                    if (с) { с->перемещён = 1; с->токен_move = выр->токен; }
                }
            }
            break;
        }

        case УЗЕЛ_ОБЪЯВЛЕНИЕ: {
            long значение = 0;
            int значение_известно = 0;
            int длина_известна = 0;
            long длина_среза = 0;
            int длина_из_переменной_новая = 0;   // «выделить(н)»: длина == «н»
            Токен длина_токен_новый = { 0 };
            int владеет_новый = 0;  // объявляемый срез становится владельцем
            int поток_новый = 0;    // объявляемая переменная — дескриптор потока
            int индекс_стр = -1;

            // Переменная-указатель на функцию: «БинарОп оп [= функ | нуль]».
            // Инициализатор — имя подходящей функции (сверяем сигнатуру),
            // указатель того же типа, или «нуль». Без инициализатора — нулевой.
            if (узел->тип_данных == ТОКЕН_ФУНКУКАЗАТЕЛЬ) {
                int idx = найти_индекс_типфункции(а, &узел->имя_типа);
                if (idx < 0) {
                    char msg[256];
                    snprintf(msg, sizeof msg,
                        "неизвестный тип-указатель на функцию «%.*s»",
                        (int)узел->имя_типа.длина, узел->имя_типа.начало);
                    ошибка_семантики(а, &узел->имя_типа, msg);
                    break;
                }
                int может_нуль = 1;
                if (узел_число_детей(узел) > 0) {
                    Узел *инит = узел_ребёнок(узел, 0);
                    if (инит->тип == УЗЕЛ_НУЛЬ) {
                        может_нуль = 1;
                    } else if (инит->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                        СигнатураФункции *сф = найти_сигнатуру_по_имени(а,
                            инит->токен.начало, инит->токен.длина);
                        Символ *ис = найти_символ(а, инит->токен.начало,
                                                  инит->токен.длина);
                        if (сф && сигнатура_совпадает_с_типфункцией(сф,
                                &а->типыфункций[idx])) {
                            может_нуль = 0;  // присвоена конкретная функция
                        } else if (сф) {
                            ошибка_семантики(а, &инит->токен,
                                "сигнатура функции не совпадает с типом-указателем");
                            break;
                        } else if (ис && ис->тип == ТОКЕН_ФУНКУКАЗАТЕЛЬ
                                   && ис->индекс_структуры == idx) {
                            может_нуль = ис->может_быть_нуль;  // копия указателя
                        } else {
                            ошибка_семантики(а, &инит->токен,
                                "инициализатор указателя на функцию — имя "
                                "подходящей функции, указатель того же типа или «нуль»");
                            break;
                        }
                    } else {
                        ошибка_семантики(а, &инит->токен,
                            "инициализатор указателя на функцию — имя функции или «нуль»");
                        break;
                    }
                }
                объявить_символ(а, узел->токен.начало, узел->токен.длина,
                    ТОКЕН_ФУНКУКАЗАТЕЛЬ, 0, 0, 0, 0, 0, 0, &узел->токен, 0, 0);
                Символ *с = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (с) {
                    с->индекс_структуры = idx;
                    с->может_быть_нуль = может_нуль;
                    с->инициализирована = 1;
                    с->неизменяемый = узел->неизменяемый;
                }
                break;
            }

            // Тип-структура: проверяем существование и (для не-указателя)
            // объявляем переменную; инициализатор пока не поддерживается.
            if (узел->тип_данных == ТОКЕН_СТРУКТУРА) {
                индекс_стр = найти_индекс_структуры(а, &узел->имя_типа);
                if (индекс_стр < 0) {
                    char msg[256];
                    snprintf(msg, sizeof(msg), "неизвестная структура «%.*s»",
                             (int)узел->имя_типа.длина, узел->имя_типа.начало);
                    ошибка_семантики(а, &узел->имя_типа, msg);
                    break;
                }
                // Инициализатор — литерал полей «{ поле = выражение, … }» либо
                // выражение-структура того же типа (вызов функции, другая
                // переменная-структура, доступ к полю-структуре): последнее —
                // обычное копирование значения. Копию разрешаем, только если
                // структуре НЕ нужна деструкция; иначе скопировался бы дескриптор
                // владения (срез/деструктор) и оба экземпляра освободили бы одну
                // память (double-free). Функция, возвращающая структуру, уже
                // гарантированно не требует деструкции (проверка в анализ_функции).
                if (узел_число_детей(узел) > 0) {
                    Узел *лит = узел_ребёнок(узел, 0);
                    if (лит->тип != УЗЕЛ_ЛИТЕРАЛ_СТРУКТУРЫ) {
                        if (узел->указатели > 0) {
                            ошибка_семантики(а, &узел->токен,
                                "таким инициализатором задаётся только сама "
                                "структура, не указатель на неё");
                            break;
                        }
                        ИнфоТипа тип_ист = типизировать_выражение(а, лит);
                        if (а->диаг->ошибка) break;
                        ИнфоТипа тип_цель = { ТОКЕН_СТРУКТУРА, 0, 0, 0, индекс_стр };
                        if (!совместимы_для_присваивания(тип_цель, тип_ист)) {
                            ошибка_семантики(а, &узел->токен,
                                "несовместимый тип инициализатора структуры "
                                "(ожидалась структура этого типа, литерал полей "
                                "«{ … }» или вызов функции)");
                            break;
                        }
                        if (структура_с_деструктором(а, тип_ист)) {
                            ошибка_семантики(а, &узел->токен,
                                "нельзя копировать структуру, которой нужна "
                                "деструкция (владеет срезом/имеет деструктор): "
                                "копия привела бы к двойному освобождению");
                            break;
                        }
                        объявить_символ(а, узел->токен.начало, узел->токен.длина,
                            ТОКЕН_СТРУКТУРА, 0, 0, 0, 0, 0, 0, &узел->токен, 0, 0);
                        Символ *нов = найти_символ(а, узел->токен.начало, узел->токен.длина);
                        if (нов) нов->индекс_структуры = индекс_стр;
                        break;
                    }
                    if (узел->указатели > 0) {
                        ошибка_семантики(а, &узел->токен,
                            "литералом инициализируется только сама структура, "
                            "не указатель на неё");
                        break;
                    }
                    ОписаниеСтруктуры *оп = &а->структуры[индекс_стр];
                    int был_сбой = 0;
                    for (size_t i = 0; i < узел_число_детей(лит); ++i) {
                        Узел *иниц = узел_ребёнок(лит, i);
                        // Найти поле по имени.
                        ПолеСтруктуры *поле = nullptr;
                        for (size_t j = 0; j < оп->число_полей; ++j) {
                            if (байты_равны(оп->поля[j].имя, strlen(оп->поля[j].имя),
                                            иниц->токен.начало, иниц->токен.длина)) {
                                поле = &оп->поля[j];
                                break;
                            }
                        }
                        if (!поле) {
                            char msg[256];
                            snprintf(msg, sizeof(msg),
                                     "у структуры «%s» нет поля «%.*s»", оп->имя,
                                     (int)иниц->токен.длина, иниц->токен.начало);
                            ошибка_семантики(а, &иниц->токен, msg);
                            был_сбой = 1;
                            break;
                        }
                        Узел *знач_поля = узел_ребёнок(иниц, 0);
                        // Поле-срез: «выделить(N)»/«клон(x)» — структура становится
                        // владельцем (как «срез<T> имя = выделить(N)» для локальной
                        // переменной), поэтому типизируем аргумент напрямую, минуя
                        // общую типизацию выражения (которая эти встроенные вне
                        // объявления среза отклоняет как несуществующие функции).
                        if (поле->срез && (это_выделить(знач_поля) || это_клон(знач_поля))) {
                            if (узел_число_детей(знач_поля) != 1) {
                                ошибка_семантики(а, &знач_поля->токен,
                                    "встроенная функция принимает один аргумент");
                                был_сбой = 1;
                                break;
                            }
                            Узел *арг = узел_ребёнок(знач_поля, 0);
                            ИнфоТипа та = типизировать_выражение(а, арг);
                            if (а->диаг->ошибка) { был_сбой = 1; break; }
                            if (это_выделить(знач_поля)) {
                                if (та.тип != ТОКЕН_НЕИЗВЕСТНЫЙ && !тип_целочисленный(та)) {
                                    ошибка_семантики(а, &знач_поля->токен,
                                        "количество элементов в «выделить» должно быть целым");
                                    был_сбой = 1;
                                    break;
                                }
                            } else if (!та.срез) {
                                ошибка_семантики(а, &знач_поля->токен,
                                    "«клон» применим только к срезу");
                                был_сбой = 1;
                                break;
                            }
                            continue;
                        }
                        if (поле->срез) {
                            ошибка_семантики(а, &знач_поля->токен,
                                "поле-срез инициализируется только «выделить(N)» "
                                "или «клон(x)» — структура становится владельцем");
                            был_сбой = 1;
                            break;
                        }
                        ИнфоТипа тип_зн = типизировать_выражение(а, знач_поля);
                        if (а->диаг->ошибка) { был_сбой = 1; break; }
                        ИнфоТипа тип_поля = { поле->тип, поле->указатели,
                            поле->беззнаковый, поле->срез, поле->индекс_структуры };
                        if (!совместимы_для_присваивания(тип_поля, тип_зн)) {
                            char msg[256];
                            snprintf(msg, sizeof(msg),
                                     "несовместимый тип значения поля «%s»", поле->имя);
                            ошибка_семантики(а, &иниц->токен, msg);
                            был_сбой = 1;
                            break;
                        }
                        // Инициализация поля значением структуры, которой нужна
                        // деструкция (владеет срезом/имеет деструктор), скопировала
                        // бы дескриптор владения — и объемлющая структура, и
                        // источник освободили бы одну память (double-free). Тот же
                        // запрет, что для присваивания/передачи по значению.
                        if (структура_с_деструктором(а, тип_зн)) {
                            char msg[512];
                            snprintf(msg, sizeof(msg),
                                "поле «%s»: нельзя инициализировать значением "
                                "структуры, которой нужна деструкция (владеет "
                                "срезом/имеет деструктор) — копия дескриптора "
                                "владения привела бы к двойному освобождению",
                                поле->имя);
                            ошибка_семантики(а, &иниц->токен, msg);
                            был_сбой = 1;
                            break;
                        }
                    }
                    if (был_сбой) break;
                }
                объявить_символ(а, узел->токен.начало, узел->токен.длина,
                    ТОКЕН_СТРУКТУРА, узел->указатели, 0, 0, 0, 0, 0,
                    &узел->токен, 0, 0);
                Символ *нов = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (нов) {
                    нов->индекс_структуры = индекс_стр;
                    // Структура с деструктором (и не указатель) владеет ресурсом:
                    // autofree вызовет деструктор при выходе из области.
                    if (узел->указатели == 0
                        && структура_индекс_нужна_деструкция(а, индекс_стр, 0)) {
                        нов->владеет_срезом = 1;
                    }
                }
                break;
            }

            ИнфоТипа тип_объявления = {
                узел->тип_данных, узел->указатели, узел->беззнаковый, узел->срез,
                узел->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ
                    ? найти_индекс_перечисления(а, &узел->имя_типа) : -1
            };
            if (узел->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ
                && тип_объявления.индекс_структуры < 0) {
                ошибка_семантики(а, &узел->токен, "неизвестное перечисление");
                break;
            }

            // Массив фиксированного размера «тип имя[N]»: длина известна сразу.
            if (узел->массив && узел->размер_массива > 0) {
                длина_известна = 1;
                длина_среза = узел->размер_массива;
            }

            // «статический срез» невозможен: инициализатор «выделить» — не
            // константа, а статическая длительность требует константного init.
            if (узел->статический && узел->срез) {
                ошибка_семантики(а, &узел->токен,
                    "статические срезы не поддерживаются: "
                    "статическая переменная требует константного инициализатора");
                break;
            }

            if (узел_число_детей(узел) > 0) {
                Узел *инит = узел_ребёнок(узел, 0);

                // Массив фикс. размера + литерал значений: проверяем количество
                // (не больше размера; меньше — допустимо, остальное C занулит)
                // и совместимость типа каждого элемента с типом массива.
                if (узел->массив && инит->тип == УЗЕЛ_ЛИТЕРАЛ_МАССИВА) {
                    size_t n_иниц = узел_число_детей(инит);
                    if ((long)n_иниц > узел->размер_массива) {
                        char msg[256];
                        snprintf(msg, sizeof(msg),
                            "слишком много инициализаторов для массива «%.*s» "
                            "размера %ld (передано %zu)",
                            (int)узел->токен.длина, узел->токен.начало,
                            узел->размер_массива, n_иниц);
                        ошибка_семантики(а, &узел->токен, msg);
                        break;
                    }
                    ИнфоТипа тип_элемента = {
                        узел->тип_данных, узел->указатели, узел->беззнаковый, 0, -1
                    };
                    int был_сбой = 0;
                    for (size_t i = 0; i < n_иниц; ++i) {
                        Узел *эл = узел_ребёнок(инит, i);
                        ИнфоТипа тип_эл = типизировать_выражение(а, эл);
                        if (а->диаг->ошибка) { был_сбой = 1; break; }
                        if (!совместимы_для_присваивания(тип_элемента, тип_эл)) {
                            char тл[64], тп[64];
                            описание_типа(тип_элемента, тл, sizeof(тл));
                            описание_типа(тип_эл, тп, sizeof(тп));
                            char msg[256];
                            snprintf(msg, sizeof(msg),
                                "несовместимый тип элемента [%zu] массива «%.*s»: "
                                "«%s» ← «%s»",
                                i, (int)узел->токен.длина, узел->токен.начало,
                                тл, тп);
                            ошибка_семантики(а, &эл->токен, msg);
                            был_сбой = 1;
                            break;
                        }
                    }
                    if (был_сбой) break;
                } else
                // «поток<T> п = запустить(ф, арг)»: создание потока.
                // Модель — передача владения: поток получает данные по значению
                // или перемещением, поэтому разделяемого состояния нет и гонки
                // невозможны по построению (проверки статические).
                if (узел->поток) {
                    if (!это_запустить(инит)) {
                        ошибка_семантики(а, &узел->токен,
                            "«поток<T>» инициализируется только «запустить(функция, аргумент)»");
                        break;
                    }
                    if (узел_число_детей(инит) < 1 || узел_число_детей(инит) > 2) {
                        ошибка_семантики(а, &инит->токен,
                            "«запустить» принимает функцию и не более одного аргумента");
                        break;
                    }
                    Узел *фу = узел_ребёнок(инит, 0);
                    if (фу->тип != УЗЕЛ_ИДЕНТИФИКАТОР) {
                        ошибка_семантики(а, &инит->токен,
                            "первый аргумент «запустить» — имя функции потока");
                        break;
                    }
                    const СигнатураФункции *сиг =
                        найти_сигнатуру_по_имени(а, фу->токен.начало,
                                                  фу->токен.длина);
                    if (!сиг) {
                        ошибка_семантики(а, &фу->токен,
                            "неизвестная функция потока");
                        break;
                    }
                    if (!проверить_сигнатуру_потока(а, сиг, &инит->токен)) break;
                    size_t дано = узел_число_детей(инит) - 1;
                    if (дано != сиг->число_параметров) {
                        char msg[320];
                        snprintf(msg, sizeof msg,
                            "функция потока «%s» ожидает %zu аргумент(ов), передано %zu",
                            сиг->имя, сиг->число_параметров, дано);
                        ошибка_семантики(а, &инит->токен, msg);
                        break;
                    }
                    // Тип результата потока обязан совпасть с «поток<T>».
                    if (сиг->тип_возврата != узел->тип_данных
                        || сиг->беззнаковый != узел->беззнаковый
                        || сиг->срез) {
                        ошибка_семантики(а, &узел->токен,
                            "тип результата функции не совпадает с «поток<T>» "
                            "(срез как результат потока пока не поддержан)");
                        break;
                    }
                    if (дано == 1) {
                        Узел *арг = узел_ребёнок(инит, 1);
                        ИнфоТипа та = типизировать_выражение(а, арг);
                        if (а->диаг->ошибка) break;
                        const ИнфоПараметра *п0 = &сиг->параметры[0];
                        if (та.тип != ТОКЕН_НЕИЗВЕСТНЫЙ
                            && (та.тип != п0->тип || та.срез != п0->срез
                                || та.беззнаковый != п0->беззнаковый)) {
                            ошибка_семантики(а, &арг->токен,
                                "тип аргумента не совпадает с параметром функции потока");
                            break;
                        }
                        // Аргумент-срез перемещается в поток: у запускающего
                        // доступа больше нет (иначе была бы гонка за ту же
                        // память). Переиспользуем общий move-анализ.
                        if (п0->срез && сиг->владеет_параметра[0]
                            && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                            Символ *са = найти_символ(а, арг->токен.начало,
                                                       арг->токен.длина);
                            if (са && са->владеет_срезом) {
                                са->перемещён = 1;
                                са->токен_move = арг->токен;
                            }
                        }
                    }
                    // Дескриптор, объявленный в цикле, авто-join'ится в конце
                    // ИТЕРАЦИИ (его область — тело цикла), поэтому потоки идут
                    // строго по очереди: код выглядит параллельным, компилируется
                    // и не ускоряет ничего. Присоединить его вне цикла нельзя —
                    // имя туда не видно, — значит ложных срабатываний нет.
                    if (а->глубина_цикла > 0) {
                        предупреждение_семантики(а, &узел->токен,
                            "поток объявлен в теле цикла: он присоединяется в конце "
                            "итерации, поэтому потоки выполняются последовательно "
                            "и ускорения нет; для параллелизма по данным используйте "
                            "«параллельно для»");
                    }
                    поток_новый = 1;
                } else
                // «срез<T> имя = выделить(N)»: спец-обработка инициализатора.
                if (узел->срез && это_выделить(инит)) {
                    if (узел_число_детей(инит) != 1) {
                        ошибка_семантики(а, &инит->токен,
                            "«выделить» принимает ровно один аргумент — количество элементов");
                        break;
                    }
                    Узел *кол = узел_ребёнок(инит, 0);
                    ИнфоТипа тип_кол = типизировать_выражение(а, кол);
                    if (а->диаг->ошибка) break;
                    if (тип_кол.тип != ТОКЕН_НЕИЗВЕСТНЫЙ && !тип_целочисленный(тип_кол)) {
                        ошибка_семантики(а, &инит->токен,
                            "количество элементов в «выделить» должно быть целым");
                        break;
                    }
                    // Если количество — константа И срез не переназначается
                    // позже, длину знаем на этапе компиляции (иначе после «д = e»
                    // сохранённая длина устарела бы — статическую длину не берём).
                    long n;
                    if (вычислить_константу(а, кол, &n)) {
                        if (n < 0) {
                            ошибка_семантики(а, &инит->токен,
                                "количество элементов в «выделить» не может быть отрицательным");
                            break;
                        }
                        // Статическая проверка переполнения размера: n * sizeof(T).
                        ИнфоТипа эл = { узел->тип_данных, узел->указатели,
                                        узел->беззнаковый, 0, -1 };
                        long эр = размер_типа(эл), всего;
                        if (эр > 0 && __builtin_mul_overflow(n, эр, &всего)) {
                            ошибка_семантики(а, &инит->токен,
                                "размер выделения переполняется (количество × размер элемента)");
                            break;
                        }
                        if (!переменная_изменяемая(а, узел->токен.начало, узел->токен.длина)) {
                            длина_известна = 1;
                            длина_среза = n;
                        }
                    } else if (кол && кол->тип == УЗЕЛ_ИДЕНТИФИКАТОР
                               && !переменная_изменяемая(а, узел->токен.начало,
                                                          узел->токен.длина)) {
                        // Количество — простая переменная «н»: длина среза равна
                        // ЕЙ. Пока ни срез, ни «н» не переприсваивались, условие
                        // «и < н» доказывает границы так же, как «и < длина(с)»,
                        // и рантайм-проверку можно снять (нулевая цена вместо
                        // вызова с ветвью на abort в каждом доступе).
                        длина_из_переменной_новая = 1;
                        длина_токен_новый = кол->токен;
                    }
                    владеет_новый = 1;  // «выделить» создаёт владеющий срез
                } else if (узел->срез && это_клон(инит)) {
                    // «срез<T> б = клон(а)»: глубокая копия — новый владелец с
                    // собственными данными. Источник остаётся жив (не move).
                    if (узел_число_детей(инит) != 1) {
                        ошибка_семантики(а, &инит->токен,
                            "«клон» принимает ровно один аргумент — срез");
                        break;
                    }
                    Узел *арг = узел_ребёнок(инит, 0);
                    ИнфоТипа тип_арг = типизировать_выражение(а, арг);
                    if (а->диаг->ошибка) break;
                    if (!тип_арг.срез) {
                        ошибка_семантики(а, &инит->токен,
                            "«клон» применим только к срезу");
                        break;
                    }
                    if (тип_арг.тип != узел->тип_данных
                        || тип_арг.беззнаковый != узел->беззнаковый) {
                        ошибка_семантики(а, &узел->токен,
                            "тип элемента клона не совпадает с типом объявления");
                        break;
                    }
                    // Длина копии равна длине источника, если та известна.
                    if (арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                        Символ *ист = найти_символ(а, арг->токен.начало, арг->токен.длина);
                        if (ист && ист->длина_известна) {
                            длина_известна = 1;
                            длина_среза = ист->длина;
                        }
                    }
                    владеет_новый = 1;  // клон создаёт нового владельца
                } else {
                    ИнфоТипа тип_инит = типизировать_выражение(а, инит);
                    if (а->диаг->ошибка) break;

                    // Проверка совместимости типов.
                    if (!совместимы_для_присваивания(тип_объявления, тип_инит)) {
                        char тл[64], тп[64];
                        описание_типа(тип_объявления, тл, sizeof(тл));
                        описание_типа(тип_инит, тп, sizeof(тп));
                        char msg[256];
                        snprintf(msg, sizeof(msg),
                                 "несовместимые типы при объявлении «%.*s»: «%s» ← «%s»",
                                 (int)узел->токен.длина, узел->токен.начало, тл, тп);
                        ошибка_семантики(а, &узел->токен, msg);
                        break;
                    }

                    if (сужение_запрещено(а, тип_объявления, инит, тип_инит)) {
                        char тл[64], тп[64];
                        описание_типа(тип_объявления, тл, sizeof(тл));
                        описание_типа(тип_инит, тп, sizeof(тп));
                        char msg[400];
                        snprintf(msg, sizeof(msg),
                            "неявное сужение при объявлении «%.*s»: «%s» ← «%s» "
                            "теряет данные; используйте «как<%s>(…)»",
                            (int)узел->токен.длина, узел->токен.начало, тл, тп, тл);
                        ошибка_семантики(а, &узел->токен, msg);
                        break;
                    }

                    // Срез ← срез: получение владения. От срез-переменной это
                    // перемещение (move): источник становится недействительным.
                    // От вызова функции, возвращающей срез, — приём нового
                    // владения. Данные не копируются (только дескриптор).
                    if (узел->срез) {
                        if (инит->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                            Символ *ист = найти_символ(а, инит->токен.начало,
                                                       инит->токен.длина);
                            if (!ист || !ист->владеет_срезом) {
                                char msg[256];
                                snprintf(msg, sizeof(msg),
                                    "нельзя переместить «%.*s»: это заимствованный срез, "
                                    "владелец находится снаружи",
                                    (int)инит->токен.длина, инит->токен.начало);
                                ошибка_семантики(а, &инит->токен, msg);
                                break;
                            }
                            // В теле цикла move из объемлющей области запрещён
                            // (см. ту же проверку в УЗЕЛ_ПРИСВАИВАНИЕ).
                            if (ист->глубина < а->глубина && а->глубина_цикла > 0) {
                                ошибка_семантики(а, &инит->токен,
                                    "перемещение среза из объемлющей области "
                                    "внутри цикла запрещено (на втором витке "
                                    "источник уже перемещён)");
                                break;
                            }
                            ист->перемещён = 1;   // источник недействителен после move
                            ист->токен_move = инит->токен;
                            // Длину переносим — статическая проверка/элиминация
                            // продолжат работать после перемещения.
                            if (ист->длина_известна) {
                                длина_известна = 1;
                                длина_среза = ист->длина;
                            }
                        } else if (инит->тип != УЗЕЛ_ВЫЗОВ) {
                            ошибка_семантики(а, &узел->токен,
                                "источником среза может быть срез-переменная или "
                                "вызов функции, возвращающей срез");
                            break;
                        }
                        владеет_новый = 1;     // получатель становится владельцем
                    } else {
                        if (!переменная_изменяемая(а, узел->токен.начало, узел->токен.длина))
                            значение_известно = вычислить_константу(а, инит, &значение);

                        // Статическая переменная требует константного инициализатора
                        // (как в C: объект статической длительности). Допустимы также
                        // строковый литерал и «нуль».
                        if (узел->статический) {
                            long _к;
                            int конст = вычислить_константу(а, инит, &_к)
                                || инит->тип == УЗЕЛ_СТРОКА
                                || инит->тип == УЗЕЛ_НУЛЬ;
                            if (!конст) {
                                ошибка_семантики(а, &узел->токен,
                                    "инициализатор статической переменной должен быть "
                                    "константой");
                                break;
                            }
                        }
                    }
                }
            }

            // Константа-инициализатор скаляра вне диапазона объявленного
            // целого типа — ошибка (тихое усечение в C). Только сворачиваемые
            // в константу значения; переменные/выражения не трогаем.
            if (узел_число_детей(узел) > 0 && !узел->массив && !узел->срез
                && узел->указатели == 0 && узел->тип_данных != ТОКЕН_СТРУКТУРА) {
                long знач_к, лм, вм;
                if (вычислить_константу(а, узел_ребёнок(узел, 0), &знач_к)
                    && диапазон_целого(узел->тип_данных, узел->беззнаковый, &лм, &вм)
                    && (знач_к < лм || знач_к > вм)) {
                    char тип_оп[64];
                    ИнфоТипа ти = { узел->тип_данных, 0, узел->беззнаковый, 0, -1 };
                    описание_типа(ти, тип_оп, sizeof(тип_оп));
                    char msg[256];
                    snprintf(msg, sizeof(msg),
                        "константа %ld вне диапазона типа «%s» [%ld..%ld]",
                        знач_к, тип_оп, лм, вм);
                    ошибка_семантики(а, &узел->токен, msg);
                    break;
                }
            }

            объявить_символ(а,
                узел->токен.начало, узел->токен.длина,
                узел->тип_данных, узел->указатели, узел->массив,
                узел->беззнаковый, узел->срез, длина_известна, длина_среза,
                &узел->токен, значение_известно, значение);

            // Отметить нового владельца среза (после объявления символа).
            if (узел->срез && владеет_новый) {
                Символ *нов = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (нов) нов->владеет_срезом = 1;
            }

            // Связь «длина(с) == н» из «выделить(н)» — для снятия проверки
            // границ по условию «и < н» (см. индекс_в_безопасном_контексте).
            if (длина_из_переменной_новая) {
                Символ *нов = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (нов) {
                    нов->длина_из_переменной = 1;
                    нов->длина_токен = длина_токен_новый;
                }
            }

            // Дескриптор потока: помечаем символ, чтобы «присоединить» его
            // нашло и потребило (повторный join = use-after-move).
            if (поток_новый) {
                Символ *нов = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (нов) нов->поток = 1;
            }

            // Переменная-перечисление: сохранить индекс enum-типа в символе
            // (тот же слот, что у структур; тип их различает).
            if (узел->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) {
                Символ *нов = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (нов) нов->индекс_структуры = тип_объявления.индекс_структуры;
            }

            // Сырой указатель доказанно нулевой, если объявлен без инициализатора
            // (кодоген занулит его в nullptr) либо инициализирован «нуль».
            // Индексирование такого указателя ловится как нулевое разыменование.
            if (узел->указатели > 0 && !узел->срез) {
                Узел *инит0 = узел_число_детей(узел) > 0
                    ? узел_ребёнок(узел, 0) : nullptr;
                int инит_нуль = (инит0 == nullptr) || (инит0->тип == УЗЕЛ_НУЛЬ);
                // Non-null по умолчанию: обычный «T*» не может быть нулевым —
                // ни «= нуль», ни объявление без инициализатора (кодоген занулил
                // бы его в nullptr). Для nullable нужен «возможно<T*>».
                if (!узел->возможно && инит_нуль) {
                    ошибка_семантики(а, &узел->токен, инит0
                        ? "указатель ненулевой по умолчанию: «нуль» ему присвоить "
                          "нельзя — объявите «возможно<T*>» для nullable-указателя"
                        : "указатель ненулевой по умолчанию: инициализируйте "
                          "ненулевым значением или объявите «возможно<T*>»");
                }
                Символ *псим = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (псим) {
                    псим->возможно = узел->возможно;
                    // Обычный указатель гарантированно ненулевой (нуль отвергнут
                    // выше); «возможно» — нулевой, если так инициализирован.
                    псим->может_быть_нуль = узел->возможно ? инит_нуль : 0;
                    // Провенанс строкового литерала: прямой литерал или копия
                    // указателя, который сам из литерала (запись → .rodata).
                    псим->из_литерала = инит0 && (инит0->тип == УЗЕЛ_СТРОКА
                        || (инит0->тип == УЗЕЛ_ИДЕНТИФИКАТОР
                            && указатель_из_литерала(а, инит0)));
                    // Borrow-провенанс (безопасно ли вернуть): «нуль», строковый
                    // литерал, либо копия указателя, который сам безопасен для
                    // возврата (параметр/argv/литерал). Всё остальное (результат
                    // C-функции, неизвестное) — по умолчанию 0: вернуть нельзя.
                    псим->безопасен_для_возврата =
                        (инит0 == nullptr)                         // зануляется в nullptr
                        || (инит0->тип == УЗЕЛ_НУЛЬ)
                        || (инит0->тип == УЗЕЛ_СТРОКА)
                        || (инит0->тип == УЗЕЛ_ИДЕНТИФИКАТОР
                            && указатель_безопасен_для_возврата(а, инит0));
                }
            }

            // Definite assignment: скалярная арифметическая переменная,
            // объявленная без инициализатора (не массив/срез/структура/указатель —
            // те зануляются), считается неинициализированной до первого
            // присваивания; её чтение — ошибка. Указатели идут своим путём
            // (нулевое разыменование), массивы/структуры зануляются.
            if (узел_число_детей(узел) == 0
                && !узел->массив && !узел->срез && узел->указатели == 0
                && узел->тип_данных != ТОКЕН_СТРУКТУРА) {
                Символ *ссим = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (ссим) ссим->инициализирована = 0;
            }
            // Иммутабельность («конст»): помечаем переменную неизменяемой. Такая
            // переменная должна быть инициализирована при объявлении (иначе её
            // нельзя было бы задать вовсе — переприсваивание запрещено).
            if (узел->неизменяемый) {
                Символ *кс = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (кс) кс->неизменяемый = 1;
                if (узел_число_детей(узел) == 0) {
                    ошибка_семантики(а, &узел->токен,
                        "«конст»-переменная должна быть инициализирована при "
                        "объявлении (переприсваивание запрещено)");
                }
            }
            break;
        }

        case УЗЕЛ_ПРИСВАИВАНИЕ:
        case УЗЕЛ_ВЫЗОВ:
            типизировать_выражение(а, узел);
            break;

        case УЗЕЛ_БЛОК:
            анализ_блока(а, узел);
            break;

        case УЗЕЛ_НЕБЕЗОПАСНО:
            // Внутри блока разрешены операции с сырыми указателями; учёт глубины
            // включает/выключает статический гейт. Область видимости и autofree
            // обеспечивает анализ_блока (как у обычного блока).
            а->глубина_небезопасно++;
            анализ_блока(а, узел_ребёнок(узел, 0));
            а->глубина_небезопасно--;
            break;

        default:
            break;
    }
}

static void анализ_блока(Анализатор *а, Узел *блок)
{
    if (!блок) return;
    войти_в_область(а);
    for (size_t i = 0; i < узел_число_детей(блок); ++i) {
        if (а->диаг->ошибка) break;  // не-батч режим: стоп на первой ошибке
        // Снимок «рабочего» состояния до оператора — чтобы в батч-режиме после
        // ошибки восстановиться и продолжить со следующего оператора, не
        // повреждая таблицу символов и стек областей (иначе рассинхрон глубины
        // → порча). Значения — только индексы в фикс. массивах, восстановление
        // безопасно (никаких висячих указателей).
        size_t сн_символов   = а->число_символов;
        int    сн_глубина    = а->глубина;
        int    сн_глуб_цикла = а->глубина_цикла;
        size_t сн_безопасных = а->число_безопасных;
        size_t сн_связей     = а->число_связей;
        size_t сн_ограничений = а->глубина_ограничений;

        анализ_оператора(а, узел_ребёнок(блок, i));

        if (а->диаг->ошибка && а->ошибки) {
            собрать_ошибку(а);  // сохранить и сбросить
            // Откатить «рабочее» состояние к снимку до оператора.
            а->число_символов    = сн_символов;
            а->глубина           = сн_глубина;
            а->глубина_цикла     = сн_глуб_цикла;
            а->число_безопасных  = сн_безопасных;
            а->число_связей      = сн_связей;
            а->глубина_ограничений = сн_ограничений;
        }
    }
    выйти_из_области(а);
}

static void анализ_функции(Анализатор *а, Узел *функция)
{
    войти_в_область(а);

    // Базовая линия ошибок: чтобы проверку «возврат по всем путям» запускать
    // только на функции, тело которой разобралось без ошибок (иначе — каскад).
    size_t ошибок_до = а->ошибки ? а->ошибки->длина : 0;

    а->тип_возврата       = функция->тип_данных;
    а->беззнаковый_возврата = функция->беззнаковый;
    а->указатели_возврата = функция->указатели;  // косвенность типа возврата «Тип*»
    а->срез_возврата      = функция->срез;
    а->индекс_структуры_возврата = -1;

    // Тип возврата — пользовательская структура/перечисление: разрешаем, но
    // структуру, которой нужна деструкция (собственная или транзитивная),
    // возвращать по значению пока нельзя — это move-семантика возврата
    // (аналогично «владеет»-параметру среза), отдельная от простого
    // копирования POD-структуры; сделать её безопасно «на скорую руку»
    // рискованно (двойной вызов деструктора), поэтому явная ошибка вместо
    // тихой генерации некорректного/небезопасного кода.
    if (функция->тип_данных == ТОКЕН_СТРУКТУРА) {
        а->индекс_структуры_возврата = найти_индекс_структуры(а, &функция->имя_типа);
        if (а->индекс_структуры_возврата < 0) {
            char msg[256];
            snprintf(msg, sizeof(msg), "неизвестная структура «%.*s» в типе возврата",
                     (int)функция->имя_типа.длина, функция->имя_типа.начало);
            ошибка_семантики(а, &функция->имя_типа, msg);
            выйти_из_области(а);
            return;
        }
        if (структура_индекс_нужна_деструкция(а, а->индекс_структуры_возврата, 0)) {
            ошибка_семантики(а, &функция->имя_типа,
                "структуру, которой нужна деструкция (собственная или через "
                "поле), нельзя возвращать по значению; возвращайте через "
                "указательный параметр «Тип*»");
            выйти_из_области(а);
            return;
        }
    } else if (функция->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) {
        а->индекс_структуры_возврата = найти_индекс_перечисления(а, &функция->имя_типа);
        if (а->индекс_структуры_возврата < 0) {
            char msg[256];
            snprintf(msg, sizeof(msg), "неизвестное перечисление «%.*s» в типе возврата",
                     (int)функция->имя_типа.длина, функция->имя_типа.начало);
            ошибка_семантики(а, &функция->имя_типа, msg);
            выйти_из_области(а);
            return;
        }
    }

    а->число_изменяемых = 0;
    собрать_изменяемые(а, функция);

    if (функция->это_main) {
        связь_добавить(а, "аргументы", strlen("аргументы"),
                       "количество_аргументов", strlen("количество_аргументов"));
        объявить_символ(а, "количество_аргументов", strlen("количество_аргументов"),
                        ТОКЕН_ЦЕЛОЕ32, 0, 0, 0, 0, 0, 0, nullptr, 0, 0);
        объявить_символ(а, "аргументы", strlen("аргументы"),
                        ТОКЕН_ТИП_СИМВОЛ, 2, 0, 0, 0, 0, 0, nullptr, 0, 0);
        // «аргументы» (argv) живёт всё время работы программы — указатели из него
        // безопасно возвращать (borrow-провенанс).
        Символ *арг = найти_символ(а, "аргументы", strlen("аргументы"));
        if (арг) арг->безопасен_для_возврата = 1;
    }

    size_t n = узел_число_детей(функция);
    for (size_t i = 0; i + 1 < n; ++i) {
        Узел *пар = узел_ребёнок(функция, i);
        if (пар && пар->тип == УЗЕЛ_ПАРАМЕТР && пар->токен.длина > 0) {
            if (пар->владеет && !пар->срез) {
                ошибка_семантики(а, &пар->токен,
                    "«владеет» применимо только к параметру-срезу");
                выйти_из_области(а);
                return;
            }
            // Параметр по ссылке (ref/out): срезы пока не поддержаны (у них своя
            // move-семантика владения), сочетание с «владеет» бессмысленно.
            if (пар->по_ссылке && пар->срез) {
                ошибка_семантики(а, &пар->токен,
                    "«изменяемый»/«вывод» не применимы к срезу (у среза своя "
                    "семантика владения)");
                выйти_из_области(а);
                return;
            }
            объявить_символ(а,
                пар->токен.начало, пар->токен.длина,
                пар->тип_данных, пар->указатели, пар->массив,
                пар->беззнаковый, пар->срез, 0, 0, &пар->токен, 0, 0);
            Символ *с = найти_символ(а, пар->токен.начало, пар->токен.длина);
            // Параметр по ссылке: «изменяемый» приходит инициализированным
            // (вызывающий передал значение), «вывод» — нет (тело обязано присвоить).
            if (с && пар->по_ссылке) {
                с->по_ссылке = пар->по_ссылке;
                с->инициализирована = (пар->по_ссылке == 1) ? 1 : 0;
            }
            // Указатель-параметр указывает в память вызывающего → его безопасно
            // вернуть (borrow): память переживёт возврат, освобождать не нужно.
            if (с && пар->указатели > 0) {
                с->безопасен_для_возврата = 1;
                // «возможно<T*>»-параметр может быть нулевым → разыменование в
                // теле только под охранником. Обычный указатель-параметр ненулевой.
                с->возможно = пар->возможно;
                с->может_быть_нуль = пар->возможно ? 1 : 0;
            }
            // Параметр-структура: запоминаем её описание для доступа к полям.
            if (с && пар->тип_данных == ТОКЕН_СТРУКТУРА) {
                с->индекс_структуры = найти_индекс_структуры(а, &пар->имя_типа);
            }
            // Параметр-перечисление: сохраняем индекс enum-типа (для сравнений).
            if (с && пар->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) {
                с->индекс_структуры = найти_индекс_перечисления(а, &пар->имя_типа);
            }
            // Параметр-указатель на функцию: индекс псевдонима; параметр считается
            // не-нулевым (вызывающий передаёт функцию), вызов через него разрешён.
            if (с && пар->тип_данных == ТОКЕН_ФУНКУКАЗАТЕЛЬ) {
                с->индекс_структуры = найти_индекс_типфункции(а, &пар->имя_типа);
                с->может_быть_нуль = 0;
            }
            // «владеет»-параметр: функция забирает владение → освобождается
            // внутри (autofree), и его можно перемещать/возвращать.
            if (пар->владеет && с) {
                с->владеет_срезом = 1;
            }
        }
    }

    Узел *тело = nullptr;
    if (n > 0) {
        тело = узел_ребёнок(функция, n - 1);
        if (тело && тело->тип == УЗЕЛ_БЛОК)
            анализ_блока(а, тело);
        else
            тело = nullptr;
    }

    // Возврат значения по всем путям обязателен для любой non-«ничего» функции
    // (включая точка_входа): молчаливый fall-through в C возвращает мусор (UB).
    // Проверяем только если тело разобралось без ошибок — иначе неполный AST дал
    // бы ложную диагностику поверх настоящей.
    int тело_чистое = !а->диаг->ошибка
        && (!а->ошибки || а->ошибки->длина == ошибок_до);
    if (функция->тип_данных != ТОКЕН_НИЧЕГО && тело_чистое
        && !все_пути_возвращают(тело)) {
        char тип[64];
        описание_типа((ИнфоТипа){ функция->тип_данных, 0, функция->беззнаковый,
                                  функция->срез, а->индекс_структуры_возврата },
                      тип, sizeof(тип));
        char msg[256];
        snprintf(msg, sizeof(msg),
            "функция типа «%s» не возвращает значение по всем путям "
            "(добавьте «вернуть»)", тип);
        ошибка_семантики(а, &функция->токен, msg);
    }

    // «вывод»-параметры на fall-through пути в конце тела: если тело может
    // «выпасть» из функции (не гарантирует возврат по всем путям), out обязан
    // быть присвоен и на этом пути. Если все пути возвращают — они уже проверены
    // на каждом «вернуть», а конец тела недостижим.
    if (тело_чистое && тело && !все_пути_возвращают(тело)) {
        проверить_вывод_присвоен(а, &функция->токен);
    }

    выйти_из_области(а);
}

// Собирает сигнатуры всех функций до анализа тел, чтобы вызовы (в т.ч. вперёд)
// знали тип возврата — особенно для приёма возвращаемого среза.
static void собрать_сигнатуры(Анализатор *а, Узел *корень)
{
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип != УЗЕЛ_ФУНКЦИЯ || узел->это_main) continue;
        if (а->число_функций >= МАКС_ФУНКЦИЙ) break;
        if (узел->токен.длина == 0) continue;
        СигнатураФункции *с = &а->функции[а->число_функций++];
        size_t n = узел->токен.длина < МАКС_ИМЯ - 1 ? узел->токен.длина : МАКС_ИМЯ - 1;
        memcpy(с->имя, узел->токен.начало, n);
        с->имя[n] = '\0';
        с->тип_возврата = узел->тип_данных;
        с->беззнаковый = узел->беззнаковый;
        с->указатели_возврата = узел->указатели;  // косвенность типа возврата
        с->срез = узел->срез;
        с->индекс_структуры_возврата = -1;
        if (узел->тип_данных == ТОКЕН_СТРУКТУРА) {
            с->индекс_структуры_возврата = найти_индекс_структуры(а, &узел->имя_типа);
        } else if (узел->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) {
            с->индекс_структуры_возврата = найти_индекс_перечисления(а, &узел->имя_типа);
        }
        // Параметры (без тела — последнего ребёнка): отмечаем «владеет».
        с->число_параметров = 0;
        size_t всего = узел_число_детей(узел);
        for (size_t j = 0; j + 1 < всего; ++j) {
            Узел *пар = узел_ребёнок(узел, j);
            if (!пар || пар->тип != УЗЕЛ_ПАРАМЕТР) continue;
            if (с->число_параметров >= МАКС_ПАРАМЕТРОВ) break;
            с->владеет_параметра[с->число_параметров] = пар->владеет;
            с->параметры[с->число_параметров] = (ИнфоПараметра){
                пар->тип_данных, пар->беззнаковый, пар->указатели, пар->срез,
                пар->тип_данных == ТОКЕН_ФУНКУКАЗАТЕЛЬ
                    ? найти_индекс_типфункции(а, &пар->имя_типа) : -1,
                пар->по_ссылке
            };
            с->число_параметров++;
        }
    }
}

// ─── вывод чистоты функций ───────────────────────────────────────────────────
// Чистая функция не может создать гонку, поэтому её вызов разрешён в теле
// «параллельно для». Чистота ВЫВОДИТСЯ, а не объявляется: новое ключевое слово
// отняло бы имя у программиста, а вывод работает и на уже написанном коде.
//
// Вывод возможен только потому, что в языке НЕТ глобальных переменных
// (инвариант №4): единственные каналы наружу — «изменяемый»/«вывод», запись в
// срез-параметр (общие данные), указатель, «небезопасно» и вызов нечистой или
// внешней функции. Их полный перечень и даёт полноту анализа.

// Помечает нечистой; возвращает 1, если состояние изменилось (для fixpoint).
// Первая найденная причина побеждает — она же показывается пользователю.
static int пометить_нечистой(СигнатураФункции *с, ВидЭффекта вид,
                              const Токен *где, const Токен *имя)
{
    if (!с->чистая) return 0;
    с->чистая = 0;
    с->вид_эффекта = вид;
    if (где) с->где_эффект = *где;
    с->имя_эффекта[0] = '\0';
    if (имя) скопировать_имя_токена(с->имя_эффекта, имя);
    return 1;
}

// Прямые эффекты в теле — те, что видны без знания о вызываемых функциях.
static void искать_прямой_эффект(СигнатураФункции *с, Узел *у,
                                  const Токен **срез_пар, size_t число_срезов)
{
    if (!у || !с->чистая) return;
    // «параллельно для» внутри функции — сам по себе запуск потоков.
    if (у->тип == УЗЕЛ_ДЛЯ && у->параллельно) {
        пометить_нечистой(с, ЭФФЕКТ_ПОТОК, &у->токен, nullptr);
        return;
    }
    switch (у->тип) {
        case УЗЕЛ_НЕБЕЗОПАСНО:
            // За гейтом возможен сырой указатель — доказать отсутствие эффекта
            // нельзя, поэтому вся функция считается нечистой.
            пометить_нечистой(с, ЭФФЕКТ_НЕБЕЗОПАСНО, &у->токен, nullptr);
            return;
        case УЗЕЛ_ВЫЗОВ:
            if (токен_текст_равен(&у->токен, "выделить")
                || токен_текст_равен(&у->токен, "клон")) {
                пометить_нечистой(с, ЭФФЕКТ_ВЫДЕЛЕНИЕ, &у->токен, &у->токен);
                return;
            }
            if (токен_текст_равен(&у->токен, "запустить")
                || токен_текст_равен(&у->токен, "присоединить")) {
                пометить_нечистой(с, ЭФФЕКТ_ПОТОК, &у->токен, &у->токен);
                return;
            }
            break;
        case УЗЕЛ_ПРИСВАИВАНИЕ: {
            // Запись в срез-параметр — это запись в данные вызывающего, общие
            // для всех потоков: непересечение индексов здесь не доказать.
            Узел *цель = узел_ребёнок(у, 0);
            if (цель && цель->тип == УЗЕЛ_ИНДЕКС) {
                // У старой формы «с[и]» имя базы лежит в ТОКЕНЕ узла-индекса,
                // у новой — в дочернем идентификаторе.
                const Токен *база = &цель->токен;
                if (узел_число_детей(цель) == 2) {
                    Узел *б = узел_ребёнок(цель, 0);
                    if (б && б->тип == УЗЕЛ_ИДЕНТИФИКАТОР) база = &б->токен;
                }
                for (size_t i = 0; i < число_срезов; ++i) {
                    if (токены_равны_(база, срез_пар[i])) {
                        пометить_нечистой(с, ЭФФЕКТ_ЗАПИСЬ_В_СРЕЗ, база, база);
                        return;
                    }
                }
            }
            break;
        }
        default: break;
    }
    for (size_t i = 0; i < узел_число_детей(у); ++i)
        искать_прямой_эффект(с, узел_ребёнок(у, i), срез_пар, число_срезов);
}

// Эффекты через вызовы. Возвращает 1, если что-то пометили (шаг fixpoint).
static int искать_эффект_вызовов(Анализатор *а, СигнатураФункции *с, Узел *у)
{
    if (!у || !с->чистая) return 0;
    if (у->тип == УЗЕЛ_ВЫЗОВ && !токен_текст_равен(&у->токен, "длина")) {
        СигнатураФункции *в = найти_сигнатуру_по_имени(а, у->токен.начало,
                                                        у->токен.длина);
        if (!в) {
            // Внешняя/libc (в т.ч. printf) — об эффектах ничего не известно.
            return пометить_нечистой(с, ЭФФЕКТ_ВНЕШНЯЯ, &у->токен, &у->токен);
        }
        if (!в->чистая) {
            return пометить_нечистой(с, ЭФФЕКТ_ВЫЗОВ_НЕЧИСТОЙ, &у->токен,
                                     &у->токен);
        }
    }
    int изменилось = 0;
    for (size_t i = 0; i < узел_число_детей(у); ++i)
        изменилось |= искать_эффект_вызовов(а, с, узел_ребёнок(у, i));
    return изменилось;
}

// Тело функции — последний ребёнок узла (после типопараметров и параметров).
static Узел *тело_функции(Узел *функция)
{
    size_t n = узел_число_детей(функция);
    return n > 0 ? узел_ребёнок(функция, n - 1) : nullptr;
}

// Заполняет «чистая» у всех сигнатур. Оптимистичный fixpoint: сперва все
// чистые, затем помечаем нечистыми, пока есть изменения. Так само- и взаимно
// рекурсивные чистые функции остаются чистыми (наивный обход зациклился бы
// или объявил их нечистыми).
static void вывести_чистоту(Анализатор *а, Узел *корень)
{
    int есть_тело[МАКС_ФУНКЦИЙ] = { 0 };

    for (size_t i = 0; i < а->число_функций; ++i) {
        а->функции[i].чистая = 1;
        а->функции[i].вид_эффекта = ЭФФЕКТ_НЕТ;
        а->функции[i].имя_эффекта[0] = '\0';
    }

    // Прямые эффекты: сигнатура + тело.
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип != УЗЕЛ_ФУНКЦИЯ || узел->это_main) continue;
        СигнатураФункции *с = найти_сигнатуру_по_имени(а, узел->токен.начало,
                                                        узел->токен.длина);
        if (!с) continue;
        есть_тело[с - а->функции] = 1;

        const Токен *срез_пар[МАКС_ПАРАМЕТРОВ];
        size_t число_срезов = 0;
        size_t всего = узел_число_детей(узел);
        for (size_t j = 0; j + 1 < всего; ++j) {
            Узел *пар = узел_ребёнок(узел, j);
            if (!пар || пар->тип != УЗЕЛ_ПАРАМЕТР) continue;
            if (пар->по_ссылке) {
                // «изменяемый»/«вывод» — запись в кадр вызывающего.
                пометить_нечистой(с, ЭФФЕКТ_ССЫЛОЧНЫЙ_ПАРАМЕТР, &пар->токен,
                                  &пар->токен);
                break;
            }
            if (пар->указатели > 0) {
                пометить_нечистой(с, ЭФФЕКТ_УКАЗАТЕЛЬ_ПАРАМЕТР, &пар->токен,
                                  &пар->токен);
                break;
            }
            if (пар->владеет) {
                // Потребление среза — эффект: в общих данных дало бы double-free.
                пометить_нечистой(с, ЭФФЕКТ_ВЛАДЕЕТ_ПАРАМЕТР, &пар->токен,
                                  &пар->токен);
                break;
            }
            if (пар->срез && число_срезов < МАКС_ПАРАМЕТРОВ)
                срез_пар[число_срезов++] = &пар->токен;
        }
        if (с->чистая)
            искать_прямой_эффект(с, тело_функции(узел), срез_пар, число_срезов);
    }

    // Сигнатура без тела (из интерфейса библиотеки `.и`) — тело не проверить,
    // формат `.и` чистоту не переносит, поэтому консервативно нечистая.
    for (size_t i = 0; i < а->число_функций; ++i)
        if (!есть_тело[i])
            пометить_нечистой(&а->функции[i], ЭФФЕКТ_ВНЕШНЯЯ, nullptr, nullptr);

    // Fixpoint по вызовам: нечистота распространяется вверх, пока не стабильно.
    // Итераций не больше числа функций — каждая проходка метит хотя бы одну.
    for (size_t шаг = 0; шаг <= а->число_функций; ++шаг) {
        int изменилось = 0;
        for (size_t i = 0; i < узел_число_детей(корень); ++i) {
            Узел *узел = узел_ребёнок(корень, i);
            if (узел->тип != УЗЕЛ_ФУНКЦИЯ || узел->это_main) continue;
            СигнатураФункции *с = найти_сигнатуру_по_имени(а, узел->токен.начало,
                                                            узел->токен.длина);
            if (!с || !с->чистая) continue;
            изменилось |= искать_эффект_вызовов(а, с, тело_функции(узел));
        }
        if (!изменилось) break;
    }
}

// Собирает описания структур (имена и поля) до анализа функций.
static void собрать_перечисления(Анализатор *а, Узел *корень)
{
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип != УЗЕЛ_ПЕРЕЧИСЛЕНИЕ || узел->токен.длина == 0) continue;
        if (найти_индекс_перечисления(а, &узел->токен) >= 0) {
            ошибка_семантики(а, &узел->токен,
                "перечисление с таким именем уже объявлено");
            return;
        }
        if (а->число_перечислений >= МАКС_ПЕРЕЧИСЛЕНИЙ) break;
        ОписаниеПеречисления *п = &а->перечисления[а->число_перечислений];
        size_t n = узел->токен.длина < МАКС_ИМЯ - 1 ? узел->токен.длина : МАКС_ИМЯ - 1;
        memcpy(п->имя, узел->токен.начало, n);
        п->имя[n] = '\0';
        п->число_вариантов = 0;
        п->это_sum = 0;
        // Регистрируем до заполнения вариантов, чтобы «B = A + 1» мог сослаться
        // на предыдущий вариант этого же перечисления через свёртку констант.
        а->число_перечислений++;
        long текущее = 0;
        for (size_t j = 0; j < узел_число_детей(узел); ++j) {
            Узел *вар = узел_ребёнок(узел, j);
            if (!вар || вар->тип != УЗЕЛ_ВАРИАНТ) continue;
            if (найти_вариант(а, вар->токен.начало, вар->токен.длина, nullptr) >= 0) {
                ошибка_семантики(а, &вар->токен,
                    "вариант перечисления с таким именем уже объявлен");
                return;
            }
            if (п->число_вариантов >= МАКС_ВАРИАНТОВ) break;
            ВариантПеречисления *в = &п->варианты[п->число_вариантов++];
            в->число_полей = 0;
            size_t m = вар->токен.длина < МАКС_ИМЯ - 1 ? вар->токен.длина : МАКС_ИМЯ - 1;
            memcpy(в->имя, вар->токен.начало, m);
            в->имя[m] = '\0';
            // Полезная нагрузка sum-типа: дети-УЗЕЛ_ПАРАМЕТР. Иначе (ребёнок-
            // выражение) — явное «= значение» C-стиля enum.
            Узел *перв = узел_ребёнок(вар, 0);
            if (перв && перв->тип == УЗЕЛ_ПАРАМЕТР) {
                п->это_sum = 1;
                for (size_t k = 0; k < узел_число_детей(вар); ++k) {
                    Узел *пл = узел_ребёнок(вар, k);
                    if (!пл || пл->тип != УЗЕЛ_ПАРАМЕТР) continue;
                    // Ограничения v1: без срезов (владение) и без ref/out.
                    if (пл->срез) {
                        ошибка_семантики(а, &пл->токен,
                            "поле-срез в варианте sum-типа запрещено (владение "
                            "неоднозначно); используйте примитивы или указатели");
                        return;
                    }
                    if (пл->по_ссылке || пл->владеет) {
                        ошибка_семантики(а, &пл->токен,
                            "модификаторы «владеет»/«изменяемый»/«вывод» неприменимы "
                            "к полю варианта");
                        return;
                    }
                    if (в->число_полей >= МАКС_ПОЛЕЙ_ВАРИАНТА) break;
                    ПолеВарианта *пв = &в->поля[в->число_полей++];
                    size_t im = пл->токен.длина < МАКС_ИМЯ - 1 ? пл->токен.длина : МАКС_ИМЯ - 1;
                    memcpy(пв->имя, пл->токен.начало, im);
                    пв->имя[im] = '\0';
                    пв->тип = пл->тип_данных;
                    пв->беззнаковый = пл->беззнаковый;
                    пв->указатели = пл->указатели;
                    пв->индекс_структуры =
                        (пл->тип_данных == ТОКЕН_СТРУКТУРА)
                            ? найти_индекс_структуры(а, &пл->имя_типа)
                        : (пл->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ)
                            ? найти_индекс_перечисления(а, &пл->имя_типа)
                        : -1;
                }
            } else if (перв) {
                // Явное значение «= выражение» → константа (только C-стиль enum).
                long зн;
                if (!вычислить_константу(а, перв, &зн)) {
                    ошибка_семантики(а, &вар->токен,
                        "значение варианта перечисления должно быть константой");
                    return;
                }
                текущее = зн;
            }
            в->значение = текущее;
            текущее++;
        }
    }
}

// Регистрирует именованные типы-указатели на функцию (сигнатуры для сверки).
static void собрать_типыфункций(Анализатор *а, Узел *корень)
{
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип != УЗЕЛ_ТИПФУНКЦИИ || узел->токен.длина == 0) continue;
        if (найти_индекс_типфункции(а, &узел->токен) >= 0) {
            ошибка_семантики(а, &узел->токен,
                "тип-указатель на функцию с таким именем уже объявлен");
            return;
        }
        if (а->число_типовфункций >= МАКС_ТИПФУНКЦИЙ) break;
        ОписаниеТипфункции *т = &а->типыфункций[а->число_типовфункций++];
        size_t n = узел->токен.длина < МАКС_ИМЯ - 1 ? узел->токен.длина : МАКС_ИМЯ - 1;
        memcpy(т->имя, узел->токен.начало, n);
        т->имя[n] = '\0';
        т->тип_возврата = узел->тип_данных;
        т->беззнаковый_возврата = узел->беззнаковый;
        т->указатели_возврата = узел->указатели;
        т->индекс_структуры_возврата =
            (узел->тип_данных == ТОКЕН_СТРУКТУРА)
                ? найти_индекс_структуры(а, &узел->имя_типа)
            : (узел->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ)
                ? найти_индекс_перечисления(а, &узел->имя_типа)
            : -1;
        т->число_параметров = 0;
        for (size_t j = 0; j < узел_число_детей(узел); ++j) {
            Узел *п = узел_ребёнок(узел, j);
            if (!п || п->тип != УЗЕЛ_ПАРАМЕТР) continue;
            if (т->число_параметров >= МАКС_ПАРАМЕТРОВ) break;
            ИнфоПараметра *ип = &т->параметры[т->число_параметров++];
            ип->тип = п->тип_данных;
            ип->беззнаковый = п->беззнаковый;
            ип->указатели = п->указатели;
            ип->срез = п->срез;
        }
    }
}

static void собрать_структуры(Анализатор *а, Узел *корень)
{
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип != УЗЕЛ_СТРУКТУРА || узел->токен.длина == 0) continue;
        if (найти_индекс_структуры(а, &узел->токен) >= 0) {
            ошибка_семантики(а, &узел->токен, "структура с таким именем уже объявлена");
            return;
        }
        if (а->число_структур >= МАКС_СТРУКТУР) break;
        ОписаниеСтруктуры *с = &а->структуры[а->число_структур];
        size_t n = узел->токен.длина < МАКС_ИМЯ - 1 ? узел->токен.длина : МАКС_ИМЯ - 1;
        memcpy(с->имя, узел->токен.начало, n);
        с->имя[n] = '\0';
        с->число_полей = 0;
        с->это_союз = узел->это_союз;
        // Деструктор из «[удалить = …]» (хранится в имя_типа узла структуры).
        с->есть_деструктор = узел->имя_типа.длина > 0;
        if (с->есть_деструктор) {
            size_t d = узел->имя_типа.длина < МАКС_ИМЯ - 1
                ? узел->имя_типа.длина : МАКС_ИМЯ - 1;
            memcpy(с->деструктор, узел->имя_типа.начало, d);
            с->деструктор[d] = '\0';
        }
        for (size_t j = 0; j < узел_число_детей(узел); ++j) {
            Узел *поле = узел_ребёнок(узел, j);
            if (!поле || поле->тип != УЗЕЛ_ПАРАМЕТР) continue;
            if (с->число_полей >= МАКС_ПОЛЕЙ) break;
            // Безопасность союза: поле-срез владело бы памятью, но какой член
            // активен — неизвестно, поэтому autofree/деструкция союза
            // невозможны без риска утечки/двойного free. Запрещаем.
            if (узел->это_союз && поле->срез) {
                ошибка_семантики(а, &поле->токен,
                    "поле-срез в союзе запрещено: владение памятью в союзе "
                    "небезопасно (неизвестно, какой член активен)");
            }
            ПолеСтруктуры *п = &с->поля[с->число_полей++];
            size_t m = поле->токен.длина < МАКС_ИМЯ - 1 ? поле->токен.длина : МАКС_ИМЯ - 1;
            memcpy(п->имя, поле->токен.начало, m);
            п->имя[m] = '\0';
            п->тип = поле->тип_данных;
            п->беззнаковый = поле->беззнаковый;
            п->указатели = поле->указатели;
            п->срез = поле->срез;
            п->индекс_структуры =
                (поле->тип_данных == ТОКЕН_СТРУКТУРА)
                    ? найти_индекс_структуры(а, &поле->имя_типа)
                : (поле->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ)
                    ? найти_индекс_перечисления(а, &поле->имя_типа)
                : -1;
        }
        а->число_структур++;
    }
}

