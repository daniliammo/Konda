// Доказанно-безопасный диапазон индекса цикла «для (и=c0; …; и=и+ш)», c0≥0,
// ш>0. Верхняя граница задаётся одной из двух форм условия:
//   режим среза:   «и < длина(с)»  → безопасен доступ именно к «с» (его длине);
//   режим предела: «и < N» (конст) → безопасен доступ к любому массиву/срезу
//                                    с известной длиной ≥ N.
typedef struct {
    ИмяСсылка индекс;  // переменная-индекс цикла
    ИмяСсылка срез;    // режим среза: имя индексируемой переменной
    int есть_срез;     // 1 — активен режим среза
    long предел;       // режим предела: верхняя граница N
    int есть_предел;   // 1 — активен режим предела
} БезопаснаяПара;

