static void войти_в_область(Анализатор *а) { а->глубина++; }

static void выйти_из_области(Анализатор *а)
{
    while (а->число_символов > 0
           && а->символы[а->число_символов - 1].глубина >= а->глубина) {
        а->число_символов--;
    }
    if (а->глубина > 0) а->глубина--;
}

static Символ *найти_символ(Анализатор *а, const char *имя, size_t длина_имени)
{
    for (size_t i = а->число_символов; i > 0; --i) {
        Символ *с = &а->символы[i - 1];
        if (байты_равны(с->имя, strlen(с->имя), имя, длина_имени))
            return с;
    }
    return nullptr;
}

// Указатель-идентификатор ссылается на строковый литерал (провенанс .rodata)?
static int указатель_из_литерала(Анализатор *а, const Узел *узел)
{
    if (!узел || узел->тип != УЗЕЛ_ИДЕНТИФИКАТОР) return 0;
    Символ *с = найти_символ(а, узел->токен.начало, узел->токен.длина);
    return с && с->из_литерала;
}

// Указатель-идентификатор имеет borrow-провенанс, безопасный для возврата?
static int указатель_безопасен_для_возврата(Анализатор *а, const Узел *узел)
{
    if (!узел || узел->тип != УЗЕЛ_ИДЕНТИФИКАТОР) return 0;
    Символ *с = найти_символ(а, узел->токен.начало, узел->токен.длина);
    return с && с->безопасен_для_возврата;
}

// Можно ли безопасно вернуть это выражение как указатель (borrow)? Разрешены
// только источники, чья память переживёт возврат и принадлежит вызывающему/
// статике: «нуль», строковый литерал, borrow-безопасный указатель-переменная
// (параметр/argv/литерал или их копия), индексация такого указателя/среза
// (напр. «аргументы[i]»). Всё прочее — локальные данные, результаты C-функций,
// арифметика — отклоняется (возможен висячий указатель / ручное управление).
static int выражение_безопасно_для_возврата(Анализатор *а, const Узел *выр)
{
    if (!выр) return 0;
    switch (выр->тип) {
        case УЗЕЛ_НУЛЬ:
        case УЗЕЛ_СТРОКА:
            return 1;
        case УЗЕЛ_ИДЕНТИФИКАТОР: {
            Символ *с = найти_символ(а, выр->токен.начало, выр->токен.длина);
            return с && !с->массив && с->безопасен_для_возврата;
        }
        case УЗЕЛ_ИНДЕКС: {
            // Индексация borrow-безопасного указателя/среза (память вызывающего).
            Символ *с = найти_символ(а, выр->токен.начало, выр->токен.длина);
            return с && с->безопасен_для_возврата && (с->указатели > 0 || с->срез);
        }
        default:
            return 0;
    }
}

static void объявить_символ(Анализатор *а,
                             const char *имя, size_t длина_имени,
                             ТипТокена тип, int указатели, int массив,
                             int беззнаковый, int срез,
                             int длина_известна, long длина,
                             const Токен *место,
                             int значение_известно, long значение)
{
    for (size_t i = а->число_символов; i > 0; --i) {
        Символ *с = &а->символы[i - 1];
        if (с->глубина != а->глубина) break;
        if (байты_равны(с->имя, strlen(с->имя), имя, длина_имени)) {
            char msg[256];
            snprintf(msg, sizeof(msg),
                     "переменная «%.*s» уже объявлена в этой области",
                     (int)длина_имени, имя);
            ошибка_семантики(а, место, msg);
            return;
        }
    }
    if (а->число_символов >= МАКС_СИМВОЛОВ) return;
    Символ *с = &а->символы[а->число_символов++];
    size_t n = длина_имени < МАКС_ИМЯ - 1 ? длина_имени : МАКС_ИМЯ - 1;
    memcpy(с->имя, имя, n);
    с->имя[n] = '\0';
    с->тип = тип;
    с->указатели = указатели;
    с->массив = массив;
    с->беззнаковый = беззнаковый;
    с->срез = срез;
    с->владеет_срезом = 0;  // уточняется вызывающим после объявления
    с->перемещён = 0;
    с->токен_move = (Токен){0};
    с->индекс_структуры = -1;  // уточняется вызывающим для типа-структуры
    с->длина_известна = длина_известна;
    с->длина = длина;
    с->глубина = а->глубина;
    с->значение_известно = значение_известно;
    с->значение = значение;
    с->может_быть_нуль = 0;
    с->инициализирована = 1;  // по умолчанию; скаляр без init выставит 0
    с->из_литерала = 0;
}

static int переменная_изменяемая(const Анализатор *а,
                                  const char *имя, size_t длина_имени)
{
    for (size_t i = 0; i < а->число_изменяемых; ++i) {
        if (байты_равны(а->изменяемые[i].начало, а->изменяемые[i].длина,
                        имя, длина_имени))
            return 1;
    }
    return 0;
}

