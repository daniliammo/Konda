// Инъекция сигнатур из Konda-интерфейса библиотеки (безопасный путь 2): каждую
// экспортируемую функцию библиотеки добавляем в таблицу сигнатур как обычную
// известную функцию — вызовы к ней получают полную типо- и владение-проверку
// вместо непрозрачной ветки внешнего C. Локальные определения уже добавлены
// (собрать_сигнатуры) и имеют приоритет при поиске.
static void внедрить_интерфейс_сигнатуры(Анализатор *а,
                                         const ТаблицаИнтерфейса *интерфейсы)
{
    if (!интерфейсы) return;
    size_t m = интерфейс_число(интерфейсы);
    for (size_t i = 0; i < m; ++i) {
        const ИнтФункция *иф = интерфейс_получить(интерфейсы, i);
        if (!иф || а->число_функций >= МАКС_ФУНКЦИЙ) break;
        СигнатураФункции *с = &а->функции[а->число_функций++];
        snprintf(с->имя, sizeof(с->имя), "%s", иф->имя);
        с->тип_возврата = иф->тип_возврата;
        с->беззнаковый = иф->беззнаковый_возврата;
        с->указатели_возврата = иф->указатели_возврата;
        с->срез = иф->срез_возврата;
        с->индекс_структуры_возврата = -1;
        с->число_параметров = 0;
        for (size_t j = 0; j < иф->число_параметров && с->число_параметров < МАКС_ПАРАМЕТРОВ; ++j) {
            const ИнтПараметр *п = &иф->параметры[j];
            с->владеет_параметра[с->число_параметров] = п->владеет;
            с->параметры[с->число_параметров] = (ИнфоПараметра){
                п->тип, п->беззнаковый, п->указатели, п->срез, -1, п->по_ссылке
            };
            с->число_параметров++;
        }
    }
}

int анализировать_полный_и(Узел *корень, Диагностика *диаг,
                           const ТаблицаВнешних *внешние,
                           const ТаблицаИнтерфейса *интерфейсы,
                           ДинМассив *предупреждения, ДинМассив *ошибки)
{
    Анализатор а = { .диаг = диаг, .внешние = внешние,
                     .предупреждения = предупреждения, .ошибки = ошибки };
    собрать_перечисления(&а, корень);
    собрать_структуры(&а, корень);
    собрать_типыфункций(&а, корень);
    собрать_сигнатуры(&а, корень);
    внедрить_интерфейс_сигнатуры(&а, интерфейсы);
    // После обеих точек заполнения таблицы: вывод чистоты должен видеть и
    // библиотечные сигнатуры (у них нет тела → нечистые).
    вывести_чистоту(&а, корень);
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        // Не-батч режим: стоп на первой ошибке. Батч-режим: продолжаем, ошибки
        // накоплены в списке (диаг->ошибка сбрасывается на границах).
        if (диаг->ошибка && !ошибки) break;
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип != УЗЕЛ_ФУНКЦИЯ) continue;

        анализ_функции(&а, узел);

        if (диаг->ошибка && ошибки) собрать_ошибку(&а);
        // Сброс «рабочего» состояния к базовой линии перед следующей функцией:
        // даже если ошибка внутри функции оставила стек областей несбаланси-
        // рованным, следующая функция начинает с чистого листа (глобальное —
        // структуры/перечисления/сигнатуры — не трогаем, оно собрано выше).
        if (ошибки) {
            а.число_символов = 0;
            а.глубина = 0;
            а.глубина_цикла = 0;
            а.число_безопасных = 0;
            а.число_связей = 0;
            а.глубина_ограничений = 0;
            а.число_изменяемых = 0;
        }
    }
    // В батч-режиме успех определяется пустотой списка ошибок (диаг->ошибка мог
    // быть сброшен при сборе). В обычном — флагом диагностики.
    if (ошибки) return ошибки->длина == 0;
    return !диаг->ошибка;
}

int анализировать_полный(Узел *корень, Диагностика *диаг,
                         const ТаблицаВнешних *внешние,
                         ДинМассив *предупреждения, ДинМассив *ошибки)
{
    return анализировать_полный_и(корень, диаг, внешние, nullptr,
                                  предупреждения, ошибки);
}

int анализировать(Узел *корень, Диагностика *диаг)
{
    return анализировать_полный(корень, диаг, nullptr, nullptr, nullptr);
}

