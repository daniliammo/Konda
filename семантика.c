    СигнатураФункции функции[МАКС_ФУНКЦИЙ];
    size_t число_функций;
    // Объявленные пользовательские структуры.
    ОписаниеСтруктуры структуры[МАКС_СТРУКТУР];
    size_t число_структур;
    ОписаниеТипфункции типыфункций[МАКС_ТИПФУНКЦИЙ];
    size_t число_типовфункций;
    // Объявленные перечисления.
    ОписаниеПеречисления перечисления[МАКС_ПЕРЕЧИСЛЕНИЙ];
    size_t число_перечислений;
    // Внешние (C) функции, увиденные через заголовки. nullptr — заголовки не
    // сканировались (старый режим): тогда неизвестные вызовы не диагностируются
