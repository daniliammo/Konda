// ─── безопасность потоков: модель «передача владения» ────────────────────────
//
// Гонки данных невозможны ПО ПОСТРОЕНИЮ: поток получает данные только по
// значению (копия) или перемещением владения (у запускающего доступа больше
// нет — ловит существующий move-анализ). Разделяемое изменяемое состояние
// просто нельзя выразить, поэтому синхронизация не нужна, а проверки —
// статические (нулевая рантайм-цена, инвариант №1).
//
// Здесь проверяется сама СИГНАТУРА функции потока: она не должна принимать
// ничего, что является ссылкой в кадр запускающего или заимствованием.
static int проверить_сигнатуру_потока(Анализатор *а, const СигнатураФункции *сиг,
                                       const Токен *где)
{
    for (size_t i = 0; i < сиг->число_параметров; ++i) {
        const ИнфоПараметра *п = &сиг->параметры[i];
        char msg[512];
        // «изменяемый»/«вывод» — указатель в кадр вызывающего. Поток пережил бы
        // кадр → висячий указатель И гонка за ту же переменную.
        if (п->по_ссылке) {
            snprintf(msg, sizeof msg,
                "функция потока «%s» не может иметь параметр «%s» — это ссылка "
                "в кадр запускающего (висячий указатель и гонка); передавайте "
                "по значению или перемещением «владеет срез<T>»",
                сиг->имя, п->по_ссылке == 2 ? "вывод" : "изменяемый");
            ошибка_семантики(а, где, msg);
            return 0;
        }
        // Срез без «владеет» — заимствование: запускающий сохранил бы доступ к
        // той же памяти → гонка и возможный use-after-free.
        if (п->срез && !сиг->владеет_параметра[i]) {
            snprintf(msg, sizeof msg,
                "функция потока «%s» принимает срез без «владеет» — запускающий "
                "сохранит доступ к той же памяти (гонка); объявите параметр как "
                "«владеет срез<T>», чтобы владение перешло потоку",
                сиг->имя);
            ошибка_семантики(а, где, msg);
            return 0;
        }
        // Сырой указатель — заимствование чужой памяти с той же проблемой.
        if (п->указатели > 0) {
            snprintf(msg, sizeof msg,
                "функция потока «%s» принимает указатель — это заимствование, "
                "которое поток может пережить (висячий указатель и гонка); "
                "передавайте по значению или «владеет срез<T>»",
                сиг->имя);
            ошибка_семантики(а, где, msg);
            return 0;
        }
    }
    return 1;
}

