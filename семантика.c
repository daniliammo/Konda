static int байты_равны(const char *a, size_t la, const char *b, size_t lb)
{
    return la == lb && memcmp(a, b, la) == 0;
}

static int токен_текст_равен(const Токен *t, const char *текст)
{
    return байты_равны(t->начало, t->длина, текст, strlen(текст));
}

// Закрытый список функций libc с неограниченной записью в буфер/разбором
// формата: классические источники переполнения буфера. Их вызов из безопасного
// кода запрещён — только в «небезопасно { }». Проверка чисто статическая
// (нулевая рантайм-цена); список намеренно узкий (безусловно опасные функции),
// чтобы не мешать обычному interop (printf, strlen, malloc и т.п. — разрешены).
static int libc_небезопасна(const char *имя, size_t длина)
{
    static const char *const список[] = {
        "gets",              // читает stdin без границ буфера
        "strcpy",  "strcat", // копирование/конкатенация строк без границ
        "stpcpy",
        "sprintf", "vsprintf", // форматирование в буфер без границ
        "wcscpy",  "wcscat", // широкие аналоги
    };
    for (size_t i = 0; i < sizeof(список) / sizeof(список[0]); ++i) {
        if (байты_равны(имя, длина, список[i], strlen(список[i]))) return 1;
    }
    return 0;
}

