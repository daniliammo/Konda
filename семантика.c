// Запись таблицы символов.
typedef struct {
    char имя[МАКС_ИМЯ];
    ТипТокена тип;
    int указатели;
    int массив;
    int беззнаковый;
    int срез;               // переменная — срез<T>
    int владеет_срезом;     // срез владеет памятью (из «выделить» или move),
                            // а не заимствует (параметр функции)
    int перемещён;          // владение ушло (move): использование — ошибка
    Токен токен_move;       // место, где произошёл move (для диагностик
                            // use-after-move и для path-merge владения)
    int индекс_структуры;   // для типа-структуры: индекс в таблице структур, иначе -1
    int длина_известна;     // 1, если длина среза известна на этапе компиляции
    long длина;             // длина среза, если длина_известна
    // «срез<T> с = выделить(н)»: длина среза РАВНА значению переменной «н».
    // Пока ни «с», ни «н» не переприсваивались, «и < н» доказывает попадание в
    // границы ровно так же, как «и < длина(с)» → проверку можно снять.
    int длина_из_переменной;
    Токен длина_токен;      // имя «н» (валидно при длина_из_переменной)
    int поток;              // переменная — поток<T> (дескриптор); «присоединить»
                            // потребляет его (перемещён=1 → двойной join ловится)
    int глубина;
    int значение_известно;
    long значение;
    // Указатель может быть нулём на каком-то пути исполнения: объявлен «= нуль»,
    // без инициализатора, переприсвоен «нуль», либо после слияния ветвей, где
    // хотя бы на одном пути он остаётся нулевым. Индексирование/разыменование
    // такого указателя — ошибка компиляции (нулевое разыменование). Поле
    // путь-чувствительно: сливается по «нуль на любом пути → нуль».
    int может_быть_нуль;
    // Скалярная переменная инициализирована (присвоена) к моменту чтения.
    // 0 только для скаляра, объявленного без инициализатора и пока не
    // присвоенного; чтение такого — ошибка (использование до инициализации).
    int инициализирована;
    // Указатель инициализирован строковым литералом (или скопирован из такого).
    // Строковый литерал лежит в .rodata — запись через такой указатель
    // (`с[i] = …`) недопустима (порча/крах). Проверяется статически.
    int из_литерала;
    // Указатель безопасно вернуть из функции (borrow-провенанс): значение
    // указывает в память, которой владеет ВЫЗЫВАЮЩИЙ (параметр-указатель,
    // «аргументы»), в статику (строковый литерал) или «нуль» — то есть память
    // переживёт возврат и не требует освобождения вызывающим. По умолчанию 0
    // (небезопасно): значения неизвестного/локального происхождения и результаты
    // C-функций вернуть нельзя — так исключены висячие указатели и ручное
    // управление памятью. Проставляется только для доказанно-безопасных источников.
    int безопасен_для_возврата;
    // Параметр по ссылке: 0 — нет; 1 — «изменяемый» (ref); 2 — «вывод» (out).
    // Для out проверяется definite-assignment: присвоен до каждого возврата.
    int по_ссылке;
    // «конст»: переменная иммутабельна — переприсваивание и передача в
    // «изменяемый»/«вывод» запрещены (проверка статическая, нулевая рантайм-цена).
    int неизменяемый;
    // Указатель объявлен как «возможно<T*>» (nullable). Без этого флага указатель
    // ненулевой по умолчанию: «нуль» ему присвоить нельзя, разыменование не
    // требует охранника. Для «возможно» разыменование допустимо только под
    // проверкой на «нуль» (через путь-чувствительный может_быть_нуль).
    int возможно;
} Символ;

typedef struct {
    const char *начало;
    size_t длина;
} ИмяСсылка;

// Доказанно-безопасный диапазон индекса цикла «для (и=c0; …; и=и+ш)», c0≥0,
// ш>0. Верхняя граница задаётся одной из двух форм условия:
//   режим среза:   «и < длина(с)»  → безопасен доступ именно к «с» (его длине);
//   режим предела: «и < N» (конст) → безопасен доступ к любому массиву/срезу
//                                    с известной длиной ≥ N;
//   режим переменной: «и < н»      → безопасен доступ к срезу, созданному как
//                                    «выделить(н)», пока ни он, ни «н» не
//                                    переприсваивались (длина(с) == н).
typedef struct {
    ИмяСсылка индекс;  // переменная-индекс цикла
    ИмяСсылка срез;    // режим среза: имя индексируемой переменной
    int есть_срез;     // 1 — активен режим среза
    long предел;       // режим предела: верхняя граница N
    int есть_предел;   // 1 — активен режим предела
    ИмяСсылка перем;   // режим переменной: имя «н» из условия «и < н»
    int есть_перем;    // 1 — активен режим переменной
} БезопаснаяПара;

