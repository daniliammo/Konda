#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "аст.h"

#define МАКС_ИМЯ 128
#define МАКС_СВЯЗЕЙ_МАССИВОВ 32
#define МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ 64
#define МАКС_СИМВОЛОВ 256

typedef struct {
    char массив[МАКС_ИМЯ];
    char длина[МАКС_ИМЯ];
} СвязьМассива;

typedef struct {
    char переменная[МАКС_ИМЯ];
    long минимальная_длина;
    int известна;
} ОграничениеДлины;

// Полная информация о типе (как в C: базовый тип + косвенность + знаковость).
typedef struct {
    ТипТокена тип;      // ТОКЕН_ЦЕЛОЕ32 и т.д.; ТОКЕН_НЕИЗВЕСТНЫЙ = «не знаем»
    int указатели;      // число уровней «*»
    int беззнаковый;    // префикс «неподписанный»
    int срез;           // тип «срез<T>»; тип = тип элемента T
} ИнфоТипа;

// Специальный маркер для nullptr: совместим с любым указателем.
#define ТОКЕН_НУЛ_УКАЗАТЕЛЬ ТОКЕН_НУЛЬ

static ИнфоТипа ТИП_НЕИЗВЕСТЕН_V = { ТОКЕН_НЕИЗВЕСТНЫЙ, 0, 0, 0 };
static ИнфоТипа ТИП_ЦЕЛОЕ32_V    = { ТОКЕН_ЦЕЛОЕ32,     0, 0, 0 };

// Запись таблицы символов.
typedef struct {
    char имя[МАКС_ИМЯ];
    ТипТокена тип;
    int указатели;
    int массив;
    int беззнаковый;
    int срез;               // переменная — срез<T>
    int владеет_срезом;     // срез владеет памятью (из «выделить» или move),
                            // а не заимствует (параметр функции)
    int перемещён;          // владение ушло (move): использование — ошибка
    int длина_известна;     // 1, если длина среза известна на этапе компиляции
    long длина;             // длина среза, если длина_известна
    int глубина;
    int значение_известно;
    long значение;
} Символ;

typedef struct {
    const char *начало;
    size_t длина;
} ИмяСсылка;

// Доказанно-безопасный диапазон индекса цикла «для (и=c0; …; и=и+ш)», c0≥0,
// ш>0. Верхняя граница задаётся одной из двух форм условия:
//   режим среза:   «и < длина(с)»  → безопасен доступ именно к «с» (его длине);
//   режим предела: «и < N» (конст) → безопасен доступ к любому массиву/срезу
//                                    с известной длиной ≥ N.
typedef struct {
    ИмяСсылка индекс;  // переменная-индекс цикла
    ИмяСсылка срез;    // режим среза: имя индексируемой переменной
    int есть_срез;     // 1 — активен режим среза
    long предел;       // режим предела: верхняя граница N
    int есть_предел;   // 1 — активен режим предела
} БезопаснаяПара;

#define МАКС_БЕЗОПАСНЫХ 32
#define МАКС_ФУНКЦИЙ 128

#define МАКС_ПАРАМЕТРОВ 16

// Сигнатура функции (тип возврата) — для типизации вызовов, в т.ч. возврата
// среза, который вызывающий принимает: «срез<T> д = создать()». Хранит также,
// какие параметры объявлены «владеет» (потребляют переданный срез — передача
// в них = move аргумента).
typedef struct {
    char имя[МАКС_ИМЯ];
    ТипТокена тип_возврата;
    int беззнаковый;
    int срез;
    int владеет_параметра[МАКС_ПАРАМЕТРОВ];
    size_t число_параметров;
} СигнатураФункции;

typedef struct {
    Диагностика *диаг;
    СвязьМассива связи[МАКС_СВЯЗЕЙ_МАССИВОВ];
    size_t число_связей;
    ОграничениеДлины ограничения[МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ];
    size_t глубина_ограничений;
    Символ символы[МАКС_СИМВОЛОВ];
    size_t число_символов;
    int глубина;
    ИмяСсылка изменяемые[МАКС_СИМВОЛОВ];
    size_t число_изменяемых;
    // Стек доказанно-безопасных доступов (по одной паре на вложенный цикл).
    БезопаснаяПара безопасные[МАКС_БЕЗОПАСНЫХ];
    size_t число_безопасных;
    // Тип возврата текущей функции (для проверки «вернуть»).
    ТипТокена тип_возврата;
    int беззнаковый_возврата;
    int указатели_возврата;
    int срез_возврата;       // функция возвращает срез<T>
    // Сигнатуры всех функций (собираются до анализа тел — для вызовов вперёд).
    СигнатураФункции функции[МАКС_ФУНКЦИЙ];
    size_t число_функций;
} Анализатор;

// ─── вспомогательные ────────────────────────────────────────────────────────

static int байты_равны(const char *a, size_t la, const char *b, size_t lb)
{
    return la == lb && memcmp(a, b, la) == 0;
}

static int токен_текст_равен(const Токен *t, const char *текст)
{
    return байты_равны(t->начало, t->длина, текст, strlen(текст));
}

static void ошибка_семантики(Анализатор *а, const Токен *t, const char *текст)
{
    if (а->диаг->ошибка) return;
    а->диаг->ошибка = 1;
    а->диаг->строка = t ? t->строка : 0;
    а->диаг->колонка = t ? t->позиция_в_строке : 0;
    snprintf(а->диаг->сообщение, sizeof(а->диаг->сообщение), "%s", текст);
}

// ─── работа с типами ────────────────────────────────────────────────────────

static int тип_арифметический(ИнфоТипа т)
{
    if (т.срез || т.указатели > 0) return 0;
    switch (т.тип) {
        case ТОКЕН_ЦЕЛОЕ8: case ТОКЕН_ЦЕЛОЕ16:
        case ТОКЕН_ЦЕЛОЕ32: case ТОКЕН_ЦЕЛОЕ64:
        case ТОКЕН_БАЙТ: case ТОКЕН_ВЕЩЕСТВЕННОЕ:
            return 1;
        default: return 0;
    }
}

static int тип_целочисленный(ИнфоТипа т)
{
    if (т.срез || т.указатели > 0) return 0;
    switch (т.тип) {
        case ТОКЕН_ЦЕЛОЕ8: case ТОКЕН_ЦЕЛОЕ16:
        case ТОКЕН_ЦЕЛОЕ32: case ТОКЕН_ЦЕЛОЕ64:
        case ТОКЕН_БАЙТ: return 1;
        default: return 0;
    }
}

static int тип_указатель(ИнфоТипа т)
{
    if (т.срез) return 0;
    return т.указатели > 0 || т.тип == ТОКЕН_НУЛ_УКАЗАТЕЛЬ;
}

static int тип_скалярный(ИнфоТипа т)
{
    return тип_арифметический(т) || тип_указатель(т);
}

// Человекочитаемое описание типа для сообщений об ошибках.
static void описание_типа(ИнфоТипа т, char *out, size_t cap)
{
    const char *база;
    switch (т.тип) {
        case ТОКЕН_ЦЕЛОЕ8:       база = т.беззнаковый ? "неподписанный целое8"  : "целое8";  break;
        case ТОКЕН_ЦЕЛОЕ16:      база = т.беззнаковый ? "неподписанный целое16" : "целое16"; break;
        case ТОКЕН_ЦЕЛОЕ32:      база = т.беззнаковый ? "неподписанный целое32" : "целое32"; break;
        case ТОКЕН_ЦЕЛОЕ64:      база = т.беззнаковый ? "неподписанный целое64" : "целое64"; break;
        case ТОКЕН_БАЙТ:         база = "байт";         break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ: база = "вещественное"; break;
        case ТОКЕН_ТИП_СИМВОЛ:  база = "символ";        break;
        case ТОКЕН_НУЛ_УКАЗАТЕЛЬ: snprintf(out, cap, "нуль"); return;
        case ТОКЕН_НЕИЗВЕСТНЫЙ:  snprintf(out, cap, "<неизвестный>"); return;
        default:                 база = "?"; break;
    }
    if (т.срез) {
        snprintf(out, cap, "срез<%s>", база);
    } else if (т.указатели == 0) {
        snprintf(out, cap, "%s", база);
    } else {
        char звёзды[16] = {0};
        int n = т.указатели < 8 ? т.указатели : 8;
        for (int i = 0; i < n; ++i) звёзды[i] = '*';
        snprintf(out, cap, "%s%s", база, звёзды);
    }
}

// Совместимость типов при присваивании: цель ← источник.
// Правила как в C (без явного приведения):
//   - оба арифметические → OK (неявные числовые преобразования)
//   - цель = указатель, источник = нуль → OK
//   - оба указатели одного базового типа и уровня → OK
//   - указатель ← арифметика → ОШИБКА
//   - арифметика ← указатель → ОШИБКА
//   - указатели разных типов → ОШИБКА
static int совместимы_для_присваивания(ИнфоТипа цель, ИнфоТипа источник)
{
    // Срезы: цель-срез принимает только срез. Источник с неизвестным типом
    // элемента (результат «выделить») совместим с любым срезом.
    if (цель.срез || источник.срез) {
        if (!цель.срез || !источник.срез)
            return 0;  // срез ↔ не-срез несовместимы
        if (источник.тип == ТОКЕН_НЕИЗВЕСТНЫЙ || цель.тип == ТОКЕН_НЕИЗВЕСТНЫЙ)
            return 1;  // выделить(...) либо неизвестный элемент
        return цель.тип == источник.тип && цель.беззнаковый == источник.беззнаковый;
    }

    if (цель.тип == ТОКЕН_НЕИЗВЕСТНЫЙ || источник.тип == ТОКЕН_НЕИЗВЕСТНЫЙ)
        return 1;  // неизвестный тип — не проверяем

    int ц_ук = тип_указатель(цель);
    int и_ук = тип_указатель(источник);

    // nullptr → любой указатель
    if (ц_ук && источник.тип == ТОКЕН_НУЛ_УКАЗАТЕЛЬ)
        return 1;

    // оба арифметические
    if (!ц_ук && !и_ук)
        return 1;

    // указатель ← арифметика  или  арифметика ← указатель
    if (ц_ук != и_ук)
        return 0;

    // оба указатели: проверяем базовый тип и уровень косвенности
    return цель.тип == источник.тип && цель.указатели == источник.указатели;
}

// Совместимость операндов бинарного оператора. Возвращает 1 и кладёт
// тип результата в *рез при успехе; 0 при несовместимости.
static int тип_бинопа(ИнфоТипа л, ИнфоТипа п,
                       const Токен *оп, ИнфоТипа *рез)
{
    int л_ук = тип_указатель(л);
    int п_ук = тип_указатель(п);
    int л_ар = тип_арифметический(л);
    int п_ар = тип_арифметический(п);

    int сложение = токен_текст_равен(оп, "+");
    int вычит    = токен_текст_равен(оп, "-");
    int сравнение = токен_текст_равен(оп, ">")  || токен_текст_равен(оп, "<")
                 || токен_текст_равен(оп, ">=") || токен_текст_равен(оп, "==");

    // Сравнение: допускаем арифметику и указатели одного типа, результат int.
    if (сравнение) {
        if ((л_ук && п_ук) &&
            (л.тип != п.тип || л.указатели != п.указатели) &&
            п.тип != ТОКЕН_НУЛ_УКАЗАТЕЛЬ && л.тип != ТОКЕН_НУЛ_УКАЗАТЕЛЬ) {
            return 0;  // сравнение указателей разных типов
        }
        *рез = ТИП_ЦЕЛОЕ32_V;
        return 1;
    }

    // Оба арифметические — стандартные числовые преобразования (как в C).
    if (л_ар && п_ар) {
        // Результат — «старший» тип (упрощение: возвращаем левый).
        *рез = л;
        return 1;
    }

    // Арифметика указателей: ptr ± integer → ptr.
    if ((сложение || вычит) && л_ук && п_ар) {
        *рез = л;
        return 1;
    }
    if (сложение && л_ар && п_ук) {
        *рез = п;
        return 1;
    }

    // ptr - ptr → ptrdiff_t (целое32).
    if (вычит && л_ук && п_ук) {
        if (л.тип != п.тип || л.указатели != п.указатели)
            return 0;  // разные типы указателей
        *рез = ТИП_ЦЕЛОЕ32_V;
        return 1;
    }

    return 0;  // всё остальное: ptr OP ptr, ptr * int, и т.д.
}

// ─── элиминация проверок границ ──────────────────────────────────────────────

// Доступ «база[индекс]» доказанно-безопасен в текущем контексте? Истинно, если
// индекс — переменная активного канонического цикла, и его верхняя граница не
// превосходит длину базы (по имени среза или по известной длине ≥ предела).
static int индекс_в_безопасном_контексте(const Анализатор *а, Узел *узел,
                                          int длина_известна, long длина)
{
    Узел *индекс = узел_ребёнок(узел, 0);
    if (!индекс || индекс->тип != УЗЕЛ_ИДЕНТИФИКАТОР) {
        return 0;
    }
    for (size_t i = 0; i < а->число_безопасных; ++i) {
        const БезопаснаяПара *п = &а->безопасные[i];
        if (!байты_равны(п->индекс.начало, п->индекс.длина,
                         индекс->токен.начало, индекс->токен.длина)) {
            continue;
        }
        // Режим среза: тот же срез/массив, что в условии «и < длина(с)».
        if (п->есть_срез
            && байты_равны(п->срез.начало, п->срез.длина,
                           узел->токен.начало, узел->токен.длина)) {
            return 1;
        }
        // Режим предела: «и < N», доступ безопасен если длина базы ≥ N.
        if (п->есть_предел && длина_известна && п->предел <= длина) {
            return 1;
        }
    }
    return 0;
}

// ─── таблица символов ───────────────────────────────────────────────────────

static void войти_в_область(Анализатор *а) { а->глубина++; }

static void выйти_из_области(Анализатор *а)
{
    while (а->число_символов > 0
           && а->символы[а->число_символов - 1].глубина >= а->глубина) {
        а->число_символов--;
    }
    if (а->глубина > 0) а->глубина--;
}

static Символ *найти_символ(Анализатор *а, const char *имя, size_t длина_имени)
{
    for (size_t i = а->число_символов; i > 0; --i) {
        Символ *с = &а->символы[i - 1];
        if (байты_равны(с->имя, strlen(с->имя), имя, длина_имени))
            return с;
    }
    return nullptr;
}

static void объявить_символ(Анализатор *а,
                             const char *имя, size_t длина_имени,
                             ТипТокена тип, int указатели, int массив,
                             int беззнаковый, int срез,
                             int длина_известна, long длина,
                             const Токен *место,
                             int значение_известно, long значение)
{
    for (size_t i = а->число_символов; i > 0; --i) {
        Символ *с = &а->символы[i - 1];
        if (с->глубина != а->глубина) break;
        if (байты_равны(с->имя, strlen(с->имя), имя, длина_имени)) {
            char msg[256];
            snprintf(msg, sizeof(msg),
                     "переменная «%.*s» уже объявлена в этой области",
                     (int)длина_имени, имя);
            ошибка_семантики(а, место, msg);
            return;
        }
    }
    if (а->число_символов >= МАКС_СИМВОЛОВ) return;
    Символ *с = &а->символы[а->число_символов++];
    size_t n = длина_имени < МАКС_ИМЯ - 1 ? длина_имени : МАКС_ИМЯ - 1;
    memcpy(с->имя, имя, n);
    с->имя[n] = '\0';
    с->тип = тип;
    с->указатели = указатели;
    с->массив = массив;
    с->беззнаковый = беззнаковый;
    с->срез = срез;
    с->владеет_срезом = 0;  // уточняется вызывающим после объявления
    с->перемещён = 0;
    с->длина_известна = длина_известна;
    с->длина = длина;
    с->глубина = а->глубина;
    с->значение_известно = значение_известно;
    с->значение = значение;
}

static int переменная_изменяемая(const Анализатор *а,
                                  const char *имя, size_t длина_имени)
{
    for (size_t i = 0; i < а->число_изменяемых; ++i) {
        if (байты_равны(а->изменяемые[i].начало, а->изменяемые[i].длина,
                        имя, длина_имени))
            return 1;
    }
    return 0;
}

// ─── свёртка констант ───────────────────────────────────────────────────────

static int вычислить_константу(Анализатор *а, Узел *узел, long *из)
{
    if (!узел) return 0;
    switch (узел->тип) {
        case УЗЕЛ_ЧИСЛО:
            *из = strtol(узел->токен.начало, nullptr, 10);
            return 1;
        case УЗЕЛ_ИДЕНТИФИКАТОР: {
            Символ *с = найти_символ(а, узел->токен.начало, узел->токен.длина);
            if (с && с->значение_известно) { *из = с->значение; return 1; }
            return 0;
        }
        case УЗЕЛ_УНОП: {
            long в;
            if (токен_текст_равен(&узел->токен, "-")
                && вычислить_константу(а, узел_ребёнок(узел, 0), &в)) {
                *из = -в; return 1;
            }
            return 0;
        }
        case УЗЕЛ_БИНОП: {
            long л, п;
            if (!вычислить_константу(а, узел_ребёнок(узел, 0), &л)
                || !вычислить_константу(а, узел_ребёнок(узел, 1), &п))
                return 0;
            const Токен *о = &узел->токен;
            if (токен_текст_равен(о, "+")) { *из = л + п; return 1; }
            if (токен_текст_равен(о, "-")) { *из = л - п; return 1; }
            if (токен_текст_равен(о, "*")) { *из = л * п; return 1; }
            if (токен_текст_равен(о, "/")) { if (п == 0) return 0; *из = л / п; return 1; }
            if (токен_текст_равен(о, "%")) { if (п == 0) return 0; *из = л % п; return 1; }
            return 0;
        }
        default: return 0;
    }
}

static void собрать_изменяемые(Анализатор *а, Узел *узел)
{
    if (!узел) return;
    if (узел->тип == УЗЕЛ_ПРИСВАИВАНИЕ) {
        Узел *цель = узел_ребёнок(узел, 0);
        if (цель && цель->тип == УЗЕЛ_ИДЕНТИФИКАТОР
            && а->число_изменяемых < МАКС_СИМВОЛОВ
            && !переменная_изменяемая(а, цель->токен.начало, цель->токен.длина)) {
            а->изменяемые[а->число_изменяемых++] =
                (ИмяСсылка){ цель->токен.начало, цель->токен.длина };
        }
    }
    for (size_t i = 0; i < узел_число_детей(узел); ++i)
        собрать_изменяемые(а, узел_ребёнок(узел, i));
}

// ─── связи массивов и ограничения длины ─────────────────────────────────────

static void связь_добавить(Анализатор *а,
                            const char *массив, size_t lm,
                            const char *длина, size_t ld)
{
    if (а->число_связей >= МАКС_СВЯЗЕЙ_МАССИВОВ) return;
    СвязьМассива *s = &а->связи[а->число_связей++];
    size_t n = lm < МАКС_ИМЯ - 1 ? lm : МАКС_ИМЯ - 1;
    memcpy(s->массив, массив, n); s->массив[n] = '\0';
    n = ld < МАКС_ИМЯ - 1 ? ld : МАКС_ИМЯ - 1;
    memcpy(s->длина, длина, n); s->длина[n] = '\0';
}

static void ограничения_сохранить(Анализатор *а)
{
    if (а->глубина_ограничений >= МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ) return;
    а->ограничения[а->глубина_ограничений++] = (ОграничениеДлины){0};
}

static void ограничения_восстановить(Анализатор *а)
{
    if (а->глубина_ограничений > 0) а->глубина_ограничений--;
}

static void ограничение_установить(Анализатор *а,
                                    const char *переменная, size_t длина_имени,
                                    long минимальная_длина)
{
    if (а->глубина_ограничений == 0) return;
    ОграничениеДлины *o = &а->ограничения[а->глубина_ограничений - 1];
    size_t n = длина_имени < МАКС_ИМЯ - 1 ? длина_имени : МАКС_ИМЯ - 1;
    memcpy(o->переменная, переменная, n);
    o->переменная[n] = '\0';
    o->минимальная_длина = минимальная_длина;
    o->известна = 1;
}

static long минимальная_длина_для(const Анализатор *а,
                                   const char *имя, size_t длина_имени)
{
    for (size_t i = а->глубина_ограничений; i > 0; --i) {
        const ОграничениеДлины *o = &а->ограничения[i - 1];
        if (o->известна
            && байты_равны(o->переменная, strlen(o->переменная), имя, длина_имени))
            return o->минимальная_длина;
    }
    return 0;
}

static int известна_длина_массива(const Анализатор *а,
                                   const char *имя_массива, size_t длина_имени,
                                   long *минимальная_длина)
{
    const char *имя_длины = nullptr;
    for (size_t i = 0; i < а->число_связей; ++i) {
        if (байты_равны(а->связи[i].массив, strlen(а->связи[i].массив),
                        имя_массива, длина_имени)) {
            имя_длины = а->связи[i].длина;
            break;
        }
    }
    if (!имя_длины) return 0;
    long m = минимальная_длина_для(а, имя_длины, strlen(имя_длины));
    if (m <= 0) return 0;
    *минимальная_длина = m;
    return 1;
}

static void проверить_доступ_к_массиву(Анализатор *а, const Токен *t,
                                         const char *имя, size_t длина_имени,
                                         long индекс)
{
    long минимум = 0;
    if (!известна_длина_массива(а, имя, длина_имени, &минимум)) {
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "доступ к массиву «%.*s»: длина неизвестна на этапе компиляции",
                 (int)длина_имени, имя);
        ошибка_семантики(а, t, msg);
        return;
    }
    if (индекс < 0 || индекс >= минимум) {
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "индекс %ld вне границ: при текущих условиях длина не меньше %ld",
                 индекс, минимум);
        ошибка_семантики(а, t, msg);
    }
}

// ─── вывод и проверка типов выражений ───────────────────────────────────────

// Выводит тип узла-выражения И выполняет все семантические проверки.
static ИнфоТипа типизировать_выражение(Анализатор *а, Узел *узел);
static int это_выделить(const Узел *узел);
static int это_клон(const Узел *узел);

static ИнфоТипа типизировать_выражение(Анализатор *а, Узел *узел)
{
    if (!узел || а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;

    switch (узел->тип) {

        case УЗЕЛ_ЧИСЛО:
            return ТИП_ЦЕЛОЕ32_V;

        case УЗЕЛ_СТРОКА:
            return (ИнфоТипа){ ТОКЕН_ТИП_СИМВОЛ, 1, 0, 0 };  // const char*

        case УЗЕЛ_НУЛЬ:
            return (ИнфоТипа){ ТОКЕН_НУЛ_УКАЗАТЕЛЬ, 1, 0, 0 };

        case УЗЕЛ_ИДЕНТИФИКАТОР: {
            Символ *с = найти_символ(а, узел->токен.начало, узел->токен.длина);
            if (!с) {
                char msg[256];
                snprintf(msg, sizeof(msg), "неизвестная переменная «%.*s»",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (с->перемещён) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "срез «%.*s» использован после передачи владения (move)",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            return (ИнфоТипа){ с->тип, с->указатели, с->беззнаковый, с->срез };
        }

        case УЗЕЛ_ИНДЕКС: {
            Символ *база = найти_символ(а, узел->токен.начало, узел->токен.длина);
            if (!база) {
                char msg[256];
                snprintf(msg, sizeof(msg), "неизвестная переменная «%.*s»",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (!база->срез && база->указатели == 0 && база->массив == 0) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "индексирование «%.*s»: тип не является срезом, указателем или массивом",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (база->перемещён) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "срез «%.*s» использован после передачи владения (move)",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            Узел *индекс = узел_ребёнок(узел, 0);
            // Тип индекса должен быть целочисленным.
            ИнфоТипа тип_инд = типизировать_выражение(а, индекс);
            if (!а->диаг->ошибка && тип_инд.тип != ТОКЕН_НЕИЗВЕСТНЫЙ
                && !тип_целочисленный(тип_инд)) {
                char тб[64];
                описание_типа(тип_инд, тб, sizeof(тб));
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "индекс должен быть целым числом, получен «%s»", тб);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }

            if (база->срез) {
                // Кодоген отличает срезовый доступ по флагу и типу элемента.
                узел->срез = 1;
                узел->тип_данных = база->тип;
                узел->беззнаковый = база->беззнаковый;
                long idx;
                int конст = вычислить_константу(а, индекс, &idx);
                // Статическая проверка границ для константного индекса.
                if (база->длина_известна && конст
                    && (idx < 0 || idx >= база->длина)) {
                    char msg[256];
                    snprintf(msg, sizeof(msg),
                             "индекс %ld вне границ среза «%.*s» длины %ld",
                             idx, (int)узел->токен.длина, узел->токен.начало,
                             база->длина);
                    ошибка_семантики(а, &индекс->токен, msg);
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                // Элиминация проверки: безопасно, если константный индекс в
                // границах известной длины, либо индекс цикла по этому срезу.
                if ((база->длина_известна && конст && idx >= 0 && idx < база->длина)
                    || индекс_в_безопасном_контексте(а, узел,
                                                      база->длина_известна, база->длина)) {
                    узел->безопасный_индекс = 1;
                }
                return (ИнфоТипа){ база->тип, 0, база->беззнаковый, 0 };
            }

            // Массив фиксированного размера: длина известна на этапе компиляции.
            // Константный безопасный индекс и индекс канонического цикла —
            // напрямую; прочий динамический индекс — рантайм-проверка.
            if (база->массив && база->длина_известна) {
                узел->массив = 1;
                узел->тип_данных = база->тип;
                узел->беззнаковый = база->беззнаковый;
                long idx;
                int конст = вычислить_константу(а, индекс, &idx);
                if (конст && (idx < 0 || idx >= база->длина)) {
                    char msg[256];
                    snprintf(msg, sizeof(msg),
                             "индекс %ld вне границ массива «%.*s» длины %ld",
                             idx, (int)узел->токен.длина, узел->токен.начало,
                             база->длина);
                    ошибка_семантики(а, &индекс->токен, msg);
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                if ((конст && idx >= 0 && idx < база->длина)
                    || индекс_в_безопасном_контексте(а, узел,
                                                      база->длина_известна, база->длина)) {
                    узел->безопасный_индекс = 1;
                }
                return (ИнфоТипа){ база->тип, база->указатели, база->беззнаковый, 0 };
            }

            // Указатель/массив-параметр: проверка границ только при
            // зарегистрированной длине (аргументы ↔ количество_аргументов).
            if (индекс && индекс->тип == УЗЕЛ_ЧИСЛО) {
                long мин = 0;
                if (известна_длина_массива(а, узел->токен.начало,
                                           узел->токен.длина, &мин)) {
                    long idx = strtol(индекс->токен.начало, nullptr, 10);
                    проверить_доступ_к_массиву(а, &индекс->токен,
                        узел->токен.начало, узел->токен.длина, idx);
                }
            }
            int ур = база->указатели > 0 ? база->указатели - 1 : 0;
            return (ИнфоТипа){ база->тип, ур, база->беззнаковый, 0 };
        }

        case УЗЕЛ_УНОП: {
            ИнфоТипа операнд = типизировать_выражение(а, узел_ребёнок(узел, 0));
            if (!а->диаг->ошибка && токен_текст_равен(&узел->токен, "-")) {
                if (!тип_арифметический(операнд) && операнд.тип != ТОКЕН_НЕИЗВЕСТНЫЙ) {
                    ошибка_семантики(а, &узел->токен,
                        "унарный «-» применим только к арифметическим типам");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
            }
            return операнд;
        }

        case УЗЕЛ_БИНОП: {
            ИнфоТипа л = типизировать_выражение(а, узел_ребёнок(узел, 0));
            ИнфоТипа п = типизировать_выражение(а, узел_ребёнок(узел, 1));
            if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;

            // Проверка деления/остатка на ноль.
            if (токен_текст_равен(&узел->токен, "/")
                || токен_текст_равен(&узел->токен, "%")) {
                Узел *делитель = узел_ребёнок(узел, 1);
                long значение;
                if (вычислить_константу(а, делитель, &значение) && значение == 0) {
                    const Токен *место = делитель ? &делитель->токен : &узел->токен;
                    ошибка_семантики(а, место,
                        "деление на ноль: делитель константно равен нулю");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
            }

            // Проверка совместимости операндов.
            if (л.тип != ТОКЕН_НЕИЗВЕСТНЫЙ && п.тип != ТОКЕН_НЕИЗВЕСТНЫЙ) {
                ИнфоТипа рез;
                if (!тип_бинопа(л, п, &узел->токен, &рез)) {
                    char тл[64], тп[64];
                    описание_типа(л, тл, sizeof(тл));
                    описание_типа(п, тп, sizeof(тп));
                    char msg[256];
                    snprintf(msg, sizeof(msg),
                             "несовместимые типы для «%.*s»: «%s» и «%s»",
                             (int)узел->токен.длина, узел->токен.начало, тл, тп);
                    ошибка_семантики(а, &узел->токен, msg);
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                return рез;
            }
            return л.тип != ТОКЕН_НЕИЗВЕСТНЫЙ ? л : п;
        }

        case УЗЕЛ_ПРИСВАИВАНИЕ: {
            Узел *цель_у = узел_ребёнок(узел, 0);
            Узел *знач_у = узел_ребёнок(узел, 1);
            ИнфоТипа тц = типизировать_выражение(а, цель_у);
            if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;

            // Переприсваивание срезу: старое значение освобождается (кодоген
            // вставит free), затем цель получает новое владение. Источником может
            // быть «выделить»/«клон» (новая память), вызов функции-среза или
            // другой владеющий срез (move). Цель остаётся владельцем.
            if (тц.срез) {
                if (цель_у->тип != УЗЕЛ_ИДЕНТИФИКАТОР) {
                    ошибка_семантики(а, &узел->токен,
                        "присваивать можно только срез-переменной");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                Символ *цс = найти_символ(а, цель_у->токен.начало, цель_у->токен.длина);
                if (!цс || !цс->владеет_срезом) {
                    ошибка_семантики(а, &цель_у->токен,
                        "переприсваивать можно только владеющий срез "
                        "(заимствованный параметр нельзя)");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                узел->срез = 1;  // метка для кодогена: освободить старое + присвоить
                узел->тип_данных = цс->тип;       // тип элемента — для sizeof
                узел->беззнаковый = цс->беззнаковый;
                // После переприсваивания длина консервативно неизвестна:
                // присваивание могло быть условным, поэтому статическую длину
                // не сохраняем (доступы пойдут через рантайм-проверку).
                цс->длина_известна = 0;

                if (это_выделить(знач_у) || это_клон(знач_у)) {
                    if (узел_число_детей(знач_у) != 1) {
                        ошибка_семантики(а, &знач_у->токен,
                            "встроенная функция принимает один аргумент");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    Узел *арг = узел_ребёнок(знач_у, 0);
                    ИнфоТипа та = типизировать_выражение(а, арг);
                    if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                    if (это_выделить(знач_у)) {
                        if (та.тип != ТОКЕН_НЕИЗВЕСТНЫЙ && !тип_целочисленный(та)) {
                            ошибка_семантики(а, &знач_у->токен,
                                "количество элементов в «выделить» должно быть целым");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                    } else if (!та.срез) {
                        ошибка_семантики(а, &знач_у->токен,
                            "«клон» применим только к срезу");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                } else {
                    ИнфоТипа тз = типизировать_выражение(а, знач_у);
                    if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                    if (!тз.срез) {
                        ошибка_семантики(а, &знач_у->токен,
                            "срезу можно присвоить только срез");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    if (тз.тип != цс->тип || тз.беззнаковый != цс->беззнаковый) {
                        ошибка_семантики(а, &знач_у->токен,
                            "тип элемента не совпадает с типом цели");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    if (знач_у->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                        Символ *ис = найти_символ(а, знач_у->токен.начало,
                                                  знач_у->токен.длина);
                        if (!ис || !ис->владеет_срезом) {
                            ошибка_семантики(а, &знач_у->токен,
                                "нельзя переместить заимствованный срез");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                        if (ис->глубина != а->глубина) {
                            ошибка_семантики(а, &знач_у->токен,
                                "перемещение среза из внешней области пока не "
                                "поддерживается");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                        ис->перемещён = 1;
                    }
                }
                return тц;
            }

            ИнфоТипа тз = типизировать_выражение(а, знач_у);
            if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;

            if (!совместимы_для_присваивания(тц, тз)) {
                char тл[64], тп[64];
                описание_типа(тц, тл, sizeof(тл));
                описание_типа(тз, тп, sizeof(тп));
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "несовместимые типы при присваивании: «%s» ← «%s»", тл, тп);
                ошибка_семантики(а,
                    цель_у ? &цель_у->токен : &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            return тц;
        }

        case УЗЕЛ_ВЫЗОВ: {
            // Встроенная «длина(срез)» → целое32 (поле .длина).
            if (токен_текст_равен(&узел->токен, "длина")) {
                if (узел_число_детей(узел) != 1) {
                    ошибка_семантики(а, &узел->токен,
                        "«длина» принимает ровно один аргумент-срез");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                ИнфоТипа арг = типизировать_выражение(а, узел_ребёнок(узел, 0));
                if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                if (!арг.срез && арг.тип != ТОКЕН_НЕИЗВЕСТНЫЙ) {
                    ошибка_семантики(а, &узел->токен,
                        "«длина» применима только к срезу");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                return ТИП_ЦЕЛОЕ32_V;
            }
            // Встроенная «выделить(N)» допустима только как инициализатор
            // объявления среза (там известен тип элемента) — см. УЗЕЛ_ОБЪЯВЛЕНИЕ.
            if (токен_текст_равен(&узел->токен, "выделить")) {
                ошибка_семантики(а, &узел->токен,
                    "«выделить» допустимо только при объявлении среза: "
                    "срез<T> имя = выделить(N)");
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (токен_текст_равен(&узел->токен, "клон")) {
                ошибка_семантики(а, &узел->токен,
                    "«клон» допустим только при объявлении среза: "
                    "срез<T> имя = клон(другой)");
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            // Сигнатура (если функция известна) — нужна для «владеет»-параметров.
            СигнатураФункции *сиг = nullptr;
            for (size_t i = 0; i < а->число_функций; ++i) {
                if (байты_равны(а->функции[i].имя, strlen(а->функции[i].имя),
                                узел->токен.начало, узел->токен.длина)) {
                    сиг = &а->функции[i];
                    break;
                }
            }

            // Аргументы типизируются (заодно проверки use-after-move и т.п.).
            // Аргумент, идущий в «владеет»-параметр, перемещается в функцию.
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                Узел *арг = узел_ребёнок(узел, i);
                типизировать_выражение(а, арг);
                if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                if (сиг && i < сиг->число_параметров && сиг->владеет_параметра[i]
                    && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                    Символ *ис = найти_символ(а, арг->токен.начало, арг->токен.длина);
                    if (ис && ис->срез) {
                        if (!ис->владеет_срезом) {
                            ошибка_семантики(а, &арг->токен,
                                "нельзя передать заимствованный срез в параметр "
                                "«владеет»: владелец находится снаружи");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                        ис->перемещён = 1;  // владение ушло в функцию-потребитель
                    }
                }
            }

            if (сиг) {
                return (ИнфоТипа){ сиг->тип_возврата, 0, сиг->беззнаковый, сиг->срез };
            }
            return ТИП_НЕИЗВЕСТЕН_V;
        }

        default:
            return ТИП_НЕИЗВЕСТЕН_V;
    }
}

// Узел является вызовом встроенной «выделить»?
static int это_выделить(const Узел *узел)
{
    return узел && узел->тип == УЗЕЛ_ВЫЗОВ
        && токен_текст_равен(&узел->токен, "выделить");
}

// Узел является вызовом встроенной «клон»?
static int это_клон(const Узел *узел)
{
    return узел && узел->тип == УЗЕЛ_ВЫЗОВ
        && токен_текст_равен(&узел->токен, "клон");
}

