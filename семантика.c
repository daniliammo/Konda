// Идентификатор — вариант какого-либо перечисления? Возвращает индекс
// перечисления (или -1) и кладёт значение варианта в *значение.
static int найти_вариант(const Анализатор *а, const char *имя, size_t длина,
                          long *значение)
{
    for (size_t i = 0; i < а->число_перечислений; ++i) {
        const ОписаниеПеречисления *п = &а->перечисления[i];
        for (size_t j = 0; j < п->число_вариантов; ++j) {
            if (байты_равны(п->варианты[j].имя, strlen(п->варианты[j].имя),
                            имя, длина)) {
                if (значение) *значение = п->варианты[j].значение;
                return (int)i;
            }
        }
    }
    return -1;
}

// Находит вариант по имени и возвращает его вместе с описанием перечисления и
// индексами (перечисление/вариант). Возвращает индекс перечисления или -1.
static int найти_вариант_подробно(const Анализатор *а, const char *имя, size_t длина,
                                   const ОписаниеПеречисления **вых_п,
                                   const ВариантПеречисления **вых_в, int *вых_вар)
{
    for (size_t i = 0; i < а->число_перечислений; ++i) {
        const ОписаниеПеречисления *п = &а->перечисления[i];
        for (size_t j = 0; j < п->число_вариантов; ++j) {
            if (байты_равны(п->варианты[j].имя, strlen(п->варианты[j].имя),
                            имя, длина)) {
                if (вых_п) *вых_п = п;
                if (вых_в) *вых_в = &п->варианты[j];
                if (вых_вар) *вых_вар = (int)j;
                return (int)i;
            }
        }
    }
    return -1;
}

