// ─── свёртка констант ───────────────────────────────────────────────────────

static int вычислить_константу(Анализатор *а, Узел *узел, long *из)
{
    if (!узел) return 0;
    switch (узел->тип) {
        case УЗЕЛ_ЧИСЛО:
            if (узел->токен.тип == ТОКЕН_ИСТИНА) { *из = 1; return 1; }
            if (узел->токен.тип == ТОКЕН_ЛОЖЬ)   { *из = 0; return 1; }
            *из = strtol(узел->токен.начало, nullptr, 10);
            return 1;
        case УЗЕЛ_ИДЕНТИФИКАТОР: {
            Символ *с = найти_символ(а, узел->токен.начало, узел->токен.длина);
            if (с && с->значение_известно) { *из = с->значение; return 1; }
            // Вариант перечисления — целочисленная константа.
            if (!с) {
                long зн;
                if (найти_вариант(а, узел->токен.начало, узел->токен.длина, &зн) >= 0) {
                    *из = зн; return 1;
                }
            }
            return 0;
        }
        case УЗЕЛ_УНОП: {
            long в;
            if (токен_текст_равен(&узел->токен, "-")
                && вычислить_константу(а, узел_ребёнок(узел, 0), &в)) {
                *из = -в; return 1;
            }
            return 0;
        }
        case УЗЕЛ_БИНОП: {
            long л, п;
            if (!вычислить_константу(а, узел_ребёнок(узел, 0), &л)
                || !вычислить_константу(а, узел_ребёнок(узел, 1), &п))
                return 0;
            const Токен *о = &узел->токен;
            // Свёртка с защитой от UB в самом транспиляторе: при переполнении
            // long или недопустимом делении/сдвиге считаем «не константа» —
            // типизация выдаст понятную ошибку отдельно.
            long r;
            if (токен_текст_равен(о, "+")) {
                if (__builtin_add_overflow(л, п, &r)) return 0;
                *из = r;
                return 1;
            }
            if (токен_текст_равен(о, "-")) {
                if (__builtin_sub_overflow(л, п, &r)) return 0;
                *из = r;
                return 1;
            }
            if (токен_текст_равен(о, "*")) {
                if (__builtin_mul_overflow(л, п, &r)) return 0;
                *из = r;
                return 1;
            }
            if (токен_текст_равен(о, "/")) {
                if (п == 0 || (п == -1 && л == LONG_MIN)) return 0;
                *из = л / п;
                return 1;
            }
            if (токен_текст_равен(о, "%")) {
                if (п == 0 || (п == -1 && л == LONG_MIN)) return 0;
                *из = л % п;
                return 1;
            }
            if (токен_текст_равен(о, "<<")) {
                if (п < 0 || п >= 64) return 0;
                *из = (long)((unsigned long)л << п);
                return 1;
            }
            if (токен_текст_равен(о, ">>")) {
                if (п < 0 || п >= 64) return 0;
                *из = л >> п;
                return 1;
            }
            return 0;
        }
        default: return 0;
    }
}

// Диапазон значений целого типа (для проверки констант-инициализаторов на
// выход за пределы). Возвращает 1 и кладёт [мин, макс], если тип проверяемый;
// 0 для непроверяемых (целое64 — не вмещается в long-сравнение надёжно,
// вещественное). Символ берём как объединение signed/unsigned char.
static int диапазон_целого(ТипТокена тип, int беззнаковый, long *мин, long *макс)
{
    switch (тип) {
        case ТОКЕН_ЦЕЛОЕ8:
            if (беззнаковый) { *мин = 0; *макс = 255; }
            else { *мин = -128; *макс = 127; }
            return 1;
        case ТОКЕН_ЦЕЛОЕ16:
            if (беззнаковый) { *мин = 0; *макс = 65535; }
            else { *мин = -32768; *макс = 32767; }
            return 1;
        case ТОКЕН_ЦЕЛОЕ32:
            if (беззнаковый) { *мин = 0; *макс = 4294967295L; }
            else { *мин = -2147483648L; *макс = 2147483647L; }
            return 1;
        case ТОКЕН_БАЙТ:
            *мин = 0; *макс = 255; return 1;
        case ТОКЕН_ТИП_СИМВОЛ:
            *мин = -128; *макс = 255; return 1;
        default:
            return 0;
    }
}

static void собрать_изменяемые(Анализатор *а, Узел *узел)
{
    if (!узел) return;
    if (узел->тип == УЗЕЛ_ПРИСВАИВАНИЕ) {
        Узел *цель = узел_ребёнок(узел, 0);
        if (цель && цель->тип == УЗЕЛ_ИДЕНТИФИКАТОР
            && а->число_изменяемых < МАКС_СИМВОЛОВ
            && !переменная_изменяемая(а, цель->токен.начало, цель->токен.длина)) {
            а->изменяемые[а->число_изменяемых++] =
                (ИмяСсылка){ цель->токен.начало, цель->токен.длина };
        }
    }
    for (size_t i = 0; i < узел_число_детей(узел); ++i)
        собрать_изменяемые(а, узел_ребёнок(узел, i));
}

// ─── связи массивов и ограничения длины ─────────────────────────────────────

static void связь_добавить(Анализатор *а,
                            const char *массив, size_t lm,
                            const char *длина, size_t ld)
{
    if (а->число_связей >= МАКС_СВЯЗЕЙ_МАССИВОВ) return;
    СвязьМассива *s = &а->связи[а->число_связей++];
    size_t n = lm < МАКС_ИМЯ - 1 ? lm : МАКС_ИМЯ - 1;
    memcpy(s->массив, массив, n); s->массив[n] = '\0';
    n = ld < МАКС_ИМЯ - 1 ? ld : МАКС_ИМЯ - 1;
    memcpy(s->длина, длина, n); s->длина[n] = '\0';
}

static void ограничения_сохранить(Анализатор *а)
{
    if (а->глубина_ограничений >= МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ) return;
    а->ограничения[а->глубина_ограничений++] = (ОграничениеДлины){0};
}

static void ограничения_восстановить(Анализатор *а)
{
    if (а->глубина_ограничений > 0) а->глубина_ограничений--;
}

static void ограничение_установить(Анализатор *а,
                                    const char *переменная, size_t длина_имени,
                                    long минимальная_длина)
{
    if (а->глубина_ограничений == 0) return;
    ОграничениеДлины *o = &а->ограничения[а->глубина_ограничений - 1];
    size_t n = длина_имени < МАКС_ИМЯ - 1 ? длина_имени : МАКС_ИМЯ - 1;
    memcpy(o->переменная, переменная, n);
    o->переменная[n] = '\0';
    o->минимальная_длина = минимальная_длина;
    o->известна = 1;
}

static long минимальная_длина_для(const Анализатор *а,
                                   const char *имя, size_t длина_имени)
{
    for (size_t i = а->глубина_ограничений; i > 0; --i) {
        const ОграничениеДлины *o = &а->ограничения[i - 1];
        if (o->известна
            && байты_равны(o->переменная, strlen(o->переменная), имя, длина_имени))
            return o->минимальная_длина;
    }
    return 0;
}

// Есть ли у указателя зарегистрированная переменная-длина (связь
// «аргументы»↔«количество_аргументов»)? В отличие от известна_длина_массива
// это не зависит от ограничений в области — длина (argc) известна всегда, что
// и позволяет вставить рантайм-проверку при динамическом индексе.
static int у_указателя_есть_длина(const Анализатор *а,
                                   const char *имя, size_t длина_имени)
{
    for (size_t i = 0; i < а->число_связей; ++i)
        if (байты_равны(а->связи[i].массив, strlen(а->связи[i].массив),
                        имя, длина_имени))
            return 1;
    return 0;
}

static int известна_длина_массива(const Анализатор *а,
                                   const char *имя_массива, size_t длина_имени,
                                   long *минимальная_длина)
{
    const char *имя_длины = nullptr;
    for (size_t i = 0; i < а->число_связей; ++i) {
        if (байты_равны(а->связи[i].массив, strlen(а->связи[i].массив),
                        имя_массива, длина_имени)) {
            имя_длины = а->связи[i].длина;
            break;
        }
    }
    if (!имя_длины) return 0;
    long m = минимальная_длина_для(а, имя_длины, strlen(имя_длины));
    if (m <= 0) return 0;
    *минимальная_длина = m;
    return 1;
}

static void проверить_доступ_к_массиву(Анализатор *а, const Токен *t,
                                         const char *имя, size_t длина_имени,
                                         long индекс)
{
    long минимум = 0;
    if (!известна_длина_массива(а, имя, длина_имени, &минимум)) {
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "доступ к массиву «%.*s»: длина неизвестна на этапе компиляции",
                 (int)длина_имени, имя);
        ошибка_семантики(а, t, msg);
        return;
    }
    if (индекс < 0 || индекс >= минимум) {
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "индекс %ld вне границ: при текущих условиях длина не меньше %ld",
                 индекс, минимум);
        ошибка_семантики(а, t, msg);
    }
}

// ─── вывод и проверка типов выражений ───────────────────────────────────────

// Выводит тип узла-выражения И выполняет все семантические проверки.
static ИнфоТипа типизировать_выражение(Анализатор *а, Узел *узел);
static int это_выделить(const Узел *узел);
static int это_клон(const Узел *узел);

static ИнфоТипа типизировать_выражение(Анализатор *а, Узел *узел)
{
    if (!узел || а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;

    switch (узел->тип) {

        case УЗЕЛ_ЧИСЛО:
            // Логические литералы «истина»/«ложь» — тип «логический».
            if (узел->токен.тип == ТОКЕН_ИСТИНА || узел->токен.тип == ТОКЕН_ЛОЖЬ)
                return (ИнфоТипа){ ТОКЕН_ЛОГИЧЕСКИЙ, 0, 0, 0, -1 };
            return ТИП_ЦЕЛОЕ32_V;

        case УЗЕЛ_СТРОКА:
            return (ИнфоТипа){ ТОКЕН_ТИП_СИМВОЛ, 1, 0, 0, -1 };  // const char*

        case УЗЕЛ_F_СТРОКА: {
            // Типизируем каждую вставку «\(выр)» и сохраняем её тип на холдере
            // (для выбора спецификатора в кодогене). Срез/структуру вставлять
            // нельзя — нет однозначного текстового представления.
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                Узел *реб = узел_ребёнок(узел, i);
                if (!реб || реб->тип != УЗЕЛ_ИНТЕРПОЛЯЦИЯ) continue;
                Узел *выр = узел_число_детей(реб) > 0 ? узел_ребёнок(реб, 0) : nullptr;
                if (!выр) {
                    ошибка_семантики(а, &реб->токен,
                        "пустая интерполяция «\\()» в строке");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                ИнфоТипа т = типизировать_выражение(а, выр);
                if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                if (т.срез || (т.тип == ТОКЕН_СТРУКТУРА && т.указатели == 0)) {
                    // Координаты вставки — из под-лексера (фрагмент), поэтому для
                    // диагностики берём позицию самой f-строки (реб->токен = *t).
                    ошибка_семантики(а, &реб->токен,
                        "в интерполяцию строки нельзя поставить срез или структуру");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                реб->тип_данных = т.тип;
                реб->беззнаковый = т.беззнаковый;
                реб->указатели = т.указатели;
                реб->срез = т.срез;
            }
            return (ИнфоТипа){ ТОКЕН_ТИП_СИМВОЛ, 1, 0, 0, -1 };  // char*
        }

        case УЗЕЛ_НУЛЬ:
            return (ИнфоТипа){ ТОКЕН_НУЛ_УКАЗАТЕЛЬ, 1, 0, 0, -1 };

        case УЗЕЛ_ПРИВЕДЕНИЕ: {
            ИнфоТипа ист = типизировать_выражение(а, узел_ребёнок(узел, 0));
            if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
            // Приводить можно только скаляры: числа, указатели, перечисления.
            int ок = ист.тип == ТОКЕН_НЕИЗВЕСТНЫЙ || тип_арифметический(ист)
                  || тип_указатель(ист) || ист.тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ;
            if (ист.срез || ист.тип == ТОКЕН_СТРУКТУРА || !ок) {
                ошибка_семантики(а, &узел->токен,
                    "приведение «как» применимо только к числам, указателям "
                    "и перечислениям");
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            ИнфоТипа цель = { узел->тип_данных, узел->указатели,
                              узел->беззнаковый, 0, -1 };
            // Запрет создания указателя из целого/перечисления: это позволило бы
            // сфабриковать произвольный адрес и разыменовать его (обход всех
            // проверок). Допустимы указатель→указатель и «нуль»→указатель.
            if (тип_указатель(цель) && !тип_указатель(ист)
                && ист.тип != ТОКЕН_НЕИЗВЕСТНЫЙ) {
                ошибка_семантики(а, &узел->токен,
                    "нельзя приводить целое/перечисление к указателю: фабрикация "
                    "произвольного указателя небезопасна (приводите указатель к "
                    "указателю или используйте «нуль»)");
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            return цель;
        }

        case УЗЕЛ_ИДЕНТИФИКАТОР: {
            Символ *с = найти_символ(а, узел->токен.начало, узел->токен.длина);
            if (!с) {
                // Идентификатор может быть вариантом перечисления — это значение
                // соответствующего enum-типа (константа).
                int ие = найти_вариант(а, узел->токен.начало, узел->токен.длина,
                                       nullptr);
                if (ие >= 0) {
                    return (ИнфоТипа){ ТОКЕН_ПЕРЕЧИСЛЕНИЕ, 0, 0, 0, ие };
                }
                char msg[256];
                snprintf(msg, sizeof(msg), "неизвестная переменная «%.*s»",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (с->перемещён) {
                char msg[256];
                if (с->токен_move.строка > 0) {
                    snprintf(msg, sizeof(msg),
                             "срез «%.*s» использован после передачи владения "
                             "(move на строке %d)",
                             (int)узел->токен.длина, узел->токен.начало,
                             с->токен_move.строка);
                } else {
                    snprintf(msg, sizeof(msg),
                             "срез «%.*s» использован после передачи владения (move)",
                             (int)узел->токен.длина, узел->токен.начало);
                }
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (!с->инициализирована) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "переменная «%.*s» используется до инициализации",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            return (ИнфоТипа){ с->тип, с->указатели, с->беззнаковый, с->срез, с->индекс_структуры };
        }

        case УЗЕЛ_ПОЛЕ: {
            ИнфоТипа база = типизировать_выражение(а, узел_ребёнок(узел, 0));
            if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
            // Доступ «.» — у структуры или указателя на структуру (авто-«->»).
            if (база.тип != ТОКЕН_СТРУКТУРА || база.индекс_структуры < 0
                || база.указатели > 1) {
                ошибка_семантики(а, &узел->токен,
                    "доступ к полю «.» возможен только у структуры");
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            // Для кодогена: доступ через указатель → «->», иначе «.».
            узел->указатели = (база.указатели == 1) ? 1 : 0;
            ОписаниеСтруктуры *с = &а->структуры[база.индекс_структуры];
            for (size_t i = 0; i < с->число_полей; ++i) {
                ПолеСтруктуры *п = &с->поля[i];
                if (байты_равны(п->имя, strlen(п->имя),
                                узел->токен.начало, узел->токен.длина)) {
                    return (ИнфоТипа){ п->тип, п->указатели, п->беззнаковый, п->срез,
                                       п->индекс_структуры };
                }
            }
            char msg[256];
            snprintf(msg, sizeof(msg),
                     "у структуры «%s» нет поля «%.*s»",
                     с->имя, (int)узел->токен.длина, узел->токен.начало);
            ошибка_семантики(а, &узел->токен, msg);
            return ТИП_НЕИЗВЕСТЕН_V;
        }

        case УЗЕЛ_ИНДЕКС: {
            Символ *база = найти_символ(а, узел->токен.начало, узел->токен.длина);
            if (!база) {
                char msg[256];
                snprintf(msg, sizeof(msg), "неизвестная переменная «%.*s»",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (!база->срез && база->указатели == 0 && база->массив == 0) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "индексирование «%.*s»: тип не является срезом, указателем или массивом",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (база->перемещён) {
                char msg[256];
                if (база->токен_move.строка > 0) {
                    snprintf(msg, sizeof(msg),
                             "срез «%.*s» использован после передачи владения "
                             "(move на строке %d)",
                             (int)узел->токен.длина, узел->токен.начало,
                             база->токен_move.строка);
                } else {
                    snprintf(msg, sizeof(msg),
                             "срез «%.*s» использован после передачи владения (move)",
                             (int)узел->токен.длина, узел->токен.начало);
                }
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            // Нулевое разыменование: индексирование указателя, который может быть
            // «нуль» на каком-то пути исполнения (объявлен «= нуль», без
            // инициализатора, переприсвоен «нуль», либо после if/цикла остаётся
            // нулевым хотя бы на одной ветви). Срезы/массивы сюда не попадают —
            // у них своя проверка границ.
            if (база->указатели > 0 && !база->срез && !база->массив
                && база->может_быть_нуль) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "разыменование указателя «%.*s», который может быть "
                         "нулевым (нуль на одном из путей исполнения)",
                         (int)узел->токен.длина, узел->токен.начало);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            Узел *индекс = узел_ребёнок(узел, 0);
            // Тип индекса должен быть целочисленным.
            ИнфоТипа тип_инд = типизировать_выражение(а, индекс);
            if (!а->диаг->ошибка && тип_инд.тип != ТОКЕН_НЕИЗВЕСТНЫЙ
                && !тип_целочисленный(тип_инд)) {
                char тб[64];
                описание_типа(тип_инд, тб, sizeof(тб));
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "индекс должен быть целым числом, получен «%s»", тб);
                ошибка_семантики(а, &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }

            if (база->срез) {
                // Кодоген отличает срезовый доступ по флагу и типу элемента.
                узел->срез = 1;
                узел->тип_данных = база->тип;
                узел->беззнаковый = база->беззнаковый;
                long idx;
                int конст = вычислить_константу(а, индекс, &idx);
                // Статическая проверка границ для константного индекса.
                if (база->длина_известна && конст
                    && (idx < 0 || idx >= база->длина)) {
                    char msg[256];
                    snprintf(msg, sizeof(msg),
                             "индекс %ld вне границ среза «%.*s» длины %ld",
                             idx, (int)узел->токен.длина, узел->токен.начало,
                             база->длина);
                    ошибка_семантики(а, &индекс->токен, msg);
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                // Элиминация проверки: безопасно, если константный индекс в
                // границах известной длины, либо индекс цикла по этому срезу.
                if ((база->длина_известна && конст && idx >= 0 && idx < база->длина)
                    || индекс_в_безопасном_контексте(а, узел,
                                                      база->длина_известна, база->длина)) {
                    узел->безопасный_индекс = 1;
                }
                return (ИнфоТипа){ база->тип, 0, база->беззнаковый, 0, -1 };
            }

            // Массив фиксированного размера: длина известна на этапе компиляции.
            // Константный безопасный индекс и индекс канонического цикла —
            // напрямую; прочий динамический индекс — рантайм-проверка.
            if (база->массив && база->длина_известна) {
                узел->массив = 1;
                узел->тип_данных = база->тип;
                узел->беззнаковый = база->беззнаковый;
                long idx;
                int конст = вычислить_константу(а, индекс, &idx);
                if (конст && (idx < 0 || idx >= база->длина)) {
                    char msg[256];
                    snprintf(msg, sizeof(msg),
                             "индекс %ld вне границ массива «%.*s» длины %ld",
                             idx, (int)узел->токен.длина, узел->токен.начало,
                             база->длина);
                    ошибка_семантики(а, &индекс->токен, msg);
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                if ((конст && idx >= 0 && idx < база->длина)
                    || индекс_в_безопасном_контексте(а, узел,
                                                      база->длина_известна, база->длина)) {
                    узел->безопасный_индекс = 1;
                }
                return (ИнфоТипа){ база->тип, база->указатели, база->беззнаковый, 0, -1 };
            }

            // Указатель/массив-параметр: проверка границ только при
            // зарегистрированной длине (аргументы ↔ количество_аргументов).
            // Указатель с зарегистрированной длиной («аргументы» ↔ argc) —
            // безопасная обёртка над argv. Константный индекс при наличии
            // ограничения проверяется статически (и при доказанной безопасности
            // доступ прямой); динамический или недоказанный — рантайм-проверкой.
            if (у_указателя_есть_длина(а, узел->токен.начало, узел->токен.длина)) {
                long idx, мин;
                int проверен_статически = 0;
                if (вычислить_константу(а, индекс, &idx)
                    && известна_длина_массива(а, узел->токен.начало,
                                              узел->токен.длина, &мин)) {
                    проверить_доступ_к_массиву(а, &индекс->токен,
                        узел->токен.начало, узел->токен.длина, idx);
                    проверен_статически = 1;  // доказан в границах → прямой доступ
                }
                if (!проверен_статически) {
                    узел->проверка_аргументов = 1;  // рантайм-проверка границ
                }
            }
            int ур = база->указатели > 0 ? база->указатели - 1 : 0;
            return (ИнфоТипа){ база->тип, ур, база->беззнаковый, 0, -1 };
        }

        case УЗЕЛ_УНОП: {
            ИнфоТипа операнд = типизировать_выражение(а, узел_ребёнок(узел, 0));
            if (!а->диаг->ошибка && токен_текст_равен(&узел->токен, "-")) {
                if (!тип_арифметический(операнд) && операнд.тип != ТОКЕН_НЕИЗВЕСТНЫЙ) {
                    ошибка_семантики(а, &узел->токен,
                        "унарный «-» применим только к арифметическим типам");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
            }
            return операнд;
        }

        case УЗЕЛ_БИНОП: {
            ИнфоТипа л = типизировать_выражение(а, узел_ребёнок(узел, 0));
            ИнфоТипа п = типизировать_выражение(а, узел_ребёнок(узел, 1));
            if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;

            const Токен *оп = &узел->токен;
            int деление = токен_текст_равен(оп, "/") || токен_текст_равен(оп, "%");
            int сдвиг   = токен_текст_равен(оп, "<<") || токен_текст_равен(оп, ">>");
            int слож_выч_умн = токен_текст_равен(оп, "+")
                            || токен_текст_равен(оп, "-")
                            || токен_текст_равен(оп, "*");

            // Свёртка операндов (если они константны) — для статических проверок
            // деления/сдвига/переполнения без рантайма.
            long л0, п0;
            int кл = вычислить_константу(а, узел_ребёнок(узел, 0), &л0);
            int кп = вычислить_константу(а, узел_ребёнок(узел, 1), &п0);

            // Деление/остаток на константный ноль.
            if (деление && кп && п0 == 0) {
                ошибка_семантики(а, &узел_ребёнок(узел, 1)->токен,
                    "деление на ноль: делитель константно равен нулю");
                return ТИП_НЕИЗВЕСТЕН_V;
            }

            // Проверка совместимости операндов.
            if (л.тип != ТОКЕН_НЕИЗВЕСТНЫЙ && п.тип != ТОКЕН_НЕИЗВЕСТНЫЙ) {
                ИнфоТипа рез;
                if (!тип_бинопа(л, п, оп, &рез)) {
                    char тл[64], тп[64];
                    описание_типа(л, тл, sizeof(тл));
                    описание_типа(п, тп, sizeof(тп));
                    char msg[256];
                    snprintf(msg, sizeof(msg),
                             "несовместимые типы для «%.*s»: «%s» и «%s»",
                             (int)узел->токен.длина, узел->токен.начало, тл, тп);
                    ошибка_семантики(а, &узел->токен, msg);
                    return ТИП_НЕИЗВЕСТЕН_V;
                }

                long мин, макс;
                int есть_диапазон = диапазон_целого(рез.тип, рез.беззнаковый,
                                                    &мин, &макс);

                // Сдвиг на константное число бит вне [0, ширина): UB в C.
                if (сдвиг && кп) {
                    int ширина = ширина_бит(л);
                    if (ширина > 0 && (п0 < 0 || п0 >= ширина)) {
                        char msg[256];
                        snprintf(msg, sizeof(msg),
                            "сдвиг на %ld бит вне диапазона [0, %d) для типа операнда",
                            п0, ширина);
                        ошибка_семантики(а, оп, msg);
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                }

                // Переполнение деления: МИН_типа / -1 (или % -1) — UB.
                if (деление && кл && кп && п0 == -1
                    && есть_диапазон && мин < 0 && л0 == мин) {
                    ошибка_семантики(а, оп,
                        "переполнение деления: минимальное значение типа делится на -1");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }

                // Переполнение константного «+/-/*» относительно типа результата.
                if (слож_выч_умн && кл && кп) {
                    long r;
                    int пер = токен_текст_равен(оп, "+")
                                ? __builtin_add_overflow(л0, п0, &r)
                            : токен_текст_равен(оп, "-")
                                ? __builtin_sub_overflow(л0, п0, &r)
                                : __builtin_mul_overflow(л0, п0, &r);
                    if (пер || (есть_диапазон && (r < мин || r > макс))) {
                        char тр[64];
                        описание_типа(рез, тр, sizeof(тр));
                        char msg[256];
                        snprintf(msg, sizeof(msg),
                            "переполнение в константном выражении типа «%s»", тр);
                        ошибка_семантики(а, оп, msg);
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                }

                // Неконстантный делитель/сдвиг — статически не доказать; кодоген
                // вставит одну рантайм-проверку (деление на ноль / диапазон бит).
                if (деление && !кп) {
                    узел->проверка_бинопа = 1;
                } else if (сдвиг && !кп) {
                    узел->проверка_бинопа = 2;
                    узел->размер_массива = ширина_бит(л);  // разрядность для guard
                } else if (слож_выч_умн && !(кл && кп)
                           && тип_целочисленный(л) && тип_целочисленный(п)
                           && !рез.беззнаковый) {
                    // Неконстантное «+/-/*» знаковых целых: подписанное
                    // переполнение — UB в C (константы уже проверены выше).
                    // Кодоген вставит один checked-builtin (стиль Rust): при
                    // выходе результата за диапазон типа → сообщение и abort.
                    // Беззнаковые исключены: их перенос определён в C (wrap).
                    // Арифметика указателей исключена: рез — указатель, не
                    // целочисленный. Разность указателей (рез = целое32) тоже
                    // исключена — операнды не целочисленны.
                    int ш = ширина_бит(рез);
                    if (ш > 0) {
                        узел->проверка_бинопа = 3;
                        узел->размер_массива = ш;  // разрядность результата
                    }
                }
                return рез;
            }
            return л.тип != ТОКЕН_НЕИЗВЕСТНЫЙ ? л : п;
        }

        case УЗЕЛ_ПРИСВАИВАНИЕ: {
            Узел *цель_у = узел_ребёнок(узел, 0);
            Узел *знач_у = узел_ребёнок(узел, 1);
            // Простой идентификатор-цель присваивается → инициализирован.
            // Помечаем до типизации цели, чтобы (1) сама цель не считалась
            // «чтением до инициализации» и (2) последующее использование было
            // законным. Источник (знач_у) типизируется ниже и его чтения
            // неинициализированных переменных по-прежнему ловятся.
            if (цель_у && цель_у->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                Символ *цс0 = найти_символ(а, цель_у->токен.начало,
                                           цель_у->токен.длина);
                if (цс0) цс0->инициализирована = 1;
            }
            // Присваивание переменной-указателю на функцию: «оп = функ | указ | нуль».
            if (цель_у && цель_у->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                Символ *цфп = найти_символ(а, цель_у->токен.начало,
                                           цель_у->токен.длина);
                if (цфп && цфп->тип == ТОКЕН_ФУНКУКАЗАТЕЛЬ) {
                    int idx = цфп->индекс_структуры;
                    if (знач_у->тип == УЗЕЛ_НУЛЬ) {
                        цфп->может_быть_нуль = 1;
                    } else if (знач_у->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                        СигнатураФункции *сф = найти_сигнатуру_по_имени(а,
                            знач_у->токен.начало, знач_у->токен.длина);
                        Символ *ис = найти_символ(а, знач_у->токен.начало,
                                                  знач_у->токен.длина);
                        if (сф && idx >= 0 && сигнатура_совпадает_с_типфункцией(сф,
                                &а->типыфункций[idx])) {
                            цфп->может_быть_нуль = 0;
                        } else if (сф) {
                            ошибка_семантики(а, &знач_у->токен,
                                "сигнатура функции не совпадает с типом-указателем");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        } else if (ис && ис->тип == ТОКЕН_ФУНКУКАЗАТЕЛЬ
                                   && ис->индекс_структуры == idx) {
                            цфп->может_быть_нуль = ис->может_быть_нуль;
                        } else {
                            ошибка_семантики(а, &знач_у->токен,
                                "указателю на функцию присваивается имя подходящей "
                                "функции, указатель того же типа или «нуль»");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                    } else {
                        ошибка_семантики(а, &знач_у->токен,
                            "указателю на функцию присваивается имя функции или «нуль»");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    return (ИнфоТипа){ ТОКЕН_ФУНКУКАЗАТЕЛЬ, 0, 0, 0, idx };
                }
            }
            // Запрет записи в строковый литерал: «с[i] = …», где «с» — указатель
            // из строкового литерала (.rodata только для чтения).
            if (цель_у && цель_у->тип == УЗЕЛ_ИНДЕКС) {
                Символ *цб = найти_символ(а, цель_у->токен.начало,
                                          цель_у->токен.длина);
                if (цб && цб->из_литерала) {
                    ошибка_семантики(а, &цель_у->токен,
                        "запись в строковый литерал запрещена (данные только для "
                        "чтения); скопируйте в срез/массив, если нужна изменяемость");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
            }

            ИнфоТипа тц = типизировать_выражение(а, цель_у);
            if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;

            // Структуру-значение с деструктором копировать нельзя: оба экземпляра
            // вызвали бы деструктор (двойное освобождение ресурса).
            if (структура_с_деструктором(а, тц)) {
                ошибка_семантики(а, цель_у ? &цель_у->токен : &узел->токен,
                    "структуру с деструктором нельзя присваивать "
                    "(привело бы к двойному вызову деструктора)");
                return ТИП_НЕИЗВЕСТЕН_V;
            }

            // Переприсваивание срезу: старое значение освобождается (кодоген
            // вставит free), затем цель получает новое владение. Источником может
            // быть «выделить»/«клон» (новая память), вызов функции-среза или
            // другой владеющий срез (move). Цель остаётся владельцем.
            if (тц.срез) {
                if (цель_у->тип != УЗЕЛ_ИДЕНТИФИКАТОР) {
                    // Цель — не простой идентификатор (например, поле «о.данные»):
                    // берём токен цели, у него есть позиция; токен узла-присваивания
                    // не задаётся парсером (иначе «строка 0, колонка 0»). Заодно
                    // подсказываем, что срез-поле нельзя переприсваивать.
                    ошибка_семантики(а, &цель_у->токен,
                        "срезу можно присваивать только по имени переменной; "
                        "переприсваивание срез-поля структуры не поддерживается "
                        "(структура — единственный владелец на весь свой срок)");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                Символ *цс = найти_символ(а, цель_у->токен.начало, цель_у->токен.длина);
                if (!цс || !цс->владеет_срезом) {
                    ошибка_семантики(а, &цель_у->токен,
                        "переприсваивать можно только владеющий срез "
                        "(заимствованный параметр нельзя)");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                узел->срез = 1;  // метка для кодогена: освободить старое + присвоить
                узел->тип_данных = цс->тип;       // тип элемента — для sizeof
                узел->беззнаковый = цс->беззнаковый;
                // После переприсваивания длина консервативно неизвестна:
                // присваивание могло быть условным, поэтому статическую длину
                // не сохраняем (доступы пойдут через рантайм-проверку).
                цс->длина_известна = 0;

                if (это_выделить(знач_у) || это_клон(знач_у)) {
                    if (узел_число_детей(знач_у) != 1) {
                        ошибка_семантики(а, &знач_у->токен,
                            "встроенная функция принимает один аргумент");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    Узел *арг = узел_ребёнок(знач_у, 0);
                    ИнфоТипа та = типизировать_выражение(а, арг);
                    if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                    if (это_выделить(знач_у)) {
                        if (та.тип != ТОКЕН_НЕИЗВЕСТНЫЙ && !тип_целочисленный(та)) {
                            ошибка_семантики(а, &знач_у->токен,
                                "количество элементов в «выделить» должно быть целым");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                    } else if (!та.срез) {
                        ошибка_семантики(а, &знач_у->токен,
                            "«клон» применим только к срезу");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                } else {
                    ИнфоТипа тз = типизировать_выражение(а, знач_у);
                    if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                    if (!тз.срез) {
                        ошибка_семантики(а, &знач_у->токен,
                            "срезу можно присвоить только срез");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    if (тз.тип != цс->тип || тз.беззнаковый != цс->беззнаковый) {
                        ошибка_семантики(а, &знач_у->токен,
                            "тип элемента не совпадает с типом цели");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    if (знач_у->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                        Символ *ис = найти_символ(а, знач_у->токен.начало,
                                                  знач_у->токен.длина);
                        if (!ис || !ис->владеет_срезом) {
                            ошибка_семантики(а, &знач_у->токен,
                                "нельзя переместить заимствованный срез");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                        // В теле цикла move из объемлющей области запрещён:
                        // на втором витке источник уже перемещён → use-after-
                        // move; однопроходный анализ это не гарантирует без
                        // фикс-точки. Вне цикла move из внешней области
                        // разрешён — слияние в если/иначе следит за
                        // согласованностью между ветвями.
                        if (ис->глубина < а->глубина && а->глубина_цикла > 0) {
                            ошибка_семантики(а, &знач_у->токен,
                                "перемещение среза из объемлющей области "
                                "внутри цикла запрещено (на втором витке "
                                "источник уже перемещён)");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                        ис->перемещён = 1;
                        ис->токен_move = знач_у->токен;
                    }
                }
                return тц;
            }

            ИнфоТипа тз = типизировать_выражение(а, знач_у);
            if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;

            if (!совместимы_для_присваивания(тц, тз)) {
                char тл[64], тп[64];
                описание_типа(тц, тл, sizeof(тл));
                описание_типа(тз, тп, sizeof(тп));
                char msg[256];
                snprintf(msg, sizeof(msg),
                         "несовместимые типы при присваивании: «%s» ← «%s»", тл, тп);
                ошибка_семантики(а,
                    цель_у ? &цель_у->токен : &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }

            if (сужение_запрещено(а, тц, знач_у, тз)) {
                char тл[64], тп[64];
                описание_типа(тц, тл, sizeof(тл));
                описание_типа(тз, тп, sizeof(тп));
                char msg[400];
                snprintf(msg, sizeof(msg),
                    "неявное сужение «%s» ← «%s» теряет данные; используйте "
                    "явное приведение «как<%s>(…)»", тл, тп, тл);
                ошибка_семантики(а, цель_у ? &цель_у->токен : &узел->токен, msg);
                return ТИП_НЕИЗВЕСТЕН_V;
            }

            // Сырой указатель-цель: переприсвоение «нуль» снова делает его
            // доказанно нулевым; провенанс строкового литерала обновляем так же.
            if (цель_у && цель_у->тип == УЗЕЛ_ИДЕНТИФИКАТОР
                && тц.указатели > 0 && !тц.срез) {
                Символ *цс = найти_символ(а, цель_у->токен.начало,
                                          цель_у->токен.длина);
                if (цс) {
                    цс->может_быть_нуль = (знач_у && знач_у->тип == УЗЕЛ_НУЛЬ);
                    цс->из_литерала = знач_у && (знач_у->тип == УЗЕЛ_СТРОКА
                        || (знач_у->тип == УЗЕЛ_ИДЕНТИФИКАТОР
                            && указатель_из_литерала(а, знач_у)));
                    // Borrow-провенанс переприсваивается ВСЕГДА (закрываем дыру:
                    // «п = параметр» затем «п = strchr(…)» должно снять безопасность).
                    цс->безопасен_для_возврата = знач_у &&
                        ((знач_у->тип == УЗЕЛ_НУЛЬ)
                         || (знач_у->тип == УЗЕЛ_СТРОКА)
                         || (знач_у->тип == УЗЕЛ_ИДЕНТИФИКАТОР
                             && указатель_безопасен_для_возврата(а, знач_у)));
                }
            }
            return тц;
        }

        case УЗЕЛ_ВЫЗОВ: {
            // Вызов через указатель на функцию: имя вызова — переменная-указатель
            // на функцию. Проверяем не-нулевой (иначе крах), число и типы
            // аргументов по сигнатуре; тип результата — тип возврата псевдонима.
            {
                Символ *фпс = найти_символ(а, узел->токен.начало, узел->токен.длина);
                if (фпс && фпс->тип == ТОКЕН_ФУНКУКАЗАТЕЛЬ) {
                    ОписаниеТипфункции *т = &а->типыфункций[фпс->индекс_структуры];
                    if (фпс->может_быть_нуль) {
                        ошибка_семантики(а, &узел->токен,
                            "вызов указателя на функцию, который может быть «нуль» "
                            "(присвойте функцию перед вызовом)");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    if (узел_число_детей(узел) != т->число_параметров) {
                        char msg[256];
                        snprintf(msg, sizeof msg,
                            "указатель на функцию ожидает %zu аргумент(ов), передано %zu",
                            т->число_параметров, узел_число_детей(узел));
                        ошибка_семантики(а, &узел->токен, msg);
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                        ИнфоТипа та = типизировать_выражение(а, узел_ребёнок(узел, i));
                        if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                        ИнфоТипа тп = { т->параметры[i].тип, т->параметры[i].указатели,
                                        т->параметры[i].беззнаковый, т->параметры[i].срез, -1 };
                        if (та.тип != ТОКЕН_НЕИЗВЕСТНЫЙ
                            && !совместимы_для_присваивания(тп, та)) {
                            ошибка_семантики(а, &узел->токен,
                                "несовместимый тип аргумента при вызове указателя на функцию");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                    }
                    return (ИнфоТипа){ т->тип_возврата, т->указатели_возврата,
                                       т->беззнаковый_возврата, 0,
                                       т->индекс_структуры_возврата };
                }
            }
            // Встроенная «длина(срез)» → целое32 (поле .длина).
            if (токен_текст_равен(&узел->токен, "длина")) {
                if (узел_число_детей(узел) != 1) {
                    ошибка_семантики(а, &узел->токен,
                        "«длина» принимает ровно один аргумент-срез");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                ИнфоТипа арг = типизировать_выражение(а, узел_ребёнок(узел, 0));
                if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                if (!арг.срез && арг.тип != ТОКЕН_НЕИЗВЕСТНЫЙ) {
                    ошибка_семантики(а, &узел->токен,
                        "«длина» применима только к срезу");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                return ТИП_ЦЕЛОЕ32_V;
            }
            // Встроенная «выделить(N)» допустима только как инициализатор
            // объявления среза (там известен тип элемента) — см. УЗЕЛ_ОБЪЯВЛЕНИЕ.
            if (токен_текст_равен(&узел->токен, "выделить")) {
                ошибка_семантики(а, &узел->токен,
                    "«выделить» допустимо только при объявлении среза: "
                    "срез<T> имя = выделить(N)");
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            if (токен_текст_равен(&узел->токен, "клон")) {
                ошибка_семантики(а, &узел->токен,
                    "«клон» допустим только при объявлении среза: "
                    "срез<T> имя = клон(другой)");
                return ТИП_НЕИЗВЕСТЕН_V;
            }
            // Сигнатура (если функция известна) — нужна для «владеет»-параметров.
            СигнатураФункции *сиг = nullptr;
            for (size_t i = 0; i < а->число_функций; ++i) {
                if (байты_равны(а->функции[i].имя, strlen(а->функции[i].имя),
                                узел->токен.начало, узел->токен.длина)) {
                    сиг = &а->функции[i];
                    break;
                }
            }

            // Функция не объявлена ни в Konda, ни в подключённых заголовках —
            // предупреждение с позицией в .конда (вместо тихого делегирования
            // компилятору C, который сообщил бы об ошибке по сгенерированному
            // .c). Только когда заголовки сканировались (иначе не знаем, что
            // валидно из C, и молчим — escape-hatch для interop).
            const ВнешняяФункция *внеш = а->внешние
                ? найти_внешнюю(а->внешние, узел->токен.начало, узел->токен.длина)
                : nullptr;
            if (а->внешние && !сиг && !внеш) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                    "функция «%.*s» не объявлена: не найдена ни среди функций "
                    "Konda, ни в подключённых заголовках (проверьте имя и "
                    "«#содержит»)",
                    (int)узел->токен.длина, узел->токен.начало);
                предупреждение_семантики(а, &узел->токен, msg);
            }
            // Известная не-вариативная функция (Konda или C): сверяем число
            // аргументов. Вариативные и функции с неизвестной арностью («()»)
            // пропускаем. Для C-функций типы не сверяем (отображение C→Konda
            // лоссовое) — только количество.
            if (внеш && !сиг && !внеш->переменное_число
                && внеш->число_параметров >= 0
                && (int)узел_число_детей(узел) != внеш->число_параметров) {
                char msg[256];
                snprintf(msg, sizeof(msg),
                    "функция «%.*s» ожидает %d аргумент(ов), передано %zu",
                    (int)узел->токен.длина, узел->токен.начало,
                    внеш->число_параметров, узел_число_детей(узел));
                предупреждение_семантики(а, &узел->токен, msg);
            }

            // Аргументы типизируются (заодно проверки use-after-move и т.п.).
            // Аргумент, идущий в «владеет»-параметр, перемещается в функцию.
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                Узел *арг = узел_ребёнок(узел, i);
                // Аргумент в параметр по ссылке («изменяемый»/«вывод»): должен
                // быть переменной (lvalue) совпадающего типа. «изменяемый» — уже
                // инициализированной (читается); «вывод» — не обязательно (тело
                // присвоит), и после вызова переменная считается инициализированной.
                if (сиг && i < сиг->число_параметров
                    && сиг->параметры[i].по_ссылке > 0) {
                    int режим = сиг->параметры[i].по_ссылке;
                    if (арг->тип != УЗЕЛ_ИДЕНТИФИКАТОР) {
                        ошибка_семантики(а, &узел->токен, режим == 2
                            ? "аргумент для «вывод» должен быть переменной (lvalue)"
                            : "аргумент для «изменяемый» должен быть переменной (lvalue)");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    Символ *ас = найти_символ(а, арг->токен.начало, арг->токен.длина);
                    if (!ас) {
                        char msg[256];
                        snprintf(msg, sizeof msg, "неизвестная переменная «%.*s»",
                                 (int)арг->токен.длина, арг->токен.начало);
                        ошибка_семантики(а, &арг->токен, msg);
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    ИнфоПараметра *п = &сиг->параметры[i];
                    if (ас->тип != п->тип || ас->указатели != п->указатели
                        || ас->беззнаковый != п->беззнаковый || ас->срез) {
                        ошибка_семантики(а, &арг->токен,
                            "тип аргумента не совпадает с типом параметра по ссылке");
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    if (режим == 1 && !ас->инициализирована) {
                        char msg[256];
                        snprintf(msg, sizeof msg,
                            "«изменяемый»-аргумент «%.*s» должен быть инициализирован "
                            "до вызова", (int)арг->токен.длина, арг->токен.начало);
                        ошибка_семантики(а, &арг->токен, msg);
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                    ас->инициализирована = 1;  // out присвоит; ref мог изменить
                    continue;
                }
                // Аргумент в параметр-указатель на функцию: имя подходящей функции
                // (в общем случае имя функции — не значение, обычная типизация дала
                // бы «неизвестная переменная») или указатель того же типа.
                if (сиг && i < сиг->число_параметров
                    && сиг->параметры[i].тип == ТОКЕН_ФУНКУКАЗАТЕЛЬ
                    && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                    int idx = сиг->параметры[i].индекс_структуры;
                    СигнатураФункции *сф = найти_сигнатуру_по_имени(а,
                        арг->токен.начало, арг->токен.длина);
                    Символ *ис = найти_символ(а, арг->токен.начало, арг->токен.длина);
                    if (idx >= 0 && сф && сигнатура_совпадает_с_типфункцией(сф,
                            &а->типыфункций[idx])) {
                        continue;  // валидная функция-аргумент
                    }
                    if (ис && ис->тип == ТОКЕН_ФУНКУКАЗАТЕЛЬ
                        && ис->индекс_структуры == idx) {
                        continue;  // указатель того же типа
                    }
                    ошибка_семантики(а, &арг->токен,
                        "аргумент не подходит к параметру-указателю на функцию: "
                        "нужна функция с совпадающей сигнатурой или указатель того же типа");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                ИнфоТипа тарг = типизировать_выражение(а, арг);
                if (а->диаг->ошибка) return ТИП_НЕИЗВЕСТЕН_V;
                // Структуру с деструктором по значению передавать нельзя
                // (копия → двойной вызов деструктора) — только по указателю.
                if (структура_с_деструктором(а, тарг)) {
                    ошибка_семантики(а, &арг->токен,
                        "структуру с деструктором нельзя передавать по значению "
                        "(передавайте по указателю «Имя*»)");
                    return ТИП_НЕИЗВЕСТЕН_V;
                }
                // Авто-конверсия массива в срез: если параметр объявлен
                // «срез<T>», а аргумент — переменная-массив `T[N]`, на месте
                // вызова кодоген построит дескриптор {&арр[0], N, sizeof(T)}.
                // Бесплатно: массив остаётся на стеке, никаких аллокаций.
                if (сиг && i < сиг->число_параметров
                    && сиг->параметры[i].срез
                    && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                    Символ *ис = найти_символ(а, арг->токен.начало, арг->токен.длина);
                    if (ис && ис->массив && ис->длина_известна) {
                        ИнфоПараметра *п = &сиг->параметры[i];
                        if (ис->тип != п->тип || ис->беззнаковый != п->беззнаковый
                            || ис->указатели != п->указатели) {
                            char msg[256];
                            snprintf(msg, sizeof(msg),
                                "тип элементов массива «%.*s» не совпадает с типом "
                                "среза-параметра %zu функции «%.*s»",
                                (int)арг->токен.длина, арг->токен.начало,
                                i + 1,
                                (int)узел->токен.длина, узел->токен.начало);
                            ошибка_семантики(а, &арг->токен, msg);
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                        // Аннотация для кодогена: «здесь нужна авто-конверсия
                        // массива в Срез». Сам массив остаётся доступен через имя.
                        арг->массив = 1;
                        арг->размер_массива = ис->длина;
                        арг->тип_данных = ис->тип;
                        арг->беззнаковый = ис->беззнаковый;
                        арг->указатели = ис->указатели;
                    }
                }
                // Передача массива в СЫРОЙ указательный параметр известной
                // функции теряет длину → в теле возможен выход за границы без
                // проверки. Требуем срез<T> (у него есть длина и рантайм-проверки).
                // Для внешних (C) функций сигнатуры нет → decay сохраняется.
                if (сиг && i < сиг->число_параметров
                    && !сиг->параметры[i].срез
                    && сиг->параметры[i].указатели > 0
                    && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                    Символ *ис = найти_символ(а, арг->токен.начало, арг->токен.длина);
                    if (ис && ис->массив && ис->длина_известна) {
                        char msg[256];
                        snprintf(msg, sizeof(msg),
                            "передача массива «%.*s» в сырой указательный параметр "
                            "теряет длину (риск выхода за границы в теле); объявите "
                            "параметр как «срез<T>»",
                            (int)арг->токен.длина, арг->токен.начало);
                        ошибка_семантики(а, &арг->токен, msg);
                        return ТИП_НЕИЗВЕСТЕН_V;
                    }
                }
                if (сиг && i < сиг->число_параметров && сиг->владеет_параметра[i]
                    && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                    Символ *ис = найти_символ(а, арг->токен.начало, арг->токен.длина);
                    if (ис && ис->срез) {
                        if (!ис->владеет_срезом) {
                            ошибка_семантики(а, &арг->токен,
                                "нельзя передать заимствованный срез в параметр "
                                "«владеет»: владелец находится снаружи");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                        // Как и для move-через-присваивание: передача владельца
                        // из объемлющей области в «владеет»-параметр внутри цикла
                        // запрещена — на втором витке источник уже перемещён
                        // (use-after-move), а однопроходный анализ это без
                        // фикс-точки не гарантирует.
                        if (ис->глубина < а->глубина && а->глубина_цикла > 0) {
                            ошибка_семантики(а, &арг->токен,
                                "перемещение среза из объемлющей области "
                                "внутри цикла запрещено (на втором витке "
                                "источник уже перемещён)");
                            return ТИП_НЕИЗВЕСТЕН_V;
                        }
                        ис->перемещён = 1;  // владение ушло в функцию-потребитель
                        ис->токен_move = арг->токен;
                    }
                }
            }

            if (сиг) {
                return (ИнфоТипа){ сиг->тип_возврата, сиг->указатели_возврата,
                                   сиг->беззнаковый, сиг->срез,
                                   сиг->индекс_структуры_возврата };
            }
            return ТИП_НЕИЗВЕСТЕН_V;
        }

        default:
            return ТИП_НЕИЗВЕСТЕН_V;
    }
}

