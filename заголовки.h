#ifndef ZAGOLOVKI_H
#define ZAGOLOVKI_H

#include <stddef.h>
#include "аст.h"

// ─── Внешние (C) функции, «увиденные» через заголовки ───────────────────────
//
// Транспилятор читает C-заголовки, подключённые директивами «#содержит», и
// извлекает прототипы функций, чтобы знать, какие имена валидны, — вместо того
// чтобы делегировать проверку целиком компилятору C. Извлекаются имя, число
// параметров и признак вариативности (эвристический разбор прототипов; точное
// сопоставление типов не выполняется — типы C отображаются на Konda лишь
// приблизительно).

typedef struct {
    char имя[128];
    int число_параметров;   // -1 если неизвестно (пустые «()», K&R)
    int переменное_число;   // 1 — вариативная функция («...»)
} ВнешняяФункция;

typedef struct ТаблицаВнешних ТаблицаВнешних;

ТаблицаВнешних *внешние_создать(void);
void            внешние_освободить(ТаблицаВнешних *);

// Сканирует заголовки из «#содержит»-директив программы (узлы препроцессора в
// корне AST), разрешая пути и следуя вложенным «#include». база_путей —
// каталог исходного .конда для разрешения «локальных.h» (может быть NULL:
// тогда используются только системные каталоги).
void собрать_внешние_функции(const Узел *корень, const char *база_путей,
                             ТаблицаВнешних *куда);

// Поиск внешней функции по имени (nullptr, если не найдена).
const ВнешняяФункция *найти_внешнюю(const ТаблицаВнешних *т,
                                    const char *имя, size_t длина);

size_t                внешние_число(const ТаблицаВнешних *т);
const ВнешняяФункция *внешние_получить(const ТаблицаВнешних *т, size_t i);

#endif /* ZAGOLOVKI_H */
