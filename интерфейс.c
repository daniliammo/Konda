// Konda-интерфейс библиотек: эмиссия «.и», чтение и сбор у потребителя.
// Синтаксически простой построчный формат — отдельный маленький ридер, чтобы не
// трогать основной парсер (он не умеет прототипы без тела).

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <sys/stat.h>

#include "интерфейс.h"

// ─── таблица ─────────────────────────────────────────────────────────────────

struct ТаблицаИнтерфейса {
    ДинМассив функции;     // ИнтФункция
    ДинМассив библиотеки;  // char[512] — пути к .so для линковки
};

ТаблицаИнтерфейса *интерфейс_создать(void)
{
    ТаблицаИнтерфейса *т = calloc(1, sizeof(*т));
    if (!т) { perror("calloc"); exit(1); }
    дин_массив_инициализировать(&т->функции, sizeof(ИнтФункция));
    дин_массив_инициализировать(&т->библиотеки, 512);
    return т;
}

void интерфейс_освободить(ТаблицаИнтерфейса *т)
{
    if (!т) return;
    дин_массив_освободить(&т->функции);
    дин_массив_освободить(&т->библиотеки);
    free(т);
}

size_t интерфейс_число(const ТаблицаИнтерфейса *т) { return т ? т->функции.длина : 0; }

const ИнтФункция *интерфейс_получить(const ТаблицаИнтерфейса *т, size_t i)
{
    if (!т || i >= т->функции.длина) return nullptr;
    return (const ИнтФункция *)((char *)т->функции.данные + i * т->функции.размер_элемента);
}

size_t интерфейс_число_библиотек(const ТаблицаИнтерфейса *т) { return т ? т->библиотеки.длина : 0; }

const char *интерфейс_библиотека(const ТаблицаИнтерфейса *т, size_t i)
{
    if (!т || i >= т->библиотеки.длина) return nullptr;
    return (const char *)((char *)т->библиотеки.данные + i * т->библиотеки.размер_элемента);
}

// ─── отображение тип ↔ ключевое слово ───────────────────────────────────────

static const char *тип_в_кейворд(ТипТокена т)
{
    switch (т) {
        case ТОКЕН_ЦЕЛОЕ8:  return "целое8";
        case ТОКЕН_ЦЕЛОЕ16: return "целое16";
        case ТОКЕН_ЦЕЛОЕ32: return "целое32";
        case ТОКЕН_ЦЕЛОЕ64: return "целое64";
        case ТОКЕН_БАЙТ:        return "байт";
        case ТОКЕН_ВЕЩЕСТВЕННОЕ: return "вещественное";
        case ТОКЕН_ВЕЩЕСТВЕННОЕ64: return "вещественное64";
        case ТОКЕН_ТИП_СИМВОЛ: return "символ";
        case ТОКЕН_ЛОГИЧЕСКИЙ: return "логический";
        case ТОКЕН_НИЧЕГО:     return "ничего";
        default: return "целое32";  // именованные типы сюда не доходят (пропускаются)
    }
}

static ТипТокена тип_из_кейворда(const char *с)
{
    if (strcmp(с, "целое8") == 0)  return ТОКЕН_ЦЕЛОЕ8;
    if (strcmp(с, "целое16") == 0) return ТОКЕН_ЦЕЛОЕ16;
    if (strcmp(с, "целое32") == 0) return ТОКЕН_ЦЕЛОЕ32;
    if (strcmp(с, "целое64") == 0) return ТОКЕН_ЦЕЛОЕ64;
    if (strcmp(с, "байт") == 0)         return ТОКЕН_БАЙТ;
    if (strcmp(с, "вещественное") == 0)   return ТОКЕН_ВЕЩЕСТВЕННОЕ;
    if (strcmp(с, "вещественное64") == 0) return ТОКЕН_ВЕЩЕСТВЕННОЕ64;
    if (strcmp(с, "символ") == 0)     return ТОКЕН_ТИП_СИМВОЛ;
    if (strcmp(с, "логический") == 0) return ТОКЕН_ЛОГИЧЕСКИЙ;
    if (strcmp(с, "ничего") == 0)     return ТОКЕН_НИЧЕГО;
    return ТОКЕН_НЕИЗВЕСТНЫЙ;
}

static int именованный_тип(ТипТокена т)
{
    return т == ТОКЕН_СТРУКТУРА || т == ТОКЕН_ПЕРЕЧИСЛЕНИЕ || т == ТОКЕН_ФУНКУКАЗАТЕЛЬ;
}

// ─── эмиссия ─────────────────────────────────────────────────────────────────

static void эмит_число(БуферКода *б, int v)
{
    char буф[16];
    snprintf(буф, sizeof(буф), "%d", v);
    буфер_кода_добавить(б, буф);
}

// Проверка: у функции нет именованных типов в возврате/параметрах (v1).
static int функция_экспортируема(const Узел *ф)
{
    if (именованный_тип(ф->тип_данных)) return 0;
    size_t всего = узел_число_детей(ф);
    for (size_t j = 0; j + 1 < всего; ++j) {
        const Узел *п = узел_ребёнок(ф, j);
        if (!п || п->тип != УЗЕЛ_ПАРАМЕТР) continue;
        if (именованный_тип(п->тип_данных)) return 0;
    }
    return 1;
}

void эмит_интерфейс(const Узел *корень, БуферКода *буфер)
{
    буфер_кода_добавить(буфер, "konda-интерфейс 1\n");
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        const Узел *ф = узел_ребёнок(корень, i);
        if (!ф || ф->тип != УЗЕЛ_ФУНКЦИЯ) continue;
        if (ф->статический || ф->это_main) continue;      // не экспортируется
        if (ф->токен.длина == 0) continue;
        if (!функция_экспортируема(ф)) continue;           // именованные типы — непрозрачны

        буфер_кода_добавить(буфер, "Ф ");
        буфер_кода_добавить_отрезок(буфер, ф->токен.начало, ф->токен.длина);
        буфер_кода_добавить(буфер, "\n");

        // Возврат: В <тип> <беззн> <указ> <срез>
        буфер_кода_добавить(буфер, "В ");
        буфер_кода_добавить(буфер, тип_в_кейворд(ф->тип_данных));
        буфер_кода_добавить(буфер, " "); эмит_число(буфер, ф->беззнаковый);
        буфер_кода_добавить(буфер, " "); эмит_число(буфер, ф->указатели);
        буфер_кода_добавить(буфер, " "); эмит_число(буфер, ф->срез);
        буфер_кода_добавить(буфер, "\n");

        // Параметры: П <тип> <беззн> <указ> <срез> <массив> <владеет> <по_ссылке>
        size_t всего = узел_число_детей(ф);
        for (size_t j = 0; j + 1 < всего; ++j) {
            const Узел *п = узел_ребёнок(ф, j);
            if (!п || п->тип != УЗЕЛ_ПАРАМЕТР) continue;
            буфер_кода_добавить(буфер, "П ");
            буфер_кода_добавить(буфер, тип_в_кейворд(п->тип_данных));
            буфер_кода_добавить(буфер, " "); эмит_число(буфер, п->беззнаковый);
            буфер_кода_добавить(буфер, " "); эмит_число(буфер, п->указатели);
            буфер_кода_добавить(буфер, " "); эмит_число(буфер, п->срез);
            буфер_кода_добавить(буфер, " "); эмит_число(буфер, п->массив);
            буфер_кода_добавить(буфер, " "); эмит_число(буфер, п->владеет);
            буфер_кода_добавить(буфер, " "); эмит_число(буфер, п->по_ссылке);
            буфер_кода_добавить(буфер, "\n");
        }
        буфер_кода_добавить(буфер, "К\n");
    }
}

// ─── чтение ──────────────────────────────────────────────────────────────────

static int файл_есть(const char *путь)
{
    struct stat st;
    return stat(путь, &st) == 0 && S_ISREG(st.st_mode);
}

// Разбор «.и» в таблицу. Токен-поток (границы строк не важны): «Ф имя» →
// новая функция; «В …» → возврат; «П …» → параметр; «К» → завершить.
static void загрузить_интерфейс(const char *путь, ТаблицаИнтерфейса *куда)
{
    size_t размер = 0;
    char *текст = прочитать_файл((char *)путь, &размер);
    if (!текст) return;

    ИнтФункция тек;
    memset(&тек, 0, sizeof(тек));
    int в_функции = 0;

    char *сост = nullptr;
    char *т = strtok_r(текст, " \t\r\n", &сост);
    while (т) {
        if (strcmp(т, "Ф") == 0) {
            if (в_функции) дин_массив_добавить(&куда->функции, &тек);
            memset(&тек, 0, sizeof(тек));
            в_функции = 1;
            char *имя = strtok_r(nullptr, " \t\r\n", &сост);
            if (имя) {
                size_t n = strlen(имя);
                if (n >= sizeof(тек.имя)) n = sizeof(тек.имя) - 1;
                memcpy(тек.имя, имя, n);
                тек.имя[n] = '\0';
            }
        } else if (strcmp(т, "В") == 0) {
            char *a = strtok_r(nullptr, " \t\r\n", &сост);
            char *b = strtok_r(nullptr, " \t\r\n", &сост);
            char *c = strtok_r(nullptr, " \t\r\n", &сост);
            char *d = strtok_r(nullptr, " \t\r\n", &сост);
            if (a && b && c && d) {
                тек.тип_возврата = тип_из_кейворда(a);
                тек.беззнаковый_возврата = atoi(b);
                тек.указатели_возврата = atoi(c);
                тек.срез_возврата = atoi(d);
            }
        } else if (strcmp(т, "П") == 0) {
            char *p[7];
            int ок = 1;
            for (int k = 0; k < 7; ++k) { p[k] = strtok_r(nullptr, " \t\r\n", &сост); if (!p[k]) ок = 0; }
            if (ок && тек.число_параметров < ИНТ_МАКС_ПАРАМ) {
                ИнтПараметр *ип = &тек.параметры[тек.число_параметров++];
                ип->тип = тип_из_кейворда(p[0]);
                ип->беззнаковый = atoi(p[1]);
                ип->указатели = atoi(p[2]);
                ип->срез = atoi(p[3]);
                ип->массив = atoi(p[4]);
                ип->владеет = atoi(p[5]);
                ип->по_ссылке = atoi(p[6]);
            }
        } else if (strcmp(т, "К") == 0) {
            if (в_функции) { дин_массив_добавить(&куда->функции, &тек); в_функции = 0; }
            memset(&тек, 0, sizeof(тек));
        }
        // прочее (заголовок «konda-интерфейс», «1») — пропускаем
        т = strtok_r(nullptr, " \t\r\n", &сост);
    }
    if (в_функции) дин_массив_добавить(&куда->функции, &тек);
    free(текст);
}

// ─── сбор у потребителя ─────────────────────────────────────────────────────

// Извлекает из «#содержит "имя"» локальное имя заголовка (не системное «<>»).
static int локальный_заголовок(const Узел *узел, char *имя, size_t cap)
{
    char строка[1024];
    size_t дл = узел->токен.длина < sizeof(строка) - 1 ? узел->токен.длина : sizeof(строка) - 1;
    memcpy(строка, узел->токен.начало, дл);
    строка[дл] = '\0';
    const char *p = строка;
    while (*p == ' ' || *p == '\t') p++;
    if (*p != '#') return 0;
    p++;
    while (*p == ' ' || *p == '\t') p++;
    // «содержит» или «include»
    if (strncmp(p, "содержит", strlen("содержит")) == 0) p += strlen("содержит");
    else if (strncmp(p, "include", 7) == 0) p += 7;
    else return 0;
    while (*p == ' ' || *p == '\t') p++;
    if (*p != '"') return 0;  // только локальные «"…"»
    p++;
    size_t k = 0;
    while (*p && *p != '"' && *p != '\n' && k + 1 < cap) имя[k++] = *p++;
    имя[k] = '\0';
    return k > 0;
}

void собрать_интерфейсы(const Узел *корень, const char *база_путей,
                        ТаблицаИнтерфейса *куда)
{
    if (!корень || !куда) return;
    const char *база = (база_путей && база_путей[0]) ? база_путей : ".";
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        const Узел *узел = узел_ребёнок(корень, i);
        if (!узел || узел->тип != УЗЕЛ_ПРЕПРОЦЕССОР) continue;
        char имя[512];
        if (!локальный_заголовок(узел, имя, sizeof(имя))) continue;

        // «X.h» → базовое «X»; ищем «X.и» и «X.so» рядом.
        char основа[512];
        snprintf(основа, sizeof(основа), "%s", имя);
        char *точка = strrchr(основа, '.');
        if (точка && strcmp(точка, ".h") == 0) *точка = '\0';

        char путь_и[1024], путь_so[1024];
        snprintf(путь_и, sizeof(путь_и), "%s/%s.и", база, основа);
        snprintf(путь_so, sizeof(путь_so), "%s/%s.so", база, основа);

        if (файл_есть(путь_и)) {
            загрузить_интерфейс(путь_и, куда);
            if (файл_есть(путь_so)) {
                char абс[512];
                snprintf(абс, sizeof(абс), "%.511s", путь_so);
                дин_массив_добавить(&куда->библиотеки, абс);
            }
        }
    }
}
