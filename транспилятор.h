#ifndef TOKENIZER_H
#define TOKENIZER_H

#include <stdio.h>

typedef enum {
    ТОКЕН_ИДЕНТИФИКАТОР,
    ТОКЕН_ЧИСЛО,
    ТОКЕН_СТРОКА,
    ТОКЕН_СИМВОЛ,

    // Операторы
    ТОКЕН_НЕИЗВЕСТНЫЙ_ОПЕРАТОР,
    ТОКЕН_ЗВЕЗДА,
    ТОКЕН_КОСАЯ_ЧЕРТА,
    ТОКЕН_ПЛЮС,
    ТОКЕН_МИНУС,
    ТОКЕН_РАВНО,
    ТОКЕН_АМПЕРСАНД, // &
    ТОКЕН_ПРОЦЕНТ,
    ТОКЕН_ПРЯМАЯ_ЧЕРТА,
    ТОКЕН_ВОСКЛИЦАТЕЛЬНЫЙ_ЗНАК,
    ТОКЕН_СТРЕЛКА_ВЛЕВО,
    ТОКЕН_СТРЕЛКА_ВПРАВО,
    ТОКЕН_СТРЕЛКА_ВВЕРХ,
    ТОКЕН_ТИЛЬДА,
    ТОКЕН_ИНКРЕМЕНТ,
    ТОКЕН_ДЕКРЕМЕНТ,

    ТОКЕН_РАЗМЕР_ОБЬЕКТА, // sizeof
    ТОКЕН_ЕСЛИ, // if
    ТОКЕН_ИНАЧЕ, // else
    ТОКЕН_ИЛИ, // or
    ТОКЕН_ПОКА, // while
    ТОКЕН_ДЛЯ, // for
    ТОКЕН_ДЛЯ_КАЖДОГО, // foreach: для_каждого (имя из коллекция) { ... }
    ТОКЕН_ИЗ,          // из — разделитель в для_каждого
    ТОКЕН_ВЕРНУТЬ, // return
    ТОКЕН_ТОЧКА_ВХОДА,
    ТОКЕН_НУЛЬ, // nullptr

    // Примитивные типы (ключевые слова, не идентификаторы)
    ТОКЕН_ЦЕЛОЕ8,       // int8
    ТОКЕН_ЦЕЛОЕ16,      // int16
    ТОКЕН_ЦЕЛОЕ32,      // int32
    ТОКЕН_ЦЕЛОЕ64,      // int64
    ТОКЕН_БАЙТ,         // byte / uint8_t
    ТОКЕН_ВЕЩЕСТВЕННОЕ, // float
    ТОКЕН_ТИП_СИМВОЛ,   // char (тип, не литерал '...')
    ТОКЕН_НЕПОДПИСАННЫЙ,// unsigned
    ТОКЕН_СРЕЗ,         // срез<T> — толстый указатель {данные, длина}
    ТОКЕН_СТАТИЧЕСКИЙ,  // static — внутренняя связь / статическая длительность
    ТОКЕН_ВЛАДЕЕТ,      // владеет — параметр забирает владение срезом (потребитель)
    ТОКЕН_СТРУКТУРА,    // структура — пользовательский составной тип
    ТОКЕН_ПЕРЕЧИСЛЕНИЕ, // перечисление — пользовательский тип-перечисление (enum)
    ТОКЕН_КАК,          // как<тип>(выр) — явное приведение типа (cast)
    ТОКЕН_ВЫБОР,        // выбор выр { ВАРИАНТ { … } } — match по перечислению

    // Пунктуация
    // ТОКЕН_ПУНКТУАЦИЯ,
    ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ,
    ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ,

    ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ,
    ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ,

    ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ,
    ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ,

    ТОКЕН_ТОЧКА,
    ТОКЕН_ЗАПЯТАЯ,

    ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ,

    ТОКЕН_РЕШЕТКА,


    ТОКЕН_ПРЕПРОЦЕССОР,
    ТОКЕН_КОММЕНТАРИЙ,
    ТОКЕН_НОВАЯ_СТРОКА,
    ТОКЕН_КОНЕЦ_ФАЙЛА,
    ТОКЕН_НЕИЗВЕСТНЫЙ
} ТипТокена;

typedef struct {
    ТипТокена тип;
    int строка;
    int позиция_в_строке;
    const char *начало;
    size_t длина;
} Токен;

typedef struct {
    void *данные;
    size_t длина;
    size_t емкость;
    size_t размер_элемента;
} ДинМассив;

void дин_массив_инициализировать(ДинМассив *a, size_t размер_элемента);
void дин_массив_освободить(ДинМассив *a);
void дин_массив_добавить(ДинМассив *a, const void *элемент);
void *дин_массив_получить(ДинМассив *a, size_t индекс);
void дин_массив_удалить_последний(ДинМассив *a);

typedef struct {
    ДинМассив базовый;
} МассивТокенов;

void массив_токенов_инициализировать(МассивТокенов *a);
void массив_токенов_освободить(МассивТокенов *a);
void массив_токенов_добавить(
    МассивТокенов *a,
    ТипТокена тип,
    int строка,
    int позиция_в_строке,
    const char *начало,
    size_t длина
);

typedef struct {
    ДинМассив базовый;
} БуферКода;

void буфер_кода_инициализировать(БуферКода *b);
void буфер_кода_освободить(БуферКода *b);
void буфер_кода_добавить(БуферКода *b, const char *текст);
void буфер_кода_добавить_отрезок(БуферКода *b, const char *начало, size_t длина);
const char *буфер_кода_строка(БуферКода *b);

const char* имя_типа_токена(ТипТокена тип);
char* прочитать_файл(char *путь_к_файлу, size_t *возвращаемое_значение_размер);
МассивТокенов токенизировать_код(size_t размер, char *буфер, int печатать_отладку);
int скомпилировать_си(const char *путь_си, const char *путь_исполняемого);
void очистка(МассивТокенов *токены, char* буфер);

#endif
