#include <stdio.h>
#include <string.h>

#include "аст.h"

typedef struct {
    БуферКода *буфер;
    int в_точке_входа;
} Кодоген;

static int срез_равен(const char *начало, size_t длина, const char *текст)
{
    size_t n = strlen(текст);
    return длина == n && memcmp(начало, текст, n) == 0;
}

static void имя_в_си(const Кодоген *к, const char *начало, size_t длина, char *out, size_t out_cap)
{
    if (к->в_точке_входа && срез_равен(начало, длина, "точка_входа")) {
        snprintf(out, out_cap, "main");
        return;
    }
    if (к->в_точке_входа && срез_равен(начало, длина, "количество_аргументов")) {
        snprintf(out, out_cap, "argc");
        return;
    }
    if (к->в_точке_входа && срез_равен(начало, длина, "аргументы")) {
        snprintf(out, out_cap, "argv");
        return;
    }
    size_t n = длина < out_cap - 1 ? длина : out_cap - 1;
    memcpy(out, начало, n);
    out[n] = '\0';
}

static void тип_токена_в_си(ТипТокена t, char *out, size_t out_cap)
{
    switch (t) {
        case ТОКЕН_ЦЕЛОЕ8:       snprintf(out, out_cap, "int8_t");  break;
        case ТОКЕН_ЦЕЛОЕ16:      snprintf(out, out_cap, "int16_t"); break;
        case ТОКЕН_ЦЕЛОЕ32:      snprintf(out, out_cap, "int32_t"); break;
        case ТОКЕН_ЦЕЛОЕ64:      snprintf(out, out_cap, "int64_t"); break;
        case ТОКЕН_БАЙТ:         snprintf(out, out_cap, "uint8_t"); break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ: snprintf(out, out_cap, "float");   break;
        case ТОКЕН_ТИП_СИМВОЛ:   snprintf(out, out_cap, "char");    break;
        default:                 snprintf(out, out_cap, "int");     break;
    }
}

static void эмит(Кодоген *к, const char *текст)
{
    буфер_кода_добавить(к->буфер, текст);
}

static void эмит_отрезок(Кодоген *к, const char *начало, size_t длина)
{
    буфер_кода_добавить_отрезок(к->буфер, начало, длина);
}

static void эмит_имя(Кодоген *к, const Токен *токен)
{
    char имя_си[256];
    имя_в_си(к, токен->начало, токен->длина, имя_си, sizeof(имя_си));
    эмит(к, имя_си);
}

static void кодоген_выражения(Кодоген *к, Узел *узел)
{
    switch (узел->тип) {
        case УЗЕЛ_БИНОП:
            эмит(к, "(");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, " ");
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            эмит(к, " ");
            кодоген_выражения(к, узел_ребёнок(узел, 1));
            эмит(к, ")");
            break;
        case УЗЕЛ_ВЫЗОВ: {
            эмит_имя(к, &узел->токен);
            эмит(к, "(");
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                if (i > 0) {
                    эмит(к, ", ");
                }
                кодоген_выражения(к, узел_ребёнок(узел, i));
            }
            эмит(к, ")");
            break;
        }
        case УЗЕЛ_ИНДЕКС:
            эмит_имя(к, &узел->токен);
            эмит(к, "[");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, "]");
            break;
        case УЗЕЛ_ИДЕНТИФИКАТОР:
            эмит_имя(к, &узел->токен);
            break;
        case УЗЕЛ_ЧИСЛО:
        case УЗЕЛ_СТРОКА:
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            break;
        case УЗЕЛ_НУЛЬ:
            эмит(к, "nullptr");
            break;
        default:
            break;
    }
}

static void кодоген_блока(Кодоген *к, Узел *блок);

static void кодоген_объявления(Кодоген *к, Узел *узел)
{
    char тип_си[64];
    тип_токена_в_си(узел->тип_данных, тип_си, sizeof(тип_си));
    эмит(к, "    ");
    эмит(к, тип_си);
    эмит(к, " ");
    эмит_имя(к, &узел->токен);
    if (узел_число_детей(узел) > 0) {
        эмит(к, " = ");
        кодоген_выражения(к, узел_ребёнок(узел, 0));
    }
    эмит(к, ";\n");
}

static void кодоген_если(Кодоген *к, Узел *узел)
{
    эмит(к, "    if (");
    кодоген_выражения(к, узел_ребёнок(узел, 0));
    эмит(к, ") {\n");
    кодоген_блока(к, узел_ребёнок(узел, 1));

    Узел *иначе = узел_ребёнок(узел, 2);
    if (иначе) {
        эмит(к, "    } else {\n");
        кодоген_блока(к, иначе);
    }
    эмит(к, "    }\n");
}

static void кодоген_оператора(Кодоген *к, Узел *узел)
{
    switch (узел->тип) {
        case УЗЕЛ_ЕСЛИ:
            кодоген_если(к, узел);
            break;
        case УЗЕЛ_ОБЪЯВЛЕНИЕ:
            кодоген_объявления(к, узел);
            break;
        case УЗЕЛ_ВЫЗОВ:
            эмит(к, "    ");
            кодоген_выражения(к, узел);
            эмит(к, ";\n");
            break;
        default:
            break;
    }
}

static void кодоген_блока(Кодоген *к, Узел *блок)
{
    if (!блок) {
        return;
    }
    for (size_t i = 0; i < узел_число_детей(блок); ++i) {
        кодоген_оператора(к, узел_ребёнок(блок, i));
    }
}

static void кодоген_препроцессора(Кодоген *к, Узел *узел)
{
    char строка[512];
    size_t n = узел->токен.длина < sizeof(строка) - 1 ? узел->токен.длина : sizeof(строка) - 1;
    memcpy(строка, узел->токен.начало, n);
    строка[n] = '\0';

    const char *содержит = strstr(строка, "содержит");
    if (содержит) {
        size_t смещение = (size_t)(содержит - строка);
        эмит_отрезок(к, строка, смещение);
        эмит(к, "include");
        эмит(к, содержит + strlen("содержит"));
    } else {
        эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
    }
    эмит(к, "\n");
}

static void кодоген_функции(Кодоген *к, Узел *узел)
{
    к->в_точке_входа = узел->это_main;

    char тип_си[64];
    тип_токена_в_си(узел->тип_данных, тип_си, sizeof(тип_си));
    эмит(к, тип_си);
    эмит(к, " ");

    if (узел->это_main) {
        эмит(к, "main");
    } else {
        эмит_имя(к, &узел->токен);
    }
    эмит(к, "(");

    size_t всего = узел_число_детей(узел);
    size_t тело_индекс = всего > 0 ? всего - 1 : 0;

    if (узел->это_main) {
        эмит(к, "int32_t argc, char **argv");
    } else {
        int первый = 1;
        for (size_t i = 0; i < тело_индекс; ++i) {
            Узел *пар = узел_ребёнок(узел, i);
            if (пар->тип != УЗЕЛ_ПАРАМЕТР) {
                continue;
            }
            if (!первый) {
                эмит(к, ", ");
            }
            первый = 0;
            char tsi[64];
            тип_токена_в_си(пар->тип_данных, tsi, sizeof(tsi));
            эмит(к, tsi);
            for (int p = 0; p < пар->указатели; ++p) {
                эмит(к, "*");
            }
            if (пар->массив) {
                эмит(к, "[]");
            }
            эмит(к, " ");
            if (пар->токен.длина > 0) {
                эмит_имя(к, &пар->токен);
            }
        }
    }

    эмит(к, ")\n{\n");

    if (всего > 0) {
        кодоген_блока(к, узел_ребёнок(узел, тело_индекс));
    }

    эмит(к, "    return 0;\n}\n");
    к->в_точке_входа = 0;
}

int кодоген(Узел *корень, БуферКода *буфер)
{
    Кодоген к = { .буфер = буфер, .в_точке_входа = 0 };

    эмит(&к, "/* Сгенерировано транспилятором Konda */\n");
    эмит(&к, "#include <stdint.h>\n");

    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ПРЕПРОЦЕССОР) {
            кодоген_препроцессора(&к, узел);
        } else if (узел->тип == УЗЕЛ_ФУНКЦИЯ) {
            кодоген_функции(&к, узел);
        }
    }

    return 1;
}
