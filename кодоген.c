// Проверка знакового целочисленного переполнения «+/-/*» при неконстантных
// операндах: подписанное переполнение — UB в C. Считаем в long (для целых ≤64
// бит промежуточный результат не переполняет long у +/-, для * используем
// checked-builtin), затем сверяем с диапазоном типа результата. Стиль Rust:
// при переполнении — понятное сообщение и abort.
static const char ПРОЛОГ_ПЕРЕПОЛНЕНИЯ[] =
    "\n"
    "#include <limits.h>\n"
    "[[maybe_unused]] static long арифметика_безопасно(long a, long b, int оп,\n"
    "                                       int разрядность, int строка)\n"
    "{\n"
    "    long r;\n"
    "    int пер = оп == 0 ? __builtin_add_overflow(a, b, &r)\n"
    "            : оп == 1 ? __builtin_sub_overflow(a, b, &r)\n"
    "            :           __builtin_mul_overflow(a, b, &r);\n"
    "    long мин = разрядность >= 64 ? LONG_MIN : -(1L << (разрядность - 1));\n"
    "    long макс = разрядность >= 64 ? LONG_MAX : (1L << (разрядность - 1)) - 1;\n"
    "    if (пер || r < мин || r > макс) {\n"
    "        fprintf(stderr, \"Konda: переполнение целого (строка %d)\\n\", строка);\n"
    "        abort();\n"
    "    }\n"
    "    return r;\n"
    "}\n";

