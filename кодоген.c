#include <stdio.h>
#include <string.h>

#include "аст.h"

#define МАКС_ВЛАДЕЮЩИХ 128

typedef struct {
    БуферКода *буфер;
    int в_точке_входа;
    // Стек владеющих срезов (созданных «выделить») для autofree: при выходе из
    // области и перед «вернуть» вставляется free(.данные) в обратном порядке.
    const Токен *владеющие[МАКС_ВЛАДЕЮЩИХ];
    int уровень_владеющего[МАКС_ВЛАДЕЮЩИХ];
    int перемещён_владеющего[МАКС_ВЛАДЕЮЩИХ];  // владение ушло (move) — не free
    size_t число_владеющих;
    int уровень_блока;
    char тип_возврата_си[64];  // тип возврата текущей функции (для «вернуть»)
    int срез_возврата;         // функция возвращает срез
} Кодоген;

static int срез_равен(const char *начало, size_t длина, const char *текст)
{
    size_t n = strlen(текст);
    return длина == n && memcmp(начало, текст, n) == 0;
}

static void имя_в_си(const Кодоген *к, const char *начало, size_t длина, char *out, size_t out_cap)
{
    if (к->в_точке_входа && срез_равен(начало, длина, "точка_входа")) {
        snprintf(out, out_cap, "main");
        return;
    }
    if (к->в_точке_входа && срез_равен(начало, длина, "количество_аргументов")) {
        snprintf(out, out_cap, "argc");
        return;
    }
    if (к->в_точке_входа && срез_равен(начало, длина, "аргументы")) {
        snprintf(out, out_cap, "argv");
        return;
    }
    size_t n = длина < out_cap - 1 ? длина : out_cap - 1;
    memcpy(out, начало, n);
    out[n] = '\0';
}

static void тип_токена_в_си(ТипТокена t, int беззнаковый, char *out, size_t out_cap)
{
    switch (t) {
        case ТОКЕН_ЦЕЛОЕ8:       snprintf(out, out_cap, беззнаковый ? "uint8_t"  : "int8_t");  break;
        case ТОКЕН_ЦЕЛОЕ16:      snprintf(out, out_cap, беззнаковый ? "uint16_t" : "int16_t"); break;
        case ТОКЕН_ЦЕЛОЕ32:      snprintf(out, out_cap, беззнаковый ? "uint32_t" : "int32_t"); break;
        case ТОКЕН_ЦЕЛОЕ64:      snprintf(out, out_cap, беззнаковый ? "uint64_t" : "int64_t"); break;
        case ТОКЕН_БАЙТ:         snprintf(out, out_cap, "uint8_t"); break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ: snprintf(out, out_cap, "float");   break;
        case ТОКЕН_ТИП_СИМВОЛ:   snprintf(out, out_cap, "char");    break;
        default:                 snprintf(out, out_cap, "int");     break;
    }
}

static void эмит(Кодоген *к, const char *текст)
{
    буфер_кода_добавить(к->буфер, текст);
}

static void эмит_отрезок(Кодоген *к, const char *начало, size_t длина)
{
    буфер_кода_добавить_отрезок(к->буфер, начало, длина);
}

static void эмит_имя(Кодоген *к, const Токен *токен)
{
    char имя_си[256];
    имя_в_си(к, токен->начало, токен->длина, имя_си, sizeof(имя_си));
    эмит(к, имя_си);
}

// Регистрирует владеющий срез для autofree на текущем уровне вложенности.
static void зарегистрировать_владеющий(Кодоген *к, const Токен *имя)
{
    if (к->число_владеющих >= МАКС_ВЛАДЕЮЩИХ) {
        return;
    }
    к->владеющие[к->число_владеющих] = имя;
    к->уровень_владеющего[к->число_владеющих] = к->уровень_блока;
    к->перемещён_владеющего[к->число_владеющих] = 0;
    к->число_владеющих++;
}

// Помечает владеющий срез перемещённым (move): его освобождать больше не нужно,
// владение перешло к получателю. Семантика гарантирует, что источник и
// получатель в одной области, поэтому ищем последнюю запись с этим именем.
static void пометить_перемещённым(Кодоген *к, const Токен *имя)
{
    for (size_t i = к->число_владеющих; i > 0; --i) {
        const Токен *в = к->владеющие[i - 1];
        if (в->длина == имя->длина
            && memcmp(в->начало, имя->начало, имя->длина) == 0) {
            к->перемещён_владеющего[i - 1] = 1;
            return;
        }
    }
}

// Освобождение одного владеющего среза: free(имя.данные).
static void эмит_освобождение(Кодоген *к, const Токен *имя)
{
    эмит(к, "    free(");
    эмит_имя(к, имя);
    эмит(к, ".данные);\n");
}

// Вставляет free для владеющих срезов, объявленных на указанном уровне (в
// обратном порядке объявления). Перемещённые пропускаются (владение ушло).
// Если убрать = 1 — снимает их со стека.
static void освободить_уровень(Кодоген *к, int уровень, int убрать)
{
    for (size_t i = к->число_владеющих; i > 0; --i) {
        if (к->уровень_владеющего[i - 1] != уровень) {
            break;  // элементы уровня идут сплошной группой на вершине стека
        }
        if (!к->перемещён_владеющего[i - 1]) {
            эмит_освобождение(к, к->владеющие[i - 1]);
        }
        if (убрать) {
            к->число_владеющих--;
        }
    }
}

// Перед «вернуть»: освобождает ВСЕ живые владеющие срезы (раскрутка всех
// объемлющих областей), не снимая со стека — блоки уберут их сами.
// Перемещённые срезы пропускаются.
static void освободить_все(Кодоген *к)
{
    for (size_t i = к->число_владеющих; i > 0; --i) {
        if (!к->перемещён_владеющего[i - 1]) {
            эмит_освобождение(к, к->владеющие[i - 1]);
        }
    }
}

// Используется ли в программе хоть один срез? Тогда печатаем рантайм-пролог.
static int узел_использует_срезы(Узел *узел)
{
    if (!узел) {
        return 0;
    }
    if (узел->срез) {
        return 1;
    }
    for (size_t i = 0; i < узел_число_детей(узел); ++i) {
        if (узел_использует_срезы(узел_ребёнок(узел, i))) {
            return 1;
        }
    }
    return 0;
}

// Есть ли доступ к массиву фикс. размера с динамическим (не литеральным)
// индексом? Только для таких нужна рантайм-функция массив_доступ; при
// литеральном индексе границы уже проверены на этапе компиляции.
static int узел_использует_динамический_массив(Узел *узел)
{
    if (!узел) {
        return 0;
    }
    if (узел->тип == УЗЕЛ_ИНДЕКС && узел->массив && !узел->безопасный_индекс) {
        Узел *индекс = узел_ребёнок(узел, 0);
        if (!индекс || индекс->тип != УЗЕЛ_ЧИСЛО) {
            return 1;
        }
    }
    for (size_t i = 0; i < узел_число_детей(узел); ++i) {
        if (узел_использует_динамический_массив(узел_ребёнок(узел, i))) {
            return 1;
        }
    }
    return 0;
}

// Заголовки рантайм-поддержки (печатаются один раз, если нужны срезы
// или динамический доступ к массиву).
static const char ПРОЛОГ_ЗАГОЛОВКИ[] =
    "#include <stdlib.h>\n"
    "#include <stdio.h>\n";

// Рантайм-поддержка срезов: толстый указатель {данные, длина} и доступ
// с проверкой границ. Цена доступа — одно сравнение (как в Go/Rust).
static const char ПРОЛОГ_СРЕЗОВ[] =
    "\n"
    "/* Срез: указатель + длина для проверки границ на этапе выполнения. */\n"
    "typedef struct { void *данные; size_t длина; size_t эл_размер; } Срез;\n"
    "\n"
    "[[maybe_unused]] static void *срез_доступ(Срез с, long индекс, int строка)\n"
    "{\n"
    "    if (индекс < 0 || (size_t)индекс >= с.длина) {\n"
    "        fprintf(stderr, \"Konda: выход за границы среза (строка %d): \"\n"
    "                \"индекс %ld, длина %zu\\n\", строка, индекс, с.длина);\n"
    "        abort();\n"
    "    }\n"
    "    return (char *)с.данные + (size_t)индекс * с.эл_размер;\n"
    "}\n"
    "\n"
    "[[maybe_unused]] static Срез срез_выделить(long количество, size_t эл_размер)\n"
    "{\n"
    "    Срез с;\n"
    "    с.длина = количество < 0 ? 0 : (size_t)количество;\n"
    "    с.эл_размер = эл_размер;\n"
    "    с.данные = calloc(с.длина, эл_размер);\n"
    "    return с;\n"
    "}\n";

// Рантайм-проверка границ массива фикс. размера при динамическом индексе.
// Длина передаётся вызывающим как литерал (через sizeof) — массив не хранит
// её в себе, поэтому стоимость — одно сравнение, без накладных расходов памяти.
static const char ПРОЛОГ_МАССИВОВ[] =
    "\n"
    "[[maybe_unused]] static void *массив_доступ(void *база, long длина, long индекс,\n"
    "                           size_t эл_размер, int строка)\n"
    "{\n"
    "    if (индекс < 0 || индекс >= длина) {\n"
    "        fprintf(stderr, \"Konda: выход за границы массива (строка %d): \"\n"
    "                \"индекс %ld, длина %ld\\n\", строка, индекс, длина);\n"
    "        abort();\n"
    "    }\n"
    "    return (char *)база + (size_t)индекс * эл_размер;\n"
    "}\n";

static void кодоген_выражения(Кодоген *к, Узел *узел)
{
    switch (узел->тип) {
        case УЗЕЛ_БИНОП:
            эмит(к, "(");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, " ");
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            эмит(к, " ");
            кодоген_выражения(к, узел_ребёнок(узел, 1));
            эмит(к, ")");
            break;
        case УЗЕЛ_УНОП:
            эмит(к, "(");
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, ")");
            break;
        case УЗЕЛ_ПРИСВАИВАНИЕ:
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, " = ");
            кодоген_выражения(к, узел_ребёнок(узел, 1));
            break;
        case УЗЕЛ_ВЫЗОВ: {
            // Встроенная «длина(срез)» → поле .длина (приводим к целое32,
            // как типизирует семантика, чтобы избежать sign-compare с size_t).
            if (срез_равен(узел->токен.начало, узел->токен.длина, "длина")
                && узел_число_детей(узел) == 1) {
                эмит(к, "((int32_t)(");
                кодоген_выражения(к, узел_ребёнок(узел, 0));
                эмит(к, ").длина)");
                break;
            }
            эмит_имя(к, &узел->токен);
            эмит(к, "(");
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                if (i > 0) {
                    эмит(к, ", ");
                }
                кодоген_выражения(к, узел_ребёнок(узел, i));
            }
            эмит(к, ")");
            break;
        }
        case УЗЕЛ_ИНДЕКС:
            if (узел->срез) {
                // Доказанно-безопасный доступ — напрямую, без рантайм-проверки:
                // ((T *)имя.данные)[индекс]
                if (узел->безопасный_индекс) {
                    char тип_си[64];
                    тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
                    эмит(к, "((");
                    эмит(к, тип_си);
                    эмит(к, " *)");
                    эмит_имя(к, &узел->токен);
                    эмит(к, ".данные)[");
                    кодоген_выражения(к, узел_ребёнок(узел, 0));
                    эмит(к, "]");
                    break;
                }
                // Доступ к срезу с проверкой границ:
                // (*(T *)срез_доступ(имя, индекс, строка))
                char тип_си[64];
                тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
                char строка_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                эмит(к, "(*(");
                эмит(к, тип_си);
                эмит(к, " *)срез_доступ(");
                эмит_имя(к, &узел->токен);
                эмит(к, ", ");
                кодоген_выражения(к, узел_ребёнок(узел, 0));
                эмит(к, ", ");
                эмит(к, строка_текст);
                эмит(к, "))");
                break;
            }
            if (узел->массив) {
                Узел *индекс = узел_ребёнок(узел, 0);
                // Доказанно-безопасный индекс (константа в границах или индекс
                // канонического цикла) — обращаемся напрямую, без проверки.
                if (узел->безопасный_индекс) {
                    эмит_имя(к, &узел->токен);
                    эмит(к, "[");
                    кодоген_выражения(к, индекс);
                    эмит(к, "]");
                    break;
                }
                // Динамический индекс: рантайм-проверка границ. Длину массива
                // берём через sizeof (для массива sizeof — это его полный размер).
                char тип_си[64];
                тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
                char строка_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                эмит(к, "(*(");
                эмит(к, тип_си);
                эмит(к, " *)массив_доступ(");
                эмит_имя(к, &узел->токен);
                эмит(к, ", sizeof(");
                эмит_имя(к, &узел->токен);
                эмит(к, ") / sizeof(");
                эмит_имя(к, &узел->токен);
                эмит(к, "[0]), ");
                кодоген_выражения(к, индекс);
                эмит(к, ", sizeof(");
                эмит(к, тип_си);
                эмит(к, "), ");
                эмит(к, строка_текст);
                эмит(к, "))");
                break;
            }
            эмит_имя(к, &узел->токен);
            эмит(к, "[");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, "]");
            break;
        case УЗЕЛ_ИДЕНТИФИКАТОР:
            эмит_имя(к, &узел->токен);
            break;
        case УЗЕЛ_ЧИСЛО:
        case УЗЕЛ_СТРОКА:
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            break;
        case УЗЕЛ_НУЛЬ:
            эмит(к, "nullptr");
            break;
        default:
            break;
    }
}

static void кодоген_блока(Кодоген *к, Узел *блок);

static void эмит_указатели(Кодоген *к, int указатели)
{
    for (int i = 0; i < указатели; ++i) {
        эмит(к, "*");
    }
}

// «срез<T> имя = выделить(N)» → «Срез имя = срез_выделить(N, sizeof(T));».
// Без инициализатора → пустой срез (длина 0): любой доступ безопасно прервётся.
static void кодоген_объявления_среза(Кодоген *к, Узел *узел)
{
    эмит(к, "    Срез ");
    эмит_имя(к, &узел->токен);
    Узел *инит = узел_число_детей(узел) > 0 ? узел_ребёнок(узел, 0) : nullptr;
    if (инит && инит->тип == УЗЕЛ_ВЫЗОВ
        && срез_равен(инит->токен.начало, инит->токен.длина, "выделить")
        && узел_число_детей(инит) == 1) {
        char тип_си[64];
        тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
        эмит(к, " = срез_выделить(");
        кодоген_выражения(к, узел_ребёнок(инит, 0));
        эмит(к, ", sizeof(");
        эмит(к, тип_си);
        эмит(к, "))");
        // Срез владеет памятью — регистрируем для autofree в конце области.
        // Статический срез семантика запрещает, поэтому drop всегда корректен.
        зарегистрировать_владеющий(к, &узел->токен);
    } else if (инит && инит->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
        // Перемещение (move): «срез<T> б = а». Копируется только дескриптор;
        // данные не трогаются. Источник помечается перемещённым (его больше не
        // освобождаем), а получатель становится владельцем. Семантика уже
        // проверила, что источник владеет и находится в той же области.
        эмит(к, " = ");
        кодоген_выражения(к, инит);
        пометить_перемещённым(к, &инит->токен);
        зарегистрировать_владеющий(к, &узел->токен);
    } else if (инит && инит->тип == УЗЕЛ_ВЫЗОВ) {
        // Приём владения от функции, возвращающей срез: «срез<T> д = создать()».
        эмит(к, " = ");
        кодоген_выражения(к, инит);
        зарегистрировать_владеющий(к, &узел->токен);
    } else {
        эмит(к, " = {0}");
    }
    эмит(к, ";\n");
}

static void кодоген_объявления(Кодоген *к, Узел *узел)
{
    if (узел->срез) {
        кодоген_объявления_среза(к, узел);
        return;
    }
    char тип_си[64];
    тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
    эмит(к, "    ");
    if (узел->статический) {
        эмит(к, "static ");
    }
    эмит(к, тип_си);
    эмит(к, " ");
    эмит_указатели(к, узел->указатели);
    эмит_имя(к, &узел->токен);

    // Массив фиксированного размера «тип имя[N]» — зануляется для безопасности
    // (нет чтения неинициализированной памяти).
    if (узел->массив) {
        char размер_текст[24];
        snprintf(размер_текст, sizeof(размер_текст), "[%ld] = {0}", узел->размер_массива);
        эмит(к, размер_текст);
        эмит(к, ";\n");
        return;
    }

    if (узел_число_детей(узел) > 0) {
        эмит(к, " = ");
        кодоген_выражения(к, узел_ребёнок(узел, 0));
    }
    эмит(к, ";\n");
}

static void кодоген_если(Кодоген *к, Узел *узел)
{
    эмит(к, "    if (");
    кодоген_выражения(к, узел_ребёнок(узел, 0));
    эмит(к, ") {\n");
    кодоген_блока(к, узел_ребёнок(узел, 1));

    Узел *иначе = узел_ребёнок(узел, 2);
    if (иначе) {
        эмит(к, "    } else {\n");
        кодоген_блока(к, иначе);
    }
    эмит(к, "    }\n");
}

// Объявление или выражение/присваивание в одну строку, без отступа,
// «;» и перевода строки — для заголовка цикла «для».
static void кодоген_инлайн(Кодоген *к, Узел *узел)
{
    if (!узел) {
        return;
    }
    if (узел->тип == УЗЕЛ_ОБЪЯВЛЕНИЕ) {
        char тип_си[64];
        тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
        эмит(к, тип_си);
        эмит(к, " ");
        эмит_указатели(к, узел->указатели);
        эмит_имя(к, &узел->токен);
        if (узел_число_детей(узел) > 0) {
            эмит(к, " = ");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
        }
        return;
    }
    кодоген_выражения(к, узел);
}

static void кодоген_пока(Кодоген *к, Узел *узел)
{
    эмит(к, "    while (");
    кодоген_выражения(к, узел_ребёнок(узел, 0));
    эмит(к, ") {\n");
    кодоген_блока(к, узел_ребёнок(узел, 1));
    эмит(к, "    }\n");
}

static void кодоген_для(Кодоген *к, Узел *узел)
{
    эмит(к, "    for (");
    кодоген_инлайн(к, узел_ребёнок(узел, 0));
    эмит(к, "; ");
    кодоген_выражения(к, узел_ребёнок(узел, 1));
    эмит(к, "; ");
    кодоген_инлайн(к, узел_ребёнок(узел, 2));
    эмит(к, ") {\n");
    кодоген_блока(к, узел_ребёнок(узел, 3));
    эмит(к, "    }\n");
}

static void кодоген_возврата(Кодоген *к, Узел *узел)
{
    int есть_значение = узел_число_детей(узел) > 0;

    // Возврат среза = передача владения наружу. Возвращаемый срез исключаем из
    // autofree (его освободит вызывающий), остальные владеющие — освобождаем.
    if (есть_значение && к->срез_возврата) {
        Узел *выр = узел_ребёнок(узел, 0);
        if (выр->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
            пометить_перемещённым(к, &выр->токен);
        }
        освободить_все(к);
        эмит(к, "    return ");
        кодоген_выражения(к, выр);
        эмит(к, ";\n");
        return;
    }

    // Если возвращаемое выражение может читать владеющий срез, его значение надо
    // вычислить ДО освобождения — иначе use-after-free. Сохраняем во временную.
    if (есть_значение && к->число_владеющих > 0) {
        эмит(к, "    ");
        эмит(к, к->тип_возврата_си);
        эмит(к, " _результат_konda = ");
        кодоген_выражения(к, узел_ребёнок(узел, 0));
        эмит(к, ";\n");
        освободить_все(к);
        эмит(к, "    return _результат_konda;\n");
        return;
    }

    // Иначе порядок не важен: освобождаем живые срезы и возвращаем.
    освободить_все(к);
    эмит(к, "    return");
    if (есть_значение) {
        эмит(к, " ");
        кодоген_выражения(к, узел_ребёнок(узел, 0));
    }
    эмит(к, ";\n");
}

static void кодоген_оператора(Кодоген *к, Узел *узел)
{
    switch (узел->тип) {
        case УЗЕЛ_ЕСЛИ:
            кодоген_если(к, узел);
            break;
        case УЗЕЛ_ПОКА:
            кодоген_пока(к, узел);
            break;
        case УЗЕЛ_ДЛЯ:
            кодоген_для(к, узел);
            break;
        case УЗЕЛ_ВОЗВРАТ:
            кодоген_возврата(к, узел);
            break;
        case УЗЕЛ_ОБЪЯВЛЕНИЕ:
            кодоген_объявления(к, узел);
            break;
        case УЗЕЛ_ПРИСВАИВАНИЕ:
        case УЗЕЛ_ВЫЗОВ:
            эмит(к, "    ");
            кодоген_выражения(к, узел);
            эмит(к, ";\n");
            break;
        default:
            break;
    }
}

static void кодоген_блока(Кодоген *к, Узел *блок)
{
    if (!блок) {
        return;
    }
    к->уровень_блока++;
    size_t n = узел_число_детей(блок);
    for (size_t i = 0; i < n; ++i) {
        кодоген_оператора(к, узел_ребёнок(блок, i));
    }
    // Autofree: освобождаем владеющие срезы этого блока (обратный порядок).
    // Если блок заканчивается «вернуть», тот уже всё освободил — free не
    // дублируем, но срезы со стека снимаем (область логически закрыта).
    int кончается_возвратом = n > 0
        && узел_ребёнок(блок, n - 1)->тип == УЗЕЛ_ВОЗВРАТ;
    if (кончается_возвратом) {
        while (к->число_владеющих > 0
               && к->уровень_владеющего[к->число_владеющих - 1] == к->уровень_блока) {
            к->число_владеющих--;
        }
    } else {
        освободить_уровень(к, к->уровень_блока, 1);
    }
    к->уровень_блока--;
}

static void кодоген_препроцессора(Кодоген *к, Узел *узел)
{
    char строка[512];
    size_t n = узел->токен.длина < sizeof(строка) - 1 ? узел->токен.длина : sizeof(строка) - 1;
    memcpy(строка, узел->токен.начало, n);
    строка[n] = '\0';

    const char *содержит = strstr(строка, "содержит");
    if (содержит) {
        size_t смещение = (size_t)(содержит - строка);
        эмит_отрезок(к, строка, смещение);
        эмит(к, "include");
        эмит(к, содержит + strlen("содержит"));
    } else {
        эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
    }
    эмит(к, "\n");
}

static void кодоген_функции(Кодоген *к, Узел *узел)
{
    к->в_точке_входа = узел->это_main;
    // Каждая функция — со своим стеком владеющих срезов и уровнем вложенности.
    к->число_владеющих = 0;
    к->уровень_блока = 0;
    к->срез_возврата = узел->срез;
    if (узел->срез) {
        snprintf(к->тип_возврата_си, sizeof(к->тип_возврата_си), "Срез");
    } else {
        тип_токена_в_си(узел->тип_данных, узел->беззнаковый,
                        к->тип_возврата_си, sizeof(к->тип_возврата_си));
    }

    if (узел->статический) {
        эмит(к, "static ");
    }
    эмит(к, к->тип_возврата_си);
    эмит(к, " ");

    if (узел->это_main) {
        эмит(к, "main");
    } else {
        эмит_имя(к, &узел->токен);
    }
    эмит(к, "(");

    size_t всего = узел_число_детей(узел);
    size_t тело_индекс = всего > 0 ? всего - 1 : 0;

    if (узел->это_main) {
        эмит(к, "int32_t argc, char **argv");
    } else {
        int первый = 1;
        for (size_t i = 0; i < тело_индекс; ++i) {
            Узел *пар = узел_ребёнок(узел, i);
            if (пар->тип != УЗЕЛ_ПАРАМЕТР) {
                continue;
            }
            if (!первый) {
                эмит(к, ", ");
            }
            первый = 0;
            if (пар->срез) {
                эмит(к, "Срез ");
                if (пар->токен.длина > 0) {
                    эмит_имя(к, &пар->токен);
                }
                continue;
            }
            char tsi[64];
            тип_токена_в_си(пар->тип_данных, пар->беззнаковый, tsi, sizeof(tsi));
            эмит(к, tsi);
            for (int p = 0; p < пар->указатели; ++p) {
                эмит(к, "*");
            }
            if (пар->массив) {
                эмит(к, "[]");
            }
            эмит(к, " ");
            if (пар->токен.длина > 0) {
                эмит_имя(к, &пар->токен);
            }
        }
    }

    эмит(к, ")\n{\n");

    int тело_завершается_возвратом = 0;
    if (всего > 0) {
        Узел *тело = узел_ребёнок(узел, тело_индекс);
        кодоген_блока(к, тело);
        size_t операторов = узел_число_детей(тело);
        if (операторов > 0) {
            Узел *последний = узел_ребёнок(тело, операторов - 1);
            тело_завершается_возвратом = последний && последний->тип == УЗЕЛ_ВОЗВРАТ;
        }
    }

    // Неявный «return 0;» — только если тело не закончилось явным возвратом.
    if (!тело_завершается_возвратом) {
        эмит(к, "    return 0;\n");
    }
    эмит(к, "}\n");
    к->в_точке_входа = 0;
}

int кодоген(Узел *корень, БуферКода *буфер)
{
    Кодоген к = { .буфер = буфер, .в_точке_входа = 0 };

    эмит(&к, "/* Сгенерировано транспилятором Konda */\n");
    эмит(&к, "#include <stdint.h>\n");

    int нужны_срезы = узел_использует_срезы(корень);
    int нужны_массивы = узел_использует_динамический_массив(корень);
    if (нужны_срезы || нужны_массивы) {
        эмит(&к, ПРОЛОГ_ЗАГОЛОВКИ);
    }
    if (нужны_срезы) {
        эмит(&к, ПРОЛОГ_СРЕЗОВ);
    }
    if (нужны_массивы) {
        эмит(&к, ПРОЛОГ_МАССИВОВ);
    }

    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ПРЕПРОЦЕССОР) {
            кодоген_препроцессора(&к, узел);
        } else if (узел->тип == УЗЕЛ_ФУНКЦИЯ) {
            кодоген_функции(&к, узел);
        }
    }

    return 1;
}
