    "                                       int разрядность, int строка)\n"
    "{\n"
    "    long r;\n"
    "    int пер = оп == 0 ? __builtin_add_overflow(a, b, &r)\n"
    "            : оп == 1 ? __builtin_sub_overflow(a, b, &r)\n"
    "            :           __builtin_mul_overflow(a, b, &r);\n"
    "    long мин = разрядность >= 64 ? LONG_MIN : -(1L << (разрядность - 1));\n"
    "    long макс = разрядность >= 64 ? LONG_MAX : (1L << (разрядность - 1)) - 1;\n"
    "    if (пер || r < мин || r > макс) {\n"
    "        fprintf(stderr, \"Konda: переполнение целого (строка %d)\\n\", строка);\n"
    "        abort();\n"
    "    }\n"
    "    return r;\n"
    "}\n";

static void эмит_тип(Кодоген *к, ТипТокена тип, int беззнаковый, const Токен *имя_типа);
static void эмит_указатели(Кодоген *к, int указатели);
static void кодоген_выражения(Кодоген *к, Узел *узел);

// Спецификатор printf для интерполяции по типу её значения (холдер
// УЗЕЛ_ИНТЕРПОЛЯЦИЯ хранит тип, проставленный семантикой).
static const char *спецификатор_интерполяции(const Узел *реб)
{
    if (реб->указатели > 0) {
        if (реб->тип_данных == ТОКЕН_ТИП_СИМВОЛ && реб->указатели == 1) return "%s";
        return "%p";
    }
    switch (реб->тип_данных) {
        case ТОКЕН_ТИП_СИМВОЛ:   return "%c";
        case ТОКЕН_ВЕЩЕСТВЕННОЕ:  return "%f";
        case ТОКЕН_ВЕЩЕСТВЕННОЕ64: return "%f";
        case ТОКЕН_ЦЕЛОЕ64:       return реб->беззнаковый ? "%llu" : "%lld";
        default:                  return реб->беззнаковый ? "%u" : "%d";
    }
}

// Эмитит чанк-литерал в тело формата, удваивая «%» (остальные C-эскейпы вроде
// «\n» уже в исходной форме и проходят как есть внутри C-строки).
static void эмит_формат_чанк(Кодоген *к, const char *начало, size_t длина)
{
    for (size_t i = 0; i < длина; ++i) {
        if (начало[i] == '%') эмит(к, "%%");
        else буфер_кода_добавить_отрезок(к->буфер, начало + i, 1);
    }
}

// Эмитит аргумент интерполяции с приведением под выбранный спецификатор
// (чтобы избежать рассогласования типов в varargs).
static void эмит_аргумент_интерполяции(Кодоген *к, const Узел *реб)
{
    Узел *выр = узел_ребёнок(реб, 0);
    const char *пре = "", *пост = ")";
    if (реб->указатели > 0) {
        пре = (реб->тип_данных == ТОКЕН_ТИП_СИМВОЛ && реб->указатели == 1)
                ? "(" : "(void *)(";
    } else if (реб->тип_данных == ТОКЕН_ЦЕЛОЕ64) {
        пре = реб->беззнаковый ? "(unsigned long long)(" : "(long long)(";
    } else if (реб->тип_данных == ТОКЕН_ВЕЩЕСТВЕННОЕ
               || реб->тип_данных == ТОКЕН_ВЕЩЕСТВЕННОЕ64) {
        пре = "(double)(";
    } else if (реб->тип_данных == ТОКЕН_ТИП_СИМВОЛ) {
        пре = "(";  // %c: char промотируется сам
    } else {
        пре = реб->беззнаковый ? "(unsigned)(" : "(int)(";
    }
    эмит(к, пре);
    кодоген_выражения(к, выр);
    эмит(к, пост);
}

// F-строка из единственной строковой вставки — «"\(выр)"», где выр уже символ*.
// Форматировать нечего: значение и есть готовая строка. Такую f-строку эмитим
// как само выражение — без буфера и без snprintf (быстрый путь).
static int fстрока_только_строка(const Узел *узел)
{
    if (узел_число_детей(узел) != 1) return 0;
    Узел *реб = узел_ребёнок(узел, 0);
    return реб && реб->тип == УЗЕЛ_ИНТЕРПОЛЯЦИЯ
        && реб->указатели == 1 && реб->тип_данных == ТОКЕН_ТИП_СИМВОЛ;
}

// Сколько f-строк в поддереве требуют буфер (не быстрый путь). Буферы f-строк
// поднимаются в начало функции — автоматическая память на стеке вместо `static`
// (потокобезопасно и реентерабельно; разные вставки не затирают друг друга).
static size_t посчитать_fбуферы(const Узел *узел)
{
    if (!узел) return 0;
    size_t n = (узел->тип == УЗЕЛ_F_СТРОКА && !fстрока_только_строка(узел)) ? 1 : 0;
    for (size_t i = 0; i < узел_число_детей(узел); ++i)
        n += посчитать_fбуферы(узел_ребёнок(узел, i));
    return n;
}

static void кодоген_выражения(Кодоген *к, Узел *узел)
{
    switch (узел->тип) {
        case УЗЕЛ_БИНОП: {
            // Неконстантный делитель/сдвиг/переполнение — рантайм-проверка
            // (inline helper). В релизе проверки нет — прямая арифметика (как в C).
            if (узел->проверка_бинопа == 1 && !к->релиз) {  // деление/остаток на ноль
                char строка_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                int остаток = узел->токен.длина == 1 && узел->токен.начало[0] == '%';
                эмит(к, "делить_безопасно((long)(");
                кодоген_выражения(к, узел_ребёнок(узел, 0));
                эмит(к, "), (long)(");
                кодоген_выражения(к, узел_ребёнок(узел, 1));
                эмит(к, остаток ? "), 1, " : "), 0, ");
                эмит(к, строка_текст);
                эмит(к, ")");
                break;
            }
            if (узел->проверка_бинопа == 2 && !к->релиз) {  // сдвиг вне разрядности
                char строка_текст[16], ширина_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                snprintf(ширина_текст, sizeof(ширина_текст), "%d",
                         узел->размер_массива > 0 ? (int)узел->размер_массива : 32);
                int вправо = узел->токен.длина == 2 && узел->токен.начало[0] == '>';
                эмит(к, "сдвиг_безопасно((long)(");
                кодоген_выражения(к, узел_ребёнок(узел, 0));
                эмит(к, "), (long)(");
                кодоген_выражения(к, узел_ребёнок(узел, 1));
                эмит(к, вправо ? "), 1, " : "), 0, ");
                эмит(к, ширина_текст);
                эмит(к, ", ");
                эмит(к, строка_текст);
                эмит(к, ")");
                break;
            }
            if (узел->проверка_бинопа == 3 && !к->релиз) {  // переполнение знакового +/-/*
                char строка_текст[16], ширина_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                snprintf(ширина_текст, sizeof(ширина_текст), "%d",
                         узел->размер_массива > 0 ? (int)узел->размер_массива : 32);
                const char *оп = узел->токен.начало[0] == '+' ? "0"
                               : узел->токен.начало[0] == '-' ? "1" : "2";
                эмит(к, "арифметика_безопасно((long)(");
                кодоген_выражения(к, узел_ребёнок(узел, 0));
                эмит(к, "), (long)(");
                кодоген_выражения(к, узел_ребёнок(узел, 1));
                эмит(к, "), ");
                эмит(к, оп);
                эмит(к, ", ");
                эмит(к, ширина_текст);
                эмит(к, ", ");
                эмит(к, строка_текст);
                эмит(к, ")");
                break;
            }
            эмит(к, "(");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, " ");
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            эмит(к, " ");
            кодоген_выражения(к, узел_ребёнок(узел, 1));
            эмит(к, ")");
            break;
        }
        case УЗЕЛ_УНОП:
            эмит(к, "(");
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, ")");
            break;
        case УЗЕЛ_ПРИСВАИВАНИЕ:
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, " = ");
            кодоген_выражения(к, узел_ребёнок(узел, 1));
            break;
        case УЗЕЛ_ВЫЗОВ: {
            // «присоединить(п)» → ждём поток и забираем результат. Результат
            // лежит первым полем кадра, тип T известен из типа Konda, поэтому
            // читаем его как «*(T*)кадр». Выражение-оператор GNU ({…}) даёт
            // значение прямо в выражении (компилируем с -std=gnu23).
            if (срез_равен(узел->токен.начало, узел->токен.длина, "присоединить")
                && узел_число_детей(узел) == 1) {
                // Явный join потребляет дескриптор — снимаем его с autofree,
                // иначе при выходе из области был бы второй (ub) join.
                {
                    const Узел *ад = узел_ребёнок(узел, 0);
                    if (ад && ад->тип == УЗЕЛ_ИДЕНТИФИКАТОР)
                        пометить_перемещённым(к, &ад->токен);
                }
                // Тип результата семантика проставила в узел (см. «присоединить»).
                if (узел->тип_данных == ТОКЕН_НИЧЕГО) {
                    эмит(к, "поток_присоединить_в(");
                    кодоген_выражения(к, узел_ребёнок(узел, 0));
                    эмит(к, ", nullptr, 0)");
                } else {
                    эмит(к, "({ ");
                    эмит_тип(к, узел->тип_данных, узел->беззнаковый, nullptr);
                    эмит(к, " _рез; поток_присоединить_в(");
                    кодоген_выражения(к, узел_ребёнок(узел, 0));
                    эмит(к, ", &_рез, sizeof _рез); _рез; })");
                }
                break;
            }
            // Встроенная «длина(срез)» → поле .длина (приводим к целое32,
            // как типизирует семантика, чтобы избежать sign-compare с size_t).
            if (срез_равен(узел->токен.начало, узел->токен.длина, "длина")
                && узел_число_детей(узел) == 1) {
                эмит(к, "((int32_t)(");
                кодоген_выражения(к, узел_ребёнок(узел, 0));
                эмит(к, ").длина)");
                break;
            }
            // Конструирование варианта sum-типа: «Круг(5.0)» →
            // ((Фигура){ .тег = N, .данные.Круг = { 5.0 } }).
            {
                const Узел *вар = nullptr; int тег = 0;
                const Узел *пер = найти_sum_вариант_к(к, узел->токен.начало,
                                                      узел->токен.длина, &вар, &тег);
                if (пер) {
                    эмит(к, "((");
                    эмит_манглированное(к, пер->токен.начало, пер->токен.длина);
                    char тегтекст[16];
                    snprintf(тегтекст, sizeof тегтекст, "%d", тег);
                    эмит(к, "){ .тег = ");
                    эмит(к, тегтекст);
                    if (узел_число_детей(узел) > 0) {
                        эмит(к, ", .данные.");
                        эмит_отрезок(к, вар->токен.начало, вар->токен.длина);
                        эмит(к, " = { ");
                        for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                            if (i > 0) эмит(к, ", ");
                            кодоген_выражения(к, узел_ребёнок(узел, i));
                        }
                        эмит(к, " }");
                    }
                    эмит(к, " })");
                    break;
                }
            }
            СигнатураК *сиг = найти_сигнатуру_к(к, &узел->токен);
            эмит_имя(к, &узел->токен);
            эмит(к, "(");
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                if (i > 0) {
                    эмит(к, ", ");
                }
                Узел *арг = узел_ребёнок(узел, i);
                int конвертировать_в_срез = сиг && i < сиг->число
                    && сиг->срез_парам[i]
                    && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР
                    && арг->массив;
                if (конвертировать_в_срез) {
                    // Авто-конверсия массива в Срез: на стеке формируется
                    // дескриптор {данные, длина, эл_размер}. Массив остаётся
                    // на стеке вызывающего; функция получает {ptr, N} —
                    // никаких аллокаций, бесплатная коэрция.
                    char тип_си[64];
                    тип_токена_в_си(арг->тип_данных, арг->беззнаковый,
                                    тип_си, sizeof(тип_си));
                    эмит(к, "(Срез){ .данные = ");
                    эмит_имя(к, &арг->токен);
                    char длина_текст[32];
                    snprintf(длина_текст, sizeof(длина_текст),
                             ", .длина = %ld", арг->размер_массива);
                    эмит(к, длина_текст);
                    эмит(к, ", .эл_размер = sizeof(");
                    эмит(к, тип_си);
                    эмит_указатели(к, арг->указатели);
                    эмит(к, ") }");
                } else {
                    // Параметр по ссылке (ref/out): передаём адрес переменной.
                    // «&(*x)» для аргумента, который сам — параметр по ссылке,
                    // C сворачивает обратно в «x» (передача указателя насквозь).
                    if (сиг && i < сиг->число && сиг->по_ссылке[i]) эмит(к, "&");
                    кодоген_выражения(к, арг);
                }
                // Аргумент, переданный в «владеет»-параметр, перемещается в
                // функцию — у вызывающего его освобождать больше не нужно.
                if (сиг && i < сиг->число && сиг->владеет[i]
                    && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                    пометить_перемещённым(к, &арг->токен);
                }
            }
            эмит(к, ")");
            break;
        }
        case УЗЕЛ_ИНДЕКС:
            // Индексация произвольного lvalue (структура.поле[i], функция()[i]):
            // новая форма — дети [база-выражение, индекс]. Только срез: рантайм-
            // проверка границ через срез_доступ (длина в самом срезе).
            if (узел_число_детей(узел) == 2) {
                char тип_си[64];
                тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
                char строка_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                эмит(к, "(*(");
                эмит(к, тип_си);
                эмит(к, " *)срез_доступ(");
                кодоген_выражения(к, узел_ребёнок(узел, 0));   // база-выражение
                эмит(к, ", ");
                кодоген_выражения(к, узел_ребёнок(узел, 1));   // индекс
                эмит(к, ", ");
                эмит(к, строка_текст);
                эмит(к, "))");
                break;
            }
            if (узел->срез) {
                // Доказанно-безопасный доступ — напрямую, без рантайм-проверки:
                // ((T *)имя.данные)[индекс]. Проверка границ остаётся и в релизе
                // (безопасность памяти важнее — как в Rust; снимается лишь
                // арифметический guard переполнения).
                if (узел->безопасный_индекс) {
                    char тип_си[64];
                    тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
                    эмит(к, "((");
                    эмит(к, тип_си);
                    эмит(к, " *)");
                    эмит_имя(к, &узел->токен);
                    эмит(к, ".данные)[");
                    кодоген_выражения(к, узел_ребёнок(узел, 0));
                    эмит(к, "]");
                    break;
                }
                // Доступ к срезу с проверкой границ:
                // (*(T *)срез_доступ(имя, индекс, строка))
                char тип_си[64];
                тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
                char строка_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                эмит(к, "(*(");
                эмит(к, тип_си);
                эмит(к, " *)срез_доступ(");
                эмит_имя(к, &узел->токен);
                эмит(к, ", ");
                кодоген_выражения(к, узел_ребёнок(узел, 0));
                эмит(к, ", ");
                эмит(к, строка_текст);
                эмит(к, "))");
                break;
            }
            if (узел->массив) {
                Узел *индекс = узел_ребёнок(узел, 0);
                // Доказанно-безопасный индекс (константа в границах или индекс
                // канонического цикла) — обращаемся напрямую. Иначе проверка
                // границ остаётся и в релизе (безопасность памяти).
                if (узел->безопасный_индекс) {
                    эмит_имя(к, &узел->токен);
                    эмит(к, "[");
                    кодоген_выражения(к, индекс);
                    эмит(к, "]");
                    break;
                }
                // Динамический индекс: рантайм-проверка границ. Длину массива
                // берём через sizeof (для массива sizeof — это его полный размер).
                char тип_си[64];
                тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
                char строка_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                эмит(к, "(*(");
                эмит(к, тип_си);
                эмит(к, " *)массив_доступ(");
                эмит_имя(к, &узел->токен);
                эмит(к, ", sizeof(");
                эмит_имя(к, &узел->токен);
                эмит(к, ") / sizeof(");
                эмит_имя(к, &узел->токен);
                эмит(к, "[0]), ");
                кодоген_выражения(к, индекс);
                эмит(к, ", sizeof(");
                эмит(к, тип_си);
                эмит(к, "), ");
                эмит(к, строка_текст);
                эмит(к, "))");
                break;
            }
            // Безопасная обёртка над «аргументы»: динамический индекс
            // проверяется в рантайме (argv[аргументы_индекс(argc, и, строка)]).
            // Проверка границ остаётся и в релизе (безопасность памяти).
            if (узел->проверка_аргументов) {
                char строка_текст[16];
                snprintf(строка_текст, sizeof(строка_текст), "%d", узел->токен.строка);
                эмит_имя(к, &узел->токен);  // argv
                эмит(к, "[аргументы_индекс(argc, ");
                кодоген_выражения(к, узел_ребёнок(узел, 0));
                эмит(к, ", ");
                эмит(к, строка_текст);
                эмит(к, ")]");
                break;
            }
            эмит_имя(к, &узел->токен);
            эмит(к, "[");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, "]");
            break;
        case УЗЕЛ_ИДЕНТИФИКАТОР: {
            // Голый вариант sum-типа без данных (напр. «Пусто») → компаунд-литерал
            // с одним тегом: ((Имя){ .тег = N }).
            const Узел *вар = nullptr; int тег = 0;
            const Узел *пер = найти_sum_вариант_к(к, узел->токен.начало,
                                                  узел->токен.длина, &вар, &тег);
            if (пер) {
                char тегтекст[16];
                snprintf(тегтекст, sizeof тегтекст, "%d", тег);
                эмит(к, "((");
                эмит_манглированное(к, пер->токен.начало, пер->токен.длина);
                эмит(к, "){ .тег = ");
                эмит(к, тегтекст);
                эмит(к, " })");
                break;
            }
            // Параметр по ссылке (ref/out) — авто-разыменование «(*имя)».
            if (идентификатор_по_ссылке(к, &узел->токен)) {
                эмит(к, "(*");
                эмит_имя(к, &узел->токен);
                эмит(к, ")");
            } else {
                эмит_имя(к, &узел->токен);
            }
            break;
        }
        case УЗЕЛ_ПОЛЕ:
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, узел->указатели ? "->" : ".");  // указатель на структуру → «->»
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            break;
        case УЗЕЛ_ЧИСЛО:
            // Логические литералы: кириллический токен → C-ключевые слова C23.
            if (узел->токен.тип == ТОКЕН_ИСТИНА) { эмит(к, "true"); break; }
            if (узел->токен.тип == ТОКЕН_ЛОЖЬ)   { эмит(к, "false"); break; }
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            break;
        case УЗЕЛ_СТРОКА:
            эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
            break;
        case УЗЕЛ_НУЛЬ:
            эмит(к, "nullptr");
            break;
        case УЗЕЛ_F_СТРОКА: {
            // Быстрый путь: единственная строковая вставка «"\(выр)"» — само
            // выражение, без буфера и snprintf.
            if (fстрока_только_строка(узел)) {
                эмит_аргумент_интерполяции(к, узел_ребёнок(узел, 0));
                break;
            }
            // Иначе → GNU statement-expression с буфером, поднятым в начало
            // функции (автоматическая память, потокобезопасно):
            // ({ snprintf(_kfN, …, "fmt", args); _kfN; })
            char имя[32];
            snprintf(имя, sizeof(имя), "_kf%zu", к->счётчик_fстрок++);
            эмит(к, "({ snprintf(");
            эмит(к, имя);
            эмит(к, ", sizeof(");
            эмит(к, имя);
            эмит(к, "), \"");
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                Узел *реб = узел_ребёнок(узел, i);
                if (реб->тип == УЗЕЛ_ИНТЕРПОЛЯЦИЯ) {
                    эмит(к, спецификатор_интерполяции(реб));
                } else {  // чанк (УЗЕЛ_СТРОКА)
                    эмит_формат_чанк(к, реб->токен.начало, реб->токен.длина);
                }
            }
            эмит(к, "\"");
            for (size_t i = 0; i < узел_число_детей(узел); ++i) {
                Узел *реб = узел_ребёнок(узел, i);
                if (реб->тип != УЗЕЛ_ИНТЕРПОЛЯЦИЯ) continue;
                эмит(к, ", ");
                эмит_аргумент_интерполяции(к, реб);
            }
            эмит(к, "); ");
            эмит(к, имя);
            эмит(к, "; })");
            break;
        }
        case УЗЕЛ_ПРИВЕДЕНИЕ: {
            // «как<тип>(выр)» → «(тип*…)(выр)».
            char тип_си[64];
            тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
            эмит(к, "(");
            эмит(к, тип_си);
            эмит_указатели(к, узел->указатели);
            эмит(к, ")(");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
            эмит(к, ")");
            break;
        }
        default:
            break;
    }
}

static void кодоген_блока(Кодоген *к, Узел *блок);

static void эмит_указатели(Кодоген *к, int указатели)
{
    for (int i = 0; i < указатели; ++i) {
        эмит(к, "*");
    }
}

// «срез<T> имя = выделить(N)» → «Срез имя = срез_выделить(N, sizeof(T));».
// Без инициализатора → пустой срез (длина 0): любой доступ безопасно прервётся.
static void кодоген_объявления_среза(Кодоген *к, Узел *узел)
{
    эмит(к, "    Срез ");
    эмит_имя(к, &узел->токен);
    Узел *инит = узел_число_детей(узел) > 0 ? узел_ребёнок(узел, 0) : nullptr;
    if (инит && инит->тип == УЗЕЛ_ВЫЗОВ
        && срез_равен(инит->токен.начало, инит->токен.длина, "выделить")
        && узел_число_детей(инит) == 1) {
        char тип_си[64];
        тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
        эмит(к, " = срез_выделить(");
        кодоген_выражения(к, узел_ребёнок(инит, 0));
        эмит(к, ", sizeof(");
        эмит(к, тип_си);
        эмит(к, "))");
        // Срез владеет памятью — регистрируем для autofree в конце области.
        // Статический срез семантика запрещает, поэтому drop всегда корректен.
        зарегистрировать_владеющий(к, &узел->токен);
    } else if (инит && инит->тип == УЗЕЛ_ВЫЗОВ
               && срез_равен(инит->токен.начало, инит->токен.длина, "клон")
               && узел_число_детей(инит) == 1) {
        // «срез<T> б = клон(а)»: глубокая копия — новый владелец.
        эмит(к, " = срез_клон(");
        кодоген_выражения(к, узел_ребёнок(инит, 0));
        эмит(к, ")");
        зарегистрировать_владеющий(к, &узел->токен);
    } else if (инит && инит->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
        // Перемещение (move): «срез<T> б = а». Копируется только дескриптор;
        // данные не трогаются. Источник помечается перемещённым (его больше не
        // освобождаем), а получатель становится владельцем. Семантика уже
        // проверила, что источник владеет и находится в той же области.
        эмит(к, " = ");
        кодоген_выражения(к, инит);
        пометить_перемещённым(к, &инит->токен);
        зарегистрировать_владеющий(к, &узел->токен);
    } else if (инит && инит->тип == УЗЕЛ_ВЫЗОВ) {
        // Приём владения от функции, возвращающей срез: «срез<T> д = создать()».
        эмит(к, " = ");
        кодоген_выражения(к, инит);
        зарегистрировать_владеющий(к, &узел->токен);
    } else {
        эмит(к, " = {0}");
    }
    эмит(к, ";\n");
}

// «поток<T> п = запустить(ф, арг)» → «Поток п = _запуск_ф(арг);».
// Вся возня с pthread спрятана в сгенерированный запускатель «_запуск_ф»
// (см. кодоген_шимов_потоков), поэтому здесь — обычный вызов.
static void кодоген_объявления_потока(Кодоген *к, Узел *узел)
{
    Узел *инит = узел_число_детей(узел) > 0 ? узел_ребёнок(узел, 0) : nullptr;
    эмит(к, "    Поток ");
    эмит_имя(к, &узел->токен);
    эмит(к, " = _запуск_");
    if (инит && узел_число_детей(инит) > 0) {
        Узел *фу = узел_ребёнок(инит, 0);
        эмит_манглированное(к, фу->токен.начало, фу->токен.длина);
        эмит(к, "(");
        if (узел_число_детей(инит) > 1) {
            Узел *арг = узел_ребёнок(инит, 1);
            кодоген_выражения(к, арг);
            // Аргумент-срез уходит в поток по «владеет» — владение перешло
            // потоку, и освобождать его здесь больше нельзя (иначе double-free:
            // функция потока освободит его сама при выходе). Семантика этот move
            // уже проверила, но у кодогена свой учёт autofree — метим и тут.
            СигнатураК *сиг = найти_сигнатуру_к(к, &фу->токен);
            if (сиг && сиг->число > 0 && сиг->владеет[0]
                && арг->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
                пометить_перемещённым(к, &арг->токен);
            }
        }
        эмит(к, ")");
    }
    эмит(к, ";\n");
    зарегистрировать_поток_к(к, &узел->токен);  // авто-join при выходе из области
}

static void кодоген_объявления(Кодоген *к, Узел *узел)
{
    if (узел->поток) {
        кодоген_объявления_потока(к, узел);
        return;
    }
    if (узел->срез) {
        кодоген_объявления_среза(к, узел);
        return;
    }
    // Переменная-перечисление «Имя ц = ВАРИАНТ» → «Имя ц = ВАРИАНТ;».
    if (узел->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) {
        эмит(к, "    ");
        if (узел->статический) эмит(к, "static ");
        эмит_тип(к, ТОКЕН_ПЕРЕЧИСЛЕНИЕ, 0, &узел->имя_типа);
        эмит(к, " ");
        эмит_имя(к, &узел->токен);
        if (узел_число_детей(узел) > 0) {
            эмит(к, " = ");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
        }
        эмит(к, ";\n");
        return;
    }
    // Переменная-указатель на функцию «БинарОп оп [= функ|нуль]».
    if (узел->тип_данных == ТОКЕН_ФУНКУКАЗАТЕЛЬ) {
        эмит(к, "    ");
        if (узел->статический) эмит(к, "static ");
        эмит_тип(к, ТОКЕН_ФУНКУКАЗАТЕЛЬ, 0, &узел->имя_типа);
        эмит(к, " ");
        эмит_имя(к, &узел->токен);
        эмит(к, " = ");
        if (узел_число_детей(узел) > 0)
            кодоген_выражения(к, узел_ребёнок(узел, 0));  // имя функции или «нуль»
        else
            эмит(к, "nullptr");
        эмит(к, ";\n");
        return;
    }
    // Переменная-структура «Имя п» → «Имя п = {0};» (зануление).
    if (узел->тип_данных == ТОКЕН_СТРУКТУРА) {
        эмит(к, "    ");
        if (узел->статический) {
            эмит(к, "static ");
        }
        эмит_тип(к, ТОКЕН_СТРУКТУРА, 0, &узел->имя_типа);
        эмит(к, " ");
        эмит_указатели(к, узел->указатели);
        эмит_имя(к, &узел->токен);
        Узел *иниц = узел_число_детей(узел) > 0 ? узел_ребёнок(узел, 0) : nullptr;
        if (узел->указатели > 0) {
            // Указатель на структуру: с инициализатором — его значение
            // (семантика уже проверила совместимость: «холст* х = холст_создать(…)»);
            // без — nullptr не бывает (семантика требует инициализатор у
            // обычных указателей), но оставляем как страховку.
            if (иниц) {
                эмит(к, " = ");
                кодоген_выражения(к, иниц);
                эмит(к, ";\n");
            } else {
                эмит(к, " = nullptr;\n");
            }
        } else if (иниц && иниц->тип != УЗЕЛ_ЛИТЕРАЛ_СТРУКТУРЫ) {
            // Инициализатор-выражение (вызов функции, другая переменная-структура,
            // доступ к полю…) — обычное копирование значения структуры. Семантика
            // уже проверила совместимость типов и что структуре не нужна
            // деструкция (иначе копия запрещена — во избежание double-free).
            эмит(к, " = ");
            кодоген_выражения(к, иниц);
            эмит(к, ";\n");
        } else if (иниц && иниц->тип == УЗЕЛ_ЛИТЕРАЛ_СТРУКТУРЫ) {
            // Литерал полей → designated initializer C: «{ .поле = знач, … }».
            // Незаданные поля C занулит автоматически.
            эмит(к, " = { ");
            for (size_t i = 0; i < узел_число_детей(иниц); ++i) {
                Узел *пиниц = узел_ребёнок(иниц, i);
                if (i > 0) {
                    эмит(к, ", ");
                }
                эмит(к, ".");
                эмит_отрезок(к, пиниц->токен.начало, пиниц->токен.длина);
                эмит(к, " = ");
                Узел *знач_поля = узел_ребёнок(пиниц, 0);
                // Поле-срез: «выделить(N)»/«клон(x)» — как для обычного
                // владеющего объявления среза, но без registerации в стеке
                // autofree (структура владеет полем целиком — см.
                // эмит_деструкцию_структуры, которая освобождает поля-срезы
                // при выходе структуры-владельца из области).
                if (знач_поля->тип == УЗЕЛ_ВЫЗОВ
                    && срез_равен(знач_поля->токен.начало, знач_поля->токен.длина, "выделить")
                    && узел_число_детей(знач_поля) == 1) {
                    // Тип элемента поля-среза — из объявления структуры (пиниц
                    // хранит только имя поля, не его тип; тип нужен для sizeof).
                    char тип_си[64];
                    тип_си[0] = '\0';
                    const Узел *стр_узел = найти_узел_структуры_к(к, &узел->имя_типа);
                    if (стр_узел) {
                        for (size_t j = 0; j < узел_число_детей(стр_узел); ++j) {
                            const Узел *поле_д = узел_ребёнок(стр_узел, j);
                            if (поле_д && поле_д->тип == УЗЕЛ_ПАРАМЕТР
                                && поле_д->токен.длина == пиниц->токен.длина
                                && memcmp(поле_д->токен.начало, пиниц->токен.начало,
                                          пиниц->токен.длина) == 0) {
                                тип_токена_в_си(поле_д->тип_данных, поле_д->беззнаковый,
                                                тип_си, sizeof(тип_си));
                                break;
                            }
                        }
                    }
                    эмит(к, "срез_выделить(");
                    кодоген_выражения(к, узел_ребёнок(знач_поля, 0));
                    эмит(к, ", sizeof(");
                    эмит(к, тип_си);
                    эмит(к, "))");
                } else if (знач_поля->тип == УЗЕЛ_ВЫЗОВ
                           && срез_равен(знач_поля->токен.начало, знач_поля->токен.длина, "клон")
                           && узел_число_детей(знач_поля) == 1) {
                    эмит(к, "срез_клон(");
                    кодоген_выражения(к, узел_ребёнок(знач_поля, 0));
                    эмит(к, ")");
                } else {
                    кодоген_выражения(к, знач_поля);
                }
            }
            эмит(к, " };\n");
        } else {
            эмит(к, " = {0};\n");
        }
        // Структура-значение, требующая деструкции (собственный деструктор
        // или поле-структура с деструктором), — под autofree (RAII).
        if (узел->указатели == 0
            && структура_нужна_деструкция_к(к, &узел->имя_типа, 0)) {
            const Токен *дестр = найти_деструктор_к(к, &узел->имя_типа);
            зарегистрировать_ресурс(к, &узел->токен, дестр, &узел->имя_типа);
        }
        return;
    }
    char тип_си[64];
    тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
    эмит(к, "    ");
    if (узел->статический) {
        эмит(к, "static ");
    }
    эмит(к, тип_си);
    эмит(к, " ");
    эмит_указатели(к, узел->указатели);
    эмит_имя(к, &узел->токен);

    // Массив фиксированного размера «тип имя[N]». Без инициализатора —
    // зануляется автоматически (нет чтения неинициализированной памяти).
    // С литералом «{ значения }» — эмитим как есть; недостающие хвостовые
    // элементы C-компилятор занулит сам (правило C для агрегатной инициализации).
    if (узел->массив) {
        char размер_текст[24];
        snprintf(размер_текст, sizeof(размер_текст), "[%ld]", узел->размер_массива);
        эмит(к, размер_текст);
        Узел *иниц = узел_число_детей(узел) > 0 ? узел_ребёнок(узел, 0) : nullptr;
        if (иниц && иниц->тип == УЗЕЛ_ЛИТЕРАЛ_МАССИВА
            && узел_число_детей(иниц) > 0) {
            эмит(к, " = { ");
            for (size_t i = 0; i < узел_число_детей(иниц); ++i) {
                if (i > 0) эмит(к, ", ");
                кодоген_выражения(к, узел_ребёнок(иниц, i));
            }
            эмит(к, " };\n");
        } else {
            эмит(к, " = {0};\n");
        }
        return;
    }

    if (узел_число_детей(узел) > 0) {
        эмит(к, " = ");
        кодоген_выражения(к, узел_ребёнок(узел, 0));
    } else if (узел->указатели > 0 && !узел->срез) {
        // Защитное зануление сырого указателя без инициализатора: вместо
        // неопределённого значения — детерминированный nullptr. Индексирование
        // такого указателя семантика ловит как нулевое разыменование.
        эмит(к, " = nullptr");
    }
    эмит(к, ";\n");
}

static void кодоген_если(Кодоген *к, Узел *узел)
{
    эмит(к, "    if (");
    кодоген_выражения(к, узел_ребёнок(узел, 0));
    эмит(к, ") {\n");
    кодоген_блока(к, узел_ребёнок(узел, 1));

    Узел *иначе = узел_ребёнок(узел, 2);
    if (иначе) {
        эмит(к, "    } else {\n");
        кодоген_блока(к, иначе);
    }
    эмит(к, "    }\n");
}

// Sum-тип: «выбор ф { Круг(р) { … } … }» → switch по .тег с извлечением полей
// во временную. Выбираемое вычисляется один раз во временную «_выбор_N».
static void кодоген_sum_выбор(Кодоген *к, Узел *узел, const Узел *пер)
{
    size_t nid = к->счётчик_выбор++;
    char имявр[32];
    snprintf(имявр, sizeof имявр, "_выбор_%zu", nid);
    эмит(к, "    { ");
    эмит_манглированное(к, пер->токен.начало, пер->токен.длина);
    эмит(к, " ");
    эмит(к, имявр);
    эмит(к, " = ");
    кодоген_выражения(к, узел_ребёнок(узел, 0));
    эмит(к, ";\n    switch (");
    эмит(к, имявр);
    эмит(к, ".тег) {\n");
    for (size_t b = 1; b < узел_число_детей(узел); ++b) {
        Узел *ветка = узел_ребёнок(узел, b);
        if (!ветка || ветка->тип != УЗЕЛ_ВЕТКА_ВЫБОРА) continue;
        const Узел *вар = nullptr; int тег = 0;
        if (срез_равен(ветка->токен.начало, ветка->токен.длина, "_")) {
            эмит(к, "    default: {\n");
        } else {
            найти_sum_вариант_к(к, ветка->токен.начало, ветка->токен.длина, &вар, &тег);
            char тегтекст[16];
            snprintf(тегтекст, sizeof тегтекст, "%d", тег);
            эмит(к, "    case ");
            эмит(к, тегтекст);
            эмит(к, ": {\n");
        }
        // Привязка полей: «тип имя = _выбор_N.данные.Вариант.поле;».
        size_t привязок = узел_число_детей(ветка) - 1;
        if (вар && привязок > 0) {
            size_t поле_и = 0;
            for (size_t k = 0; k < узел_число_детей(вар) && поле_и < привязок; ++k) {
                Узел *пл = узел_ребёнок(вар, k);
                if (!пл || пл->тип != УЗЕЛ_ПАРАМЕТР) continue;
                Узел *имя_узел = узел_ребёнок(ветка, поле_и);
                эмит(к, "        ");
                эмит_тип(к, пл->тип_данных, пл->беззнаковый, &пл->имя_типа);
                эмит(к, " ");
                эмит_указатели(к, пл->указатели);
                эмит_имя(к, &имя_узел->токен);
                эмит(к, " = ");
                эмит(к, имявр);
                эмит(к, ".данные.");
                эмит_отрезок(к, вар->токен.начало, вар->токен.длина);
                эмит(к, ".");
                эмит_отрезок(к, пл->токен.начало, пл->токен.длина);
                эмит(к, ";\n");
                поле_и++;
            }
        }
        кодоген_блока(к, узел_последний_ребёнок(ветка));
        эмит(к, "    } break;\n");
    }
    эмит(к, "    } }\n");
}

// «выбор выр { ВАРИАНТ { … }  _ { … } }» → C switch с case по вариантам.
static void кодоген_выбор(Кодоген *к, Узел *узел)
{
    // Sum-тип определяем по первой именованной ветке (её вариант — из sum-enum).
    for (size_t b = 1; b < узел_число_детей(узел); ++b) {
        Узел *ветка = узел_ребёнок(узел, b);
        if (!ветка || ветка->тип != УЗЕЛ_ВЕТКА_ВЫБОРА) continue;
        if (срез_равен(ветка->токен.начало, ветка->токен.длина, "_")) continue;
        const Узел *пер = найти_sum_вариант_к(к, ветка->токен.начало,
                                              ветка->токен.длина, nullptr, nullptr);
        if (пер) { кодоген_sum_выбор(к, узел, пер); return; }
        break;
    }
    эмит(к, "    switch (");
    кодоген_выражения(к, узел_ребёнок(узел, 0));
    эмит(к, ") {\n");
    for (size_t b = 1; b < узел_число_детей(узел); ++b) {
        Узел *ветка = узел_ребёнок(узел, b);
        if (!ветка || ветка->тип != УЗЕЛ_ВЕТКА_ВЫБОРА) continue;
        if (срез_равен(ветка->токен.начало, ветка->токен.длина, "_")) {
            эмит(к, "    default: {\n");
        } else {
            эмит(к, "    case ");
            эмит_отрезок(к, ветка->токен.начало, ветка->токен.длина);
            эмит(к, ": {\n");
        }
        кодоген_блока(к, узел_последний_ребёнок(ветка));
        эмит(к, "    } break;\n");
    }
    эмит(к, "    }\n");
}

// Объявление или выражение/присваивание в одну строку, без отступа,
// «;» и перевода строки — для заголовка цикла «для».
static void кодоген_инлайн(Кодоген *к, Узел *узел)
{
    if (!узел) {
        return;
    }
    if (узел->тип == УЗЕЛ_ОБЪЯВЛЕНИЕ) {
        char тип_си[64];
        тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
        эмит(к, тип_си);
        эмит(к, " ");
        эмит_указатели(к, узел->указатели);
        эмит_имя(к, &узел->токен);
        if (узел_число_детей(узел) > 0) {
            эмит(к, " = ");
            кодоген_выражения(к, узел_ребёнок(узел, 0));
        }
        return;
    }
    кодоген_выражения(к, узел);
}

static void кодоген_пока(Кодоген *к, Узел *узел)
{
    эмит(к, "    while (");
    кодоген_выражения(к, узел_ребёнок(узел, 0));
    эмит(к, ") {\n");
    кодоген_блока(к, узел_ребёнок(узел, 1));
    эмит(к, "    }\n");
}

static void кодоген_оператора(Кодоген *к, Узел *узел);
static void эмит_тип_узла(Кодоген *к, Узел *у);

// «параллельно для (T и = н; и < предел; и = и + ш) { тело }» → диапазон
// дробится на непересекающиеся куски, по куску на поток. Замыканий в языке нет,
// поэтому кадр с захваченными переменными строим здесь (список собрала
// семантика — дети-УЗЕЛ_ЗАХВАТ), а внутри шима имена печатаются как «_к->имя».
//
// Синхронизации нет и не нужно: куски не делят ни одной ячейки (семантика это
// проверила), поэтому цена — только создание потоков.
// Пред-проход: печатает на УРОВНЕ ФАЙЛА кадр и шим для каждого «параллельно
// для». Именно на уровне файла, а не вложенной функцией: вложенная функция GNU,
// взятая по указателю, требует трамплина на исполняемом стеке — на现 hardened-
// сборках это SEGV (поймал ASan).
// Печатает «(тип_счётчика)» перед границей куска, если семантика доказала, что
// граница цикла не шире счётчика. Иначе — ничего (сравнение остаётся в long,
// как было: сузить было бы сменой поведения).
static void эмит_приведение_к_счётчику(Кодоген *к, Узел *цикл, Узел *иниц)
{
    if (!цикл->граница_в_типе_счётчика) return;
    эмит(к, "(");
    эмит_тип(к, иниц->тип_данных, иниц->беззнаковый, &иниц->имя_типа);
    эмит(к, ")");
}

static void кодоген_шимов_параллельно(Кодоген *к, Узел *узел)
{
    if (!узел) return;
    if (узел->тип == УЗЕЛ_ДЛЯ && узел->параллельно) {
        Узел *иниц = узел_ребёнок(узел, 0);
        Узел *шаг  = узел_ребёнок(узел, 2);
        Узел *тело = узел_ребёнок(узел, 3);
        узел->индекс_параллельного = ++к->счётчик_параллельно;
        char метка[32];
        snprintf(метка, sizeof метка, "%d", узел->индекс_параллельного);

        эмит(к, "typedef struct { ");
        for (size_t i = 4; i < узел_число_детей(узел); ++i) {
            Узел *з = узел_ребёнок(узел, i);
            эмит_тип_узла(к, з);
            эмит(к, " ");
            эмит_имя(к, &з->токен);
            эмит(к, "; ");
        }
        эмит(к, "long _нач; long _кон; } _пкадр");
        эмит(к, метка);
        эмит(к, ";\n");

        эмит(к, "static void *_пшим");
        эмит(к, метка);
        эмит(к, "(void *_а)\n{\n    _пкадр");
        эмит(к, метка);
        эмит(к, " *_к = (_пкадр");
        эмит(к, метка);
        эмит(к, " *)_а;\n    for (");
        эмит_тип(к, иниц->тип_данных, иниц->беззнаковый, &иниц->имя_типа);
        эмит(к, " ");
        эмит_имя(к, &иниц->токен);
        эмит(к, " = ");
        // Границы куска хранятся как long. Сравнивать с ними счётчик напрямую
        // нельзя без потери скорости: под -fwrapv компилятор обязан допустить,
        // что счётчик дойдёт до предела своего типа и перенесётся, — число
        // итераций становится недоказуемым и векторизация отключается (было ~4x
        // потери). Приводим границы к типу счётчика — так шим сравнивает ровно
        // как обычный «для», от которого он и отклонился. Семантика допускает
        // это только когда граница не шире счётчика (проверила семантика).
        эмит_приведение_к_счётчику(к, узел, иниц);
        эмит(к, "_к->_нач; ");
        эмит_имя(к, &иниц->токен);
        эмит(к, " < ");
        эмит_приведение_к_счётчику(к, узел, иниц);
        эмит(к, "_к->_кон; ");
        // Внутри шима захваченные имена печатаются как «_к->имя».
        к->число_захватов = узел_число_детей(узел) - 4;
        for (size_t i = 4; i < узел_число_детей(узел); ++i)
            к->захваты[i - 4] = &узел_ребёнок(узел, i)->токен;
        кодоген_выражения(к, шаг);
        эмит(к, ") {\n");
        for (size_t i = 0; i < узел_число_детей(тело); ++i)
            кодоген_оператора(к, узел_ребёнок(тело, i));
        к->число_захватов = 0;
        эмит(к, "    }\n    (void)_к;\n    return nullptr;\n}\n");
    }
    for (size_t i = 0; i < узел_число_детей(узел); ++i)
        кодоген_шимов_параллельно(к, узел_ребёнок(узел, i));
}

// Сайт цикла: дробим диапазон на непересекающиеся куски и запускаем потоки.
// Синхронизации нет и не нужно — куски не делят ни одной ячейки.
// Разрядность целого типа — для guard'а переполнения при свёртке редукции.
static int разрядность_типа(ТипТокена т)
{
    switch (т) {
        case ТОКЕН_ЦЕЛОЕ8:  case ТОКЕН_БАЙТ: return 8;
        case ТОКЕН_ЦЕЛОЕ16: return 16;
        case ТОКЕН_ЦЕЛОЕ64: return 64;
        default: return 32;   // целое32, перечисление, логический
    }
}

// Единица операции редукции: с неё стартует аккумулятор КАЖДОГО куска, поэтому
// пустой кусок сворачивается в no-op, а результат не зависит от разбиения.
static void эмит_единицу_редукции(Кодоген *к, Узел *з)
{
    switch (з->редукция_оп) {
        case ТОКЕН_ЗВЕЗДА: эмит(к, "1"); break;
        case ТОКЕН_АМПЕРСАНД:                  // нейтраль «&» — все биты
            эмит(к, "(");
            эмит_тип_узла(к, з);
            эмит(к, ")~0");
            break;
        default: эмит(к, "0"); break;          // + | ^
    }
}

static void кодоген_параллельно_для(Кодоген *к, Узел *узел)
{
    Узел *иниц  = узел_ребёнок(узел, 0);
    Узел *услов = узел_ребёнок(узел, 1);
    char метка[32];
    snprintf(метка, sizeof метка, "%d", узел->индекс_параллельного);

    эмит(к, "    {\n        long _предел = (long)(");
    кодоген_выражения(к, узел_ребёнок(услов, 1));   // правая часть «и < предел»
    эмит(к, ");\n        long _от = (long)(");
    кодоген_выражения(к, узел_ребёнок(иниц, 0));    // начальное значение
    эмит(к, ");\n");
    эмит(к, "        long _всего = _предел > _от ? _предел - _от : 0;\n");
    эмит(к, "        long _нп = конда_число_ядер();\n");
    эмит(к, "        if (_нп > _всего) _нп = _всего > 0 ? _всего : 1;\n");
    эмит(к, "        pthread_t _пт[64]; _пкадр");
    эмит(к, метка);
    эмит(к, " _пк[64];\n");
    эмит(к, "        long _куск = (_всего + _нп - 1) / _нп;\n");
    эмит(к, "        for (long _т = 0; _т < _нп; ++_т) {\n");
    for (size_t i = 4; i < узел_число_детей(узел); ++i) {
        Узел *з = узел_ребёнок(узел, i);
        эмит(к, "            _пк[_т].");
        эмит_имя(к, &з->токен);
        эмит(к, " = ");
        if (з->редукция) {
            // Аккумулятор НЕ копируем: каждый кусок считает свой частичный
            // результат от единицы, внешнее значение войдёт один раз в свёртке.
            эмит_единицу_редукции(к, з);
        } else {
            эмит_имя(к, &з->токен);
        }
        эмит(к, ";\n");
    }
    эмит(к, "            _пк[_т]._нач = _от + _т * _куск;\n");
    эмит(к, "            _пк[_т]._кон = _пк[_т]._нач + _куск;\n");
    эмит(к, "            if (_пк[_т]._кон > _предел) _пк[_т]._кон = _предел;\n");
    эмит(к, "            if (pthread_create(&_пт[_т], nullptr, _пшим");
    эмит(к, метка);
    эмит(к, ", &_пк[_т]) != 0) { perror(\"pthread_create\"); exit(1); }\n");
    эмит(к, "        }\n");
    // Свёртка — ПОСЛЕ join и в одном потоке: общей ячейки не было ни на миг.
    эмит(к, "        for (long _т = 0; _т < _нп; ++_т) {\n");
    эмит(к, "            pthread_join(_пт[_т], nullptr);\n");
    for (size_t i = 4; i < узел_число_детей(узел); ++i) {
        Узел *з = узел_ребёнок(узел, i);
        if (!з->редукция) continue;
        int знаковый = !з->беззнаковый;
        int арифм = з->редукция_оп == ТОКЕН_ПЛЮС || з->редукция_оп == ТОКЕН_ЗВЕЗДА;
        эмит(к, "            ");
        эмит_имя(к, &з->токен);
        эмит(к, " = ");
        if (арифм && знаковый && !к->релиз) {
            // Свёртка тоже может переполниться — в отладке под тем же guard'ом,
            // что и обычная арифметика (в релизе guard'ов нет, как в C).
            char ширина[16], строка[16];
            snprintf(ширина, sizeof ширина, "%d", разрядность_типа(з->тип_данных));
            snprintf(строка, sizeof строка, "%d", узел->токен.строка);
            эмит(к, "арифметика_безопасно((long)(");
            эмит_имя(к, &з->токен);
            эмит(к, "), (long)(_пк[_т].");
            эмит_имя(к, &з->токен);
            эмит(к, "), ");
            эмит(к, з->редукция_оп == ТОКЕН_ПЛЮС ? "0" : "2");
            эмит(к, ", ");
            эмит(к, ширина);
            эмит(к, ", ");
            эмит(к, строка);
            эмит(к, ")");
        } else {
            эмит_имя(к, &з->токен);
            эмит(к, " ");
            switch (з->редукция_оп) {
                case ТОКЕН_ЗВЕЗДА:         эмит(к, "*"); break;
                case ТОКЕН_ПРЯМАЯ_ЧЕРТА:   эмит(к, "|"); break;
                case ТОКЕН_СТРЕЛКА_ВВЕРХ:  эмит(к, "^"); break;
                case ТОКЕН_АМПЕРСАНД:      эмит(к, "&"); break;
                default:                   эмит(к, "+"); break;
            }
            эмит(к, " _пк[_т].");
            эмит_имя(к, &з->токен);
        }
        эмит(к, ";\n");
    }
    эмит(к, "        }\n");
    эмит(к, "    }\n");
}

static void кодоген_для(Кодоген *к, Узел *узел)
{
    эмит(к, "    for (");
    кодоген_инлайн(к, узел_ребёнок(узел, 0));
    эмит(к, "; ");
    кодоген_выражения(к, узел_ребёнок(узел, 1));
    эмит(к, "; ");
    кодоген_инлайн(к, узел_ребёнок(узел, 2));
    эмит(к, ") {\n");
    кодоген_блока(к, узел_ребёнок(узел, 3));
    эмит(к, "    }\n");
}

// foreach: «для_каждого (имя из коллекция) тело» → C-цикл с прямым доступом.
// Индекс цикла генерируем сами и контролируем границу — рантайм-проверка не
// нужна (нулевая стоимость, как у канонического «для» с BCE).
//   срез: «for (size_t __k = 0; __k < (col).длина; ++__k) { T имя = ((T*)col.данные)[__k]; … }»
//   массив: «for (size_t __k = 0; __k < N; ++__k) { T имя = col[__k]; … }»
// На месте «(col)» эмитим выражение коллекции один раз — для срезов оно может
// быть неконстантной expression-значения (например, возврат функции), поэтому
// заводим C-переменную __коллекция, чтобы оценить ровно один раз.
static void кодоген_для_каждого(Кодоген *к, Узел *узел)
{
    Узел *коллекция = узел_ребёнок(узел, 0);
    Узел *тело      = узел_ребёнок(узел, 1);

    char тип_си[64];
    тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));

    // Открываем доп. C-блок, чтобы __коллекция и __к жили только внутри цикла.
    эмит(к, "    {\n");

    if (узел->срез) {
        // Срез сохраняем как локальную копию дескриптора — оценить выражение
        // ровно один раз. Сам массив данных не копируется (дескриптор это
        // {ptr, len, sz}).
        эмит(к, "    Срез __коллекция = ");
        кодоген_выражения(к, коллекция);
        эмит(к, ";\n");
        эмит(к, "    for (size_t __к = 0; __к < __коллекция.длина; ++__к) {\n");
        эмит(к, "        ");
        эмит(к, тип_си);
        эмит_указатели(к, узел->указатели);
        эмит(к, " ");
        эмит_имя(к, &узел->токен);
        эмит(к, " = ((");
        эмит(к, тип_си);
        эмит_указатели(к, узел->указатели);
        эмит(к, " *)__коллекция.данные)[__к];\n");
    } else {
        // Массив с известным компиляционным размером — итерируем по N, индекс
        // канонически безопасен. Имя коллекции — идентификатор, потому что
        // семантика разрешает массивы только из символа.
        char размер_текст[32];
        snprintf(размер_текст, sizeof(размер_текст), "%ld", узел->размер_массива);
        эмит(к, "    for (size_t __к = 0; __к < ");
        эмит(к, размер_текст);
        эмит(к, "; ++__к) {\n");
        эмит(к, "        ");
        эмит(к, тип_си);
        эмит_указатели(к, узел->указатели);
        эмит(к, " ");
        эмит_имя(к, &узел->токен);
        эмит(к, " = ");
        кодоген_выражения(к, коллекция);
        эмит(к, "[__к];\n");
    }

    кодоген_блока(к, тело);
    эмит(к, "    }\n");
    эмит(к, "    }\n");
}

static void кодоген_возврата(Кодоген *к, Узел *узел)
{
    int есть_значение = узел_число_детей(узел) > 0;

    // Возврат среза = передача владения наружу. Возвращаемый срез исключаем из
    // autofree (его освободит вызывающий), остальные владеющие — освобождаем.
    if (есть_значение && к->срез_возврата) {
        Узел *выр = узел_ребёнок(узел, 0);
        if (выр->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
            пометить_перемещённым(к, &выр->токен);
        }
        освободить_все(к);
        эмит(к, "    return ");
        кодоген_выражения(к, выр);
        эмит(к, ";\n");
        return;
    }

    // Если возвращаемое выражение может читать владеющий срез, его значение надо
    // вычислить ДО освобождения — иначе use-after-free. Сохраняем во временную.
    if (есть_значение && к->число_владеющих > 0) {
        эмит(к, "    ");
        эмит(к, к->тип_возврата_си);
        эмит(к, " _результат_konda = ");
        кодоген_выражения(к, узел_ребёнок(узел, 0));
        эмит(к, ";\n");
        освободить_все(к);
        эмит(к, "    return _результат_konda;\n");
        return;
    }

    // Иначе порядок не важен: освобождаем живые срезы и возвращаем.
    освободить_все(к);
    эмит(к, "    return");
    if (есть_значение) {
        эмит(к, " ");
        кодоген_выражения(к, узел_ребёнок(узел, 0));
    }
    эмит(к, ";\n");
}

// Переприсваивание срезу «а = …»: сначала free прежних данных (иначе утечка),
// затем цель получает новое владение. Цель остаётся в стеке владеющих, поэтому
// в конце области освободится уже новое значение.
static void кодоген_присваивания_среза(Кодоген *к, Узел *узел)
{
    Узел *цель = узел_ребёнок(узел, 0);
    Узел *знач = узел_ребёнок(узел, 1);

    эмит(к, "    free(");
    эмит_имя(к, &цель->токен);
    эмит(к, ".данные);\n");
    эмит(к, "    ");
    эмит_имя(к, &цель->токен);
    эмит(к, " = ");

    if (знач->тип == УЗЕЛ_ВЫЗОВ
        && срез_равен(знач->токен.начало, знач->токен.длина, "выделить")
        && узел_число_детей(знач) == 1) {
        char тип_си[64];
        тип_токена_в_си(узел->тип_данных, узел->беззнаковый, тип_си, sizeof(тип_си));
        эмит(к, "срез_выделить(");
        кодоген_выражения(к, узел_ребёнок(знач, 0));
        эмит(к, ", sizeof(");
        эмит(к, тип_си);
        эмит(к, "))");
    } else if (знач->тип == УЗЕЛ_ВЫЗОВ
        && срез_равен(знач->токен.начало, знач->токен.длина, "клон")
        && узел_число_детей(знач) == 1) {
        эмит(к, "срез_клон(");
        кодоген_выражения(к, узел_ребёнок(знач, 0));
        эмит(к, ")");
    } else {
        кодоген_выражения(к, знач);
        if (знач->тип == УЗЕЛ_ИДЕНТИФИКАТОР) {
            пометить_перемещённым(к, &знач->токен);  // move: источник не освобождаем
        }
    }
    эмит(к, ";\n");
}

static void кодоген_оператора(Кодоген *к, Узел *узел)
{
    switch (узел->тип) {
        case УЗЕЛ_ЕСЛИ:
            кодоген_если(к, узел);
            break;
        case УЗЕЛ_ВЫБОР:
            кодоген_выбор(к, узел);
            break;
        case УЗЕЛ_ПОКА:
            кодоген_пока(к, узел);
            break;
        case УЗЕЛ_ДЛЯ:
            if (узел->параллельно) кодоген_параллельно_для(к, узел);
            else кодоген_для(к, узел);
            break;
        case УЗЕЛ_ДЛЯ_КАЖДОГО:
            кодоген_для_каждого(к, узел);
            break;
        case УЗЕЛ_ВОЗВРАТ:
            кодоген_возврата(к, узел);
            break;
        case УЗЕЛ_ПРЕРВАТЬ:
            эмит(к, "    break;\n");
            break;
        case УЗЕЛ_ПРОДОЛЖИТЬ:
            эмит(к, "    continue;\n");
            break;
        case УЗЕЛ_НЕБЕЗОПАСНО:
            // «небезопасно { }» — обычный C-блок: только область видимости +
            // autofree, никакой рантайм-цены (гейт был статический).
            эмит(к, "    {\n");
            кодоген_блока(к, узел_ребёнок(узел, 0));
            эмит(к, "    }\n");
            break;
        case УЗЕЛ_ОБЪЯВЛЕНИЕ:
            кодоген_объявления(к, узел);
            break;
        case УЗЕЛ_ПРИСВАИВАНИЕ:
            if (узел->срез) {  // переприсваивание срезу: free старого + новое
                кодоген_присваивания_среза(к, узел);
                break;
            }
            эмит(к, "    ");
            кодоген_выражения(к, узел);
            эмит(к, ";\n");
            break;
        case УЗЕЛ_ВЫЗОВ:
            эмит(к, "    ");
            кодоген_выражения(к, узел);
            эмит(к, ";\n");
            break;
        default:
            break;
    }
}

static void кодоген_блока(Кодоген *к, Узел *блок)
{
    if (!блок) {
        return;
    }
    к->уровень_блока++;
    size_t n = узел_число_детей(блок);
    for (size_t i = 0; i < n; ++i) {
        кодоген_оператора(к, узел_ребёнок(блок, i));
    }
    // Autofree: освобождаем владеющие срезы этого блока (обратный порядок).
    // Если блок заканчивается «вернуть», тот уже всё освободил — free не
    // дублируем, но срезы со стека снимаем (область логически закрыта).
    int кончается_возвратом = n > 0
        && узел_ребёнок(блок, n - 1)->тип == УЗЕЛ_ВОЗВРАТ;
    if (кончается_возвратом) {
        while (к->число_владеющих > 0
               && к->уровень_владеющего[к->число_владеющих - 1] == к->уровень_блока) {
            к->число_владеющих--;
        }
    } else {
        освободить_уровень(к, к->уровень_блока, 1);
    }
    к->уровень_блока--;
}

static void кодоген_препроцессора(Кодоген *к, Узел *узел)
{
    char строка[512];
    size_t n = узел->токен.длина < sizeof(строка) - 1 ? узел->токен.длина : sizeof(строка) - 1;
    memcpy(строка, узел->токен.начало, n);
    строка[n] = '\0';

    const char *содержит = strstr(строка, "содержит");
    if (содержит) {
        size_t смещение = (size_t)(содержит - строка);
        эмит_отрезок(к, строка, смещение);
        эмит(к, "include");
        эмит(к, содержит + strlen("содержит"));
    } else {
        эмит_отрезок(к, узел->токен.начало, узел->токен.длина);
    }
    эмит(к, "\n");
}

// Эмиссия «тип имя(параметры)» без терминатора. Используется и для
// прототипа (далее «;\n»), и для определения тела (далее «\n{\n…»).
static void кодоген_заголовка_функции(Кодоген *к, Узел *узел)
{
    if (узел->статический) {
        эмит(к, "static ");
    }
    if (узел->срез) {
        эмит(к, "Срез");
    } else {
        эмит_тип(к, узел->тип_данных, узел->беззнаковый, &узел->имя_типа);
        эмит_указатели(к, узел->указатели);  // косвенность типа возврата «Тип*»
    }
    эмит(к, " ");

    if (узел->это_main) {
        эмит(к, "main");
    } else {
        эмит_имя(к, &узел->токен);
    }
    эмит(к, "(");

    size_t всего = узел_число_детей(узел);
    // У «внешняя» тела нет — все дети параметры; иначе тело — последний ребёнок.
    size_t тело_индекс = узел->внешняя ? всего : (всего > 0 ? всего - 1 : 0);

    if (узел->это_main) {
        эмит(к, "int32_t argc, char **argv");
    } else {
        int первый = 1;
        for (size_t i = 0; i < тело_индекс; ++i) {
            Узел *пар = узел_ребёнок(узел, i);
            if (пар->тип != УЗЕЛ_ПАРАМЕТР) {
                continue;
            }
            if (!первый) {
                эмит(к, ", ");
            }
            первый = 0;
            if (пар->срез) {
                эмит(к, "Срез ");
                if (пар->токен.длина > 0) {
                    эмит_имя(к, &пар->токен);
                }
                continue;
            }
            if (пар->тип_данных == ТОКЕН_СТРУКТУРА
                || пар->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ
                || пар->тип_данных == ТОКЕН_ФУНКУКАЗАТЕЛЬ) {
                // Структура/перечисление/указатель на функцию — по имени
                // пользовательского типа, а не «int» (важно и для точности
                // публичного заголовка библиотеки).
                эмит_тип(к, пар->тип_данных, 0, &пар->имя_типа);
                эмит(к, " ");
                эмит_указатели(к, пар->указатели);
                if (пар->по_ссылке) эмит(к, "*");  // ref/out → передаётся как Тип*
                if (пар->токен.длина > 0) {
                    эмит_имя(к, &пар->токен);
                }
                continue;
            }
            char tsi[64];
            тип_токена_в_си(пар->тип_данных, пар->беззнаковый, tsi, sizeof(tsi));
            эмит(к, tsi);
            for (int p = 0; p < пар->указатели; ++p) {
                эмит(к, "*");
            }
            if (пар->массив) {
                эмит(к, "[]");
            }
            эмит(к, " ");
            if (пар->по_ссылке) эмит(к, "*");  // ref/out → передаётся как Тип*
            if (пар->токен.длина > 0) {
                эмит_имя(к, &пар->токен);
            }
        }
    }

    эмит(к, ")");
}

// Прототип функции — forward-объявление в эмитируемом C. Нужно, чтобы вызов
// мог встречаться до определения (порядок верхне-уровневых объявлений между
// несколькими .конда не контролируется, плюс полезно для взаимной рекурсии).
static void кодоген_прототипа(Кодоген *к, Узел *узел)
{
    if (узел->это_main) return;     // main C-компилятор знает сам
    кодоген_заголовка_функции(к, узел);
    эмит(к, ";\n");
}

static void кодоген_функции(Кодоген *к, Узел *узел)
{
    к->в_точке_входа = узел->это_main;
    // Каждая функция — со своим стеком владеющих срезов и уровнем вложенности.
    к->число_владеющих = 0;
    к->уровень_блока = 0;
    к->срез_возврата = узел->срез;
    if (узел->срез) {
        snprintf(к->тип_возврата_си, sizeof(к->тип_возврата_си), "Срез");
    } else if (узел->тип_данных == ТОКЕН_СТРУКТУРА
               || узел->тип_данных == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) {
        // Возврат структуры/перечисления по значению: тип — имя пользовательского
        // типа (как в typedef), а не «int». Нужно, чтобы временная переменная
        // результата в «вернуть» при живых владеющих срезах (см. кодоген_возврата)
        // объявлялась корректным типом, а не int (иначе — ошибка компиляции C).
        size_t n = узел->имя_типа.длина < sizeof(к->тип_возврата_си) - 1
                       ? узел->имя_типа.длина : sizeof(к->тип_возврата_си) - 1;
        memcpy(к->тип_возврата_си, узел->имя_типа.начало, n);
        к->тип_возврата_си[n] = '\0';
    } else {
        тип_токена_в_си(узел->тип_данных, узел->беззнаковый,
                        к->тип_возврата_си, sizeof(к->тип_возврата_си));
    }
    // Косвенность типа возврата «Тип*» — дописываем «*» к строке типа (для
    // временной результата в «вернуть» при живых владеющих срезах).
    if (!узел->срез) {
        size_t дл = strlen(к->тип_возврата_си);
        for (int i = 0; i < узел->указатели && дл + 1 < sizeof(к->тип_возврата_си); ++i)
            к->тип_возврата_си[дл++] = '*';
        к->тип_возврата_си[дл] = '\0';
    }

    кодоген_заголовка_функции(к, узел);
    эмит(к, "\n{\n");

    // Буферы f-строк — на стеке в начале функции (по одному на вставку,
    // требующую форматирования). Не `static`: своя память на каждый вызов →
    // потокобезопасно и реентерабельно; разные вставки не затирают друг друга.
    к->счётчик_fстрок = 0;
    size_t fбуферов = посчитать_fбуферы(узел);
    for (size_t i = 0; i < fбуферов; ++i) {
        char декл[48];
        snprintf(декл, sizeof(декл), "    char _kf%zu[1024];\n", i);
        эмит(к, декл);
    }

    size_t всего = узел_число_детей(узел);
    size_t тело_индекс = всего > 0 ? всего - 1 : 0;

    // Параметры по ссылке (ref/out) текущей функции — их использование в теле
    // авто-разыменовывается «(*имя)» (кодоген выражения-идентификатора).
    к->число_по_ссылке = 0;
    for (size_t i = 0; i < тело_индекс && !узел->это_main; ++i) {
        Узел *пар = узел_ребёнок(узел, i);
        if (пар && пар->тип == УЗЕЛ_ПАРАМЕТР && пар->по_ссылке
            && пар->токен.длина > 0 && к->число_по_ссылке < МАКС_ПАРАМ_К) {
            к->по_ссылке_имена[к->число_по_ссылке++] = &пар->токен;
        }
    }

    // «Владеет»-параметры функция освобождает сама — регистрируем их на уровне
    // тела (1), чтобы autofree вставил free в конце тела и перед «вернуть».
    for (size_t i = 0; i < тело_индекс && !узел->это_main; ++i) {
        Узел *пар = узел_ребёнок(узел, i);
        if (пар && пар->тип == УЗЕЛ_ПАРАМЕТР && пар->владеет && пар->срез
            && пар->токен.длина > 0 && к->число_владеющих < МАКС_ВЛАДЕЮЩИХ) {
            к->владеющие[к->число_владеющих] = &пар->токен;
            к->уровень_владеющего[к->число_владеющих] = 1;
            к->перемещён_владеющего[к->число_владеющих] = 0;
            к->число_владеющих++;
        }
    }

    int тело_завершается_возвратом = 0;
    if (всего > 0) {
        Узел *тело = узел_ребёнок(узел, тело_индекс);
        кодоген_блока(к, тело);
        size_t операторов = узел_число_детей(тело);
        if (операторов > 0) {
            Узел *последний = узел_ребёнок(тело, операторов - 1);
            тело_завершается_возвратом = последний && последний->тип == УЗЕЛ_ВОЗВРАТ;
        }
    }

    // Неявный «return 0;» — только если тело не закончилось явным возвратом и
    // функция не «ничего» (void): в void-функции возврат значения недопустим, а
    // падение с конца тела в C разрешено.
    if (!тело_завершается_возвратом && узел->тип_данных != ТОКЕН_НИЧЕГО) {
        эмит(к, "    return 0;\n");
    }
    эмит(к, "}\n");
    к->в_точке_входа = 0;
}

// Тип поля/переменной/параметра: имя структуры или примитив.
static void эмит_тип(Кодоген *к, ТипТокена тип, int беззнаковый, const Токен *имя_типа)
{
    if (тип == ТОКЕН_СТРУКТУРА || тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ
        || тип == ТОКЕН_ФУНКУКАЗАТЕЛЬ) {  // именованный тип (typedef-псевдоним)
        эмит_манглированное(к, имя_типа->начало, имя_типа->длина);
    } else {
        char тип_си[64];
        тип_токена_в_си(тип, беззнаковый, тип_си, sizeof(тип_си));
        эмит(к, тип_си);
    }
}

// «структура Имя { … }» → «typedef struct { … } Имя;»;
// «союз Имя { … }» → «typedef union { … } Имя;» (поля разделяют память).
static void кодоген_структуры(Кодоген *к, Узел *узел)
{
    // «внешний тип» ничего не эмитит: определение/typedef приходит из
    // «#содержит»-заголовка C-библиотеки (наш дубликат мог бы конфликтовать —
    // typedef-имя не обязано совпадать с тегом struct, как cairo_t/_cairo).
    if (узел->внешняя) return;
    эмит(к, узел->это_союз ? "typedef union {\n" : "typedef struct {\n");
    for (size_t i = 0; i < узел_число_детей(узел); ++i) {
        Узел *поле = узел_ребёнок(узел, i);
        if (!поле || поле->тип != УЗЕЛ_ПАРАМЕТР) continue;
        эмит(к, "    ");
        if (поле->срез) {
            эмит(к, "Срез ");
            эмит_отрезок(к, поле->токен.начало, поле->токен.длина);
            эмит(к, ";\n");
            continue;
        }
        эмит_тип(к, поле->тип_данных, поле->беззнаковый, &поле->имя_типа);
        эмит(к, " ");
        эмит_указатели(к, поле->указатели);
        эмит_отрезок(к, поле->токен.начало, поле->токен.длина);
        эмит(к, ";\n");
    }
    эмит(к, "} ");
    эмит_манглированное(к, узел->токен.начало, узел->токен.длина);
    эмит(к, ";\n");
}

// Sum-тип: «перечисление Имя { Круг(double р) … }» → размеченный союз:
//   typedef struct { int32_t тег; union { struct{…} Круг; … } данные; } Имя;
// Тег — индекс варианта (0,1,2…). Варианты без данных не получают член union.
static void кодоген_sum_перечисления(Кодоген *к, Узел *узел)
{
    эмит(к, "typedef struct {\n    int32_t тег;\n    union {\n");
    for (size_t i = 0; i < узел_число_детей(узел); ++i) {
        Узел *вар = узел_ребёнок(узел, i);
        if (!вар || вар->тип != УЗЕЛ_ВАРИАНТ || узел_число_детей(вар) == 0) continue;
        if (узел_ребёнок(вар, 0)->тип != УЗЕЛ_ПАРАМЕТР) continue;
        эмит(к, "        struct { ");
        for (size_t k = 0; k < узел_число_детей(вар); ++k) {
            Узел *пл = узел_ребёнок(вар, k);
            if (!пл || пл->тип != УЗЕЛ_ПАРАМЕТР) continue;
            эмит_тип(к, пл->тип_данных, пл->беззнаковый, &пл->имя_типа);
            эмит(к, " ");
            эмит_указатели(к, пл->указатели);
            эмит_отрезок(к, пл->токен.начало, пл->токен.длина);
            эмит(к, "; ");
        }
        эмит(к, "} ");
        эмит_отрезок(к, вар->токен.начало, вар->токен.длина);
        эмит(к, ";\n");
    }
    эмит(к, "    } данные;\n} ");
    эмит_манглированное(к, узел->токен.начало, узел->токен.длина);
    эмит(к, ";\n");
}

// «перечисление Имя { ВАР [= знач] … }» → «typedef enum { ВАР = знач, … } Имя;».
// Sum-тип (варианты с данными) идёт отдельным путём — размеченный союз.
static void кодоген_перечисления(Кодоген *к, Узел *узел)
{
    if (перечисление_это_sum(узел)) { кодоген_sum_перечисления(к, узел); return; }
    эмит(к, "typedef enum {\n");
    for (size_t i = 0; i < узел_число_детей(узел); ++i) {
        Узел *вар = узел_ребёнок(узел, i);
        if (!вар || вар->тип != УЗЕЛ_ВАРИАНТ) continue;
        эмит(к, "    ");
        эмит_отрезок(к, вар->токен.начало, вар->токен.длина);
        if (узел_число_детей(вар) > 0) {  // явное значение «= выражение»
            эмит(к, " = ");
            кодоген_выражения(к, узел_ребёнок(вар, 0));
        }
        эмит(к, ",\n");
    }
    эмит(к, "} ");
    эмит_манглированное(к, узел->токен.начало, узел->токен.длина);
    эмит(к, ";\n");
}

// Рантайм «параллельно для»: сколько потоков брать. Одна функция, без пула —
// диапазон дробится на равные куски, потоки создаются и сразу ожидаются.
static const char ПРОЛОГ_ПАРАЛЛЕЛЬНО[] =
    "\n"
    "/* «параллельно для»: диапазон дробится на непересекающиеся куски, поэтому\n"
    "   синхронизация не нужна — потоки не делят ни одной ячейки. */\n"
    "#include <unistd.h>\n"
    "[[maybe_unused]] static long конда_число_ядер(void)\n"
    "{\n"
    "    long н = sysconf(_SC_NPROCESSORS_ONLN);\n"
    "    return н > 0 ? (н > 64 ? 64 : н) : 1;\n"
    "}\n";

// Использует ли программа «параллельно для»?
static int узел_использует_параллельно(Узел *узел)
{
    if (!узел) return 0;
    if (узел->тип == УЗЕЛ_ДЛЯ && узел->параллельно) return 1;
    for (size_t i = 0; i < узел_число_детей(узел); ++i)
        if (узел_использует_параллельно(узел_ребёнок(узел, i))) return 1;
    return 0;
}

// Печатает C-тип узла (учитывая срез) — общий помощник для кадров потоков.
static void эмит_тип_узла(Кодоген *к, Узел *у)
{
    if (у->срез) {
        эмит(к, "Срез");
    } else {
        эмит_тип(к, у->тип_данных, у->беззнаковый, &у->имя_типа);
        эмит_указатели(к, у->указатели);
    }
}

// Для каждой функции, запущенной в потоке, печатаем кадр + шим + запускатель.
//
// Кадр: «{ R рез; A арг0; }» — РЕЗУЛЬТАТ ПЕРВЫМ ПОЛЕМ. На этом держится
// «присоединить»: оно читает «*(T*)кадр», зная T из типа Konda, и потому один
// C-тип «Поток» обслуживает любые «поток<T>» без обобщений в рантайме.
static void кодоген_шима_потока(Кодоген *к, Узел *функция)
{
    const Токен *имя = &функция->токен;
    int void_рез = (функция->тип_данных == ТОКЕН_НИЧЕГО) && !функция->срез;
    Узел *п0 = nullptr;
    for (size_t i = 0; i < узел_число_детей(функция); ++i) {
        Узел *д = узел_ребёнок(функция, i);
        if (д && д->тип == УЗЕЛ_ПАРАМЕТР) { п0 = д; break; }
    }

    эмит(к, "typedef struct { ");
    if (void_рез) {
        эмит(к, "int _пусто; ");   // кадр не бывает пустым; результата нет
    } else {
        эмит_тип_узла(к, функция);
        эмит(к, " рез; ");
    }
    if (п0) { эмит_тип_узла(к, п0); эмит(к, " арг0; "); }
    эмит(к, "} _кадр_");
    эмит_манглированное(к, имя->начало, имя->длина);
    эмит(к, ";\n");

    эмит(к, "static void *_шим_");
    эмит_манглированное(к, имя->начало, имя->длина);
    эмит(к, "(void *с)\n{\n    _кадр_");
    эмит_манглированное(к, имя->начало, имя->длина);
    эмит(к, " *к = (_кадр_");
    эмит_манглированное(к, имя->начало, имя->длина);
    эмит(к, " *)с;\n    ");
    if (!void_рез) эмит(к, "к->рез = ");
    эмит_имя(к, имя);
    эмит(к, п0 ? "(к->арг0);\n" : "();\n");
    эмит(к, "    (void)к;\n    return nullptr;\n}\n");

    эмит(к, "static Поток _запуск_");
    эмит_манглированное(к, имя->начало, имя->длина);
    эмит(к, "(");
    if (п0) { эмит_тип_узла(к, п0); эмит(к, " арг0"); } else эмит(к, "void");
    эмит(к, ")\n{\n    _кадр_");
    эмит_манглированное(к, имя->начало, имя->длина);
    эмит(к, " *к = malloc(sizeof *к);\n");
    эмит(к, "    if (!к) { perror(\"malloc\"); exit(1); }\n");
    if (п0) эмит(к, "    к->арг0 = арг0;\n");
    эмит(к, "    Поток п;\n    п.кадр = к;\n");
    эмит(к, "    if (pthread_create(&п.ид, nullptr, _шим_");
    эмит_манглированное(к, имя->начало, имя->длина);
    эмит(к, ", к) != 0) { perror(\"pthread_create\"); exit(1); }\n");
    эмит(к, "    return п;\n}\n");
}

// Имя функции запущено в потоке где-то в программе?
static int имя_запускается_в_потоке(Узел *узел, const Токен *имя)
{
    if (!узел) return 0;
    if (узел->тип == УЗЕЛ_ОБЪЯВЛЕНИЕ && узел->поток && узел_число_детей(узел) > 0) {
        Узел *инит = узел_ребёнок(узел, 0);
        if (инит && узел_число_детей(инит) > 0) {
            Узел *фу = узел_ребёнок(инит, 0);
            if (фу && фу->токен.длина == имя->длина
                && memcmp(фу->токен.начало, имя->начало, имя->длина) == 0)
                return 1;
        }
    }
    for (size_t i = 0; i < узел_число_детей(узел); ++i)
        if (имя_запускается_в_потоке(узел_ребёнок(узел, i), имя)) return 1;
    return 0;
}

// «типфункции RET Имя(T1, T2, …)» → «typedef RET (*Имя)(T1, T2, …);».
static void кодоген_типфункции(Кодоген *к, Узел *узел)
{
    эмит(к, "typedef ");
    if (узел->срез) {
        эмит(к, "Срез");
    } else {
        эмит_тип(к, узел->тип_данных, узел->беззнаковый, &узел->имя_типа);
        эмит_указатели(к, узел->указатели);
    }
    эмит(к, " (*");
    эмит_манглированное(к, узел->токен.начало, узел->токен.длина);  // имя псевдонима
    эмит(к, ")(");
    int первый = 1;
    for (size_t i = 0; i < узел_число_детей(узел); ++i) {
        Узел *п = узел_ребёнок(узел, i);
        if (!п || п->тип != УЗЕЛ_ПАРАМЕТР) continue;
        if (!первый) эмит(к, ", ");
        первый = 0;
        if (п->срез) {
            эмит(к, "Срез");
        } else {
            эмит_тип(к, п->тип_данных, п->беззнаковый, &п->имя_типа);
            эмит_указатели(к, п->указатели);
        }
    }
    if (первый) эмит(к, "void");  // пустой список → (void)
    эмит(к, ");\n");
}

// Глобальная константа верхнего уровня → «static const Тип ИМЯ = …;» на
// файловом уровне. Иммутабельность доказана семантикой (константное выражение,
// строковый литерал или литерал структуры с константами/именами функций) —
// в C это чистые данные .rodata, нулевая рантайм-цена.
static void кодоген_глобальной_константы(Кодоген *к, Узел *узел)
{
    if (узел_число_детей(узел) == 0) return;   // семантика уже дала ошибку
    эмит(к, "static const ");
    эмит_тип(к, узел->тип_данных, узел->беззнаковый,
             узел->имя_типа.длина > 0 ? &узел->имя_типа : nullptr);
    эмит(к, " ");
    эмит_указатели(к, узел->указатели);
    эмит_имя(к, &узел->токен);
    эмит(к, " = ");
    Узел *иниц = узел_ребёнок(узел, 0);
    if (иниц->тип == УЗЕЛ_ЛИТЕРАЛ_СТРУКТУРЫ) {
        эмит(к, "{ ");
        for (size_t i = 0; i < узел_число_детей(иниц); ++i) {
            Узел *пиниц = узел_ребёнок(иниц, i);
            if (i > 0) эмит(к, ", ");
            эмит(к, ".");
            эмит_отрезок(к, пиниц->токен.начало, пиниц->токен.длина);
            эмит(к, " = ");
            if (узел_число_детей(пиниц) > 0)
                кодоген_выражения(к, узел_ребёнок(пиниц, 0));
        }
        эмит(к, " }");
    } else {
        кодоген_выражения(к, иниц);
    }
    эмит(к, ";\n\n");
}

int кодоген_и(Узел *корень, БуферКода *буфер, int релиз,
              const ТаблицаИнтерфейса *интерфейсы)
{
    Кодоген к = { .буфер = буфер, .в_точке_входа = 0, .релиз = релиз };
    собрать_сигнатуры_к(&к, корень);  // «владеет»-параметры для move при вызове
    внедрить_сигнатуры_к(&к, интерфейсы);  // ref/out/владеет функций библиотек

    эмит(&к, "/* Сгенерировано транспилятором Konda */\n");
    эмит(&к, "#include <stdint.h>\n");

    int нужны_срезы = узел_использует_срезы(корень);
    int нужны_потоки = узел_использует_потоки(корень);
    int нужно_параллельно = узел_использует_параллельно(корень);
    int нужны_массивы = узел_использует_динамический_массив(корень);
    // Проверки границ (срезы/массивы/argv) остаются и в релизе. Арифметические
    // guard'ы (деление/сдвиг/переполнение) в релизе снимаются — их прологи не
    // нужны.
    int нужна_проверка_арг = узел_использует_проверку_аргументов(корень);
    int нужен_guard_деления = !релиз && узел_использует_guard_бинопа(корень, 1);
    int нужен_guard_сдвига = !релиз && узел_использует_guard_бинопа(корень, 2);
    int нужен_guard_переполнения = !релиз && узел_использует_guard_бинопа(корень, 3);
    if (нужны_срезы || нужны_массивы || нужна_проверка_арг || нужны_потоки
        || нужно_параллельно || нужен_guard_деления || нужен_guard_сдвига || нужен_guard_переполнения) {
        эмит(&к, ПРОЛОГ_ЗАГОЛОВКИ);
    } else if (узел_использует_fстроки(корень)) {
        эмит(&к, "#include <stdio.h>\n");  // snprintf для интерполяции строк
    }
    if (нужны_срезы) {
        эмит(&к, ПРОЛОГ_СРЕЗОВ);
    }
    if (нужны_потоки) {
        эмит(&к, ПРОЛОГ_ПОТОКОВ);
    }
    if (нужно_параллельно) {
        if (!нужны_потоки) эмит(&к, "#include <pthread.h>\n");
        эмит(&к, ПРОЛОГ_ПАРАЛЛЕЛЬНО);
    }
    if (нужны_массивы) {
        эмит(&к, ПРОЛОГ_МАССИВОВ);
    }
    if (нужна_проверка_арг) {
        эмит(&к, ПРОЛОГ_АРГУМЕНТОВ);
    }
    if (нужен_guard_деления) {
        эмит(&к, ПРОЛОГ_ДЕЛЕНИЯ);
    }
    if (нужен_guard_сдвига) {
        эмит(&к, ПРОЛОГ_СДВИГА);
    }
    if (нужен_guard_переполнения) {
        эмит(&к, ПРОЛОГ_ПЕРЕПОЛНЕНИЯ);
    }

    // Перечисления — раньше структур и функций: typedef enum может
    // использоваться как тип поля структуры и параметра.
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ПЕРЕЧИСЛЕНИЕ) {
            кодоген_перечисления(&к, узел);
        }
    }

    // Типы-указатели на функцию — ПЕРЕД структурами: поле-колбэк структуры
    // («Колбэк на_событие») требует готового typedef (listener-паттерн).
    // Обратная ссылка (структура by-value в параметрах типфункции) в C всё
    // равно невыразима без тега — таких программ не существует.
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ТИПФУНКЦИИ) {
            кодоген_типфункции(&к, узел);
        }
    }

    // Структуры — раньше функций: typedef должен предшествовать использованию.
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_СТРУКТУРА) {
            кодоген_структуры(&к, узел);
        }
    }

    // Прототипы всех пользовательских функций — до тел: вызов может стоять
    // раньше определения (важно при слиянии нескольких .конда в один .c, а
    // также для взаимной рекурсии).
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        // «внешняя» прототип НЕ печатаем: декларацию даёт «#содержит»-заголовок
        // самой C-библиотеки (наш дубликат конфликтовал бы со static inline и
        // макро-обёртками; забытый заголовок в C23 даст ясную ошибку cc).
        if (узел->тип == УЗЕЛ_ФУНКЦИЯ && !узел->внешняя) {
            кодоген_прототипа(&к, узел);
        }
    }

    // Глобальные «конст» — после прототипов: поле-колбэк константы-структуры
    // ссылается на функцию по имени, а C требует видимой декларации.
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ОБЪЯВЛЕНИЕ) {
            кодоген_глобальной_константы(&к, узел);
        }
    }

    // Кадры/шимы/запускатели потоков — после прототипов (шим вызывает функцию)
    // и до тел (объявление «поток<T>» в теле зовёт «_запуск_ф»). По одному
    // комплекту на каждую функцию, реально запущенную в потоке.
    if (нужны_потоки) {
        for (size_t i = 0; i < узел_число_детей(корень); ++i) {
            Узел *узел = узел_ребёнок(корень, i);
            if (узел->тип == УЗЕЛ_ФУНКЦИЯ
                && имя_запускается_в_потоке(корень, &узел->токен)) {
                кодоген_шима_потока(&к, узел);
            }
        }
    }
    // Кадры/шимы «параллельно для» — тоже на уровне файла (вложенная функция
    // потребовала бы трамплина на исполняемом стеке → SEGV).
    if (нужно_параллельно) {
        кодоген_шимов_параллельно(&к, корень);
    }

    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ПРЕПРОЦЕССОР) {
            кодоген_препроцессора(&к, узел);
        } else if (узел->тип == УЗЕЛ_ФУНКЦИЯ && !узел->внешняя) {
            // «внешняя» — только прототип (напечатан выше); тело живёт в C.
            кодоген_функции(&к, узел);
        }
    }

    return 1;
}

int кодоген(Узел *корень, БуферКода *буфер, int релиз)
{
    return кодоген_и(корень, буфер, релиз, nullptr);
}

int кодоген_заголовок(Узел *корень, БуферКода *буфер)
{
    Кодоген к = { .буфер = буфер, .в_точке_входа = 0, .релиз = 0 };
    собрать_сигнатуры_к(&к, корень);

    эмит(&к, "/* Сгенерировано транспилятором Konda — заголовок библиотеки */\n");
    эмит(&к, "#pragma once\n");
    эмит(&к, "#include <stdint.h>\n");
    if (узел_использует_срезы(корень)) {
        эмит(&к, "#include <stddef.h>\n");  // size_t для Срез
        эмит(&к, ТИПДЕФ_СРЕЗ);
    }
    эмит(&к, "\n");

    // Публичные типы: перечисления и структуры (нужны вызывающему коду).
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ПЕРЕЧИСЛЕНИЕ) кодоген_перечисления(&к, узел);
    }
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_СТРУКТУРА) кодоген_структуры(&к, узел);
    }
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ТИПФУНКЦИИ) кодоген_типфункции(&к, узел);
    }

    // Прототипы публичного API: не static (внутренние), не «точка_входа»/main
    // и не «внешняя» (чужая C-функция — не часть API библиотеки).
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *узел = узел_ребёнок(корень, i);
        if (узел->тип == УЗЕЛ_ФУНКЦИЯ && !узел->статический && !узел->это_main
            && !узел->внешняя) {
            кодоген_прототипа(&к, узел);
        }
    }

    return 1;
}
