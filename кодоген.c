#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "аст.h"
#include "интерфейс.h"

#define МАКС_ВЛАДЕЮЩИХ 128
#define МАКС_ПАРАМ_К 16
#define МАКС_ФУНКЦИЙ_К 128

// «Владеет»-параметры функции — чтобы при вызове пометить переданный аргумент
// перемещённым (его освобождает функция-потребитель, не вызывающий). Также
// хранится флаг «параметр — срез» для авто-конверсии массива в Срез на месте
// вызова (массив-аргумент → дескриптор {данные, длина, эл_размер}).
typedef struct {
    const Токен *имя;
    int владеет[МАКС_ПАРАМ_К];
    int срез_парам[МАКС_ПАРАМ_К];
    int по_ссылке[МАКС_ПАРАМ_К];  // 1 — ref, 2 — out: аргумент передаётся как «&x»
    size_t число;
} СигнатураК;

typedef struct {
    БуферКода *буфер;
    int в_точке_входа;
    СигнатураК сигнатуры[МАКС_ФУНКЦИЙ_К];
    size_t число_сигнатур;
    // Синтезированные токены имён функций Konda-библиотек (безопасный путь 2):
    // у интерфейсных сигнатур нет AST-узла, а СигнатураК.имя — Токен*.
    Токен инт_токены[МАКС_ФУНКЦИЙ_К];
    // Стек владеющих ресурсов (срезы «выделить» и структуры с деструктором) для
    // autofree: при выходе из области и перед «вернуть» вставляется освобождение
    // в обратном порядке. Если деструктор != NULL — структура (вызвать его),
    // иначе срез (free .данные).
    const Токен *владеющие[МАКС_ВЛАДЕЮЩИХ];
    int уровень_владеющего[МАКС_ВЛАДЕЮЩИХ];
    int перемещён_владеющего[МАКС_ВЛАДЕЮЩИХ];  // владение ушло (move) — не free
    const Токен *деструктор_владеющего[МАКС_ВЛАДЕЮЩИХ];  // NULL для срезов
    // Для владеющего-структуры — имя её типа (рекурсивная деструкция полей);
    // NULL для срезов.
    const Токен *тип_структуры_владеющего[МАКС_ВЛАДЕЮЩИХ];
    // Владеющий-поток: «освобождение» — это авто-join (дождаться и снять кадр).
    // Так дескриптор потока живёт по тем же правилам RAII, что срез: не
    // присоединил явно — присоединится сам при выходе из области.
    int поток_владеющего[МАКС_ВЛАДЕЮЩИХ];
    size_t число_владеющих;
    // Активные захваты «параллельно для»: пока печатаем тело внутри шима,
    // упоминания этих имён идут как «_к->имя» (кадр потока). Замыканий в языке
    // нет, поэтому кадр строит кодоген — по списку, собранному семантикой.
    const Токен *захваты[МАКС_ВЛАДЕЮЩИХ];
    size_t число_захватов;
    int счётчик_параллельно;  // уникальные имена кадров/шимов «параллельно для»
    int уровень_блока;
    char тип_возврата_си[64];  // тип возврата текущей функции (для «вернуть»)
    int срез_возврата;         // функция возвращает срез
    // Таблица структур: имя структуры → деструктор (NULL, если его нет) + узел
    // объявления (для обхода полей при рекурсивной деструкции).
    const Токен *стр_имя[МАКС_ФУНКЦИЙ_К];
    const Токен *стр_деструктор[МАКС_ФУНКЦИЙ_К];
    const Узел  *стр_узел[МАКС_ФУНКЦИЙ_К];
    size_t число_структур_к;
    size_t счётчик_fстрок;  // уникальные имена буферов интерполяции (в текущей функции)
    int релиз;              // 1 — релизная сборка: рантайм-проверки не эмитятся
    // Имена параметров текущей функции, переданных по ссылке (ref/out): их
    // использование в теле авто-разыменовывается «(*имя)».
    const Токен *по_ссылке_имена[МАКС_ПАРАМ_К];
    size_t число_по_ссылке;
    // Таблица sum-перечислений (размеченных союзов): узлы объявлений для поиска
    // варианта по имени при конструировании и в «выбор» (тег + имена полей).
    const Узел *пер_узел[МАКС_ФУНКЦИЙ_К];
    size_t число_перечислений_к;
    size_t счётчик_выбор;  // уникальные имена временных для «выбор» по sum-типу
} Кодоген;

// Перечисление объявлено как sum-тип (хотя бы один вариант несёт данные —
// ребёнок-УЗЕЛ_ПАРАМЕТР)?
static int перечисление_это_sum(const Узел *узел)
{
    for (size_t i = 0; i < узел_число_детей(узел); ++i) {
        const Узел *вар = узел_ребёнок(узел, i);
        if (вар && вар->тип == УЗЕЛ_ВАРИАНТ && узел_число_детей(вар) > 0
            && узел_ребёнок(вар, 0)->тип == УЗЕЛ_ПАРАМЕТР)
            return 1;
    }
    return 0;
}

static int срез_равен(const char *начало, size_t длина, const char *текст)
{
    size_t n = strlen(текст);
    return длина == n && memcmp(начало, текст, n) == 0;
}

static void имя_в_си(const Кодоген *к, const char *начало, size_t длина, char *out, size_t out_cap)
{
    if (к->в_точке_входа && срез_равен(начало, длина, "точка_входа")) {
        snprintf(out, out_cap, "main");
        return;
    }
    if (к->в_точке_входа && срез_равен(начало, длина, "количество_аргументов")) {
        snprintf(out, out_cap, "argc");
        return;
    }
    if (к->в_точке_входа && срез_равен(начало, длина, "аргументы")) {
        snprintf(out, out_cap, "argv");
        return;
    }
    size_t n = длина < out_cap - 1 ? длина : out_cap - 1;
    // Манглинг пространств имён: «::» → «__» (двоеточие недопустимо в C-идент.).
    for (size_t i = 0; i < n; ++i) out[i] = начало[i] == ':' ? '_' : начало[i];
    out[n] = '\0';
}

static void тип_токена_в_си(ТипТокена t, int беззнаковый, char *out, size_t out_cap)
{
    switch (t) {
        case ТОКЕН_ЦЕЛОЕ8:       snprintf(out, out_cap, беззнаковый ? "uint8_t"  : "int8_t");  break;
        case ТОКЕН_ЦЕЛОЕ16:      snprintf(out, out_cap, беззнаковый ? "uint16_t" : "int16_t"); break;
        case ТОКЕН_ЦЕЛОЕ32:      snprintf(out, out_cap, беззнаковый ? "uint32_t" : "int32_t"); break;
        case ТОКЕН_ЦЕЛОЕ64:      snprintf(out, out_cap, беззнаковый ? "uint64_t" : "int64_t"); break;
        case ТОКЕН_БАЙТ:         snprintf(out, out_cap, "uint8_t"); break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ: snprintf(out, out_cap, "float");   break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ64: snprintf(out, out_cap, "double"); break;
        case ТОКЕН_ТИП_СИМВОЛ:   snprintf(out, out_cap, "char");    break;
        case ТОКЕН_ЛОГИЧЕСКИЙ:   snprintf(out, out_cap, "bool");    break;
        case ТОКЕН_НИЧЕГО:       snprintf(out, out_cap, "void");    break;
        default:                 snprintf(out, out_cap, "int");     break;
    }
}

static void эмит(Кодоген *к, const char *текст)
{
    буфер_кода_добавить(к->буфер, текст);
}

static void эмит_отрезок(Кодоген *к, const char *начало, size_t длина)
{
    буфер_кода_добавить_отрезок(к->буфер, начало, длина);
}

// Эмит имени пользовательского типа с манглингом «::» → «__» (для имён типов,
// которые обычно выводятся сырым отрезком, а не через имя_в_си).
static void эмит_манглированное(Кодоген *к, const char *начало, size_t длина)
{
    char буф[512];
    size_t n = длина < sizeof(буф) - 1 ? длина : sizeof(буф) - 1;
    for (size_t i = 0; i < n; ++i) буф[i] = начало[i] == ':' ? '_' : начало[i];
    буф[n] = '\0';
    эмит(к, буф);
}

static void эмит_имя(Кодоген *к, const Токен *токен)
{
    // Внутри шима «параллельно для» захваченные переменные живут в кадре.
    for (size_t i = 0; i < к->число_захватов; ++i) {
        const Токен *з = к->захваты[i];
        if (з->длина == токен->длина
            && memcmp(з->начало, токен->начало, токен->длина) == 0) {
            эмит(к, "_к->");
            break;
        }
    }
    char имя_си[256];
    имя_в_си(к, токен->начало, токен->длина, имя_си, sizeof(имя_си));
    эмит(к, имя_си);
}

// Идентификатор — параметр по ссылке (ref/out) текущей функции?
static int идентификатор_по_ссылке(const Кодоген *к, const Токен *токен)
{
    for (size_t i = 0; i < к->число_по_ссылке; ++i) {
        const Токен *п = к->по_ссылке_имена[i];
        if (п->длина == токен->длина
            && memcmp(п->начало, токен->начало, токен->длина) == 0)
            return 1;
    }
    return 0;
}

// Регистрирует владеющий ресурс для autofree на текущем уровне. Для среза
// деструктор и тип_структуры == NULL (free .данные). Для структуры
// тип_структуры — имя её типа (рекурсивная деструкция полей и собственного
// деструктора), деструктор — собственный деструктор (или NULL).
static void зарегистрировать_ресурс(Кодоген *к, const Токен *имя,
                                     const Токен *деструктор,
                                     const Токен *тип_структуры)
{
    if (к->число_владеющих >= МАКС_ВЛАДЕЮЩИХ) {
        return;
    }
    к->владеющие[к->число_владеющих] = имя;
    к->уровень_владеющего[к->число_владеющих] = к->уровень_блока;
    к->перемещён_владеющего[к->число_владеющих] = 0;
    к->деструктор_владеющего[к->число_владеющих] = деструктор;
    к->тип_структуры_владеющего[к->число_владеющих] = тип_структуры;
    к->поток_владеющего[к->число_владеющих] = 0;
    к->число_владеющих++;
}

static void зарегистрировать_владеющий(Кодоген *к, const Токен *имя)
{
    зарегистрировать_ресурс(к, имя, nullptr, nullptr);  // срез
}

// Дескриптор потока под RAII: при выходе из области — авто-join (см.
// эмит_освобождение). Явное «присоединить» помечает его перемещённым, и
// повторного join не будет.
static void зарегистрировать_поток_к(Кодоген *к, const Токен *имя)
{
    зарегистрировать_ресурс(к, имя, nullptr, nullptr);
    if (к->число_владеющих > 0)
        к->поток_владеющего[к->число_владеющих - 1] = 1;
}

// Узел объявления структуры по имени типа; NULL если не найдена.
static const Узел *найти_узел_структуры_к(Кодоген *к, const Токен *имя_структуры)
{
    for (size_t i = 0; i < к->число_структур_к; ++i) {
        const Токен *н = к->стр_имя[i];
        if (н->длина == имя_структуры->длина
            && memcmp(н->начало, имя_структуры->начало, имя_структуры->длина) == 0) {
            return к->стр_узел[i];
        }
    }
    return nullptr;
}

// Деструктор структуры по имени типа; NULL если структура без деструктора.
static const Токен *найти_деструктор_к(Кодоген *к, const Токен *имя_структуры)
{
    for (size_t i = 0; i < к->число_структур_к; ++i) {
        const Токен *н = к->стр_имя[i];
        if (н->длина == имя_структуры->длина
            && memcmp(н->начало, имя_структуры->начало, имя_структуры->длина) == 0) {
            return к->стр_деструктор[i];
        }
    }
    return nullptr;
}

// Полю-структуре (по значению) нужна деструкция? Поле — узел УЗЕЛ_ПАРАМЕТР с
// тип_данных == ТОКЕН_СТРУКТУРА и без указателей; имя его типа — в имя_типа.
// Поле по значению может ссылаться только на уже объявленную структуру, значит
// цикл невозможен и цепочка не длиннее числа структур в программе (см. парную
// проверку в семантика.c: структура_индекс_нужна_деструкция). Раньше здесь
// стоял фиксированный «глубина > 16» — при более глубокой вложенности
// деструктор молча не эмитился (утечка без единой диагностики).
static int структура_нужна_деструкция_к(Кодоген *к, const Токен *имя_типа,
                                         int глубина)
{
    if ((size_t)глубина > к->число_структур_к) return 0;
    if (найти_деструктор_к(к, имя_типа)) return 1;
    const Узел *стр = найти_узел_структуры_к(к, имя_типа);
    if (!стр) return 0;
    for (size_t j = 0; j < узел_число_детей(стр); ++j) {
        const Узел *поле = узел_ребёнок(стр, j);
        if (!поле || поле->тип != УЗЕЛ_ПАРАМЕТР) continue;
        if (поле->срез) return 1;  // поле-срез — структура владеет его памятью
        if (поле->тип_данных != ТОКЕН_СТРУКТУРА || поле->указатели > 0) continue;
        if (структура_нужна_деструкция_к(к, &поле->имя_типа, глубина + 1)) return 1;
    }
    return 0;
}

// Рекурсивная деструкция структуры по выражению-доступу («о» или «о.внутр»):
// сначала собственный деструктор «удалить(&доступ)», затем поля-структуры,
// требующие деструкции, в обратном порядке объявления.
static void эмит_деструкцию_структуры(Кодоген *к, const char *доступ,
                                       const Токен *имя_типа, int глубина)
{
    if ((size_t)глубина > к->число_структур_к) return;
    const Токен *дестр = найти_деструктор_к(к, имя_типа);
    if (дестр) {
        эмит(к, "    ");
        эмит_отрезок(к, дестр->начало, дестр->длина);
        эмит(к, "(&");
        эмит(к, доступ);
        эмит(к, ");\n");
    }
    const Узел *стр = найти_узел_структуры_к(к, имя_типа);
    if (!стр) return;
    for (size_t j = узел_число_детей(стр); j > 0; --j) {
        const Узел *поле = узел_ребёнок(стр, j - 1);
        if (!поле || поле->тип != УЗЕЛ_ПАРАМЕТР) continue;
        // Поле-срез: структура — единственный владелец его памяти (инициализация
        // только через «выделить»/«клон» в литерале, переприсваивание и «увод»
        // среза из поля запрещены семантикой) — освобождаем напрямую, без
        // рекурсии (не структура и не имеет своих полей).
        if (поле->срез) {
            эмит(к, "    free(");
            эмит(к, доступ);
            эмит(к, ".");
            эмит_отрезок(к, поле->токен.начало, поле->токен.длина);
            эмит(к, ".данные);\n");
            continue;
        }
        if (поле->тип_данных != ТОКЕН_СТРУКТУРА || поле->указатели > 0) continue;
        if (!структура_нужна_деструкция_к(к, &поле->имя_типа, глубина + 1)) continue;
        // Путь доступа растёт с глубиной («о.внутр.внутр…») и не ограничен
        // сверху фиксированным размером — выделяем ровно по потребности вместо
        // char[512], который на глубокой вложенности молча обрезал путь и
        // порождал некомпилируемый (или, что хуже, синтаксически иной) C.
        size_t нужно = strlen(доступ) + 1 + поле->токен.длина + 1;
        char *под = malloc(нужно);
        if (!под) { эмит(к, "    /* нет памяти для пути деструкции */\n"); continue; }
        snprintf(под, нужно, "%s.%.*s", доступ,
                 (int)поле->токен.длина, поле->токен.начало);
        эмит_деструкцию_структуры(к, под, &поле->имя_типа, глубина + 1);
        free(под);
    }
}

// Помечает владеющий срез перемещённым (move): его освобождать больше не нужно,
// владение перешло к получателю. Семантика гарантирует, что источник и
// получатель в одной области, поэтому ищем последнюю запись с этим именем.
static void пометить_перемещённым(Кодоген *к, const Токен *имя)
{
    for (size_t i = к->число_владеющих; i > 0; --i) {
        const Токен *в = к->владеющие[i - 1];
        if (в->длина == имя->длина
            && memcmp(в->начало, имя->начало, имя->длина) == 0) {
            к->перемещён_владеющего[i - 1] = 1;
            return;
        }
    }
}

// Собирает «владеет»-флаги параметров всех функций (для пометки move при вызове).
static void собрать_сигнатуры_к(Кодоген *к, Узел *корень)
{
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *ф = узел_ребёнок(корень, i);
        if (ф->тип != УЗЕЛ_ФУНКЦИЯ || ф->это_main || ф->токен.длина == 0) continue;
        if (к->число_сигнатур >= МАКС_ФУНКЦИЙ_К) break;
        СигнатураК *с = &к->сигнатуры[к->число_сигнатур++];
        с->имя = &ф->токен;
        с->число = 0;
        size_t всего = узел_число_детей(ф);
        // У «внешняя» тела нет — все дети параметры; иначе тело — последний.
        size_t предел = ф->внешняя ? всего : (всего ? всего - 1 : 0);
        for (size_t j = 0; j < предел; ++j) {
            Узел *пар = узел_ребёнок(ф, j);
            if (!пар || пар->тип != УЗЕЛ_ПАРАМЕТР) continue;
            if (с->число >= МАКС_ПАРАМ_К) break;
            с->владеет[с->число] = пар->владеет;
            с->срез_парам[с->число] = пар->срез;
            с->по_ссылке[с->число] = пар->по_ссылке;
            с->число++;
        }
    }
    // Структуры → деструкторы (для autofree переменных-структур).
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *с = узел_ребёнок(корень, i);
        if (с->тип != УЗЕЛ_СТРУКТУРА || с->токен.длина == 0) continue;
        if (к->число_структур_к >= МАКС_ФУНКЦИЙ_К) break;
        к->стр_имя[к->число_структур_к] = &с->токен;
        к->стр_деструктор[к->число_структур_к] =
            с->имя_типа.длина > 0 ? &с->имя_типа : nullptr;
        к->стр_узел[к->число_структур_к] = с;
        к->число_структур_к++;
    }
    // Sum-перечисления → узлы (для конструирования и «выбор»).
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        Узел *п = узел_ребёнок(корень, i);
        if (п->тип != УЗЕЛ_ПЕРЕЧИСЛЕНИЕ || п->токен.длина == 0) continue;
        if (!перечисление_это_sum(п)) continue;
        if (к->число_перечислений_к >= МАКС_ФУНКЦИЙ_К) break;
        к->пер_узел[к->число_перечислений_к++] = п;
    }
}

// Ищет вариант sum-перечисления по имени. Возвращает узел перечисления (или
// nullptr) и кладёт узел варианта в *вар_вых, тег (индекс) в *тег_вых.
static const Узел *найти_sum_вариант_к(const Кодоген *к, const char *имя,
                                        size_t длина, const Узел **вар_вых,
                                        int *тег_вых)
{
    for (size_t e = 0; e < к->число_перечислений_к; ++e) {
        const Узел *пер = к->пер_узел[e];
        int тег = 0;
        for (size_t i = 0; i < узел_число_детей(пер); ++i) {
            const Узел *вар = узел_ребёнок(пер, i);
            if (!вар || вар->тип != УЗЕЛ_ВАРИАНТ) continue;
            if (длина == вар->токен.длина
                && memcmp(имя, вар->токен.начало, длина) == 0) {
                if (вар_вых) *вар_вых = вар;
                if (тег_вых) *тег_вых = тег;
                return пер;
            }
            тег++;
        }
    }
    return nullptr;
}

// Инъекция сигнатур Konda-библиотек в codegen: чтобы вызовы к библиотечным
// функциям с ref/out (изменяемый/вывод) генерировали «&x», а с «владеет» —
// пометку перемещения. Данные берём из интерфейса «.и» (семантика уже проверила).
static void внедрить_сигнатуры_к(Кодоген *к, const ТаблицаИнтерфейса *интерфейсы)
{
    if (!интерфейсы) return;
    size_t m = интерфейс_число(интерфейсы);
    for (size_t i = 0; i < m; ++i) {
        const ИнтФункция *иф = интерфейс_получить(интерфейсы, i);
        if (!иф || к->число_сигнатур >= МАКС_ФУНКЦИЙ_К) break;
        Токен *т = &к->инт_токены[к->число_сигнатур];
        *т = (Токен){ .тип = ТОКЕН_ИДЕНТИФИКАТОР, .начало = иф->имя,
                      .длина = strlen(иф->имя) };
        СигнатураК *с = &к->сигнатуры[к->число_сигнатур++];
        с->имя = т;
        с->число = 0;
        for (size_t j = 0; j < иф->число_параметров && с->число < МАКС_ПАРАМ_К; ++j) {
            const ИнтПараметр *п = &иф->параметры[j];
            с->владеет[с->число]    = п->владеет;
            с->срез_парам[с->число] = п->срез;
            с->по_ссылке[с->число]  = п->по_ссылке;
            с->число++;
        }
    }
}

static СигнатураК *найти_сигнатуру_к(Кодоген *к, const Токен *имя)
{
    for (size_t i = 0; i < к->число_сигнатур; ++i) {
        const Токен *н = к->сигнатуры[i].имя;
        if (н->длина == имя->длина
            && memcmp(н->начало, имя->начало, имя->длина) == 0) {
            return &к->сигнатуры[i];
        }
    }
    return nullptr;
}

// Освобождение одного владеющего среза: free(имя.данные).
static void эмит_освобождение(Кодоген *к, size_t i)
{
    const Токен *имя = к->владеющие[i];
    const Токен *тип_стр = к->тип_структуры_владеющего[i];
    if (к->поток_владеющего[i]) {
        // Поток не присоединили явно → присоединяем сами при выходе из области
        // (RAII, как autofree срезов): ждём завершения и снимаем кадр. Иначе
        // поток остался бы отсоединённым, а кадр утёк.
        эмит(к, "    поток_присоединить_в(");
        эмит_имя(к, имя);
        эмит(к, ", nullptr, 0);\n");
        return;
    }
    if (тип_стр) {
        // Структура, требующая деструкции: рекурсивно обходим собственный
        // деструктор и поля-структуры.
        char доступ[256];
        имя_в_си(к, имя->начало, имя->длина, доступ, sizeof(доступ));
        эмит_деструкцию_структуры(к, доступ, тип_стр, 0);
    } else {
        эмит(к, "    free(");
        эмит_имя(к, имя);
        эмит(к, ".данные);\n");
    }
}

// Вставляет освобождение для владеющих ресурсов уровня (в обратном порядке).
// Перемещённые пропускаются. Если убрать = 1 — снимает их со стека.
static void освободить_уровень(Кодоген *к, int уровень, int убрать)
{
    for (size_t i = к->число_владеющих; i > 0; --i) {
        if (к->уровень_владеющего[i - 1] != уровень) {
            break;  // элементы уровня идут сплошной группой на вершине стека
        }
        if (!к->перемещён_владеющего[i - 1]) {
            эмит_освобождение(к, i - 1);
        }
        if (убрать) {
            к->число_владеющих--;
        }
    }
}

// Перед «вернуть»: освобождает ВСЕ живые владеющие ресурсы (раскрутка всех
// объемлющих областей), не снимая со стека — блоки уберут их сами.
static void освободить_все(Кодоген *к)
{
    for (size_t i = к->число_владеющих; i > 0; --i) {
        if (!к->перемещён_владеющего[i - 1]) {
            эмит_освобождение(к, i - 1);
        }
    }
}

// Используется ли в программе хоть одна f-строка? Тогда нужен <stdio.h>
