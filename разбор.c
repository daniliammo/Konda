#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "аст.h"

#define МАКС_СТРУКТУР_РАЗБОР 128
#define МАКС_ИМЯ_РАЗБОР 128

typedef struct {
    МассивТокенов *токены;
    size_t позиция;
    Диагностика *диаг;
    // Имена объявленных структур — чтобы распознавать «Имя перем» как объявление
    // переменной пользовательского типа (структура должна быть объявлена выше).
    char структуры[МАКС_СТРУКТУР_РАЗБОР][МАКС_ИМЯ_РАЗБОР];
    size_t число_структур;
    // Имена объявленных перечислений — аналогично для «Имя перем» enum-типа.
    char перечисления[МАКС_СТРУКТУР_РАЗБОР][МАКС_ИМЯ_РАЗБОР];
    size_t число_перечислений;
    // Имена типов-указателей на функцию («типфункции RET Имя(…)») — чтобы
    // «Имя п» распознавалось как объявление переменной типа-указателя.
    char типыфункций[МАКС_СТРУКТУР_РАЗБОР][МАКС_ИМЯ_РАЗБОР];
    size_t число_типовфункций;
    // Имена типа-параметров обобщённых функций («T» в «имя<T>(…)»): собираются
    // пред-проходом по заголовкам на верхнем уровне (глубина фигурных = 0),
    // чтобы «T» распознавалось как тип в возврате/параметрах/теле.
    char типопараметры[МАКС_СТРУКТУР_РАЗБОР][МАКС_ИМЯ_РАЗБОР];
    size_t число_типопараметров;
    // Имена самих обобщённых функций (идентификатор перед «<T>»): чтобы на
    // вызове «имя<целое32>(…)» отличить список тип-аргументов от оператора «<».
    char обобщённые[МАКС_СТРУКТУР_РАЗБОР][МАКС_ИМЯ_РАЗБОР];
    size_t число_обобщённых;
    // Защита от переполнения стека самого компилятора на патологически
    // сложном выражении («1 + 1 + … » тысячи раз, глубоко вложенные скобки
    // и т.п.): разбор выражений рекурсивен (разобрать_первичное ↔
    // разобрать_бинарное ↔ разобрать_выражение), и даже там, где сам разбор
    // остаётся плоским (цепочка операторов одного приоритета обрабатывается
    // циклом, без роста стека парсера), результат — глубокое AST, которое
    // затем рекурсивно обходят типизировать_выражение/кодоген_выражения/
    // освободить_узел и другие проходы; там переполнение стека уже не
    // предотвратить. Поэтому предел ставится здесь, на этапе построения
    // дерева: «вложенность_выражения» — счётчик вложенных вызовов
    // разобрать_выражение (0 на верхнем уровне вызова), «работа_выражения» —
    // число «единиц работы» (примитивов и операторов) с начала текущего
    // верхнеуровневого выражения, обнуляется при вложенности_выражения==0.
    int вложенность_выражения;
    long работа_выражения;
} Разборщик;

// Предел «единиц работы» на одно верхнеуровневое выражение (примитивов и
// операторов). С большим запасом ниже порога, на котором сам компилятор
// (парсер и последующие рекурсивные проходы) переполняет стек — но заведомо
// больше любого выражения, которое стоило бы писать вручную.
#define МАКС_РАБОТА_ВЫРАЖЕНИЯ 2000

static Узел *разобрать_выражение(Разборщик *р);
static Узел *разобрать_блок(Разборщик *р, int с_скобками);
static Узел *разобрать_литерал_структуры(Разборщик *р);
static Узел *разобрать_литерал_массива(Разборщик *р);

static int байты_равны(const char *a, size_t la, const char *b, size_t lb)
{
    return la == lb && memcmp(a, b, la) == 0;
}

static int токен_текст_равен(const Токен *t, const char *текст)
{
    return байты_равны(t->начало, t->длина, текст, strlen(текст));
}

static void запомнить_структуру(Разборщик *р, const Токен *имя)
{
    if (р->число_структур >= МАКС_СТРУКТУР_РАЗБОР) {
        return;
    }
    size_t n = имя->длина < МАКС_ИМЯ_РАЗБОР - 1 ? имя->длина : МАКС_ИМЯ_РАЗБОР - 1;
    memcpy(р->структуры[р->число_структур], имя->начало, n);
    р->структуры[р->число_структур][n] = '\0';
    р->число_структур++;
}

// Идентификатор — имя ранее объявленной структуры (то есть тип)?
static int токен_имя_структуры(const Разборщик *р, const Токен *t)
{
    if (t->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        return 0;
    }
    for (size_t i = 0; i < р->число_структур; ++i) {
        if (байты_равны(р->структуры[i], strlen(р->структуры[i]),
                        t->начало, t->длина)) {
            return 1;
        }
    }
    return 0;
}

static void запомнить_перечисление(Разборщик *р, const Токен *имя)
{
    if (р->число_перечислений >= МАКС_СТРУКТУР_РАЗБОР) {
        return;
    }
    size_t n = имя->длина < МАКС_ИМЯ_РАЗБОР - 1 ? имя->длина : МАКС_ИМЯ_РАЗБОР - 1;
    memcpy(р->перечисления[р->число_перечислений], имя->начало, n);
    р->перечисления[р->число_перечислений][n] = '\0';
    р->число_перечислений++;
}

// Идентификатор — имя ранее объявленного перечисления (то есть enum-тип)?
static int токен_имя_перечисления(const Разборщик *р, const Токен *t)
{
    if (t->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        return 0;
    }
    for (size_t i = 0; i < р->число_перечислений; ++i) {
        if (байты_равны(р->перечисления[i], strlen(р->перечисления[i]),
                        t->начало, t->длина)) {
            return 1;
        }
    }
    return 0;
}

// Идентификатор — имя типа-параметра обобщённой функции (например «T»)?
static int токен_это_типопараметр(const Разборщик *р, const Токен *t)
{
    if (t->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        return 0;
    }
    for (size_t i = 0; i < р->число_типопараметров; ++i) {
        if (байты_равны(р->типопараметры[i], strlen(р->типопараметры[i]),
                        t->начало, t->длина)) {
            return 1;
        }
    }
    return 0;
}

static void запомнить_типфункции(Разборщик *р, const Токен *имя)
{
    if (р->число_типовфункций >= МАКС_СТРУКТУР_РАЗБОР) return;
    size_t n = имя->длина < МАКС_ИМЯ_РАЗБОР - 1 ? имя->длина : МАКС_ИМЯ_РАЗБОР - 1;
    memcpy(р->типыфункций[р->число_типовфункций], имя->начало, n);
    р->типыфункций[р->число_типовфункций][n] = '\0';
    р->число_типовфункций++;
}

// Идентификатор — имя типа-указателя на функцию (объявленного «типфункции …»)?
static int токен_имя_типфункции(const Разборщик *р, const Токен *t)
{
    if (t->тип != ТОКЕН_ИДЕНТИФИКАТОР) return 0;
    for (size_t i = 0; i < р->число_типовфункций; ++i)
        if (байты_равны(р->типыфункций[i], strlen(р->типыфункций[i]),
                        t->начало, t->длина)) return 1;
    return 0;
}

// Идентификатор — имя обобщённой функции (для разбора «имя<тип…>(…)»)?
static int токен_имя_обобщённой(const Разборщик *р, const Токен *t)
{
    if (t->тип != ТОКЕН_ИДЕНТИФИКАТОР) return 0;
    for (size_t i = 0; i < р->число_обобщённых; ++i)
        if (байты_равны(р->обобщённые[i], strlen(р->обобщённые[i]),
                        t->начало, t->длина)) return 1;
    return 0;
}

// Пред-проход: собирает имена типа-параметров из заголовков обобщённых функций.
// На верхнем уровне (глубина фигурных скобок = 0) шаблон выглядит как
// «ИМЯ < T, U, … > (…)»: идентификатор, за которым сразу «<». Имена между «<» и
// «>» — типа-параметры. Вызовы «имя<целое32>(…)» находятся в телах (глубина ≥ 1)
// и не собираются, поэтому конкретные типы-аргументы сюда не попадают.
static void собрать_типопараметры(Разборщик *р)
{
    size_t n = р->токены->базовый.длина;
    int глубина = 0;
    for (size_t i = 0; i + 1 < n; ++i) {
        Токен *t = дин_массив_получить(&р->токены->базовый, i);
        if (!t) continue;
        if (t->тип == ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) { глубина++; continue; }
        if (t->тип == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
            if (глубина > 0) глубина--;
            continue;
        }
        if (глубина != 0 || t->тип != ТОКЕН_ИДЕНТИФИКАТОР) continue;
        Токен *след = дин_массив_получить(&р->токены->базовый, i + 1);
        if (!след || след->тип != ТОКЕН_СТРЕЛКА_ВЛЕВО
            || !токен_текст_равен(след, "<")) continue;
        // Имя перед «<» — имя обобщённой функции (для дизамбигуации вызова).
        if (р->число_обобщённых < МАКС_СТРУКТУР_РАЗБОР) {
            size_t им = t->длина < МАКС_ИМЯ_РАЗБОР - 1 ? t->длина : МАКС_ИМЯ_РАЗБОР - 1;
            int есть = 0;
            for (size_t k = 0; k < р->число_обобщённых; ++k)
                if (байты_равны(р->обобщённые[k], strlen(р->обобщённые[k]),
                                t->начало, им)) { есть = 1; break; }
            if (!есть) {
                memcpy(р->обобщённые[р->число_обобщённых], t->начало, им);
                р->обобщённые[р->число_обобщённых][им] = '\0';
                р->число_обобщённых++;
            }
        }
        // Читаем имена до «>».
        for (size_t j = i + 2; j < n; ++j) {
            Токен *п = дин_массив_получить(&р->токены->базовый, j);
            if (!п) break;
            if (п->тип == ТОКЕН_СТРЕЛКА_ВПРАВО && токен_текст_равен(п, ">")) break;
            if (п->тип == ТОКЕН_ЗАПЯТАЯ) continue;
            if (п->тип != ТОКЕН_ИДЕНТИФИКАТОР) break;  // не список типа-параметров
            if (р->число_типопараметров >= МАКС_СТРУКТУР_РАЗБОР) break;
            size_t m = п->длина < МАКС_ИМЯ_РАЗБОР - 1 ? п->длина : МАКС_ИМЯ_РАЗБОР - 1;
            int дубль = 0;
            for (size_t k = 0; k < р->число_типопараметров; ++k) {
                if (байты_равны(р->типопараметры[k], strlen(р->типопараметры[k]),
                                п->начало, m)) { дубль = 1; break; }
            }
            if (дубль) continue;
            memcpy(р->типопараметры[р->число_типопараметров], п->начало, m);
            р->типопараметры[р->число_типопараметров][m] = '\0';
            р->число_типопараметров++;
        }
    }
}

static int токен_это_тип(ТипТокена t)
{
    return t == ТОКЕН_ЦЕЛОЕ8 || t == ТОКЕН_ЦЕЛОЕ16 || t == ТОКЕН_ЦЕЛОЕ32
        || t == ТОКЕН_ЦЕЛОЕ64 || t == ТОКЕН_БАЙТ
        || t == ТОКЕН_ВЕЩЕСТВЕННОЕ || t == ТОКЕН_ВЕЩЕСТВЕННОЕ64
        || t == ТОКЕН_ТИП_СИМВОЛ || t == ТОКЕН_ЛОГИЧЕСКИЙ;
}

static int токен_это_целое(ТипТокена t)
{
    return t == ТОКЕН_ЦЕЛОЕ8 || t == ТОКЕН_ЦЕЛОЕ16
        || t == ТОКЕН_ЦЕЛОЕ32 || t == ТОКЕН_ЦЕЛОЕ64;
}

// Тип может начинаться с модификатора «неподписанный» или конструктора «срез».
static int токен_начинает_тип(ТипТокена t)
{
    return токен_это_тип(t) || t == ТОКЕН_НЕПОДПИСАННЫЙ || t == ТОКЕН_СРЕЗ;
}

static void ошибка_разбора(Разборщик *р, const Токен *t, const char *текст)
{
    if (р->диаг->ошибка) {
        return;
    }
    р->диаг->ошибка = 1;
    р->диаг->строка = t ? t->строка : 0;
    р->диаг->колонка = t ? t->позиция_в_строке : 0;
    snprintf(р->диаг->сообщение, sizeof(р->диаг->сообщение), "%s", текст);
}

// Токен, которым может закончиться оператор: после него перевод строки
// завершает оператор (модель A — как автоподстановка «;» в Go).
static int токен_завершает_оператор(ТипТокена т)
{
    switch (т) {
        case ТОКЕН_ИДЕНТИФИКАТОР:
        case ТОКЕН_ЧИСЛО:
        case ТОКЕН_СТРОКА:
        case ТОКЕН_СИМВОЛ:
        case ТОКЕН_НУЛЬ:
        case ТОКЕН_ИСТИНА:    // логические литералы — значения, завершают оператор
        case ТОКЕН_ЛОЖЬ:
        case ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ:
        case ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ:
        case ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ:
        case ТОКЕН_ПРЕПРОЦЕССОР:
        case ТОКЕН_ВЕРНУТЬ:      // «вернуть» без значения в конце строки
        case ТОКЕН_ПРЕРВАТЬ:     // «прервать»/«продолжить» — самостоятельные операторы
        case ТОКЕН_ПРОДОЛЖИТЬ:
            return 1;
        default:
            return 0;
    }
}

// Один проход перед разбором: решает, какие переводы строк значимы.
// Значимый перевод (вне «(» / «[» и после токена-завершителя) остаётся
// ТОКЕН_НОВАЯ_СТРОКА — это терминатор оператора. Остальные переводы —
// продолжение строки (внутри скобок, после оператора, запятой, «{» и т.п.) —
// гасятся в ТОКЕН_КОММЕНТАРИЙ, чтобы парсер их пропускал. Фигурные скобки
// глубину не считают: переводы внутри блока разделяют операторы.
static void разметить_переносы(МассивТокенов *токены)
{
    int глубина = 0;
    ТипТокена прежний = ТОКЕН_НОВАЯ_СТРОКА;  // в начале файла перевод незначим
    size_t n = токены->базовый.длина;
    for (size_t i = 0; i < n; ++i) {
        Токен *t = дин_массив_получить(&токены->базовый, i);
        if (t->тип == ТОКЕН_КОММЕНТАРИЙ) {
            continue;  // комментарий не меняет «прежний значимый токен»
        }
        if (t->тип == ТОКЕН_НОВАЯ_СТРОКА) {
            if (глубина > 0 || !токен_завершает_оператор(прежний)) {
                t->тип = ТОКЕН_КОММЕНТАРИЙ;  // продолжение строки — гасим
            }
            continue;  // перевод тоже не меняет «прежний»
        }
        if (t->тип == ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ
            || t->тип == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            глубина++;
        } else if (t->тип == ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
                   || t->тип == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
            if (глубина > 0) глубина--;
        }
        прежний = t->тип;
    }
}

// Пропускает только комментарии. Значимые переводы строк (терминаторы) видимы.
static void пропустить_служебные(Разборщик *р)
{
    while (р->позиция < р->токены->базовый.длина) {
        Токен *t = дин_массив_получить(&р->токены->базовый, р->позиция);
        if (t->тип == ТОКЕН_КОММЕНТАРИЙ) {
            р->позиция++;
            continue;
        }
        break;
    }
}

static Токен *текущий(Разборщик *р)
{
    пропустить_служебные(р);
    if (р->позиция >= р->токены->базовый.длина) {
        return дин_массив_получить(&р->токены->базовый, р->токены->базовый.длина - 1);
    }
    return дин_массив_получить(&р->токены->базовый, р->позиция);
}

static int тип_текущего(Разборщик *р)
{
    return текущий(р)->тип;
}

static int съесть(Разборщик *р, ТипТокена ожидаемый)
{
    Токен *t = текущий(р);
    if (t->тип != ожидаемый) {
        char msg[256];
        snprintf(msg, sizeof(msg), "ожидался токен %s, встретился %s «%.*s»",
                 имя_типа_токена(ожидаемый), имя_типа_токена(t->тип),
                 (int)t->длина, t->начало);
        ошибка_разбора(р, t, msg);
        return 0;
    }
    р->позиция++;
    return 1;
}

// Пропускает пустые строки и комментарии (значимые переводы строк тоже).
// Используется на границах операторов: между операторами в блоке, перед «{».
static void пропустить_разрывы(Разборщик *р)
{
    while (р->позиция < р->токены->базовый.длина) {
        Токен *t = дин_массив_получить(&р->токены->базовый, р->позиция);
        if (t->тип == ТОКЕН_КОММЕНТАРИЙ || t->тип == ТОКЕН_НОВАЯ_СТРОКА) {
            р->позиция++;
            continue;
        }
        break;
    }
}

// Завершение оператора: перевод строки, «;», либо позиция перед «}» / концом
// файла (там терминатор не нужен). Иначе — ошибка.
static void съесть_конец_оператора(Разборщик *р)
{
    int т = тип_текущего(р);
    if (т == ТОКЕН_НОВАЯ_СТРОКА || т == ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ) {
        р->позиция++;
        return;
    }
    if (т == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ || т == ТОКЕН_КОНЕЦ_ФАЙЛА) {
        return;
    }
    ошибка_разбора(р, текущий(р), "ожидался конец строки или «;»");
}

// Считывает тип с необязательным префиксом «неподписанный» или конструктором
// «срез<T>». Возвращает 1 при успехе; заполняет *тип, *беззнаковый и *срез.
// Для «срез<T>» *тип/*беззнаковый — это тип элемента T, *срез = 1.
static int съесть_тип(Разборщик *р, ТипТокена *тип, int *беззнаковый, int *срез)
{
    *беззнаковый = 0;
    *срез = 0;

    // «срез<T>»: тип-обёртка с проверкой границ. Внутри — обычный тип элемента.
    if (тип_текущего(р) == ТОКЕН_СРЕЗ) {
        *срез = 1;
        р->позиция++;
        if (тип_текущего(р) != ТОКЕН_СТРЕЛКА_ВЛЕВО
            || !токен_текст_равен(текущий(р), "<")) {
            ошибка_разбора(р, текущий(р), "ожидался «<» после «срез»");
            return 0;
        }
        р->позиция++;  // за «<»
        // Тип элемента (без вложенных срезов): [неподписанный] базовый.
        if (тип_текущего(р) == ТОКЕН_НЕПОДПИСАННЫЙ) {
            *беззнаковый = 1;
            р->позиция++;
        }
        Токен *э = текущий(р);
        if (!токен_это_тип(э->тип)) {
            ошибка_разбора(р, э, "ожидался тип элемента среза");
            return 0;
        }
        if (*беззнаковый && !токен_это_целое(э->тип)) {
            ошибка_разбора(р, э, "«неподписанный» применим только к целым типам");
            return 0;
        }
        *тип = э->тип;
        р->позиция++;  // за типом элемента
        if (тип_текущего(р) != ТОКЕН_СТРЕЛКА_ВПРАВО
            || !токен_текст_равен(текущий(р), ">")) {
            ошибка_разбора(р, текущий(р), "ожидался «>» после типа элемента среза");
            return 0;
        }
        р->позиция++;  // за «>»
        return 1;
    }

    if (тип_текущего(р) == ТОКЕН_НЕПОДПИСАННЫЙ) {
        *беззнаковый = 1;
        р->позиция++;
    }
    Токен *t = текущий(р);
    if (!токен_это_тип(t->тип)) {
        ошибка_разбора(р, t, "ожидался тип");
        return 0;
    }
    if (*беззнаковый && !токен_это_целое(t->тип)) {
        ошибка_разбора(р, t, "«неподписанный» применим только к целым типам");
        return 0;
    }
    *тип = t->тип;
    р->позиция++;
    return 1;
}

// Приоритет бинарного оператора; -1 если токен не бинарный оператор.
static int приоритет(const Токен *t)
{
    // Логические — самый низкий приоритет (ниже сравнения), как «&&»/«||» в C.
    // «или» — ключевое слово (ТОКЕН_ИЛИ). «и» — контекстное ключевое слово:
    // идентификатор в позиции инфиксного оператора (после готового операнда),
    // чтобы не занимать распространённое имя переменной-счётчика «и». Также
    // принимаем C-запись «&&»/«||» (длина 2 отличает их от «&»/«|»).
    if (t->тип == ТОКЕН_ИЛИ) return 1;                            // или
    if (t->тип == ТОКЕН_ПРЯМАЯ_ЧЕРТА && t->длина == 2) return 1;  // ||
    if (t->тип == ТОКЕН_ИДЕНТИФИКАТОР && токен_текст_равен(t, "и")) return 2;  // и
    if (t->тип == ТОКЕН_АМПЕРСАНД && t->длина == 2) return 2;     // &&
    switch (t->тип) {
        case ТОКЕН_ЗВЕЗДА:
        case ТОКЕН_КОСАЯ_ЧЕРТА:
        case ТОКЕН_ПРОЦЕНТ:
            return 5;
        case ТОКЕН_ПЛЮС:
        case ТОКЕН_МИНУС:
            return 4;
        case ТОКЕН_СТРЕЛКА_ВПРАВО:  // > и >=
        case ТОКЕН_СТРЕЛКА_ВЛЕВО:   // < и <=
            return 3;
        case ТОКЕН_РАВНО:
            // == — сравнение; одиночное «=» (присваивание) не является
            // оператором выражения и обрабатывается на уровне оператора.
            return токен_текст_равен(t, "==") ? 3 : -1;
        default:
            return -1;
    }
}

// Содержит ли строковый литерал интерполяцию «\(…)»?
static int строка_с_интерполяцией(const Токен *t)
{
    if (t->длина < 4) return 0;  // минимум "\(x)"
    const char *c = t->начало + 1;     // после открывающей «"»
    size_t n = t->длина - 2;           // без кавычек
    for (size_t i = 0; i + 1 < n; ++i)
        if (c[i] == '\\' && c[i + 1] == '(') return 1;
    return 0;
}

// Разбор выражения внутри «\(…)»: лексим подстроку в свой буфер и парсим
// обычным разборщиком выражений. Буфер и токены намеренно не освобождаются —
// узлы AST ссылаются на этот буфер до конца компиляции (как и основной исходник).
static Узел *разобрать_встроенное_выражение(Разборщик *р, const char *начало,
                                              size_t длина)
{
    char *буф = malloc(длина + 1);
    if (!буф) return nullptr;
    memcpy(буф, начало, длина);
    буф[длина] = '\0';
    МассивТокенов *под = malloc(sizeof(МассивТокенов));
    if (!под) { free(буф); return nullptr; }
    *под = токенизировать_код(длина, буф, 0);
    Разборщик пр = *р;             // наследуем таблицы имён типов и диагностику
    пр.токены = под;
    пр.позиция = 0;
    return разобрать_выражение(&пр);
}

// «"...\(выр)..."» → УЗЕЛ_F_СТРОКА: чередование чанков (УЗЕЛ_СТРОКА без кавычек)
// и вставок (УЗЕЛ_ИНТЕРПОЛЯЦИЯ с выражением-ребёнком).
static Узел *построить_f_строку(Разборщик *р, const Токен *t)
{
    Узел *узел = узел_создать(УЗЕЛ_F_СТРОКА);
    узел->токен = *t;
    const char *c = t->начало + 1;
    size_t n = t->длина - 2;
    size_t i = 0, чанк_от = 0;

    while (i < n) {
        if (c[i] == '\\' && i + 1 < n && c[i + 1] == '(') {
            if (i > чанк_от) {
                Узел *чанк = узел_создать(УЗЕЛ_СТРОКА);
                чанк->токен = (Токен){ ТОКЕН_СТРОКА, t->строка, t->позиция_в_строке,
                                       c + чанк_от, i - чанк_от };
                узел_добавить_ребёнка(узел, чанк);
            }
            size_t от = i + 2, j = от;
            int глубина = 1;
            while (j < n) {
                if (c[j] == '(') глубина++;
                else if (c[j] == ')') { глубина--; if (глубина == 0) break; }
                j++;
            }
            if (глубина != 0) {
                ошибка_разбора(р, t, "незакрытая интерполяция «\\(…)» в строке");
                return узел;
            }
            Узел *выр = разобрать_встроенное_выражение(р, c + от, j - от);
            Узел *холдер = узел_создать(УЗЕЛ_ИНТЕРПОЛЯЦИЯ);
            холдер->токен = *t;
            if (выр) узел_добавить_ребёнка(холдер, выр);
            узел_добавить_ребёнка(узел, холдер);
            i = j + 1;
            чанк_от = i;
        } else {
            i++;
        }
    }
    if (n > чанк_от) {
        Узел *чанк = узел_создать(УЗЕЛ_СТРОКА);
        чанк->токен = (Токен){ ТОКЕН_СТРОКА, t->строка, t->позиция_в_строке,
                               c + чанк_от, n - чанк_от };
        узел_добавить_ребёнка(узел, чанк);
    }
    return узел;
}

// Достигнут предел сложности текущего выражения? Если да — ставит диагностику
// (один раз, дальше молчит, как ошибка_разбора) и возвращает 1: вызывающий
// обязан прекратить разбор этой части выражения и вернуть заглушку, не
// рекурсируя дальше — иначе именно эта рекурсия и переполнит стек.
static int превышен_предел_выражения(Разборщик *р)
{
    if (++р->работа_выражения <= МАКС_РАБОТА_ВЫРАЖЕНИЯ) return 0;
    ошибка_разбора(р, текущий(р),
        "выражение слишком сложное (переполнение лимита разбора); "
        "разбейте его на промежуточные переменные");
    return 1;
}

// Один тип-аргумент на вызове обобщённой функции «имя<ТИП, …>(…)». Возвращает
// УЗЕЛ_ТИПОАРГУМЕНТ с тип_данных/имя_типа/указатели/беззнаковый. Поддержаны:
// примитивы (в т.ч. «неподписанный»), структуры/перечисления, типа-параметры
// (для вложенных обобщённых вызовов внутри шаблона) и указатели «тип*».
// Срез как тип-аргумент в v1 не поддержан (избегаем неоднозначности «>>»).
static Узел *разобрать_тип_аргумент(Разборщик *р)
{
    Узел *та = узел_создать(УЗЕЛ_ТИПОАРГУМЕНТ);
    if (токен_имя_структуры(р, текущий(р))) {
        та->тип_данных = ТОКЕН_СТРУКТУРА;
        та->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_имя_перечисления(р, текущий(р))) {
        та->тип_данных = ТОКЕН_ПЕРЕЧИСЛЕНИЕ;
        та->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_это_типопараметр(р, текущий(р))) {
        та->тип_данных = ТОКЕН_ТИПОПАРАМЕТР;
        та->имя_типа = *текущий(р);
        р->позиция++;
    } else if (!съесть_тип(р, &та->тип_данных, &та->беззнаковый, &та->срез)) {
        return та;
    }
    while (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
        съесть(р, ТОКЕН_ЗВЕЗДА);
        та->указатели++;
    }
    return та;
}

static Узел *разобрать_первичное(Разборщик *р)
{
    if (превышен_предел_выражения(р)) {
        Узел *пустой = узел_создать(УЗЕЛ_ЧИСЛО);
        пустой->токен = *текущий(р);
        return пустой;
    }
    Токен *t = текущий(р);

    // Унарный минус: -выражение. Унарных операторов в языке больше нет.
    if (t->тип == ТОКЕН_МИНУС) {
        Токен оператор = *t;
        р->позиция++;
        Узел *уноп = узел_создать(УЗЕЛ_УНОП);
        уноп->токен = оператор;
        узел_добавить_ребёнка(уноп, разобрать_первичное(р));
        return уноп;
    }

    // Явное приведение: «как<тип>(выражение)».
    if (t->тип == ТОКЕН_КАК) {
        Узел *прив = узел_создать(УЗЕЛ_ПРИВЕДЕНИЕ);
        прив->токен = *t;  // позиция «как» — для диагностик
        р->позиция++;
        if (тип_текущего(р) != ТОКЕН_СТРЕЛКА_ВЛЕВО
            || !токен_текст_равен(текущий(р), "<")) {
            ошибка_разбора(р, текущий(р), "ожидался «<» после «как»");
            return прив;
        }
        р->позиция++;  // за «<»
        int срез_фиктивный = 0;
        if (!съесть_тип(р, &прив->тип_данных, &прив->беззнаковый, &срез_фиктивный)) {
            return прив;
        }
        if (срез_фиктивный) {
            ошибка_разбора(р, текущий(р), "приведение к срезу не поддерживается");
            return прив;
        }
        while (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
            съесть(р, ТОКЕН_ЗВЕЗДА);
            прив->указатели++;
        }
        if (тип_текущего(р) != ТОКЕН_СТРЕЛКА_ВПРАВО
            || !токен_текст_равен(текущий(р), ">")) {
            ошибка_разбора(р, текущий(р), "ожидался «>» после типа приведения");
            return прив;
        }
        р->позиция++;  // за «>»
        if (!съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
            return прив;
        }
        узел_добавить_ребёнка(прив, разобрать_выражение(р));
        съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
        return прив;
    }

    if (t->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
        Токен имя = *t;
        р->позиция++;

        // Вызов обобщённой функции: «имя<ТИП, …>(…)». «<» трактуется как список
        // тип-аргументов только для известного имени обобщённой функции — иначе
        // это оператор сравнения. Тип-аргументы станут ведущими детьми вызова
        // (их снимет мономорфизация). За «>» обязан идти «(» — обобщённая функция
        // используется только вызовом.
        Узел *вызов_с_типами = nullptr;
        if (токен_имя_обобщённой(р, &имя)
            && тип_текущего(р) == ТОКЕН_СТРЕЛКА_ВЛЕВО
            && токен_текст_равен(текущий(р), "<")) {
            вызов_с_типами = узел_создать(УЗЕЛ_ВЫЗОВ);
            вызов_с_типами->токен = имя;
            р->позиция++;  // за «<»
            узел_добавить_ребёнка(вызов_с_типами, разобрать_тип_аргумент(р));
            while (!р->диаг->ошибка && тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
                съесть(р, ТОКЕН_ЗАПЯТАЯ);
                узел_добавить_ребёнка(вызов_с_типами, разобрать_тип_аргумент(р));
            }
            if (тип_текущего(р) == ТОКЕН_СТРЕЛКА_ВПРАВО && токен_текст_равен(текущий(р), ">"))
                р->позиция++;  // за «>»
            else
                ошибка_разбора(р, текущий(р), "ожидался «>» в тип-аргументах вызова");
            if (!съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ))
                return вызов_с_типами;  // ошибка уже выставлена
        }

        Узел *база;
        if (вызов_с_типами) {
            // Значения-аргументы после тип-аргументов (тех же детей вызова).
            if (тип_текущего(р) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
                узел_добавить_ребёнка(вызов_с_типами, разобрать_выражение(р));
                while (!р->диаг->ошибка && тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
                    съесть(р, ТОКЕН_ЗАПЯТАЯ);
                    узел_добавить_ребёнка(вызов_с_типами, разобрать_выражение(р));
                }
            }
            съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
            база = вызов_с_типами;
        } else if (тип_текущего(р) == ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
            Узел *вызов = узел_создать(УЗЕЛ_ВЫЗОВ);
            вызов->токен = имя;
            if (тип_текущего(р) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
                узел_добавить_ребёнка(вызов, разобрать_выражение(р));
                while (!р->диаг->ошибка && тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
                    съесть(р, ТОКЕН_ЗАПЯТАЯ);
                    узел_добавить_ребёнка(вызов, разобрать_выражение(р));
                }
            }
            съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
            база = вызов;
        } else if (тип_текущего(р) == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
            Узел *индекс = узел_создать(УЗЕЛ_ИНДЕКС);
            индекс->токен = имя;
            узел_добавить_ребёнка(индекс, разобрать_выражение(р));
            съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
            база = индекс;
        } else {
            Узел *идент = узел_создать(УЗЕЛ_ИДЕНТИФИКАТОР);
            идент->токен = имя;
            база = идент;
        }

        // Доступ к полям: «база.поле», в т.ч. цепочкой «а.б.в».
        while (!р->диаг->ошибка && тип_текущего(р) == ТОКЕН_ТОЧКА) {
            съесть(р, ТОКЕН_ТОЧКА);
            Токен *поле = текущий(р);
            if (поле->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
                ошибка_разбора(р, поле, "ожидалось имя поля после «.»");
                return база;
            }
            Узел *доступ = узел_создать(УЗЕЛ_ПОЛЕ);
            доступ->токен = *поле;
            узел_добавить_ребёнка(доступ, база);
            р->позиция++;
            база = доступ;
        }
        return база;
    }

    if (t->тип == ТОКЕН_ЧИСЛО) {
        Узел *узел = узел_создать(УЗЕЛ_ЧИСЛО);
        узел->токен = *t;
        р->позиция++;
        return узел;
    }

    if (t->тип == ТОКЕН_СТРОКА) {
        if (строка_с_интерполяцией(t)) {
            Узел *узел = построить_f_строку(р, t);
            р->позиция++;
            return узел;
        }
        Узел *узел = узел_создать(УЗЕЛ_СТРОКА);
        узел->токен = *t;
        р->позиция++;
        return узел;
    }

    if (t->тип == ТОКЕН_НУЛЬ) {
        Узел *узел = узел_создать(УЗЕЛ_НУЛЬ);
        узел->токен = *t;
        р->позиция++;
        return узел;
    }

    // Логические литералы «истина»/«ложь» — узел-число (инертный лист-константа),
    // тип и текст «true»/«false» проставляют семантика и кодоген по типу токена.
    if (t->тип == ТОКЕН_ИСТИНА || t->тип == ТОКЕН_ЛОЖЬ) {
        Узел *узел = узел_создать(УЗЕЛ_ЧИСЛО);
        узел->токен = *t;
        р->позиция++;
        return узел;
    }

    if (t->тип == ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
        съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
        Узел *внутри = разобрать_выражение(р);
        съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
        return внутри;  // приоритет уже закодирован структурой дерева
    }

    ошибка_разбора(р, t, "неожиданное выражение");
    Узел *пустой = узел_создать(УЗЕЛ_ЧИСЛО);
    пустой->токен = *t;
    return пустой;
}

// Текст для синтезированного оператора «>=» (когда записано «=>»).
static const char ОПЕРАТОР_БОЛЬШЕ_РАВНО[] = ">=";
// C-вид логических операторов: «и»/«&&» → «&&», «или»/«||» → «||». Нормализуем
// в парсере, чтобы семантика и кодоген видели обычные операторы C.
static const char ОПЕРАТОР_И[]   = "&&";
static const char ОПЕРАТОР_ИЛИ[] = "||";

// Логический оператор (в любой записи) в текущей позиции? Возвращает C-текст
// («&&»/«||») или nullptr.
static const char *логический_оператор_текст(const Токен *op)
{
    if (op->тип == ТОКЕН_ИЛИ) return ОПЕРАТОР_ИЛИ;
    if (op->тип == ТОКЕН_ПРЯМАЯ_ЧЕРТА && op->длина == 2) return ОПЕРАТОР_ИЛИ;
    if (op->тип == ТОКЕН_ИДЕНТИФИКАТОР && токен_текст_равен(op, "и")) return ОПЕРАТОР_И;
    if (op->тип == ТОКЕН_АМПЕРСАНД && op->длина == 2) return ОПЕРАТОР_И;
    return nullptr;
}

static Узел *присоединить_биноп(Узел *лево, Токен оператор, Узел *право)
{
    Узел *бин = узел_создать(УЗЕЛ_БИНОП);
    бин->токен = оператор;
    узел_добавить_ребёнка(бин, лево);
    узел_добавить_ребёнка(бин, право);
    return бин;
}

static Узел *разобрать_бинарное(Разборщик *р, int мин_приоритет)
{
    Узел *лево = разобрать_первичное(р);

    while (!р->диаг->ошибка) {
        Токен *op = текущий(р);

        // «=>» — альтернативная запись «>=» (токены «=» затем «>»).
        if (op->тип == ТОКЕН_РАВНО && токен_текст_равен(op, "=")) {
            size_t сохранение = р->позиция;
            р->позиция++;  // за «=»
            Токен *nx = текущий(р);
            if (nx->тип == ТОКЕН_СТРЕЛКА_ВПРАВО && токен_текст_равен(nx, ">")) {
                int prec = 3;  // приоритет сравнения
                if (prec < мин_приоритет) {
                    р->позиция = сохранение;  // не наш уровень — вернуть «=>» наружу
                    break;
                }
                р->позиция++;  // за «>»
                Токен оператор = *op;
                оператор.тип = ТОКЕН_СТРЕЛКА_ВПРАВО;
                оператор.начало = ОПЕРАТОР_БОЛЬШЕ_РАВНО;
                оператор.длина = 2;
                лево = присоединить_биноп(лево, оператор, разобрать_бинарное(р, prec + 1));
                continue;
            }
            р->позиция = сохранение;  // не «=>», обработать «=» как обычно
            op = текущий(р);
        }

        int prec = приоритет(op);
        if (prec < мин_приоритет || prec < 0) {
            break;
        }
        Токен оператор = *op;
        // Нормализуем логические «и»/«или» (и C-запись «&&»/«||») к виду C:
        // семантика и кодоген получают обычный «&&»/«||».
        const char *лог = логический_оператор_текст(op);
        if (лог) {
            оператор.тип = (лог == ОПЕРАТОР_И) ? ТОКЕН_АМПЕРСАНД : ТОКЕН_ПРЯМАЯ_ЧЕРТА;
            оператор.начало = лог;
            оператор.длина = 2;
        }
        р->позиция++;
        лево = присоединить_биноп(лево, оператор, разобрать_бинарное(р, prec + 1));
    }

    return лево;
}

static Узел *разобрать_выражение(Разборщик *р)
{
    // На самом верхнем (не вложенном) вызове — новый бюджет «работы» для
    // всего этого выражения целиком (счётчик используется общий на скобки,
    // унарный минус, приведение, аргументы вызова — они разбираются через
    // разобрать_первичное, которое рекурсивно вызывает разобрать_выражение).
    if (р->вложенность_выражения == 0) р->работа_выражения = 0;
    р->вложенность_выражения++;
    Узел *рез = разобрать_бинарное(р, 0);
    р->вложенность_выражения--;
    return рез;
}

// Литерал структуры «{ поле = выражение, … }» (именованные поля; порядок
// не важен, незаданные поля зануляются). Однострочный.
static Узел *разобрать_литерал_структуры(Разборщик *р)
{
    Узел *лит = узел_создать(УЗЕЛ_ЛИТЕРАЛ_СТРУКТУРЫ);
    съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
    while (!р->диаг->ошибка
           && тип_текущего(р) != ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
           && тип_текущего(р) != ТОКЕН_КОНЕЦ_ФАЙЛА) {
        Токен *поле = текущий(р);
        if (поле->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
            ошибка_разбора(р, поле, "ожидалось имя поля в литерале структуры");
            return лит;
        }
        Узел *иниц = узел_создать(УЗЕЛ_ИНИЦ_ПОЛЯ);
        иниц->токен = *поле;
        р->позиция++;
        if (!съесть(р, ТОКЕН_РАВНО)) {
            return лит;
        }
        узел_добавить_ребёнка(иниц, разобрать_выражение(р));
        узел_добавить_ребёнка(лит, иниц);
        if (тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
            съесть(р, ТОКЕН_ЗАПЯТАЯ);
        } else {
            break;
        }
    }
    съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    return лит;
}

// Литерал массива «{ выражение, выражение, … }» (позиционный, по порядку
// элементов). Однострочный; меньше элементов, чем размер — остальные занулит
// C-компилятор автоматически.
static Узел *разобрать_литерал_массива(Разборщик *р)
{
    Узел *лит = узел_создать(УЗЕЛ_ЛИТЕРАЛ_МАССИВА);
    съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
    while (!р->диаг->ошибка
           && тип_текущего(р) != ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
           && тип_текущего(р) != ТОКЕН_КОНЕЦ_ФАЙЛА) {
        узел_добавить_ребёнка(лит, разобрать_выражение(р));
        if (тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
            съесть(р, ТОКЕН_ЗАПЯТАЯ);
        } else {
            break;
        }
    }
    съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    return лит;
}

static Узел *разобрать_объявление(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ОБЪЯВЛЕНИЕ);
    if (тип_текущего(р) == ТОКЕН_СТАТИЧЕСКИЙ) {
        узел->статический = 1;
        р->позиция++;
    }
    // Тип-структура/перечисление: «Имя перем». Иначе — примитивный тип/срез.
    if (токен_имя_структуры(р, текущий(р))) {
        узел->тип_данных = ТОКЕН_СТРУКТУРА;
        узел->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_имя_перечисления(р, текущий(р))) {
        узел->тип_данных = ТОКЕН_ПЕРЕЧИСЛЕНИЕ;
        узел->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_это_типопараметр(р, текущий(р))) {
        узел->тип_данных = ТОКЕН_ТИПОПАРАМЕТР;
        узел->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_имя_типфункции(р, текущий(р))) {
        узел->тип_данных = ТОКЕН_ФУНКУКАЗАТЕЛЬ;
        узел->имя_типа = *текущий(р);
        р->позиция++;
    } else if (!съесть_тип(р, &узел->тип_данных, &узел->беззнаковый, &узел->срез)) {
        return узел;
    }

    // Указатели в объявлении: «тип* имя», «тип** имя» и т.д.
    while (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
        съесть(р, ТОКЕН_ЗВЕЗДА);
        узел->указатели++;
    }

    Токен *имя = текущий(р);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        ошибка_разбора(р, имя, "ожидалось имя переменной");
        return узел;
    }
    узел->токен = *имя;
    р->позиция++;

    // Массив фиксированного размера: «тип имя[N]». N — обязательный
    // положительный числовой литерал, известный на этапе компиляции.
    if (тип_текущего(р) == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
        съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
        Токен *размер = текущий(р);
        if (размер->тип != ТОКЕН_ЧИСЛО) {
            ошибка_разбора(р, размер,
                "размер массива должен быть числовым литералом");
            return узел;
        }
        long n = strtol(размер->начало, nullptr, 10);
        if (n <= 0) {
            ошибка_разбора(р, размер,
                "размер массива должен быть положительным");
            return узел;
        }
        узел->массив = 1;
        узел->размер_массива = n;
        р->позиция++;
        if (!съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
            return узел;
        }
    }

    if (тип_текущего(р) == ТОКЕН_РАВНО) {
        съесть(р, ТОКЕН_РАВНО);
        // Литерал массива: «тип имя[N] = { значение, значение, … }». Закрывающая
        // «}» сама завершает оператор.
        if (узел->массив) {
            if (тип_текущего(р) != ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
                ошибка_разбора(р, текущий(р),
                    "инициализация массива должна быть литералом: { значение, … }");
                return узел;
            }
            узел_добавить_ребёнка(узел, разобрать_литерал_массива(р));
            return узел;
        }
        // Литерал структуры: «Имя п = { поле = выражение, … }». Закрывающая «}»
        // литерала сама завершает оператор, поэтому терминатор не требуем
        // (перевод после «}» в модели A гасится как продолжение строки).
        if (тип_текущего(р) == ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            узел_добавить_ребёнка(узел, разобрать_литерал_структуры(р));
            return узел;
        }
        узел_добавить_ребёнка(узел, разобрать_выражение(р));
    }

    съесть_конец_оператора(р);
    return узел;
}

static Узел *разобрать_если(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ЕСЛИ);
    съесть(р, ТОКЕН_ЕСЛИ);
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_выражение(р));  // условие
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_блок(р, 1));     // тогда

    if (тип_текущего(р) == ТОКЕН_ИНАЧЕ) {
        съесть(р, ТОКЕН_ИНАЧЕ);
        узел_добавить_ребёнка(узел, разобрать_блок(р, 1)); // иначе
    }
    return узел;
}

static Узел *разобрать_пока(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ПОКА);
    съесть(р, ТОКЕН_ПОКА);
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_выражение(р));  // условие
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_блок(р, 1));     // тело
    return узел;
}

// «выбор выражение { ВАРИАНТ { … }  _ { … } }» — match по перечислению.
// Каждая ветка: имя варианта (или «_» — по умолчанию) и блок.
static Узел *разобрать_выбор(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ВЫБОР);
    съесть(р, ТОКЕН_ВЫБОР);
    узел_добавить_ребёнка(узел, разобрать_выражение(р));  // выбираемое выражение
    пропустить_разрывы(р);
    if (!съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        return узел;
    }
    while (!р->диаг->ошибка) {
        пропустить_разрывы(р);
        if (тип_текущего(р) == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
            || тип_текущего(р) == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        Токен *имя = текущий(р);
        if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
            ошибка_разбора(р, имя, "ожидалось имя варианта или «_» в ветке выбора");
            return узел;
        }
        Узел *ветка = узел_создать(УЗЕЛ_ВЕТКА_ВЫБОРА);
        ветка->токен = *имя;
        р->позиция++;
        узел_добавить_ребёнка(ветка, разобрать_блок(р, 1));
        узел_добавить_ребёнка(узел, ветка);
    }
    съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    return узел;
}

static Узел *разобрать_возврат(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ВОЗВРАТ);
    Токен *t = текущий(р);
    узел->токен = *t;
    съесть(р, ТОКЕН_ВЕРНУТЬ);
    int т = тип_текущего(р);
    if (т != ТОКЕН_НОВАЯ_СТРОКА && т != ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ
        && т != ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ && т != ТОКЕН_КОНЕЦ_ФАЙЛА) {
        узел_добавить_ребёнка(узел, разобрать_выражение(р));
    }
    съесть_конец_оператора(р);
    return узел;
}

// Присваивание «цель = значение» (без завершающей «;»). Если присваивания нет,
// возвращает само разобранное выражение (например, вызов функции).
static Узел *разобрать_выражение_или_присваивание(Разборщик *р)
{
    Узел *лево = разобрать_выражение(р);
    if (!р->диаг->ошибка && тип_текущего(р) == ТОКЕН_РАВНО
        && токен_текст_равен(текущий(р), "=")) {
        съесть(р, ТОКЕН_РАВНО);
        Узел *присв = узел_создать(УЗЕЛ_ПРИСВАИВАНИЕ);
        узел_добавить_ребёнка(присв, лево);                 // цель
        узел_добавить_ребёнка(присв, разобрать_выражение(р)); // значение
        return присв;
    }
    return лево;
}

static Узел *разобрать_для(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ДЛЯ);
    съесть(р, ТОКЕН_ДЛЯ);
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);

    // Инициализация: объявление (само съедает «;») либо выражение/присваивание.
    if (токен_начинает_тип(тип_текущего(р))) {
        узел_добавить_ребёнка(узел, разобрать_объявление(р));
    } else {
        узел_добавить_ребёнка(узел, разобрать_выражение_или_присваивание(р));
        съесть(р, ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ);
    }

    узел_добавить_ребёнка(узел, разобрать_выражение(р));  // условие
    съесть(р, ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ);
    узел_добавить_ребёнка(узел, разобрать_выражение_или_присваивание(р)); // шаг
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);

    узел_добавить_ребёнка(узел, разобрать_блок(р, 1));    // тело — последний ребёнок
    return узел;
}

// foreach: «для_каждого (имя из коллекция) { … }». Парсер не знает типа
// элемента — это уточняет семантика, она же объявляет «имя» в области тела.
static Узел *разобрать_для_каждого(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ДЛЯ_КАЖДОГО);
    съесть(р, ТОКЕН_ДЛЯ_КАЖДОГО);
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);

    Токен *имя = текущий(р);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        ошибка_разбора(р, имя, "ожидалось имя переменной элемента");
        return узел;
    }
    узел->токен = *имя;
    р->позиция++;

    if (!съесть(р, ТОКЕН_ИЗ)) {
        return узел;
    }

    узел_добавить_ребёнка(узел, разобрать_выражение(р));   // коллекция
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_блок(р, 1));     // тело
    return узел;
}

static Узел *разобрать_оператор(Разборщик *р)
{
    пропустить_служебные(р);

    if (тип_текущего(р) == ТОКЕН_ЕСЛИ) {
        return разобрать_если(р);
    }
    if (тип_текущего(р) == ТОКЕН_ВЫБОР) {
        return разобрать_выбор(р);
    }
    if (тип_текущего(р) == ТОКЕН_ПОКА) {
        return разобрать_пока(р);
    }
    if (тип_текущего(р) == ТОКЕН_ДЛЯ) {
        return разобрать_для(р);
    }
    if (тип_текущего(р) == ТОКЕН_ДЛЯ_КАЖДОГО) {
        return разобрать_для_каждого(р);
    }
    if (тип_текущего(р) == ТОКЕН_ВЕРНУТЬ) {
        return разобрать_возврат(р);
    }
    if (тип_текущего(р) == ТОКЕН_ПРЕРВАТЬ || тип_текущего(р) == ТОКЕН_ПРОДОЛЖИТЬ) {
        Узел *узел = узел_создать(тип_текущего(р) == ТОКЕН_ПРЕРВАТЬ
                                      ? УЗЕЛ_ПРЕРВАТЬ : УЗЕЛ_ПРОДОЛЖИТЬ);
        узел->токен = *текущий(р);
        р->позиция++;
        съесть_конец_оператора(р);
        return узел;
    }

    Токен *t = текущий(р);
    if (токен_начинает_тип(t->тип) || t->тип == ТОКЕН_СТАТИЧЕСКИЙ
        || токен_имя_структуры(р, t) || токен_имя_перечисления(р, t)
        || токен_это_типопараметр(р, t) || токен_имя_типфункции(р, t)) {
        return разобрать_объявление(р);
    }

    if (t->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
        Узел *выр = разобрать_выражение_или_присваивание(р);
        if (!р->диаг->ошибка && выр->тип != УЗЕЛ_ВЫЗОВ && выр->тип != УЗЕЛ_ПРИСВАИВАНИЕ) {
            ошибка_разбора(р, t, "ожидался вызов функции или присваивание");
        }
        съесть_конец_оператора(р);
        return выр;
    }

    ошибка_разбора(р, t, "неожиданный оператор");
    return узел_создать(УЗЕЛ_БЛОК);
}

static Узел *разобрать_блок(Разборщик *р, int с_скобками)
{
    Узел *блок = узел_создать(УЗЕЛ_БЛОК);
    if (с_скобками) {
        пропустить_разрывы(р);  // «{» может стоять на следующей строке
        if (!съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
            return блок;
        }
    }

    while (!р->диаг->ошибка) {
        пропустить_разрывы(р);  // пустые строки между операторами
        int т = тип_текущего(р);
        if (т == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ || т == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        узел_добавить_ребёнка(блок, разобрать_оператор(р));
    }

    if (с_скобками) {
        съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    }
    return блок;
}

// «структура Имя { тип поле … }» — объявление пользовательского типа.
// Поля: «тип [*…] имя», по одному на строку (как объявления без инициализатора).
// Необязательный атрибут «[удалить = функция]» задаёт деструктор: компилятор
// вызовет его при выходе переменной из области (RAII), как в Vala.
static Узел *разобрать_структуру(Разборщик *р)
{
    Узел *стр = узел_создать(УЗЕЛ_СТРУКТУРА);

    if (тип_текущего(р) == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
        съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
        Токен *ключ = текущий(р);
        if (!токен_текст_равен(ключ, "удалить")) {
            ошибка_разбора(р, ключ, "ожидался атрибут «удалить»");
            return стр;
        }
        р->позиция++;
        if (!съесть(р, ТОКЕН_РАВНО)) {
            return стр;
        }
        Токен *ф = текущий(р);
        if (ф->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
            ошибка_разбора(р, ф, "ожидалось имя функции-деструктора");
            return стр;
        }
        стр->имя_типа = *ф;  // для УЗЕЛ_СТРУКТУРА имя_типа — имя деструктора
        р->позиция++;
        if (!съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
            return стр;
        }
        пропустить_разрывы(р);  // «структура» — на следующей строке
    }

    // Ключевое слово: «структура» или «союз». Союз — C union (поля разделяют
    // память); деструктор [удалить=…] для союза запрещён (какой член активен —
    // неизвестно, транзитивная деструкция небезопасна).
    if (тип_текущего(р) == ТОКЕН_СОЮЗ) {
        if (стр->имя_типа.длина > 0) {
            ошибка_разбора(р, текущий(р),
                "союз не может иметь деструктор «[удалить=…]»");
            return стр;
        }
        стр->это_союз = 1;
        съесть(р, ТОКЕН_СОЮЗ);
    } else {
        съесть(р, ТОКЕН_СТРУКТУРА);
    }

    Токен *имя = текущий(р);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        ошибка_разбора(р, имя, стр->это_союз ? "ожидалось имя союза"
                                              : "ожидалось имя структуры");
        return стр;
    }
    стр->токен = *имя;
    р->позиция++;
    запомнить_структуру(р, имя);

    пропустить_разрывы(р);  // «{» может стоять на следующей строке
    if (!съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        return стр;
    }

    while (!р->диаг->ошибка) {
        пропустить_разрывы(р);
        if (тип_текущего(р) == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
            || тип_текущего(р) == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        Узел *поле = узел_создать(УЗЕЛ_ПАРАМЕТР);
        if (токен_имя_структуры(р, текущий(р))) {
            поле->тип_данных = ТОКЕН_СТРУКТУРА;
            поле->имя_типа = *текущий(р);
            р->позиция++;
        } else if (токен_имя_перечисления(р, текущий(р))) {
            поле->тип_данных = ТОКЕН_ПЕРЕЧИСЛЕНИЕ;
            поле->имя_типа = *текущий(р);
            р->позиция++;
        } else if (!съесть_тип(р, &поле->тип_данных, &поле->беззнаковый, &поле->срез)) {
            return стр;
        }
        while (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
            съесть(р, ТОКЕН_ЗВЕЗДА);
            поле->указатели++;
        }
        Токен *имя_поля = текущий(р);
        if (имя_поля->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
            ошибка_разбора(р, имя_поля, "ожидалось имя поля структуры");
            return стр;
        }
        поле->токен = *имя_поля;
        р->позиция++;
        съесть_конец_оператора(р);
        узел_добавить_ребёнка(стр, поле);
    }

    съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    return стр;
}

// «перечисление Имя { ВАРИАНТ [= выражение] … }» — пользовательский enum-тип.
// Варианты по одному на строку (модель A); необязательное «= значение» задаёт
// явное число, иначе значение — предыдущее + 1 (нумерация как в C).
static Узел *разобрать_перечисление(Разборщик *р)
{
    Узел *перечисление = узел_создать(УЗЕЛ_ПЕРЕЧИСЛЕНИЕ);
    съесть(р, ТОКЕН_ПЕРЕЧИСЛЕНИЕ);

    Токен *имя = текущий(р);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        ошибка_разбора(р, имя, "ожидалось имя перечисления");
        return перечисление;
    }
    перечисление->токен = *имя;
    р->позиция++;
    запомнить_перечисление(р, имя);

    пропустить_разрывы(р);  // «{» может стоять на следующей строке
    if (!съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        return перечисление;
    }

    while (!р->диаг->ошибка) {
        пропустить_разрывы(р);
        if (тип_текущего(р) == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
            || тип_текущего(р) == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        Токен *имя_варианта = текущий(р);
        if (имя_варианта->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
            ошибка_разбора(р, имя_варианта, "ожидалось имя варианта перечисления");
            return перечисление;
        }
        Узел *вариант = узел_создать(УЗЕЛ_ВАРИАНТ);
        вариант->токен = *имя_варианта;
        р->позиция++;
        // Необязательное явное значение: «= выражение».
        if (тип_текущего(р) == ТОКЕН_РАВНО) {
            съесть(р, ТОКЕН_РАВНО);
            узел_добавить_ребёнка(вариант, разобрать_выражение(р));
        }
        съесть_конец_оператора(р);
        // Запятая после варианта (необязательна) — поглощаем.
        if (тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
            съесть(р, ТОКЕН_ЗАПЯТАЯ);
        }
        узел_добавить_ребёнка(перечисление, вариант);
    }

    съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    return перечисление;
}

static Узел *разобрать_параметр(Разборщик *р)
{
    Узел *пар = узел_создать(УЗЕЛ_ПАРАМЕТР);
    if (тип_текущего(р) == ТОКЕН_ВЛАДЕЕТ) {
        пар->владеет = 1;
        р->позиция++;
    }
    if (токен_имя_структуры(р, текущий(р))) {
        пар->тип_данных = ТОКЕН_СТРУКТУРА;
        пар->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_имя_перечисления(р, текущий(р))) {
        пар->тип_данных = ТОКЕН_ПЕРЕЧИСЛЕНИЕ;
        пар->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_это_типопараметр(р, текущий(р))) {
        пар->тип_данных = ТОКЕН_ТИПОПАРАМЕТР;
        пар->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_имя_типфункции(р, текущий(р))) {
        пар->тип_данных = ТОКЕН_ФУНКУКАЗАТЕЛЬ;
        пар->имя_типа = *текущий(р);
        р->позиция++;
    } else if (!съесть_тип(р, &пар->тип_данных, &пар->беззнаковый, &пар->срез)) {
        return пар;
    }

    while (!р->диаг->ошибка
           && (тип_текущего(р) == ТОКЕН_ЗВЕЗДА
               || тип_текущего(р) == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        if (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
            съесть(р, ТОКЕН_ЗВЕЗДА);
            пар->указатели++;
        } else {
            съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
            съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
            пар->массив = 1;
        }
    }

    Токен *имя = текущий(р);
    if (имя->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
        пар->токен = *имя;
        р->позиция++;
    }
    return пар;
}

// «типфункции RET Имя(ТИП1, ТИП2, …)» — именованный тип-указатель на функцию.
// Хранение: токен = имя псевдонима; тип_данных/беззнаковый/указатели/имя_типа —
// тип возврата; дети — параметры (УЗЕЛ_ПАРАМЕТР). → C «typedef RET (*Имя)(…);».
static Узел *разобрать_типфункции(Разборщик *р)
{
    Узел *тф = узел_создать(УЗЕЛ_ТИПФУНКЦИИ);
    съесть(р, ТОКЕН_ТИПФУНКЦИИ);

    // Тип возврата (как у функции): void/структура/перечисление/примитив + «*».
    if (тип_текущего(р) == ТОКЕН_НИЧЕГО) {
        тф->тип_данных = ТОКЕН_НИЧЕГО;
        р->позиция++;
    } else if (токен_имя_структуры(р, текущий(р))) {
        тф->тип_данных = ТОКЕН_СТРУКТУРА;
        тф->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_имя_перечисления(р, текущий(р))) {
        тф->тип_данных = ТОКЕН_ПЕРЕЧИСЛЕНИЕ;
        тф->имя_типа = *текущий(р);
        р->позиция++;
    } else if (!съесть_тип(р, &тф->тип_данных, &тф->беззнаковый, &тф->срез)) {
        return тф;
    }
    while (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
        съесть(р, ТОКЕН_ЗВЕЗДА);
        тф->указатели++;
    }

    Токен *имя = текущий(р);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        ошибка_разбора(р, имя, "ожидалось имя типа-указателя на функцию");
        return тф;
    }
    тф->токен = *имя;
    р->позиция++;
    запомнить_типфункции(р, имя);

    if (!съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) return тф;
    while (!р->диаг->ошибка && тип_текущего(р) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
           && тип_текущего(р) != ТОКЕН_КОНЕЦ_ФАЙЛА) {
        Токен *pt = текущий(р);
        if (токен_начинает_тип(pt->тип) || токен_имя_структуры(р, pt)
            || токен_имя_перечисления(р, pt) || токен_имя_типфункции(р, pt)) {
            узел_добавить_ребёнка(тф, разобрать_параметр(р));
        } else {
            р->позиция++;
        }
        if (тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) съесть(р, ТОКЕН_ЗАПЯТАЯ);
    }
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    съесть_конец_оператора(р);
    return тф;
}

static Узел *разобрать_функцию(Разборщик *р)
{
    Узел *функция = узел_создать(УЗЕЛ_ФУНКЦИЯ);
    if (тип_текущего(р) == ТОКЕН_СТАТИЧЕСКИЙ) {
        функция->статический = 1;
        р->позиция++;
    }
    // Тип возврата — пользовательская структура/перечисление (по значению,
    // не указатель): «Тип имя(…) { … }». Как и для параметра — по имени,
    // распознанному ранее собранным списком известных структур/перечислений.
    if (тип_текущего(р) == ТОКЕН_НИЧЕГО) {
        // «ничего» (void) — допустимо ТОЛЬКО как тип возврата функции; в объявлениях
        // переменных/параметров этот токен не тип (токен_это_тип его не пропустит).
        функция->тип_данных = ТОКЕН_НИЧЕГО;
        р->позиция++;
    } else if (токен_имя_структуры(р, текущий(р))) {
        функция->тип_данных = ТОКЕН_СТРУКТУРА;
        функция->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_имя_перечисления(р, текущий(р))) {
        функция->тип_данных = ТОКЕН_ПЕРЕЧИСЛЕНИЕ;
        функция->имя_типа = *текущий(р);
        р->позиция++;
    } else if (токен_это_типопараметр(р, текущий(р))) {
        // Тип возврата — типа-параметр «T» обобщённой функции.
        функция->тип_данных = ТОКЕН_ТИПОПАРАМЕТР;
        функция->имя_типа = *текущий(р);
        р->позиция++;
    } else if (!съесть_тип(р, &функция->тип_данных, &функция->беззнаковый, &функция->срез)) {
        return функция;
    }

    // Указательный тип возврата: «Тип* ф(…)», «Тип** ф(…)». Число «*» хранится в
    // функция->указатели (для функции это поле — косвенность типа возврата).
    while (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
        съесть(р, ТОКЕН_ЗВЕЗДА);
        функция->указатели++;
    }

    Токен *имя = текущий(р);
    if (имя->тип == ТОКЕН_ТОЧКА_ВХОДА) {
        функция->это_main = 1;
        if (функция->статический) {
            ошибка_разбора(р, имя, "точка_входа не может быть статической");
            return функция;
        }
    } else if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        ошибка_разбора(р, имя, "ожидалось имя функции или точка_входа");
        return функция;
    }
    функция->токен = *имя;
    р->позиция++;

    // Список типа-параметров «<T, U, …>» после имени обобщённой функции. Имена
    // уже собраны пред-проходом; здесь фиксируем их как дети-УЗЕЛ_ТИПОПАРАМЕТР
    // (в порядке объявления — для позиционного сопоставления с тип-аргументами)
    // перед параметрами. Мономорфизация снимет их и подставит конкретные типы.
    if (!функция->это_main && тип_текущего(р) == ТОКЕН_СТРЕЛКА_ВЛЕВО
        && токен_текст_равен(текущий(р), "<")) {
        р->позиция++;  // за «<»
        while (!р->диаг->ошибка && тип_текущего(р) == ТОКЕН_ИДЕНТИФИКАТОР) {
            Узел *тп = узел_создать(УЗЕЛ_ТИПОПАРАМЕТР);
            тп->токен = *текущий(р);
            узел_добавить_ребёнка(функция, тп);
            р->позиция++;
            if (тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) { р->позиция++; continue; }
            break;
        }
        if (!(тип_текущего(р) == ТОКЕН_СТРЕЛКА_ВПРАВО && токен_текст_равен(текущий(р), ">"))) {
            ошибка_разбора(р, текущий(р), "ожидался «>» в списке типа-параметров");
            return функция;
        }
        р->позиция++;  // за «>»
    }

    if (!съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        return функция;
    }

    if (функция->это_main) {
        // Параметры точки входа игнорируются: подставляется argc/argv.
        while (!р->диаг->ошибка && тип_текущего(р) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
               && тип_текущего(р) != ТОКЕН_КОНЕЦ_ФАЙЛА) {
            р->позиция++;
        }
    } else {
        while (!р->диаг->ошибка && тип_текущего(р) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
               && тип_текущего(р) != ТОКЕН_КОНЕЦ_ФАЙЛА) {
            Токен *pt = текущий(р);
            if (токен_начинает_тип(pt->тип) || pt->тип == ТОКЕН_ВЛАДЕЕТ
                || токен_имя_структуры(р, pt) || токен_имя_перечисления(р, pt)
                || токен_это_типопараметр(р, pt)   // «T х» — параметр обобщённой
                || токен_имя_типфункции(р, pt)) {   // «БинарОп оп» — fn-ptr параметр
                узел_добавить_ребёнка(функция, разобрать_параметр(р));
            } else {
                р->позиция++;
            }
            if (тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
                съесть(р, ТОКЕН_ЗАПЯТАЯ);
            }
        }
    }

    if (!съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
        return функция;
    }

    узел_добавить_ребёнка(функция, разобрать_блок(р, 1));  // тело — последний ребёнок
    return функция;
}

// ─── multi-file: список имён структур, известных извне ──────────────────────
//
// Когда программа собрана из нескольких .конда, парсер файла X должен знать
// о структурах, объявленных в файле Y, чтобы распознать «Y_тип п» как
// объявление переменной. Используется в основа.c: сначала собираются имена
// со всех файлов, потом каждый файл парсится с предзагруженным списком.
struct ИменаСтруктур {
    char имена[МАКС_СТРУКТУР_РАЗБОР][МАКС_ИМЯ_РАЗБОР];
    size_t число;
};

ИменаСтруктур *имена_структур_создать(void)
{
    ИменаСтруктур *с = calloc(1, sizeof(*с));
    return с;
}

void имена_структур_освободить(ИменаСтруктур *с)
{
    free(с);
}

void собрать_имена_структур(МассивТокенов *токены, ИменаСтруктур *куда)
{
    if (!токены || !куда) return;
    size_t n = токены->базовый.длина;
    for (size_t i = 0; i + 1 < n; ++i) {
        Токен *t = дин_массив_получить(&токены->базовый, i);
        if (!t || (t->тип != ТОКЕН_СТРУКТУРА && t->тип != ТОКЕН_СОЮЗ)) continue;
        Токен *имя = дин_массив_получить(&токены->базовый, i + 1);
        if (!имя || имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) continue;
        if (куда->число >= МАКС_СТРУКТУР_РАЗБОР) break;
        // Дедуп: не добавляем дважды (один и тот же файл могли просканировать
        // несколько раз, или две сборки имён слились).
        size_t m = имя->длина < МАКС_ИМЯ_РАЗБОР - 1 ? имя->длина : МАКС_ИМЯ_РАЗБОР - 1;
        int дубль = 0;
        for (size_t j = 0; j < куда->число; ++j) {
            if (байты_равны(куда->имена[j], strlen(куда->имена[j]),
                            имя->начало, m)) {
                дубль = 1;
                break;
            }
        }
        if (дубль) continue;
        memcpy(куда->имена[куда->число], имя->начало, m);
        куда->имена[куда->число][m] = '\0';
        куда->число++;
    }
}

// ─── multi-file: список имён перечислений, известных извне ──────────────────
//
// Зеркало ИменаСтруктур: без предзагрузки имён перечислений из других файлов
// парсер файла X не распознаёт «Y_перечисление п» (объявление переменной
// enum-типа, объявленного в файле Y) — «Y_перечисление» остаётся для него
// обычным идентификатором.
struct ИменаПеречислений {
    char имена[МАКС_СТРУКТУР_РАЗБОР][МАКС_ИМЯ_РАЗБОР];
    size_t число;
};

ИменаПеречислений *имена_перечислений_создать(void)
{
    return calloc(1, sizeof(ИменаПеречислений));
}

void имена_перечислений_освободить(ИменаПеречислений *п)
{
    free(п);
}

void собрать_имена_перечислений(МассивТокенов *токены, ИменаПеречислений *куда)
{
    if (!токены || !куда) return;
    size_t n = токены->базовый.длина;
    for (size_t i = 0; i + 1 < n; ++i) {
        Токен *t = дин_массив_получить(&токены->базовый, i);
        if (!t || t->тип != ТОКЕН_ПЕРЕЧИСЛЕНИЕ) continue;
        Токен *имя = дин_массив_получить(&токены->базовый, i + 1);
        if (!имя || имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) continue;
        if (куда->число >= МАКС_СТРУКТУР_РАЗБОР) break;
        size_t m = имя->длина < МАКС_ИМЯ_РАЗБОР - 1 ? имя->длина : МАКС_ИМЯ_РАЗБОР - 1;
        int дубль = 0;
        for (size_t j = 0; j < куда->число; ++j) {
            if (байты_равны(куда->имена[j], strlen(куда->имена[j]),
                            имя->начало, m)) {
                дубль = 1;
                break;
            }
        }
        if (дубль) continue;
        memcpy(куда->имена[куда->число], имя->начало, m);
        куда->имена[куда->число][m] = '\0';
        куда->число++;
    }
}

Узел *разобрать_с_именами_структур(МассивТокенов *токены, Диагностика *диаг,
                                     const ИменаСтруктур *известные_структуры,
                                     const ИменаПеречислений *известные_перечисления)
{
    разметить_переносы(токены);

    Разборщик р = { .токены = токены, .позиция = 0, .диаг = диаг };
    if (известные_структуры) {
        size_t n = известные_структуры->число < МАКС_СТРУКТУР_РАЗБОР
                       ? известные_структуры->число : МАКС_СТРУКТУР_РАЗБОР;
        for (size_t i = 0; i < n; ++i) {
            size_t m = strlen(известные_структуры->имена[i]);
            if (m >= МАКС_ИМЯ_РАЗБОР) m = МАКС_ИМЯ_РАЗБОР - 1;
            memcpy(р.структуры[i], известные_структуры->имена[i], m);
            р.структуры[i][m] = '\0';
        }
        р.число_структур = n;
    }
    if (известные_перечисления) {
        size_t n = известные_перечисления->число < МАКС_СТРУКТУР_РАЗБОР
                       ? известные_перечисления->число : МАКС_СТРУКТУР_РАЗБОР;
        for (size_t i = 0; i < n; ++i) {
            size_t m = strlen(известные_перечисления->имена[i]);
            if (m >= МАКС_ИМЯ_РАЗБОР) m = МАКС_ИМЯ_РАЗБОР - 1;
            memcpy(р.перечисления[i], известные_перечисления->имена[i], m);
            р.перечисления[i][m] = '\0';
        }
        р.число_перечислений = n;
    }
    собрать_типопараметры(&р);  // распознать «T» в заголовках обобщённых функций
    Узел *программа = узел_создать(УЗЕЛ_ПРОГРАММА);

    while (!диаг->ошибка) {
        пропустить_разрывы(&р);
        Токен *t = текущий(&р);
        if (t->тип == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        if (t->тип == ТОКЕН_ПРЕПРОЦЕССОР) {
            Узел *преп = узел_создать(УЗЕЛ_ПРЕПРОЦЕССОР);
            преп->токен = *t;
            узел_добавить_ребёнка(программа, преп);
            р.позиция++;
            continue;
        }
        if (t->тип == ТОКЕН_СТРУКТУРА || t->тип == ТОКЕН_СОЮЗ
            || t->тип == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            узел_добавить_ребёнка(программа, разобрать_структуру(&р));
            continue;
        }
        if (t->тип == ТОКЕН_ПЕРЕЧИСЛЕНИЕ) {
            узел_добавить_ребёнка(программа, разобрать_перечисление(&р));
            continue;
        }
        if (t->тип == ТОКЕН_ТИПФУНКЦИИ) {
            узел_добавить_ребёнка(программа, разобрать_типфункции(&р));
            continue;
        }
        if (токен_начинает_тип(t->тип) || t->тип == ТОКЕН_СТАТИЧЕСКИЙ
            || t->тип == ТОКЕН_НИЧЕГО  // «ничего» — void-функция (только тип возврата)
            || токен_имя_структуры(&р, t) || токен_имя_перечисления(&р, t)
            || токен_это_типопараметр(&р, t)) {  // «T имя<T>(…)» — обобщённая функция
            узел_добавить_ребёнка(программа, разобрать_функцию(&р));
            continue;
        }
        ошибка_разбора(&р, t, "ожидалась директива или функция");
        break;
    }

    if (диаг->ошибка) {
        освободить_узел(программа);
        return nullptr;
    }
    return программа;
}

Узел *разобрать(МассивТокенов *токены, Диагностика *диаг)
{
    return разобрать_с_именами_структур(токены, диаг, nullptr, nullptr);
}
