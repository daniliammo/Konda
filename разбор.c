#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "аст.h"

typedef struct {
    МассивТокенов *токены;
    size_t позиция;
    Диагностика *диаг;
} Разборщик;

static Узел *разобрать_выражение(Разборщик *р);
static Узел *разобрать_блок(Разборщик *р, int с_скобками);

static int байты_равны(const char *a, size_t la, const char *b, size_t lb)
{
    return la == lb && memcmp(a, b, la) == 0;
}

static int токен_текст_равен(const Токен *t, const char *текст)
{
    return байты_равны(t->начало, t->длина, текст, strlen(текст));
}

static int токен_это_тип(ТипТокена t)
{
    return t == ТОКЕН_ЦЕЛОЕ8 || t == ТОКЕН_ЦЕЛОЕ16 || t == ТОКЕН_ЦЕЛОЕ32
        || t == ТОКЕН_ЦЕЛОЕ64 || t == ТОКЕН_БАЙТ
        || t == ТОКЕН_ВЕЩЕСТВЕННОЕ || t == ТОКЕН_ТИП_СИМВОЛ;
}

static int токен_это_целое(ТипТокена t)
{
    return t == ТОКЕН_ЦЕЛОЕ8 || t == ТОКЕН_ЦЕЛОЕ16
        || t == ТОКЕН_ЦЕЛОЕ32 || t == ТОКЕН_ЦЕЛОЕ64;
}

// Тип может начинаться с модификатора «неподписанный» или конструктора «срез».
static int токен_начинает_тип(ТипТокена t)
{
    return токен_это_тип(t) || t == ТОКЕН_НЕПОДПИСАННЫЙ || t == ТОКЕН_СРЕЗ;
}

static void ошибка_разбора(Разборщик *р, const Токен *t, const char *текст)
{
    if (р->диаг->ошибка) {
        return;
    }
    р->диаг->ошибка = 1;
    р->диаг->строка = t ? t->строка : 0;
    р->диаг->колонка = t ? t->позиция_в_строке : 0;
    snprintf(р->диаг->сообщение, sizeof(р->диаг->сообщение), "%s", текст);
}

// Токен, которым может закончиться оператор: после него перевод строки
// завершает оператор (модель A — как автоподстановка «;» в Go).
static int токен_завершает_оператор(ТипТокена т)
{
    switch (т) {
        case ТОКЕН_ИДЕНТИФИКАТОР:
        case ТОКЕН_ЧИСЛО:
        case ТОКЕН_СТРОКА:
        case ТОКЕН_СИМВОЛ:
        case ТОКЕН_НУЛЬ:
        case ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ:
        case ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ:
        case ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ:
        case ТОКЕН_ПРЕПРОЦЕССОР:
        case ТОКЕН_ВЕРНУТЬ:   // «вернуть» без значения в конце строки
            return 1;
        default:
            return 0;
    }
}

// Один проход перед разбором: решает, какие переводы строк значимы.
// Значимый перевод (вне «(» / «[» и после токена-завершителя) остаётся
// ТОКЕН_НОВАЯ_СТРОКА — это терминатор оператора. Остальные переводы —
// продолжение строки (внутри скобок, после оператора, запятой, «{» и т.п.) —
// гасятся в ТОКЕН_КОММЕНТАРИЙ, чтобы парсер их пропускал. Фигурные скобки
// глубину не считают: переводы внутри блока разделяют операторы.
static void разметить_переносы(МассивТокенов *токены)
{
    int глубина = 0;
    ТипТокена прежний = ТОКЕН_НОВАЯ_СТРОКА;  // в начале файла перевод незначим
    size_t n = токены->базовый.длина;
    for (size_t i = 0; i < n; ++i) {
        Токен *t = дин_массив_получить(&токены->базовый, i);
        if (t->тип == ТОКЕН_КОММЕНТАРИЙ) {
            continue;  // комментарий не меняет «прежний значимый токен»
        }
        if (t->тип == ТОКЕН_НОВАЯ_СТРОКА) {
            if (глубина > 0 || !токен_завершает_оператор(прежний)) {
                t->тип = ТОКЕН_КОММЕНТАРИЙ;  // продолжение строки — гасим
            }
            continue;  // перевод тоже не меняет «прежний»
        }
        if (t->тип == ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ
            || t->тип == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            глубина++;
        } else if (t->тип == ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
                   || t->тип == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
            if (глубина > 0) глубина--;
        }
        прежний = t->тип;
    }
}

// Пропускает только комментарии. Значимые переводы строк (терминаторы) видимы.
static void пропустить_служебные(Разборщик *р)
{
    while (р->позиция < р->токены->базовый.длина) {
        Токен *t = дин_массив_получить(&р->токены->базовый, р->позиция);
        if (t->тип == ТОКЕН_КОММЕНТАРИЙ) {
            р->позиция++;
            continue;
        }
        break;
    }
}

static Токен *текущий(Разборщик *р)
{
    пропустить_служебные(р);
    if (р->позиция >= р->токены->базовый.длина) {
        return дин_массив_получить(&р->токены->базовый, р->токены->базовый.длина - 1);
    }
    return дин_массив_получить(&р->токены->базовый, р->позиция);
}

static int тип_текущего(Разборщик *р)
{
    return текущий(р)->тип;
}

static int съесть(Разборщик *р, ТипТокена ожидаемый)
{
    Токен *t = текущий(р);
    if (t->тип != ожидаемый) {
        char msg[256];
        snprintf(msg, sizeof(msg), "ожидался токен %s, встретился %s «%.*s»",
                 имя_типа_токена(ожидаемый), имя_типа_токена(t->тип),
                 (int)t->длина, t->начало);
        ошибка_разбора(р, t, msg);
        return 0;
    }
    р->позиция++;
    return 1;
}

// Пропускает пустые строки и комментарии (значимые переводы строк тоже).
// Используется на границах операторов: между операторами в блоке, перед «{».
static void пропустить_разрывы(Разборщик *р)
{
    while (р->позиция < р->токены->базовый.длина) {
        Токен *t = дин_массив_получить(&р->токены->базовый, р->позиция);
        if (t->тип == ТОКЕН_КОММЕНТАРИЙ || t->тип == ТОКЕН_НОВАЯ_СТРОКА) {
            р->позиция++;
            continue;
        }
        break;
    }
}

// Завершение оператора: перевод строки, «;», либо позиция перед «}» / концом
// файла (там терминатор не нужен). Иначе — ошибка.
static void съесть_конец_оператора(Разборщик *р)
{
    int т = тип_текущего(р);
    if (т == ТОКЕН_НОВАЯ_СТРОКА || т == ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ) {
        р->позиция++;
        return;
    }
    if (т == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ || т == ТОКЕН_КОНЕЦ_ФАЙЛА) {
        return;
    }
    ошибка_разбора(р, текущий(р), "ожидался конец строки или «;»");
}

// Считывает тип с необязательным префиксом «неподписанный» или конструктором
// «срез<T>». Возвращает 1 при успехе; заполняет *тип, *беззнаковый и *срез.
// Для «срез<T>» *тип/*беззнаковый — это тип элемента T, *срез = 1.
static int съесть_тип(Разборщик *р, ТипТокена *тип, int *беззнаковый, int *срез)
{
    *беззнаковый = 0;
    *срез = 0;

    // «срез<T>»: тип-обёртка с проверкой границ. Внутри — обычный тип элемента.
    if (тип_текущего(р) == ТОКЕН_СРЕЗ) {
        *срез = 1;
        р->позиция++;
        if (тип_текущего(р) != ТОКЕН_СТРЕЛКА_ВЛЕВО
            || !токен_текст_равен(текущий(р), "<")) {
            ошибка_разбора(р, текущий(р), "ожидался «<» после «срез»");
            return 0;
        }
        р->позиция++;  // за «<»
        // Тип элемента (без вложенных срезов): [неподписанный] базовый.
        if (тип_текущего(р) == ТОКЕН_НЕПОДПИСАННЫЙ) {
            *беззнаковый = 1;
            р->позиция++;
        }
        Токен *э = текущий(р);
        if (!токен_это_тип(э->тип)) {
            ошибка_разбора(р, э, "ожидался тип элемента среза");
            return 0;
        }
        if (*беззнаковый && !токен_это_целое(э->тип)) {
            ошибка_разбора(р, э, "«неподписанный» применим только к целым типам");
            return 0;
        }
        *тип = э->тип;
        р->позиция++;  // за типом элемента
        if (тип_текущего(р) != ТОКЕН_СТРЕЛКА_ВПРАВО
            || !токен_текст_равен(текущий(р), ">")) {
            ошибка_разбора(р, текущий(р), "ожидался «>» после типа элемента среза");
            return 0;
        }
        р->позиция++;  // за «>»
        return 1;
    }

    if (тип_текущего(р) == ТОКЕН_НЕПОДПИСАННЫЙ) {
        *беззнаковый = 1;
        р->позиция++;
    }
    Токен *t = текущий(р);
    if (!токен_это_тип(t->тип)) {
        ошибка_разбора(р, t, "ожидался тип");
        return 0;
    }
    if (*беззнаковый && !токен_это_целое(t->тип)) {
        ошибка_разбора(р, t, "«неподписанный» применим только к целым типам");
        return 0;
    }
    *тип = t->тип;
    р->позиция++;
    return 1;
}

// Приоритет бинарного оператора; -1 если токен не бинарный оператор.
static int приоритет(const Токен *t)
{
    switch (t->тип) {
        case ТОКЕН_ЗВЕЗДА:
        case ТОКЕН_КОСАЯ_ЧЕРТА:
        case ТОКЕН_ПРОЦЕНТ:
            return 3;
        case ТОКЕН_ПЛЮС:
        case ТОКЕН_МИНУС:
            return 2;
        case ТОКЕН_СТРЕЛКА_ВПРАВО:  // > и >=
        case ТОКЕН_СТРЕЛКА_ВЛЕВО:   // < и <=
            return 1;
        case ТОКЕН_РАВНО:
            // == — сравнение; одиночное «=» (присваивание) не является
            // оператором выражения и обрабатывается на уровне оператора.
            return токен_текст_равен(t, "==") ? 1 : -1;
        default:
            return -1;
    }
}

static Узел *разобрать_первичное(Разборщик *р)
{
    Токен *t = текущий(р);

    // Унарный минус: -выражение. Унарных операторов в языке больше нет.
    if (t->тип == ТОКЕН_МИНУС) {
        Токен оператор = *t;
        р->позиция++;
        Узел *уноп = узел_создать(УЗЕЛ_УНОП);
        уноп->токен = оператор;
        узел_добавить_ребёнка(уноп, разобрать_первичное(р));
        return уноп;
    }

    if (t->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
        Токен имя = *t;
        р->позиция++;

        if (тип_текущего(р) == ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
            Узел *вызов = узел_создать(УЗЕЛ_ВЫЗОВ);
            вызов->токен = имя;
            if (тип_текущего(р) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
                узел_добавить_ребёнка(вызов, разобрать_выражение(р));
                while (!р->диаг->ошибка && тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
                    съесть(р, ТОКЕН_ЗАПЯТАЯ);
                    узел_добавить_ребёнка(вызов, разобрать_выражение(р));
                }
            }
            съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
            return вызов;
        }

        if (тип_текущего(р) == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
            Узел *индекс = узел_создать(УЗЕЛ_ИНДЕКС);
            индекс->токен = имя;
            узел_добавить_ребёнка(индекс, разобрать_выражение(р));
            съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
            return индекс;
        }

        Узел *идент = узел_создать(УЗЕЛ_ИДЕНТИФИКАТОР);
        идент->токен = имя;
        return идент;
    }

    if (t->тип == ТОКЕН_ЧИСЛО) {
        Узел *узел = узел_создать(УЗЕЛ_ЧИСЛО);
        узел->токен = *t;
        р->позиция++;
        return узел;
    }

    if (t->тип == ТОКЕН_СТРОКА) {
        Узел *узел = узел_создать(УЗЕЛ_СТРОКА);
        узел->токен = *t;
        р->позиция++;
        return узел;
    }

    if (t->тип == ТОКЕН_НУЛЬ) {
        Узел *узел = узел_создать(УЗЕЛ_НУЛЬ);
        узел->токен = *t;
        р->позиция++;
        return узел;
    }

    if (t->тип == ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
        съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
        Узел *внутри = разобрать_выражение(р);
        съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
        return внутри;  // приоритет уже закодирован структурой дерева
    }

    ошибка_разбора(р, t, "неожиданное выражение");
    Узел *пустой = узел_создать(УЗЕЛ_ЧИСЛО);
    пустой->токен = *t;
    return пустой;
}

// Текст для синтезированного оператора «>=» (когда записано «=>»).
static const char ОПЕРАТОР_БОЛЬШЕ_РАВНО[] = ">=";

static Узел *присоединить_биноп(Узел *лево, Токен оператор, Узел *право)
{
    Узел *бин = узел_создать(УЗЕЛ_БИНОП);
    бин->токен = оператор;
    узел_добавить_ребёнка(бин, лево);
    узел_добавить_ребёнка(бин, право);
    return бин;
}

static Узел *разобрать_бинарное(Разборщик *р, int мин_приоритет)
{
    Узел *лево = разобрать_первичное(р);

    while (!р->диаг->ошибка) {
        Токен *op = текущий(р);

        // «=>» — альтернативная запись «>=» (токены «=» затем «>»).
        if (op->тип == ТОКЕН_РАВНО && токен_текст_равен(op, "=")) {
            size_t сохранение = р->позиция;
            р->позиция++;  // за «=»
            Токен *nx = текущий(р);
            if (nx->тип == ТОКЕН_СТРЕЛКА_ВПРАВО && токен_текст_равен(nx, ">")) {
                int prec = 1;  // приоритет сравнения
                if (prec < мин_приоритет) {
                    р->позиция = сохранение;  // не наш уровень — вернуть «=>» наружу
                    break;
                }
                р->позиция++;  // за «>»
                Токен оператор = *op;
                оператор.тип = ТОКЕН_СТРЕЛКА_ВПРАВО;
                оператор.начало = ОПЕРАТОР_БОЛЬШЕ_РАВНО;
                оператор.длина = 2;
                лево = присоединить_биноп(лево, оператор, разобрать_бинарное(р, prec + 1));
                continue;
            }
            р->позиция = сохранение;  // не «=>», обработать «=» как обычно
            op = текущий(р);
        }

        int prec = приоритет(op);
        if (prec < мин_приоритет || prec < 0) {
            break;
        }
        Токен оператор = *op;
        р->позиция++;
        лево = присоединить_биноп(лево, оператор, разобрать_бинарное(р, prec + 1));
    }

    return лево;
}

static Узел *разобрать_выражение(Разборщик *р)
{
    return разобрать_бинарное(р, 0);
}

static Узел *разобрать_объявление(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ОБЪЯВЛЕНИЕ);
    if (!съесть_тип(р, &узел->тип_данных, &узел->беззнаковый, &узел->срез)) {
        return узел;
    }

    // Указатели в объявлении: «тип* имя», «тип** имя» и т.д.
    while (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
        съесть(р, ТОКЕН_ЗВЕЗДА);
        узел->указатели++;
    }

    Токен *имя = текущий(р);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        ошибка_разбора(р, имя, "ожидалось имя переменной");
        return узел;
    }
    узел->токен = *имя;
    р->позиция++;

    if (тип_текущего(р) == ТОКЕН_РАВНО) {
        съесть(р, ТОКЕН_РАВНО);
        узел_добавить_ребёнка(узел, разобрать_выражение(р));
    }

    съесть_конец_оператора(р);
    return узел;
}

static Узел *разобрать_если(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ЕСЛИ);
    съесть(р, ТОКЕН_ЕСЛИ);
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_выражение(р));  // условие
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_блок(р, 1));     // тогда

    if (тип_текущего(р) == ТОКЕН_ИНАЧЕ) {
        съесть(р, ТОКЕН_ИНАЧЕ);
        узел_добавить_ребёнка(узел, разобрать_блок(р, 1)); // иначе
    }
    return узел;
}

static Узел *разобрать_пока(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ПОКА);
    съесть(р, ТОКЕН_ПОКА);
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_выражение(р));  // условие
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    узел_добавить_ребёнка(узел, разобрать_блок(р, 1));     // тело
    return узел;
}

static Узел *разобрать_возврат(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ВОЗВРАТ);
    Токен *t = текущий(р);
    узел->токен = *t;
    съесть(р, ТОКЕН_ВЕРНУТЬ);
    int т = тип_текущего(р);
    if (т != ТОКЕН_НОВАЯ_СТРОКА && т != ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ
        && т != ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ && т != ТОКЕН_КОНЕЦ_ФАЙЛА) {
        узел_добавить_ребёнка(узел, разобрать_выражение(р));
    }
    съесть_конец_оператора(р);
    return узел;
}

// Присваивание «цель = значение» (без завершающей «;»). Если присваивания нет,
// возвращает само разобранное выражение (например, вызов функции).
static Узел *разобрать_выражение_или_присваивание(Разборщик *р)
{
    Узел *лево = разобрать_выражение(р);
    if (!р->диаг->ошибка && тип_текущего(р) == ТОКЕН_РАВНО
        && токен_текст_равен(текущий(р), "=")) {
        съесть(р, ТОКЕН_РАВНО);
        Узел *присв = узел_создать(УЗЕЛ_ПРИСВАИВАНИЕ);
        узел_добавить_ребёнка(присв, лево);                 // цель
        узел_добавить_ребёнка(присв, разобрать_выражение(р)); // значение
        return присв;
    }
    return лево;
}

static Узел *разобрать_для(Разборщик *р)
{
    Узел *узел = узел_создать(УЗЕЛ_ДЛЯ);
    съесть(р, ТОКЕН_ДЛЯ);
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);

    // Инициализация: объявление (само съедает «;») либо выражение/присваивание.
    if (токен_начинает_тип(тип_текущего(р))) {
        узел_добавить_ребёнка(узел, разобрать_объявление(р));
    } else {
        узел_добавить_ребёнка(узел, разобрать_выражение_или_присваивание(р));
        съесть(р, ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ);
    }

    узел_добавить_ребёнка(узел, разобрать_выражение(р));  // условие
    съесть(р, ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ);
    узел_добавить_ребёнка(узел, разобрать_выражение_или_присваивание(р)); // шаг
    съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);

    узел_добавить_ребёнка(узел, разобрать_блок(р, 1));    // тело — последний ребёнок
    return узел;
}

static Узел *разобрать_оператор(Разборщик *р)
{
    пропустить_служебные(р);

    if (тип_текущего(р) == ТОКЕН_ЕСЛИ) {
        return разобрать_если(р);
    }
    if (тип_текущего(р) == ТОКЕН_ПОКА) {
        return разобрать_пока(р);
    }
    if (тип_текущего(р) == ТОКЕН_ДЛЯ) {
        return разобрать_для(р);
    }
    if (тип_текущего(р) == ТОКЕН_ВЕРНУТЬ) {
        return разобрать_возврат(р);
    }

    Токен *t = текущий(р);
    if (токен_начинает_тип(t->тип)) {
        return разобрать_объявление(р);
    }

    if (t->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
        Узел *выр = разобрать_выражение_или_присваивание(р);
        if (!р->диаг->ошибка && выр->тип != УЗЕЛ_ВЫЗОВ && выр->тип != УЗЕЛ_ПРИСВАИВАНИЕ) {
            ошибка_разбора(р, t, "ожидался вызов функции или присваивание");
        }
        съесть_конец_оператора(р);
        return выр;
    }

    ошибка_разбора(р, t, "неожиданный оператор");
    return узел_создать(УЗЕЛ_БЛОК);
}

static Узел *разобрать_блок(Разборщик *р, int с_скобками)
{
    Узел *блок = узел_создать(УЗЕЛ_БЛОК);
    if (с_скобками) {
        пропустить_разрывы(р);  // «{» может стоять на следующей строке
        if (!съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
            return блок;
        }
    }

    while (!р->диаг->ошибка) {
        пропустить_разрывы(р);  // пустые строки между операторами
        int т = тип_текущего(р);
        if (т == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ || т == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        узел_добавить_ребёнка(блок, разобрать_оператор(р));
    }

    if (с_скобками) {
        съесть(р, ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    }
    return блок;
}

static Узел *разобрать_параметр(Разборщик *р)
{
    Узел *пар = узел_создать(УЗЕЛ_ПАРАМЕТР);
    if (!съесть_тип(р, &пар->тип_данных, &пар->беззнаковый, &пар->срез)) {
        return пар;
    }

    while (!р->диаг->ошибка
           && (тип_текущего(р) == ТОКЕН_ЗВЕЗДА
               || тип_текущего(р) == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        if (тип_текущего(р) == ТОКЕН_ЗВЕЗДА) {
            съесть(р, ТОКЕН_ЗВЕЗДА);
            пар->указатели++;
        } else {
            съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
            съесть(р, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
            пар->массив = 1;
        }
    }

    Токен *имя = текущий(р);
    if (имя->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
        пар->токен = *имя;
        р->позиция++;
    }
    return пар;
}

static Узел *разобрать_функцию(Разборщик *р)
{
    Узел *функция = узел_создать(УЗЕЛ_ФУНКЦИЯ);
    if (!съесть_тип(р, &функция->тип_данных, &функция->беззнаковый, &функция->срез)) {
        return функция;
    }
    if (функция->срез) {
        ошибка_разбора(р, текущий(р),
            "тип возврата «срез» пока не поддерживается");
        return функция;
    }

    Токен *имя = текущий(р);
    if (имя->тип == ТОКЕН_ТОЧКА_ВХОДА) {
        функция->это_main = 1;
    } else if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        ошибка_разбора(р, имя, "ожидалось имя функции или точка_входа");
        return функция;
    }
    функция->токен = *имя;
    р->позиция++;

    if (!съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        return функция;
    }

    if (функция->это_main) {
        // Параметры точки входа игнорируются: подставляется argc/argv.
        while (!р->диаг->ошибка && тип_текущего(р) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
               && тип_текущего(р) != ТОКЕН_КОНЕЦ_ФАЙЛА) {
            р->позиция++;
        }
    } else {
        while (!р->диаг->ошибка && тип_текущего(р) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
               && тип_текущего(р) != ТОКЕН_КОНЕЦ_ФАЙЛА) {
            Токен *pt = текущий(р);
            if (токен_начинает_тип(pt->тип)) {
                узел_добавить_ребёнка(функция, разобрать_параметр(р));
            } else {
                р->позиция++;
            }
            if (тип_текущего(р) == ТОКЕН_ЗАПЯТАЯ) {
                съесть(р, ТОКЕН_ЗАПЯТАЯ);
            }
        }
    }

    if (!съесть(р, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
        return функция;
    }

    узел_добавить_ребёнка(функция, разобрать_блок(р, 1));  // тело — последний ребёнок
    return функция;
}

Узел *разобрать(МассивТокенов *токены, Диагностика *диаг)
{
    разметить_переносы(токены);  // модель A: значимые переводы строк = терминаторы

    Разборщик р = { .токены = токены, .позиция = 0, .диаг = диаг };
    Узел *программа = узел_создать(УЗЕЛ_ПРОГРАММА);

    while (!диаг->ошибка) {
        пропустить_разрывы(&р);
        Токен *t = текущий(&р);
        if (t->тип == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        if (t->тип == ТОКЕН_ПРЕПРОЦЕССОР) {
            Узел *преп = узел_создать(УЗЕЛ_ПРЕПРОЦЕССОР);
            преп->токен = *t;
            узел_добавить_ребёнка(программа, преп);
            р.позиция++;
            continue;
        }
        if (токен_начинает_тип(t->тип)) {
            узел_добавить_ребёнка(программа, разобрать_функцию(&р));
            continue;
        }
        ошибка_разбора(&р, t, "ожидалась директива или функция");
        break;
    }

    if (диаг->ошибка) {
        освободить_узел(программа);
        return nullptr;
    }
    return программа;
}
