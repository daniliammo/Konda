# Модель A: перевод строки завершает оператор, «;» не нужна. Проверяем все
# конструкции без «;»: объявления, присваивания, циклы, если/иначе, вернуть,
# продолжение строки по висящему оператору и многострочный вызов в скобках.
cat > nosemi.конда <<'KONDA'
#содержит <stdio.h>

целое32 удвоить(целое32 х)
{
    целое32 р = х + х
    вернуть р
}

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое32 сумма = 0
    для (целое32 и = 1; и <= 5; и = и + 1) {
        сумма = сумма + и
    }
