# Модификатор «неподписанный»: целоеN → uintN_t в объявлениях, параметрах
# и типе возврата; к нецелому типу — ошибка.
cat > unsigned.конда <<'KONDA'
#содержит <stdio.h>

неподписанный целое32 удвоить(неподписанный целое16 х)
