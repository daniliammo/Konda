#!/bin/sh
set -eu

ROOT=$(CDPATH= cd -- "$(dirname "$0")/.." && pwd)
BIN="$ROOT/Собранное/Транспилятор"
TMP=$(mktemp -d)

cleanup() {
    rm -rf "$TMP"
}
trap cleanup EXIT

fail() {
    printf 'FAIL: %s\n' "$1" >&2
    exit 1
}

assert_eq() {
    expected=$1
    actual=$2
    name=$3
    if [ "$actual" != "$expected" ]; then
        printf 'FAIL: %s\nexpected: %s\nactual:   %s\n' "$name" "$expected" "$actual" >&2
        exit 1
    fi
}

make -C "$ROOT"

cd "$TMP"

# тест.конда — это «песочница», её содержимое меняется. Здесь только
# дымовой тест: должна транспилироваться и компилироваться без ошибок.
"$BIN" "$ROOT/тест.конда" >"$TMP/build.log" 2>&1 || {
    cat "$TMP/build.log" >&2
    fail "пример тест.конда должен транспилироваться и компилироваться"
}

# Поведение printf (передача аргументов как есть, без подстановки "%s\n")
# проверяем на собственной стабильной фикстуре.
cat > basic.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов > 1) {
        printf(аргументы[1]);
    } иначе {
        printf("нет аргумента");
    }
}
KONDA

"$BIN" basic.конда >"$TMP/basic.log" 2>&1 || {
    cat "$TMP/basic.log" >&2
    fail "базовый пример должен транспилироваться и компилироваться"
}
assert_eq "hello" "$(./вывод/basic.elf hello)" "printf передаёт аргумент как есть"
assert_eq "нет аргумента" "$(./вывод/basic.elf)" "printf со строковым литералом"

cat > ge.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов >= 2) {
        printf(аргументы[1]);
    } иначе {
        printf("мало аргументов");
    }
}
KONDA

"$BIN" ge.конда >"$TMP/ge.log" 2>&1 || {
    cat "$TMP/ge.log" >&2
    fail "условие >= должно поддерживаться"
}
assert_eq "ok" "$(./вывод/ge.elf ok)" "условие >="

# Альтернативная запись => вместо >= (токены «=» затем «>»). Должна задавать
# то же ограничение длины, что и >=, и компилироваться.
cat > arrow.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов => 2) {
        printf(аргументы[1]);
    } иначе {
        printf("мало аргументов");
    }
}
KONDA

"$BIN" arrow.конда >"$TMP/arrow.log" 2>&1 || {
    cat "$TMP/arrow.log" >&2
    fail "условие => (альтернатива >=) должно поддерживаться"
}
assert_eq "ok" "$(./вывод/arrow.elf ok)" "условие =>"

# Альтернативная запись «=>» эквивалентна «>=».
cat > ge2.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов => 2) {
        printf(аргументы[1]);
    } иначе {
        printf("мало аргументов");
    }
}
KONDA

"$BIN" ge2.конда >"$TMP/ge2.log" 2>&1 || {
    cat "$TMP/ge2.log" >&2
    fail "условие => (альтернатива >=) должно поддерживаться"
}
assert_eq "ok" "$(./вывод/ge2.elf ok)" "условие => как >="

# Приоритет операторов: сейчас он перекладывается на C, и AST обязан это
# поведение сохранить. 2 + 3 * 4 = 14, а не 20.
cat > prec.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    printf("%d\n", 2 + 3 * 4);
}
KONDA

"$BIN" prec.конда >"$TMP/prec.log" 2>&1 || {
    cat "$TMP/prec.log" >&2
    fail "пример с арифметикой должен транспилироваться и компилироваться"
}
assert_eq "14" "$(./вывод/prec.elf)" "приоритет операторов: * раньше +"

# Вложенный если/иначе: проверяем вложенность блоков и стек ограничений.
cat > nest.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов > 1) {
        если (количество_аргументов > 2) {
            printf("два+\n");
        } иначе {
            printf("ровно один\n");
        }
    } иначе {
        printf("ноль\n");
    }
}
KONDA

"$BIN" nest.конда >"$TMP/nest.log" 2>&1 || {
    cat "$TMP/nest.log" >&2
    fail "вложенный если должен транспилироваться и компилироваться"
}
assert_eq "ноль"       "$(./вывод/nest.elf)"     "вложенный если: внешняя иначе"
assert_eq "ровно один" "$(./вывод/nest.elf a)"   "вложенный если: внутренняя иначе"
assert_eq "два+"       "$(./вывод/nest.elf a b)" "вложенный если: внутренняя then"

# Объявления переменных + арифметика над ними.
cat > vars.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое32 икс = 25;
    целое32 игрек = 5;
    printf("%d\n", икс + игрек);
}
KONDA

"$BIN" vars.конда >"$TMP/vars.log" 2>&1 || {
    cat "$TMP/vars.log" >&2
    fail "объявления переменных должны транспилироваться и компилироваться"
}
assert_eq "30" "$(./вывод/vars.elf)" "переменные и сложение"

# Ключевое слово «нуль» → nullptr. Рантайм-представление указателя
# платформозависимо, поэтому проверяем сгенерированный C детерминированно.
cat > nullkw.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    printf("%p\n", нуль);
}
KONDA

"$BIN" nullkw.конда >"$TMP/nullkw.log" 2>&1 || {
    cat "$TMP/nullkw.log" >&2
    fail "«нуль» должен транспилироваться и компилироваться"
}
grep -q "nullptr" вывод/nullkw.c || {
    cat вывод/nullkw.c >&2
    fail "«нуль» должен генерировать nullptr"
}

# Целочисленные типы по разрядности: целое8/16/32/64 → int8_t/16/32/64_t.
# Проверяем и сгенерированный C (детерминированно), и реальный вывод.
cat > ints.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое8 а = 1;
    целое16 б = 2;
    целое32 в = 3;
    целое64 г = 4;
    printf("%d %d %d %d\n", а, б, в, г);
}
KONDA

"$BIN" ints.конда >"$TMP/ints.log" 2>&1 || {
    cat "$TMP/ints.log" >&2
    fail "целочисленные типы должны транспилироваться и компилироваться"
}
grep -q "int8_t а"  вывод/ints.c || { cat вывод/ints.c >&2; fail "целое8 → int8_t"; }
grep -q "int16_t б" вывод/ints.c || { cat вывод/ints.c >&2; fail "целое16 → int16_t"; }
grep -q "int32_t в" вывод/ints.c || { cat вывод/ints.c >&2; fail "целое32 → int32_t"; }
grep -q "int64_t г" вывод/ints.c || { cat вывод/ints.c >&2; fail "целое64 → int64_t"; }
assert_eq "1 2 3 4" "$(./вывод/ints.elf)" "целочисленные типы: вывод"

# Остальные примитивы в объявлениях: байт → uint8_t, вещественное → float,
# символ → char.
cat > types.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    байт б = 7;
    вещественное в = 1;
    символ с = 65;
    printf("%d %.0f %c\n", б, в, с);
}
KONDA

"$BIN" types.конда >"$TMP/types.log" 2>&1 || {
    cat "$TMP/types.log" >&2
    fail "примитивные типы должны транспилироваться и компилироваться"
}
grep -q "uint8_t б" вывод/types.c || { cat вывод/types.c >&2; fail "байт → uint8_t"; }
grep -q "float в"   вывод/types.c || { cat вывод/types.c >&2; fail "вещественное → float"; }
grep -q "char с"    вывод/types.c || { cat вывод/types.c >&2; fail "символ → char"; }
assert_eq "7 1 A" "$(./вывод/types.elf)" "примитивные типы: вывод"

# Многоаргументные вызовы: аргументы передаются как есть, в исходном порядке,
# через запятую. Проверяем вызов с тремя и более аргументами.
cat > multiarg.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое32 а = 10;
    целое32 б = 20;
    целое32 в = 30;
    printf("%d %d %d\n", а, б, в);
    printf("%d\n", а + б * в);
}
KONDA

"$BIN" multiarg.конда >"$TMP/multiarg.log" 2>&1 || {
    cat "$TMP/multiarg.log" >&2
    fail "многоаргументный вызов должен транспилироваться и компилироваться"
}
assert_eq "10 20 30" "$(./вывод/multiarg.elf | head -n1)" "многоаргументный printf: три аргумента"
assert_eq "610"      "$(./вывод/multiarg.elf | tail -n1)" "многоаргументный printf: приоритет в аргументе"

# Циклы «для»/«пока», присваивание, унарный минус и «вернуть».
cat > control.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое32 сумма = 0;
    для (целое32 и = 1; и <= 5; и = и + 1) {
        сумма = сумма + и;
    }
    целое32 н = 3;
    пока (н > 0) {
        н = н - 1;
    }
    целое32 отр = -сумма;
    printf("%d %d %d\n", сумма, н, отр);
    вернуть 0;
}
KONDA

"$BIN" control.конда >"$TMP/control.log" 2>&1 || {
    cat "$TMP/control.log" >&2
    fail "циклы/присваивание/унарный минус/вернуть должны транспилироваться"
}
grep -q "for ("   вывод/control.c || { cat вывод/control.c >&2; fail "для → for"; }
grep -q "while (" вывод/control.c || { cat вывод/control.c >&2; fail "пока → while"; }
assert_eq "15 0 -15" "$(./вывод/control.elf)" "циклы, присваивание, унарный минус"

# Явный «вернуть N» должен заменять неявный «return 0;» (один return в теле).
cat > ret.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    printf("выход 7\n");
    вернуть 7;
}
KONDA

"$BIN" ret.конда >"$TMP/ret.log" 2>&1 || {
    cat "$TMP/ret.log" >&2
    fail "«вернуть N» должен транспилироваться"
}
assert_eq "1" "$(grep -c 'return' вывод/ret.c)" "явный вернуть не дублируется неявным return 0"
./вывод/ret.elf >/dev/null && rc=0 || rc=$?
assert_eq "7" "$rc" "вернуть 7 задаёт код возврата"

# Указатели в объявлениях тела функции: «тип* имя» → «тип *имя».
cat > ptr.конда <<'KONDA'
