# Sink-вернуть: ветвь, которая безусловно возвращает, не вливается в merge.
# Move в такой ветви — корректно, после if срез всё ещё жив для fallthrough.
cat > move_if_sink.конда <<'KONDA'
#содержит <stdio.h>

целое32 потребить(владеет срез<целое32> с)
{
    вернуть 0
}

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    срез<целое32> а = выделить(2)
    а[0] = 11
    если (количество_аргументов > 1) {
        потребить(а)
        вернуть 0
    }
    printf("после if %d\n", а[0])
    вернуть 0
}
KONDA

"$BIN" move_if_sink.конда >"$TMP/move_if_sink.log" 2>&1 || {
    cat "$TMP/move_if_sink.log" >&2
    fail "sink-вернуть в ветви должен сниматься с merge"
}
assert_eq "после if 11" "$(./вывод/move_if_sink.elf)" "fallthrough после if: а жив"
# С аргументом — ветвь с потребить+вернуть, фолбэк не достигается.
assert_eq "" "$(./вывод/move_if_sink.elf x)" "ветвь с потребить+вернуть: ничего не печатает"

# Move источника из объемлющей области ВНУТРИ цикла — запрещён: на втором
