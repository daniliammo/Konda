# ─── Multi-file: транспиляция нескольких .конда как одной программы ─────────

# Функция в одном файле, main в другом: семантика видит сигнатуру сразу
# (signature-pass работает на объединённом AST), кодоген эмитит прототип
# в начале сгенерированного C, так что вызов до определения корректен.
cat > mf_модуль.конда <<'KONDA'
целое32 удвоить(целое32 х)
{
    вернуть х * 2
}
KONDA
cat > mf_главный.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    printf("%d\n", удвоить(21))
    вернуть 0
}
KONDA

"$BIN" mf_главный.конда mf_модуль.конда >"$TMP/mf.log" 2>&1 || {
    cat "$TMP/mf.log" >&2
    fail "multi-file: вызов функции из соседнего файла должен работать"
}
# Прототип должен быть до main.
grep -q "^int32_t удвоить(int32_t х);" вывод/mf_главный.c || {
    cat вывод/mf_главный.c >&2
    fail "multi-file: forward-прототип отсутствует"
}
assert_eq "42" "$(./вывод/mf_главный.elf)" "вызов удвоить(21) из соседнего файла → 42"

# Структура объявлена в одном файле, используется в другом: typedef
