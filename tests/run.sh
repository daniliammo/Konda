# ─── Path-sensitive move владения (если/иначе + sink-вернуть, запрет в цикле) ─

# Симметричный move в обеих ветвях если/иначе: разрешён, после if владение
# одинаково перемещено в обоих случаях → autofree корректен.
cat > move_if_both.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    срез<целое32> а = выделить(2)
    а[0] = 7
    если (количество_аргументов > 1) {
        срез<целое32> б = а
        printf("тогда %d\n", б[0])
    } иначе {
        срез<целое32> в = а
        printf("иначе %d\n", в[0])
    }
    вернуть 0
}
KONDA

"$BIN" move_if_both.конда >"$TMP/move_if_both.log" 2>&1 || {
    cat "$TMP/move_if_both.log" >&2
    fail "симметричный move в обеих ветвях должен компилироваться"
}
assert_eq "иначе 7" "$(./вывод/move_if_both.elf)" "ветвь иначе: владение перешло в в"
assert_eq "тогда 7" "$(./вывод/move_if_both.elf x)" "ветвь тогда: владение перешло в б"

# Асимметричный move (в одной ветви — да, в другой — нет): ошибка компиляции
# с понятной диагностикой про двусмысленное освобождение.
cat > move_if_one.конда <<'KONDA'
