grep -q "uint8_t а"            вывод/unsigned.c || { cat вывод/unsigned.c >&2; fail "неподписанный целое8 → uint8_t"; }
grep -q "uint64_t б"           вывод/unsigned.c || { cat вывод/unsigned.c >&2; fail "неподписанный целое64 → uint64_t"; }
grep -q "uint32_t удвоить"     вывод/unsigned.c || { cat вывод/unsigned.c >&2; fail "неподписанный в типе возврата"; }
