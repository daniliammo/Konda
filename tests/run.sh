grep -q "неизвестная переменная" "$TMP/необъявленная_переменная.log" || {
    cat "$TMP/необъявленная_переменная.log" >&2
    fail "ошибка необъявленной переменной должна быть понятной"
}

# Повторное объявление в одной области — ошибка.
cat > повторное_объявление.конда <<'KONDA'
