}
grep -q "char \*сообщение"  вывод/ptr.c || { cat вывод/ptr.c >&2; fail "символ* → char *"; }
grep -q "int32_t \*указ"    вывод/ptr.c || { cat вывод/ptr.c >&2; fail "целое32* → int32_t *"; }
assert_eq "привет" "$(./вывод/ptr.elf)" "указатель на строку"

# Таблица символов: обращение к необъявленной переменной — ошибка.
