#!/bin/sh
set -eu

ROOT=$(CDPATH= cd -- "$(dirname "$0")/.." && pwd)
BIN="$ROOT/Собранное/Транспилятор"
TMP=$(mktemp -d)

cleanup() {
    rm -rf "$TMP"
}
trap cleanup EXIT

fail() {
    printf 'FAIL: %s\n' "$1" >&2
    exit 1
}

assert_eq() {
    expected=$1
    actual=$2
    name=$3
    if [ "$actual" != "$expected" ]; then
        printf 'FAIL: %s\nexpected: %s\nactual:   %s\n' "$name" "$expected" "$actual" >&2
        exit 1
    fi
}

make -C "$ROOT"

cd "$TMP"

# тест.конда — это «песочница», её содержимое меняется. Здесь только
# дымовой тест: должна транспилироваться и компилироваться без ошибок.
"$BIN" "$ROOT/tests/тест.конда" >"$TMP/сборка.log" 2>&1 || {
    cat "$TMP/сборка.log" >&2
    fail "пример тест.конда должен транспилироваться и компилироваться"
}

# Поведение printf (передача аргументов как есть, без подстановки "%s\n")
# проверяем на собственной стабильной фикстуре.
cat > базовый_printf.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов > 1) {
        printf(аргументы[1]);
    } иначе {
        printf("нет аргумента");
    }
}
KONDA

"$BIN" базовый_printf.конда >"$TMP/базовый_printf.log" 2>&1 || {
    cat "$TMP/базовый_printf.log" >&2
    fail "базовый пример должен транспилироваться и компилироваться"
}
assert_eq "hello" "$(./вывод/базовый_printf.elf hello)" "printf передаёт аргумент как есть"
assert_eq "нет аргумента" "$(./вывод/базовый_printf.elf)" "printf со строковым литералом"

cat > больше_или_равно.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов >= 2) {
        printf(аргументы[1]);
    } иначе {
        printf("мало аргументов");
    }
}
KONDA

"$BIN" больше_или_равно.конда >"$TMP/больше_или_равно.log" 2>&1 || {
    cat "$TMP/больше_или_равно.log" >&2
    fail "условие >= должно поддерживаться"
}
assert_eq "ok" "$(./вывод/больше_или_равно.elf ok)" "условие >="

# Альтернативная запись => вместо >= (токены «=» затем «>»). Должна задавать
# то же ограничение длины, что и >=, и компилироваться.
cat > стрелка_больше_равно.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов => 2) {
        printf(аргументы[1]);
    } иначе {
        printf("мало аргументов");
    }
}
KONDA

"$BIN" стрелка_больше_равно.конда >"$TMP/стрелка_больше_равно.log" 2>&1 || {
    cat "$TMP/стрелка_больше_равно.log" >&2
    fail "условие => (альтернатива >=) должно поддерживаться"
}
assert_eq "ok" "$(./вывод/стрелка_больше_равно.elf ok)" "условие =>"

# Альтернативная запись «=>» эквивалентна «>=».
cat > больше_или_равно_2.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов => 2) {
        printf(аргументы[1]);
    } иначе {
        printf("мало аргументов");
    }
}
KONDA

"$BIN" больше_или_равно_2.конда >"$TMP/больше_или_равно_2.log" 2>&1 || {
    cat "$TMP/больше_или_равно_2.log" >&2
    fail "условие => (альтернатива >=) должно поддерживаться"
}
assert_eq "ok" "$(./вывод/больше_или_равно_2.elf ok)" "условие => как >="

# Приоритет операторов: сейчас он перекладывается на C, и AST обязан это
# поведение сохранить. 2 + 3 * 4 = 14, а не 20.
cat > приоритет_операторов.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    printf("%d\n", 2 + 3 * 4);
}
KONDA

"$BIN" приоритет_операторов.конда >"$TMP/приоритет_операторов.log" 2>&1 || {
    cat "$TMP/приоритет_операторов.log" >&2
    fail "пример с арифметикой должен транспилироваться и компилироваться"
}
assert_eq "14" "$(./вывод/приоритет_операторов.elf)" "приоритет операторов: * раньше +"

# Вложенный если/иначе: проверяем вложенность блоков и стек ограничений.
cat > вложенный_если.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    если (количество_аргументов > 1) {
        если (количество_аргументов > 2) {
            printf("два+\n");
        } иначе {
            printf("ровно один\n");
        }
    } иначе {
        printf("ноль\n");
    }
}
KONDA

"$BIN" вложенный_если.конда >"$TMP/вложенный_если.log" 2>&1 || {
    cat "$TMP/вложенный_если.log" >&2
    fail "вложенный если должен транспилироваться и компилироваться"
}
assert_eq "ноль"       "$(./вывод/вложенный_если.elf)"     "вложенный если: внешняя иначе"
assert_eq "ровно один" "$(./вывод/вложенный_если.elf a)"   "вложенный если: внутренняя иначе"
assert_eq "два+"       "$(./вывод/вложенный_если.elf a b)" "вложенный если: внутренняя then"

# Объявления переменных + арифметика над ними.
cat > переменные.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое32 икс = 25;
    целое32 игрек = 5;
    printf("%d\n", икс + игрек);
}
KONDA

"$BIN" переменные.конда >"$TMP/переменные.log" 2>&1 || {
    cat "$TMP/переменные.log" >&2
    fail "объявления переменных должны транспилироваться и компилироваться"
}
assert_eq "30" "$(./вывод/переменные.elf)" "переменные и сложение"

# Ключевое слово «нуль» → nullptr. Рантайм-представление указателя
# платформозависимо, поэтому проверяем сгенерированный C детерминированно.
cat > ключевое_слово_нуль.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    printf("%p\n", нуль);
}
KONDA

"$BIN" ключевое_слово_нуль.конда >"$TMP/ключевое_слово_нуль.log" 2>&1 || {
    cat "$TMP/ключевое_слово_нуль.log" >&2
    fail "«нуль» должен транспилироваться и компилироваться"
}
grep -q "nullptr" вывод/ключевое_слово_нуль.c || {
    cat вывод/ключевое_слово_нуль.c >&2
    fail "«нуль» должен генерировать nullptr"
}

# Целочисленные типы по разрядности: целое8/16/32/64 → int8_t/16/32/64_t.
# Проверяем и сгенерированный C (детерминированно), и реальный вывод.
cat > целочисленные_типы.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое8 а = 1;
    целое16 б = 2;
    целое32 в = 3;
    целое64 г = 4;
    printf("%d %d %d %d\n", а, б, в, г);
}
KONDA

"$BIN" целочисленные_типы.конда >"$TMP/целочисленные_типы.log" 2>&1 || {
    cat "$TMP/целочисленные_типы.log" >&2
    fail "целочисленные типы должны транспилироваться и компилироваться"
}
grep -q "int8_t а"  вывод/целочисленные_типы.c || { cat вывод/целочисленные_типы.c >&2; fail "целое8 → int8_t"; }
grep -q "int16_t б" вывод/целочисленные_типы.c || { cat вывод/целочисленные_типы.c >&2; fail "целое16 → int16_t"; }
grep -q "int32_t в" вывод/целочисленные_типы.c || { cat вывод/целочисленные_типы.c >&2; fail "целое32 → int32_t"; }
grep -q "int64_t г" вывод/целочисленные_типы.c || { cat вывод/целочисленные_типы.c >&2; fail "целое64 → int64_t"; }
assert_eq "1 2 3 4" "$(./вывод/целочисленные_типы.elf)" "целочисленные типы: вывод"

# Остальные примитивы в объявлениях: байт → uint8_t, вещественное → float,
# символ → char.
cat > примитивные_типы.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    байт б = 7;
    вещественное в = 1;
    символ с = 65;
    printf("%d %.0f %c\n", б, в, с);
}
KONDA

"$BIN" примитивные_типы.конда >"$TMP/примитивные_типы.log" 2>&1 || {
    cat "$TMP/примитивные_типы.log" >&2
    fail "примитивные типы должны транспилироваться и компилироваться"
}
grep -q "uint8_t б" вывод/примитивные_типы.c || { cat вывод/примитивные_типы.c >&2; fail "байт → uint8_t"; }
grep -q "float в"   вывод/примитивные_типы.c || { cat вывод/примитивные_типы.c >&2; fail "вещественное → float"; }
grep -q "char с"    вывод/примитивные_типы.c || { cat вывод/примитивные_типы.c >&2; fail "символ → char"; }
assert_eq "7 1 A" "$(./вывод/примитивные_типы.elf)" "примитивные типы: вывод"

# Многоаргументные вызовы: аргументы передаются как есть, в исходном порядке,
# через запятую. Проверяем вызов с тремя и более аргументами.
cat > многоаргументный_вызов.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое32 а = 10;
    целое32 б = 20;
    целое32 в = 30;
    printf("%d %d %d\n", а, б, в);
    printf("%d\n", а + б * в);
}
KONDA

"$BIN" многоаргументный_вызов.конда >"$TMP/многоаргументный_вызов.log" 2>&1 || {
    cat "$TMP/многоаргументный_вызов.log" >&2
    fail "многоаргументный вызов должен транспилироваться и компилироваться"
}
assert_eq "10 20 30" "$(./вывод/многоаргументный_вызов.elf | head -n1)" "многоаргументный printf: три аргумента"
assert_eq "610"      "$(./вывод/многоаргументный_вызов.elf | tail -n1)" "многоаргументный printf: приоритет в аргументе"

# Циклы «для»/«пока», присваивание, унарный минус и «вернуть».
cat > управляющие_конструкции.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    целое32 сумма = 0;
    для (целое32 и = 1; и <= 5; и = и + 1) {
        сумма = сумма + и;
    }
    целое32 н = 3;
    пока (н > 0) {
        н = н - 1;
    }
    целое32 отр = -сумма;
    printf("%d %d %d\n", сумма, н, отр);
    вернуть 0;
}
KONDA

"$BIN" управляющие_конструкции.конда >"$TMP/управляющие_конструкции.log" 2>&1 || {
    cat "$TMP/управляющие_конструкции.log" >&2
    fail "циклы/присваивание/унарный минус/вернуть должны транспилироваться"
}
grep -q "for ("   вывод/управляющие_конструкции.c || { cat вывод/управляющие_конструкции.c >&2; fail "для → for"; }
grep -q "while (" вывод/управляющие_конструкции.c || { cat вывод/управляющие_конструкции.c >&2; fail "пока → while"; }
assert_eq "15 0 -15" "$(./вывод/управляющие_конструкции.elf)" "циклы, присваивание, унарный минус"

# Явный «вернуть N» должен заменять неявный «return 0;» (один return в теле).
cat > явный_возврат.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    printf("выход 7\n");
    вернуть 7;
}
KONDA

"$BIN" явный_возврат.конда >"$TMP/явный_возврат.log" 2>&1 || {
    cat "$TMP/явный_возврат.log" >&2
    fail "«вернуть N» должен транспилироваться"
}
assert_eq "1" "$(grep -c 'return' вывод/явный_возврат.c)" "явный вернуть не дублируется неявным return 0"
./вывод/явный_возврат.elf >/dev/null && rc=0 || rc=$?
assert_eq "7" "$rc" "вернуть 7 задаёт код возврата"

# Указатели в объявлениях тела функции: «тип* имя» → «тип *имя».
cat > указатели.конда <<'KONDA'
#содержит <stdio.h>

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    символ* сообщение = "привет";
    целое32* указ = нуль;
    printf("%s\n", сообщение);
}
KONDA

"$BIN" указатели.конда >"$TMP/указатели.log" 2>&1 || {
    cat "$TMP/указатели.log" >&2
    fail "указатели в теле функции должны транспилироваться"
}
grep -q "char \*сообщение"  вывод/указатели.c || { cat вывод/указатели.c >&2; fail "символ* → char *"; }
grep -q "int32_t \*указ"    вывод/указатели.c || { cat вывод/указатели.c >&2; fail "целое32* → int32_t *"; }
assert_eq "привет" "$(./вывод/указатели.elf)" "указатель на строку"

# Таблица символов: обращение к необъявленной переменной — ошибка.
cat > необъявленная_переменная.конда <<'KONDA'
#содержит <stdio.h>

