# Модификатор «неподписанный»: целоеN → uintN_t в объявлениях, параметрах
# и типе возврата; к нецелому типу — ошибка.
cat > неподписанный.конда <<'KONDA'
#содержит <stdio.h>

неподписанный целое32 удвоить(неподписанный целое16 х)
{
    неподписанный целое32 р = х;
    вернуть р + р;
}

целое32 точка_входа(целое32 количество_аргументов, символ** аргументы)
{
    неподписанный целое8 а = 200;
    неподписанный целое64 б = 5;
    printf("%u %llu %u\n", а, б, удвоить(10));
}
KONDA

"$BIN" неподписанный.конда >"$TMP/неподписанный.log" 2>&1 || {
    cat "$TMP/неподписанный.log" >&2
    fail "«неподписанный» должен транспилироваться"
}
grep -q "uint8_t а"            вывод/неподписанный.c || { cat вывод/неподписанный.c >&2; fail "неподписанный целое8 → uint8_t"; }
