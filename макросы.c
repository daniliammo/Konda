void макросы_инициализировать(ТаблицаМакросов *т) { т->число = 0; }

static Макрос *найти_макрос(ТаблицаМакросов *т, const char *имя, size_t длина)
{
    for (size_t i = 0; i < т->число; ++i)
        if (strlen(т->элементы[i].имя) == длина
            && memcmp(т->элементы[i].имя, имя, длина) == 0)
            return &т->элементы[i];
    return nullptr;
}

static void задать_макрос(ТаблицаМакросов *т, const char *имя, size_t длина, long значение)
{
    if (длина == 0 || длина >= sizeof(т->элементы[0].имя)) return;
    Макрос *м = найти_макрос(т, имя, длина);
    if (!м) {
        if (т->число >= МАКС_МАКРОСОВ) return;
        м = &т->элементы[т->число++];
        memcpy(м->имя, имя, длина);
        м->имя[длина] = '\0';
    }
    м->значение = значение;
}

int макросы_определить_аргумент(ТаблицаМакросов *т, const char *дефиниция)
{
    const char *равно = strchr(дефиниция, '=');
    size_t длина_имени = равно ? (size_t)(равно - дефиниция) : strlen(дефиниция);
    long значение = равно ? strtol(равно + 1, nullptr, 0) : 1;  // «-D ИМЯ» → 1
    if (длина_имени == 0 || длина_имени >= sizeof(т->элементы[0].имя)) return 0;
    задать_макрос(т, дефиниция, длина_имени, значение);
    return 1;
}

// ─── вычисление выражения #if ────────────────────────────────────────────────

typedef struct {
    const char *п;
    const char *конец;
    ТаблицаМакросов *м;
    int ошибка;
} Вычислитель;

static int это_имя_символ(unsigned char c)
{
    return isalnum(c) || c == '_' || c >= 0x80;  // ASCII-слово или кириллица (UTF-8)
}

static void проп_проб(Вычислитель *в)
{
    while (в->п < в->конец && (*в->п == ' ' || *в->п == '\t')) в->п++;
}

static long вычислить_или(Вычислитель *в);

static long вычислить_первичное(Вычислитель *в)
{
    проп_проб(в);
    if (в->п >= в->конец) { в->ошибка = 1; return 0; }
    if (*в->п == '(') {
        в->п++;
        long r = вычислить_или(в);
        проп_проб(в);
        if (в->п < в->конец && *в->п == ')') в->п++;
        else в->ошибка = 1;
        return r;
    }
    if (isdigit((unsigned char)*в->п)) {
        char *кон = nullptr;
        long r = strtol(в->п, &кон, 0);
        в->п = кон;
        return r;
    }
    if (это_имя_символ((unsigned char)*в->п) && !isdigit((unsigned char)*в->п)) {
        const char *нач = в->п;
        while (в->п < в->конец && это_имя_символ((unsigned char)*в->п)) в->п++;
        size_t длина = (size_t)(в->п - нач);
        // defined(ИМЯ) / определён(ИМЯ) / defined ИМЯ
        int это_defined = (длина == 7 && memcmp(нач, "defined", 7) == 0)
                       || (длина == strlen("определён")
                           && memcmp(нач, "определён", длина) == 0);
        if (это_defined) {
            проп_проб(в);
            int скобка = 0;
            if (в->п < в->конец && *в->п == '(') { в->п++; скобка = 1; проп_проб(в); }
            const char *ин = в->п;
            while (в->п < в->конец && это_имя_символ((unsigned char)*в->п)) в->п++;
            size_t ил = (size_t)(в->п - ин);
            int опр = найти_макрос(в->м, ин, ил) != nullptr;
            if (скобка) { проп_проб(в);
                if (в->п < в->конец && *в->п == ')') в->п++; else в->ошибка = 1; }
            return опр ? 1 : 0;
        }
        Макрос *м = найти_макрос(в->м, нач, длина);
        return м ? м->значение : 0;  // неопределённое имя → 0 (как в C)
    }
    в->ошибка = 1;
    return 0;
}

static long вычислить_унарное(Вычислитель *в)
{
    проп_проб(в);
    if (в->п < в->конец && *в->п == '!') { в->п++; return !вычислить_унарное(в); }
    if (в->п < в->конец && *в->п == '-') { в->п++; return -вычислить_унарное(в); }
    return вычислить_первичное(в);
}

static long вычислить_произв(Вычислитель *в)
{
    long r = вычислить_унарное(в);
    for (;;) {
        проп_проб(в);
        if (в->п >= в->конец) break;
        char c = *в->п;
        if (c == '*') { в->п++; r = r * вычислить_унарное(в); }
        else if (c == '/') { в->п++; long d = вычислить_унарное(в); r = d ? r / d : 0; }
        else if (c == '%') { в->п++; long d = вычислить_унарное(в); r = d ? r % d : 0; }
        else break;
    }
    return r;
}

static long вычислить_сумму(Вычислитель *в)
{
    long r = вычислить_произв(в);
    for (;;) {
        проп_проб(в);
        if (в->п >= в->конец) break;
        char c = *в->п;
        if (c == '+') { в->п++; r = r + вычислить_произв(в); }
        else if (c == '-') { в->п++; r = r - вычислить_произв(в); }
        else break;
    }
    return r;
}

static int двухсимв(Вычислитель *в, char a, char b)
{
    return в->п + 1 < в->конец && в->п[0] == a && в->п[1] == b;
}

static long вычислить_сравн(Вычислитель *в)
{
    long r = вычислить_сумму(в);
    for (;;) {
        проп_проб(в);
        if (в->п >= в->конец) break;
        if (двухсимв(в, '<', '=')) { в->п += 2; r = (r <= вычислить_сумму(в)); }
        else if (двухсимв(в, '>', '=')) { в->п += 2; r = (r >= вычислить_сумму(в)); }
        else if (*в->п == '<') { в->п++; r = (r < вычислить_сумму(в)); }
        else if (*в->п == '>') { в->п++; r = (r > вычислить_сумму(в)); }
        else break;
    }
    return r;
}

static long вычислить_равенство(Вычислитель *в)
{
    long r = вычислить_сравн(в);
    for (;;) {
        проп_проб(в);
        if (двухсимв(в, '=', '=')) { в->п += 2; r = (r == вычислить_сравн(в)); }
        else if (двухсимв(в, '!', '=')) { в->п += 2; r = (r != вычислить_сравн(в)); }
        else break;
    }
    return r;
}

static long вычислить_и(Вычислитель *в)
{
    long r = вычислить_равенство(в);
    for (;;) {
        проп_проб(в);
        if (двухсимв(в, '&', '&')) { в->п += 2; long p = вычислить_равенство(в); r = (r && p); }
        else break;
    }
    return r;
}

static long вычислить_или(Вычислитель *в)
{
    long r = вычислить_и(в);
    for (;;) {
        проп_проб(в);
        if (двухсимв(в, '|', '|')) { в->п += 2; long p = вычислить_и(в); r = (r || p); }
        else break;
    }
    return r;
}

// Вычисляет условие #if из подстроки [нач, конец). Возвращает 0/1; при ошибке
// синтаксиса ставит *ошибка = 1.
static int вычислить_условие(ТаблицаМакросов *м, const char *нач, const char *конец,
                             int *ошибка)
{
    Вычислитель в = { .п = нач, .конец = конец, .м = м, .ошибка = 0 };
    long r = вычислить_или(&в);
    проп_проб(&в);
    if (в.п != в.конец) в.ошибка = 1;  // лишние символы
    if (ошибка) *ошибка = в.ошибка;
    return r != 0;
}

// ─── проход условной компиляции над токенами ─────────────────────────────────

typedef struct {
    int активна_родителя;
    int активна;
    int ветка_взята;   // хотя бы одна ветка этого #if уже стала активной
} Кадр;

// Разбирает ключевое слово директивы (после «#», пропустив пробелы). Возвращает
// указатель на начало слова, длину — в *длина, и сдвигает *текст на конец слова.
static const char *слово_директивы(const char **текст, const char *конец, size_t *длина)
{
    const char *п = *текст;
    while (п < конец && (*п == ' ' || *п == '\t')) п++;
    const char *нач = п;
    while (п < конец && это_имя_символ((unsigned char)*п)) п++;
    *длина = (size_t)(п - нач);
    *текст = п;
    return нач;
}

static int слово_равно_дир(const char *с, size_t дл, const char *эталон)
{
    return дл == strlen(эталон) && memcmp(с, эталон, дл) == 0;
}

int условная_компиляция(МассивТокенов *токены, ТаблицаМакросов *макросы,
                        Диагностика *диаг)
{
    Кадр стек[64];
    int глубина = 0;

    МассивТокенов новый;
    массив_токенов_инициализировать(&новый);

    size_t n = токены->базовый.длина;
    for (size_t i = 0; i < n; ++i) {
        Токен *t = дин_массив_получить(&токены->базовый, i);
        int активна = (глубина == 0) ? 1 : стек[глубина - 1].активна;

        if (t->тип != ТОКЕН_ПРЕПРОЦЕССОР) {
            if (активна)
                массив_токенов_добавить(&новый, t->тип, t->строка,
                                        t->позиция_в_строке, t->начало, t->длина);
            continue;
        }

        // Директива: пропустить «#», прочитать ключевое слово.
        const char *текст = t->начало;
        const char *конец = t->начало + t->длина;
        if (текст < конец && *текст == '#') текст++;
        size_t дл; const char *кв = слово_директивы(&текст, конец, &дл);

        int откр = слово_равно_дир(кв, дл, "if")   || слово_равно_дир(кв, дл, "если")
                || слово_равно_дир(кв, дл, "ifdef") || слово_равно_дир(кв, дл, "опр")
                || слово_равно_дир(кв, дл, "ifndef")|| слово_равно_дир(кв, дл, "не_опр");
        int elif  = слово_равно_дир(кв, дл, "elif") || слово_равно_дир(кв, дл, "иначе_если");
        int els   = слово_равно_дир(кв, дл, "else") || слово_равно_дир(кв, дл, "иначе");
        int конс  = слово_равно_дир(кв, дл, "endif")|| слово_равно_дир(кв, дл, "конец");
        int деф   = слово_равно_дир(кв, дл, "define")|| слово_равно_дир(кв, дл, "определить");

        if (откр) {
            int ifdef  = слово_равно_дир(кв, дл, "ifdef")  || слово_равно_дир(кв, дл, "опр");
            int ifndef = слово_равно_дир(кв, дл, "ifndef") || слово_равно_дир(кв, дл, "не_опр");
            int усл;
            if (ifdef || ifndef) {
                size_t ил; const char *ин = слово_директивы(&текст, конец, &ил);
                int опр = найти_макрос(макросы, ин, ил) != nullptr;
                усл = ifndef ? !опр : опр;
            } else {  // #if / #если
                int ош = 0;
                усл = вычислить_условие(макросы, текст, конец, &ош);
                if (ош) {
                    диаг->ошибка = 1; диаг->строка = t->строка; диаг->колонка = t->позиция_в_строке;
                    snprintf(диаг->сообщение, sizeof диаг->сообщение,
                             "не удалось вычислить условие «#if»");
                    массив_токенов_освободить(&новый); return 0;
                }
            }
            if (глубина >= (int)(sizeof(стек)/sizeof(стек[0]))) {
                диаг->ошибка = 1; диаг->строка = t->строка; диаг->колонка = t->позиция_в_строке;
                snprintf(диаг->сообщение, sizeof диаг->сообщение,
                         "слишком глубокая вложенность условной компиляции");
                массив_токенов_освободить(&новый); return 0;
            }
            стек[глубина].активна_родителя = активна;
            стек[глубина].активна = активна && усл;
            стек[глубина].ветка_взята = стек[глубина].активна;
            глубина++;
            continue;
        }
        if (elif || els) {
            if (глубина == 0) {
                диаг->ошибка = 1; диаг->строка = t->строка; диаг->колонка = t->позиция_в_строке;
                snprintf(диаг->сообщение, sizeof диаг->сообщение,
                         "«#elif/#else» без «#if»");
                массив_токенов_освободить(&новый); return 0;
            }
            Кадр *к = &стек[глубина - 1];
            int усл = 1;
            if (elif) {
                int ош = 0;
                усл = вычислить_условие(макросы, текст, конец, &ош);
                if (ош) {
                    диаг->ошибка = 1; диаг->строка = t->строка; диаг->колонка = t->позиция_в_строке;
                    snprintf(диаг->сообщение, sizeof диаг->сообщение,
                             "не удалось вычислить условие «#elif»");
                    массив_токенов_освободить(&новый); return 0;
                }
            }
            if (!к->ветка_взята && к->активна_родителя && усл) {
                к->активна = 1; к->ветка_взята = 1;
            } else {
                к->активна = 0;
            }
            continue;
        }
        if (конс) {
            if (глубина == 0) {
                диаг->ошибка = 1; диаг->строка = t->строка; диаг->колонка = t->позиция_в_строке;
                snprintf(диаг->сообщение, sizeof диаг->сообщение, "«#endif» без «#if»");
                массив_токенов_освободить(&новый); return 0;
            }
            глубина--;
            continue;
        }
        if (деф) {
            if (активна) {
                size_t ил; const char *ин = слово_директивы(&текст, конец, &ил);
                // Значение: остаток строки как целое (или 1, если пусто).
                while (текст < конец && (*текст == ' ' || *текст == '\t')) текст++;
                long знач = (текст < конец) ? strtol(текст, nullptr, 0) : 1;
                задать_макрос(макросы, ин, ил, знач);
            }
            continue;
        }
        // Прочая директива (#содержит и т.п.) — проходит как есть, если активна.
        if (активна)
            массив_токенов_добавить(&новый, t->тип, t->строка,
                                    t->позиция_в_строке, t->начало, t->длина);
    }

    if (глубина != 0) {
        диаг->ошибка = 1;
        snprintf(диаг->сообщение, sizeof диаг->сообщение,
                 "незакрытый «#if» (не хватает «#endif»)");
        массив_токенов_освободить(&новый);
        return 0;
    }

    массив_токенов_освободить(токены);
    *токены = новый;
    return 1;
}
