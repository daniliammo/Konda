#ifndef VLADENIE_H
#define VLADENIE_H

#include "транспилятор.h"
#include "аст.h"

// Модуль владения: path-sensitive анализ перемещения срезов между ветвями
// потока управления. Извлечён из семантики, чтобы поведение «без рантайма»
// (snapshot владения перед if, merge при выходе, sink-«вернуть») жило
// отдельно от прочих проходов и было видно как самостоятельная подсистема.
//
// Анализатор остаётся непрозрачным для этого модуля; всё нужное — состояние
// владения каждого символа и диагностика — пробрасывается через узкий набор
// аксессоров, реализуемых в семантика.c.

// Размер снимка ограничен числом символов в таблице. Должен совпадать с
// МАКС_СИМВОЛОВ внутри семантики.c — при изменении синхронизируйте оба места.
#define МАКС_СИМВОЛОВ_ВЛАДЕНИЯ 256

typedef struct Анализатор Анализатор;

// Снимок владения для одного слота таблицы символов.
typedef struct {
    int владеет_срезом;     // символ — владеющий срез (можно перемещать/free)
    int перемещён;          // владение уже ушло (move): использование — ошибка
    Токен токен_move;       // место, где произошёл move (для диагностик)
} ВладениеЭлемент;

// Снимок владения всей таблицы символов. Используется для path-merge:
// форкаем перед ветвлением, восстанавливаем перед второй ветвью, потом
// сливаем результаты в одно итоговое состояние.
typedef struct {
    ВладениеЭлемент элементы[МАКС_СИМВОЛОВ_ВЛАДЕНИЯ];
    size_t число;
} СостояниеВладения;

// Аксессоры к таблице символов (реализованы в семантика.c).
size_t          кво_символов(const Анализатор *а);
ВладениеЭлемент владение_символа(const Анализатор *а, size_t индекс);
void            задать_владение_символа(Анализатор *а, size_t индекс,
                                         ВладениеЭлемент новое);
const char     *имя_символа(const Анализатор *а, size_t индекс);
void            диагностика_владения(Анализатор *а, const Токен *где,
                                      const char *сообщение);

// API для path-merge.
void снять_состояние_владения(const Анализатор *а, СостояниеВладения *сост);
void восстановить_состояние_владения(Анализатор *а, const СостояниеВладения *сост);
// Возвращает 1 при успехе, 0 при несогласованности (ошибка уже выдана).
int  слить_состояния_владения(Анализатор *а,
                               const СостояниеВладения *до_ветвления,
                               const СостояниеВладения *тогда,
                               const СостояниеВладения *иначе);

// Завершается ли блок безусловным «вернуть» (sink для CFG-анализа: ветвь не
// вливается в fallthrough, поэтому её снимок не участвует в merge).
int блок_завершается_возвратом(const Узел *блок);

// Гарантирует ли блок возврат значения по ВСЕМ путям (рекурсивно по if/иначе,
// выбор, «пока (1)»). Для запрета молчаливого fall-through в non-void функции.
int все_пути_возвращают(const Узел *блок);

#endif
