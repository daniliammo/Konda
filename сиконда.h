#ifndef СИКОНДА_H
#define СИКОНДА_H

#include <stddef.h>

#include "транспилятор.h"   // ТОЛЬКО ради ДинМассив/БуферКода (чистые контейнеры).
                             // Никакой транспиляции Konda конвертер не подключает.

// ─── Конвертер C → Konda ────────────────────────────────────────────────────
//
// Самостоятельный компонент: переводит подмножество C в исходник Konda
// (синтаксический перевод, без семантики). Все проверки безопасности/типов
// выполняет отдельно транспилятор Konda над полученным .конда-текстом.
//
// Пайплайн: C-текст → C-токены → C-AST (рекурсивный спуск) → эмиссия .конда.
// Ошибки — только синтаксис C и неподдержанные конструкции, с позицией в .c.

// ── Диагностика (независимая от аст.h, чтобы не тянуть транспилятор) ─────────
typedef struct {
    int ошибка;            // 1 — это ошибка (сейчас все диагностики — ошибки)
    char сообщение[512];
    int строка;            // 1-based, позиция в исходном .c
    int колонка;
} СиДиаг;

// ── C-токены ────────────────────────────────────────────────────────────────
// Ключевые слова и операторы/пунктуация не имеют отдельных видов — распознаются
// по тексту в парсере (как это делает парсер Konda для операторов).
typedef enum {
    СТ_ИДЕНТ,   // идентификатор ИЛИ ключевое слово C (различаем по тексту)
    СТ_ЧИСЛО,
    СТ_СТРОКА,  // "..."
    СТ_СИМВОЛ,  // '...'
    СТ_ПУНКТ,   // оператор или знак пунктуации (в т.ч. многосимвольный)
    СТ_КОНЕЦ
} СиВидТокена;

typedef struct {
    СиВидТокена вид;
    const char *текст;   // указывает в исходный буфер
    size_t длина;
    int строка;
    int колонка;
} СиТокен;

// ── C-типы ──────────────────────────────────────────────────────────────────
typedef enum {
    С_ПУСТО = 0,   // тип не задан
    С_VOID,
    С_BOOL,
    С_CHAR, С_SCHAR, С_UCHAR,
    С_SHORT,
    С_INT,
    С_LONG, С_LONGLONG,
    С_FLOAT, С_DOUBLE,
    С_INT8, С_INT16, С_INT32, С_INT64,
    С_UINT8, С_UINT16, С_UINT32, С_UINT64,
    С_SIZE,
    С_ИМЯ          // именованный тип: struct/union/enum/typedef-псевдоним
} СиБазовыйТип;

typedef struct {
    СиБазовыйТип базовый;
    int неподписанный;   // модификатор unsigned для целых
    int указатели;       // число '*'
    const char *имя;     // для С_ИМЯ — имя типа (в исходном буфере)
    size_t имя_длина;
} СиТип;

// ── C-AST ─────────────────────────────────────────────────────────────────--
typedef enum {
    У_ПРОГРАММА,      // дети: top-level элементы
    У_ПРЕПРОЦЕССОР,   // токен: вся #-строка
    У_ФУНКЦИЯ,        // тип: возврат; токен: имя; это_точка_входа; дети: [параметры…, тело-БЛОК]
    У_ПАРАМЕТР,       // тип; токен: имя
    У_СТРУКТУРА,      // токен: имя; это_union; дети: поля (У_ПАРАМЕТР)
    У_ПЕРЕЧИСЛЕНИЕ,   // токен: имя; дети: У_ВАРИАНТ
    У_ВАРИАНТ,        // токен: имя; дети: [значение?]
    У_ТИПФУНКЦИИ,     // токен: имя-псевдоним; тип: возврат; дети: параметры (У_ПАРАМЕТР)
    У_ОБЪЯВЛЕНИЕ,     // тип; токен: имя; есть_массив/разм_массива; дети: [инициализатор?]
    У_БЛОК,           // дети: операторы
    У_ЕСЛИ,           // дети: [условие, тогда, иначе?]
    У_ПОКА,           // дети: [условие, тело]
    У_ДЕЛАЙ,          // do-while; дети: [тело, условие]
    У_ДЛЯ,            // дети: [инициализация?, условие?, шаг?, тело]
    У_ВЫБОР,          // switch; дети: [выражение, ветки (У_ВЕТКА)…]
    У_ВЕТКА,          // токен: имя метки ('_' — default); дети: операторы (без завершающего break)
    У_ВОЗВРАТ,        // дети: [выражение?]
    У_ПРЕРВАТЬ,
    У_ПРОДОЛЖИТЬ,
    У_ВЫРАЖ_ОПЕРАТОР, // дети: [выражение]
    У_ПРИСВАИВАНИЕ,   // токен: оператор ('=' или составной уже разложен); дети: [цель, значение]
    У_ТЕРНАРНИК,      // дети: [условие, если_да, если_нет]
    У_БИНОП,          // токен: оператор; дети: [лево, право]
    У_УНОП,           // токен: оператор (префиксный); дети: [операнд]
    У_ПОСТФИКС,       // токен: оператор (++/--); дети: [операнд]
    У_ВЫЗОВ,          // дети: [вызываемое, аргументы…]
    У_ИНДЕКС,         // дети: [база, индекс]
    У_ПОЛЕ,           // токен: '.' или '->'; дети: [база]; имя поля в поле_имя
    У_КАСТ,           // тип: целевой; дети: [выражение]
    У_РАЗМЕР_ТИПА,    // sizeof(тип); тип: аргумент
    У_РАЗМЕР_ВЫРАЖ,   // sizeof выражение; дети: [выражение]
    У_ИДЕНТ,          // токен: имя
    У_ЧИСЛО,          // токен: литерал
    У_СТРОКА_ЛИТ,     // токен: литерал
    У_СИМВОЛ_ЛИТ,     // токен: литерал
    У_СПИСОК_ИНИЦ,    // { a, b, c }; дети: элементы
    У_СОСТ_ЛИТЕРАЛ    // (Тип){ ... }; тип: целевой; дети: [список-иниц]
} СиВидУзла;

typedef struct СиУзел СиУзел;
struct СиУзел {
    СиВидУзла вид;
    СиТокен токен;        // имя/оператор/литерал
    СиТип тип;            // для объявлений/параметров/каста/функции
    ДинМассив дети;       // СиУзел*
    long разм_массива;    // У_ОБЪЯВЛЕНИЕ: размер массива; -1 — «[]» без размера
    int есть_массив;      // У_ОБЪЯВЛЕНИЕ: это массив
    int это_union;        // У_СТРУКТУРА: объявлено как union
    int это_точка_входа;  // У_ФУНКЦИЯ: исходное имя было main
    СиТокен поле_имя;     // У_ПОЛЕ: имя поля
};

// ── Узлы: создание/дерево/освобождение ──────────────────────────────────────
СиУзел *си_узел(СиВидУзла вид);
void си_добавить(СиУзел *родитель, СиУзел *ребёнок);
СиУзел *си_ребёнок(const СиУзел *узел, size_t индекс);
size_t си_детей(const СиУзел *узел);
void си_освободить(СиУзел *узел);

// ── Диагностика ─────────────────────────────────────────────────────────────
void си_добавить_ошибку(ДинМассив *ошибки, int строка, int колонка,
                        const char *формат, ...);

// ── Разбор C (си_разбор.c) ──────────────────────────────────────────────────
// Возвращает У_ПРОГРАММА (может быть с уже собранными ошибками) или nullptr при
// фатальном сбое. Все ошибки складываются в «ошибки» (элементы — СиДиаг).
СиУзел *си_разобрать(const char *исходник, size_t длина, ДинМассив *ошибки);

// ── Эмиссия Konda (конда_вывод.c) ───────────────────────────────────────────
// Обходит C-AST и пишет .конда-текст в «вывод». Неподдержанные в контексте
// конструкции добавляют ошибки в «ошибки». Возвращает 1, если ошибок не было.
int конда_эмит(СиУзел *программа, БуферКода *вывод, ДинМассив *ошибки);

// ── Верхний уровень (си_разбор.c) ───────────────────────────────────────────
// Лекс + разбор + эмиссия. Возвращает 1 при успехе (0 ошибок), иначе 0.
int сиконда_конвертировать(const char *исходник, size_t длина,
                           БуферКода *вывод, ДинМассив *ошибки);

#endif /* СИКОНДА_H */
