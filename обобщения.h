#ifndef ОБОБЩЕНИЯ_H
#define ОБОБЩЕНИЯ_H

#include "аст.h"

// Мономорфизация обобщённых функций: AST→AST пред-проход между разбором и
// семантикой. Для каждого уникального вызова «имя<Тип…>(…)» генерирует
// специализированную копию шаблона с подставленными конкретными типами и
// уникальным именем (например «максимум_целое32»), переписывает вызовы на это
// имя, снимает тип-параметры/тип-аргументы и удаляет сами шаблоны из программы.
// После этого дерево полностью конкретно — семантика и кодоген работают без
// изменений и никогда не видят ТОКЕН_ТИПОПАРАМЕТР.
//
// Возвращает 1 при успехе, 0 при ошибке (тогда *диаг заполнена: неизвестный
// шаблон, несовпадение числа тип-аргументов). Если обобщённых функций нет —
// ничего не делает и возвращает 1.
int мономорфизировать(Узел *программа, Диагностика *диаг);

#endif
