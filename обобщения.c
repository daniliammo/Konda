#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "обобщения.h"

#define МАКС_ТИПОПАРАМ 8
#define МАКС_ШАБЛОНОВ  128

// ─── вспомогательное ─────────────────────────────────────────────────────────

static int байты_равны_л(const char *a, size_t la, const char *b, size_t lb)
{
    return la == lb && memcmp(a, b, la) == 0;
}

// Число ведущих детей-УЗЕЛ_ТИПОПАРАМЕТР функции (сколько у неё тип-параметров).
static size_t число_типопараметров_функции(const Узел *ф)
{
    size_t n = 0;
    for (size_t i = 0; i < узел_число_детей(ф); ++i) {
        const Узел *реб = узел_ребёнок(ф, i);
        if (реб && реб->тип == УЗЕЛ_ТИПОПАРАМЕТР) n++;
        else break;  // тип-параметры идут строго перед параметрами
    }
    return n;
}

// Число ведущих детей-УЗЕЛ_ТИПОАРГУМЕНТ вызова.
static size_t число_типоаргументов_вызова(const Узел *в)
{
    size_t n = 0;
    for (size_t i = 0; i < узел_число_детей(в); ++i) {
        const Узел *реб = узел_ребёнок(в, i);
        if (реб && реб->тип == УЗЕЛ_ТИПОАРГУМЕНТ) n++;
        else break;
    }
    return n;
}

// Удаляет (и освобождает) всех детей заданного типа, компактируя массив.
static void удалить_детей_типа(Узел *у, ТипУзла т)
{
    size_t w = 0;
    for (size_t r = 0; r < у->дети.длина; ++r) {
        Узел *реб = узел_ребёнок(у, r);
        if (реб && реб->тип == т) {
            освободить_узел(реб);
            continue;
        }
        Узел **слот = дин_массив_получить(&у->дети, w);
        *слот = реб;
        w++;
    }
    у->дети.длина = w;
}

// Konda-имя конкретного типа для суффикса мономорфизированного имени. Результат
// — валидный идентификатор Konda (кириллица/цифры/подчёркивание): указатели
// кодируются «_ук», беззнаковость — «неподписанный_».
static void суффикс_типа(const Узел *та, char *out, size_t cap)
{
    char базовый[128];
    switch (та->тип_данных) {
        case ТОКЕН_ЦЕЛОЕ8:  snprintf(базовый, sizeof базовый, "целое8");  break;
        case ТОКЕН_ЦЕЛОЕ16: snprintf(базовый, sizeof базовый, "целое16"); break;
        case ТОКЕН_ЦЕЛОЕ32: snprintf(базовый, sizeof базовый, "целое32"); break;
        case ТОКЕН_ЦЕЛОЕ64: snprintf(базовый, sizeof базовый, "целое64"); break;
        case ТОКЕН_БАЙТ:         snprintf(базовый, sizeof базовый, "байт"); break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ:  snprintf(базовый, sizeof базовый, "вещественное"); break;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ64: snprintf(базовый, sizeof базовый, "вещественное64"); break;
        case ТОКЕН_ТИП_СИМВОЛ:   snprintf(базовый, sizeof базовый, "символ"); break;
        case ТОКЕН_ЛОГИЧЕСКИЙ:   snprintf(базовый, sizeof базовый, "логический"); break;
        case ТОКЕН_СТРУКТУРА:
        case ТОКЕН_ПЕРЕЧИСЛЕНИЕ:
            snprintf(базовый, sizeof базовый, "%.*s",
                     (int)та->имя_типа.длина, та->имя_типа.начало);
            break;
        default: snprintf(базовый, sizeof базовый, "тип"); break;
    }
    char укз[24] = {0};
    for (int i = 0; i < та->указатели && i < 8; ++i) memcpy(укз + i * 3, "_ук", 3);
    snprintf(out, cap, "%s%s%s", та->беззнаковый ? "неподписанный_" : "",
             базовый, укз);
}

// ─── подстановка тип-параметров ──────────────────────────────────────────────

// Заменяет в поддереве каждый ТОКЕН_ТИПОПАРАМЕТР на соответствующий конкретный
// тип из тип-аргументов (позиционно по именам). Указатели складываются:
// «T*» при T = символ* даёт символ**.
static void подставить(Узел *узел, const Токен парам_имена[], size_t число_парам,
                       const Узел *типарг[])
{
    if (!узел) return;
    if (узел->тип_данных == ТОКЕН_ТИПОПАРАМЕТР) {
        for (size_t k = 0; k < число_парам; ++k) {
            if (байты_равны_л(узел->имя_типа.начало, узел->имя_типа.длина,
                              парам_имена[k].начало, парам_имена[k].длина)) {
                const Узел *та = типарг[k];
                узел->тип_данных  = та->тип_данных;
                узел->беззнаковый = та->беззнаковый;
                узел->срез       |= та->срез;
                узел->указатели  += та->указатели;
                узел->имя_типа    = та->имя_типа;
                break;
            }
        }
    }
    for (size_t i = 0; i < узел_число_детей(узел); ++i)
        подставить(узел_ребёнок(узел, i), парам_имена, число_парам, типарг);
}

// Присваивает узлу собственное (сгенерированное) имя и перенаправляет его токен.
static void переименовать(Узел *узел, const char *имя)
{
    free(узел->собственное_имя);
    узел->собственное_имя = strdup(имя);
    if (узел->собственное_имя) {
        узел->токен.тип = ТОКЕН_ИДЕНТИФИКАТОР;
        узел->токен.начало = узел->собственное_имя;
        узел->токен.длина = strlen(узел->собственное_имя);
    }
}

// ─── таблица шаблонов ────────────────────────────────────────────────────────

typedef struct {
    const char *имя;
    size_t      длина_имени;
    Узел       *узел;
    Токен       парам_имена[МАКС_ТИПОПАРАМ];
    size_t      число_параметров;
} Шаблон;

// ─── контекст прогона ────────────────────────────────────────────────────────

typedef struct {
    Шаблон     шаблоны[МАКС_ШАБЛОНОВ];
    size_t     число_шаблонов;
    ДинМассив  имена_инстанций;  // char*  — уже созданные специализации (дедуп)
    ДинМассив  клоны;            // Узел*  — специализации для добавления
    ДинМассив  очередь;          // Узел*  — специализации, чьи тела ещё не скан.
    Диагностика *диаг;
} Контекст;

static Шаблон *найти_шаблон(Контекст *к, const Токен *имя)
{
    for (size_t i = 0; i < к->число_шаблонов; ++i)
        if (байты_равны_л(к->шаблоны[i].имя, к->шаблоны[i].длина_имени,
                          имя->начало, имя->длина))
            return &к->шаблоны[i];
    return nullptr;
}

static int инстанция_существует(Контекст *к, const char *имя)
{
    for (size_t i = 0; i < к->имена_инстанций.длина; ++i) {
        char **p = дин_массив_получить(&к->имена_инстанций, i);
        if (*p && strcmp(*p, имя) == 0) return 1;
    }
    return 0;
}

static void ошибка(Контекст *к, const Токен *где, const char *текст)
{
    if (к->диаг->ошибка) return;  // держим первую
    к->диаг->ошибка = 1;
    к->диаг->строка = где ? где->строка : 0;
    к->диаг->колонка = где ? где->позиция_в_строке : 0;
    snprintf(к->диаг->сообщение, sizeof(к->диаг->сообщение), "%s", текст);
}

// Обрабатывает один обобщённый вызов: проверяет арность, вычисляет имя
// специализации, при необходимости создаёт клон (подстановка + переименование +
// снятие тип-параметров) и ставит его в очередь; переписывает сам вызов на
// сгенерированное имя и снимает тип-аргументы.
static void обработать_обобщённый_вызов(Контекст *к, Узел *вызов)
{
    Шаблон *ш = найти_шаблон(к, &вызов->токен);
    if (!ш) {
        ошибка(к, &вызов->токен,
               "вызов обобщённой функции: шаблон не найден в этом файле");
        return;
    }
    size_t числоТА = число_типоаргументов_вызова(вызов);
    if (числоТА != ш->число_параметров) {
        char msg[256];
        snprintf(msg, sizeof msg,
                 "обобщённая функция «%.*s» ожидает %zu тип-аргумент(ов), "
                 "передано %zu", (int)ш->длина_имени, ш->имя,
                 ш->число_параметров, числоТА);
        ошибка(к, &вызов->токен, msg);
        return;
    }

    const Узел *типарг[МАКС_ТИПОПАРАМ];
    for (size_t i = 0; i < числоТА && i < МАКС_ТИПОПАРАМ; ++i)
        типарг[i] = узел_ребёнок(вызов, i);

    // Имя специализации: имя_суффикс1[_суффикс2…].
    char имя[512];
    int поз = snprintf(имя, sizeof имя, "%.*s",
                       (int)ш->длина_имени, ш->имя);
    for (size_t i = 0; i < числоТА; ++i) {
        char суф[160];
        суффикс_типа(типарг[i], суф, sizeof суф);
        int напис = snprintf(имя + поз, sizeof имя - (size_t)поз, "_%s", суф);
        if (напис > 0) поз += напис;
        if ((size_t)поз >= sizeof имя) { поз = sizeof имя - 1; break; }
    }

    // Создаём клон-специализацию (если такой ещё нет).
    if (!инстанция_существует(к, имя)) {
        char *копияимени = strdup(имя);
        дин_массив_добавить(&к->имена_инстанций, &копияимени);

        Узел *клон = узел_копировать(ш->узел);
        подставить(клон, ш->парам_имена, ш->число_параметров, типарг);
        удалить_детей_типа(клон, УЗЕЛ_ТИПОПАРАМЕТР);
        переименовать(клон, имя);
        клон->статический = 1;  // специализация — внутренняя связь (как inline)

        Узел *указ = клон;
        дин_массив_добавить(&к->клоны, &указ);
        дин_массив_добавить(&к->очередь, &указ);
    }

    // Переписываем сам вызов: имя специализации + снятие тип-аргументов.
    удалить_детей_типа(вызов, УЗЕЛ_ТИПОАРГУМЕНТ);
    переименовать(вызов, имя);
}

// Рекурсивный обход: обрабатывает обобщённые вызовы, затем спускается в детей.
static void обойти(Контекст *к, Узел *узел)
{
    if (!узел || к->диаг->ошибка) return;
    if (узел->тип == УЗЕЛ_ВЫЗОВ && число_типоаргументов_вызова(узел) > 0)
        обработать_обобщённый_вызов(к, узел);
    for (size_t i = 0; i < узел_число_детей(узел) && !к->диаг->ошибка; ++i)
        обойти(к, узел_ребёнок(узел, i));
}

// ─── вход ────────────────────────────────────────────────────────────────────

int мономорфизировать(Узел *программа, Диагностика *диаг)
{
    if (!программа) return 1;

    Контекст к = { .диаг = диаг };
    дин_массив_инициализировать(&к.имена_инстанций, sizeof(char *));
    дин_массив_инициализировать(&к.клоны, sizeof(Узел *));
    дин_массив_инициализировать(&к.очередь, sizeof(Узел *));

    // 1. Собрать шаблоны (функции с тип-параметрами).
    for (size_t i = 0; i < узел_число_детей(программа); ++i) {
        Узел *ф = узел_ребёнок(программа, i);
        if (!ф || ф->тип != УЗЕЛ_ФУНКЦИЯ) continue;
        size_t чп = число_типопараметров_функции(ф);
        if (чп == 0) continue;
        if (к.число_шаблонов >= МАКС_ШАБЛОНОВ) break;
        Шаблон *ш = &к.шаблоны[к.число_шаблонов++];
        ш->имя = ф->токен.начало;
        ш->длина_имени = ф->токен.длина;
        ш->узел = ф;
        ш->число_параметров = чп < МАКС_ТИПОПАРАМ ? чп : МАКС_ТИПОПАРАМ;
        for (size_t j = 0; j < ш->число_параметров; ++j)
            ш->парам_имена[j] = узел_ребёнок(ф, j)->токен;
    }

    if (к.число_шаблонов == 0) {  // обобщённых функций нет — выходим
        дин_массив_освободить(&к.имена_инстанций);
        дин_массив_освободить(&к.клоны);
        дин_массив_освободить(&к.очередь);
        return 1;
    }

    // 2. Сканировать тела не-шаблонных функций верхнего уровня.
    for (size_t i = 0; i < узел_число_детей(программа) && !диаг->ошибка; ++i) {
        Узел *ф = узел_ребёнок(программа, i);
        if (!ф || ф->тип != УЗЕЛ_ФУНКЦИЯ) continue;
        if (число_типопараметров_функции(ф) > 0) continue;  // сам шаблон
        обойти(&к, ф);
    }

    // 3. Обработать очередь клонов (транзитивные обобщённые вызовы).
    for (size_t qi = 0; qi < к.очередь.длина && !диаг->ошибка; ++qi) {
        Узел **p = дин_массив_получить(&к.очередь, qi);
        обойти(&к, *p);
    }

    if (диаг->ошибка) {
        for (size_t i = 0; i < к.клоны.длина; ++i) {
            Узел **p = дин_массив_получить(&к.клоны, i);
            освободить_узел(*p);
        }
        for (size_t i = 0; i < к.имена_инстанций.длина; ++i) {
            char **p = дин_массив_получить(&к.имена_инстанций, i);
            free(*p);
        }
        дин_массив_освободить(&к.имена_инстанций);
        дин_массив_освободить(&к.клоны);
        дин_массив_освободить(&к.очередь);
        return 0;
    }

    // 4. Перестроить детей программы: убрать шаблоны, добавить специализации.
    ДинМассив новые;
    дин_массив_инициализировать(&новые, sizeof(Узел *));
    for (size_t i = 0; i < узел_число_детей(программа); ++i) {
        Узел *ф = узел_ребёнок(программа, i);
        if (ф && ф->тип == УЗЕЛ_ФУНКЦИЯ && число_типопараметров_функции(ф) > 0) {
            освободить_узел(ф);  // шаблон больше не нужен
            continue;
        }
        дин_массив_добавить(&новые, &ф);
    }
    for (size_t i = 0; i < к.клоны.длина; ++i) {
        Узел **p = дин_массив_получить(&к.клоны, i);
        дин_массив_добавить(&новые, p);
    }
    дин_массив_освободить(&программа->дети);
    программа->дети = новые;

    for (size_t i = 0; i < к.имена_инстанций.длина; ++i) {
        char **p = дин_массив_получить(&к.имена_инстанций, i);
        free(*p);
    }
    дин_массив_освободить(&к.имена_инстанций);
    дин_массив_освободить(&к.клоны);
    дин_массив_освободить(&к.очередь);
    return 1;
}
