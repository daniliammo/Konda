#include <stdlib.h>
#include <string.h>

#include "аст.h"

Узел *узел_создать(ТипУзла тип)
{
    Узел *узел = calloc(1, sizeof(Узел));
    if (!узел) {
        perror("calloc");
        exit(1);
    }
    узел->тип = тип;
    дин_массив_инициализировать(&узел->дети, sizeof(Узел *));
    return узел;
}

void узел_добавить_ребёнка(Узел *родитель, Узел *ребёнок)
{
    дин_массив_добавить(&родитель->дети, &ребёнок);
}

Узел *узел_ребёнок(const Узел *узел, size_t индекс)
{
    Узел **p = дин_массив_получить((ДинМассив *)&узел->дети, индекс);
    return p ? *p : nullptr;
}

size_t узел_число_детей(const Узел *узел)
{
    return узел->дети.длина;
}

// Глубокая копия поддерева. Все поля Узла — POD (включая токен: указатели в
// исходный буфер только для чтения, они разделяются), кроме массива детей —
// он пересоздаётся и заполняется рекурсивными копиями. Нужна для мономорфизации
// обобщённых функций: шаблон клонируется под каждую конкретную инстанциацию.
Узел *узел_копировать(const Узел *исходный)
{
    if (!исходный) {
        return nullptr;
    }
    Узел *копия = calloc(1, sizeof(Узел));
    if (!копия) {
        perror("calloc");
        exit(1);
    }
    *копия = *исходный;  // POD-поля + токен (текст разделяется с оригиналом)
    дин_массив_инициализировать(&копия->дети, sizeof(Узел *));
    // Собственное имя (мономорфизация) — владелец, поэтому дублируем и
    // перенаправляем токен в новый буфер, чтобы копия не разделяла память.
    if (исходный->собственное_имя) {
        копия->собственное_имя = strdup(исходный->собственное_имя);
        if (копия->собственное_имя) {
            копия->токен.начало = копия->собственное_имя;
            копия->токен.длина = strlen(копия->собственное_имя);
        }
    }
    for (size_t i = 0; i < исходный->дети.длина; ++i) {
        узел_добавить_ребёнка(копия, узел_копировать(узел_ребёнок(исходный, i)));
    }
    return копия;
}

void освободить_узел(Узел *узел)
{
    if (!узел) {
        return;
    }
    for (size_t i = 0; i < узел->дети.длина; ++i) {
        освободить_узел(узел_ребёнок(узел, i));
    }
    дин_массив_освободить(&узел->дети);
    free(узел->собственное_имя);
    free(узел);
}
