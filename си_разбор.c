typedef struct {
    const char *имя;
    size_t длина;
    СиТип тип;      // разрешение: скаляр → базовый; struct/enum/fnptr → С_ИМЯ
} ЗаписьТипдефа;

typedef struct {
    СиТокен *ток;
    size_t число;
    size_t поз;
    ДинМассив *ошибки;
    ДинМассив типдефы;   // ЗаписьТипдефа
    int сбой;            // фатальный сбой разбора
} Разбор;

static СиТокен *тек(Разбор *р) { return &р->ток[р->поз]; }
static СиТокен *пик(Разбор *р, size_t n)
{
    size_t j = р->поз + n;
    if (j >= р->число) j = р->число - 1;
    return &р->ток[j];
}
static int в_конце(Разбор *р) { return тек(р)->вид == СТ_КОНЕЦ; }
static СиТокен съесть(Разбор *р) { СиТокен т = р->ток[р->поз]; if (!в_конце(р)) р->поз++; return т; }

static int текст_равен(const СиТокен *т, const char *s)
{
    size_t n = strlen(s);
    return т->длина == n && memcmp(т->текст, s, n) == 0;
}
static int это(Разбор *р, const char *s) { return текст_равен(тек(р), s); }
static int съесть_если(Разбор *р, const char *s)
{
    if (это(р, s)) { съесть(р); return 1; }
    return 0;
}

static void ошибка_тут(Разбор *р, const char *формат, ...)
{
    СиТокен *т = тек(р);
    СиДиаг д = { .ошибка = 1, .строка = т->строка, .колонка = т->колонка };
    va_list ап; va_start(ап, формат);
    vsnprintf(д.сообщение, sizeof(д.сообщение), формат, ап);
    va_end(ап);
    if (р->ошибки) дин_массив_добавить(р->ошибки, &д);
}

// Ожидать конкретную пунктуацию; при несовпадении — ошибка (без остановки).
static void ожидать(Разбор *р, const char *s)
{
    if (!съесть_если(р, s)) {
        ошибка_тут(р, "ожидалось «%s», а найдено «%.*s»", s,
                   (int)тек(р)->длина, тек(р)->текст);
    }
}

// Пропуск до следующего ';' или '}' верхнего уровня — восстановление батча.
static void синхронизировать(Разбор *р)
{
    while (!в_конце(р)) {
        if (это(р, ";")) { съесть(р); return; }
        if (это(р, "}")) return;
        съесть(р);
    }
}

