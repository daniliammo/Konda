// typedef: сразу после ключевого слова typedef
static void разобрать_typedef(Разбор *р, СиУзел *программа)
{
    съесть(р); // typedef

    // typedef R (*Имя)(параметры);  — указатель на функцию
    // Определяем по шаблону: тип, затем '(' '*' Имя ')' '('
    {
        size_t сейв = р->поз;
        СиТип возврат; СиУзел *инл0 = nullptr;
        if (разобрать_тип(р, &возврат, &инл0) && это(р, "(") &&
            текст_равен(пик(р, 1), "*")) {
            съесть(р); съесть(р); // '(' '*'
            if (тек(р)->вид == СТ_ИДЕНТ) {
                СиТокен имя = съесть(р);
                if (съесть_если(р, ")") && это(р, "(")) {
                    if (инл0) си_освободить(инл0);
                    СиУзел *тф = си_узел(У_ТИПФУНКЦИИ);
                    тф->токен = имя;
                    тф->тип = возврат;
                    съесть(р); // '('
                    while (!это(р, ")") && !в_конце(р)) {
                        if (текст_равен(тек(р), "void") && текст_равен(пик(р,1), ")")) { съесть(р); break; }
                        СиТип пт; СиУзел *и2 = nullptr;
                        if (!разобрать_тип(р, &пт, &и2)) { ошибка_тут(р, "тип параметра типфункции"); break; }
                        if (и2) си_освободить(и2);
                        while (съесть_если(р, "*")) t_ук(&пт);
                        СиУзел *п = си_узел(У_ПАРАМЕТР); п->тип = пт;
                        if (тек(р)->вид == СТ_ИДЕНТ) п->токен = съесть(р);
                        си_добавить(тф, п);
                        if (!съесть_если(р, ",")) break;
                    }
                    ожидать(р, ")"); ожидать(р, ";");
                    СиТип псевдо = { .базовый = С_ИМЯ, .имя = имя.текст, .имя_длина = имя.длина };
                    зарегистрировать_типдеф(р, имя, псевдо);
                    си_добавить(программа, тф);
                    return;
                }
            }
        }
        р->поз = сейв;
    }

    // typedef struct/union/enum { … } Имя;  или  typedef <тип> Имя;
    СиТип базовый; СиУзел *инл = nullptr;
    if (!разобрать_тип(р, &базовый, &инл)) { ошибка_тут(р, "ожидался тип в typedef"); синхронизировать(р); return; }
    СиТип т = базовый;
    while (съесть_если(р, "*")) t_ук(&т);
    if (тек(р)->вид != СТ_ИДЕНТ) { ошибка_тут(р, "ожидалось имя псевдонима typedef"); if (инл) си_освободить(инл); синхронизировать(р); return; }
    СиТокен имя = съесть(р);
    ожидать(р, ";");

    if (инл) {
        // именуем анонимное определение именем псевдонима, если у него нет тега
        if (инл->токен.длина == 0) инл->токен = имя;
        си_добавить(программа, инл);
        СиТип псевдо = { .базовый = С_ИМЯ, .имя = инл->токен.текст, .имя_длина = инл->токен.длина, .указатели = т.указатели };
        зарегистрировать_типдеф(р, имя, псевдо);
    } else {
        зарегистрировать_типдеф(р, имя, т);
    }
}

static void разобрать_верхнее(Разбор *р, СиУзел *программа)
{
    // директива препроцессора
    if (тек(р)->вид == СТ_ПУНКТ && тек(р)->длина >= 1 && тек(р)->текст[0] == '#') {
        СиТокен дир = съесть(р);
        // поддержан только #include
        const char *p = дир.текст + 1;
        size_t ост = дир.длина - 1;
        while (ост && (*p == ' ' || *p == '\t')) { p++; ост--; }
        if (ост >= 7 && strncmp(p, "include", 7) == 0) {
            СиУзел *у = си_узел(У_ПРЕПРОЦЕССОР);
            у->токен = дир;
            си_добавить(программа, у);
        } else {
            си_добавить_ошибку(р->ошибки, дир.строка, дир.колонка,
                "директива препроцессора «%.*s» не поддержана (только #include)",
                (int)дир.длина, дир.текст);
        }
        return;
    }

    if (съесть_если(р, ";")) return;

    if (текст_равен(тек(р), "typedef")) { разобрать_typedef(р, программа); return; }

    // struct/union/enum-определение верхнего уровня либо объявление переменной
    if (текст_равен(тек(р), "struct") || текст_равен(тек(р), "union") || текст_равен(тек(р), "enum")) {
        // Если это чистое определение «… { … } ;» — разберём тип (создаст инлайн)
        // и проверим завершение ';'.
        СиТип базовый; СиУзел *инл = nullptr;
        if (!разобрать_тип(р, &базовый, &инл)) { ошибка_тут(р, "ошибка типа"); синхронизировать(р); return; }
        if (инл) си_добавить(программа, инл);
        if (съесть_если(р, ";")) return; // чистое определение
        // иначе — объявители (глобальные переменные) этого типа
        for (;;) {
            СиТип т = базовый;
            while (съесть_если(р, "*")) t_ук(&т);
            if (тек(р)->вид != СТ_ИДЕНТ) { ошибка_тут(р, "ожидалось имя"); синхронизировать(р); return; }
            СиУзел *о = си_узел(У_ОБЪЯВЛЕНИЕ); о->токен = съесть(р); о->тип = т;
            if (съесть_если(р, "[")) { о->есть_массив = 1;
                if (тек(р)->вид == СТ_ЧИСЛО) { о->разм_массива = strtol(тек(р)->текст, nullptr, 0); съесть(р); }
                ожидать(р, "]"); }
            if (съесть_если(р, "=")) { if (это(р, "{")) си_добавить(о, разобрать_список_иниц(р)); else си_добавить(о, разобрать_присваивание(р)); }
            си_добавить(программа, о);
            if (!съесть_если(р, ",")) break;
        }
        ожидать(р, ";");
        return;
    }

    // обычное объявление: тип, затем имя(...) — функция, либо переменные
    if (!начинается_тип(р)) {
        ошибка_тут(р, "неожиданный токен «%.*s» на верхнем уровне",
                   (int)тек(р)->длина, тек(р)->текст);
        синхронизировать(р);
        return;
    }

    СиТип базовый; СиУзел *инл = nullptr;
    if (!разобрать_тип(р, &базовый, &инл)) { ошибка_тут(р, "ошибка типа"); синхронизировать(р); return; }
    if (инл) си_добавить(программа, инл);

    for (;;) {
        СиТип т = базовый;
        while (съесть_если(р, "*")) t_ук(&т);
        if (тек(р)->вид != СТ_ИДЕНТ) { ошибка_тут(р, "ожидалось имя"); синхронизировать(р); return; }
        СиТокен имя = съесть(р);

        if (это(р, "(")) {
            // функция
            СиУзел *ф = си_узел(У_ФУНКЦИЯ);
            ф->токен = имя;
            ф->тип = т;
            if (текст_равен(&имя, "main")) ф->это_точка_входа = 1;
            съесть(р); // '('
            int вариативная = 0;
            while (!это(р, ")") && !в_конце(р)) {
                if (это(р, "...")) { вариативная = 1; съесть(р); break; }
                if (текст_равен(тек(р), "void") && текст_равен(пик(р,1), ")")) { съесть(р); break; }
                СиТип пт; СиУзел *и2 = nullptr;
                if (!разобрать_тип(р, &пт, &и2)) { ошибка_тут(р, "тип параметра"); break; }
                if (и2) си_освободить(и2);
                while (съесть_если(р, "*")) t_ук(&пт);
                СиУзел *п = си_узел(У_ПАРАМЕТР);
                п->тип = пт;
                if (тек(р)->вид == СТ_ИДЕНТ) п->токен = съесть(р);
                // массив-параметр → ещё указатель
                if (съесть_если(р, "[")) { п->тип.указатели++; if (!это(р, "]")) разобрать_выражение(р); ожидать(р, "]"); }
                си_добавить(ф, п);
                if (!съесть_если(р, ",")) break;
            }
            ожидать(р, ")");
            if (это(р, "{")) {
                if (вариативная) си_добавить_ошибку(р->ошибки, имя.строка, имя.колонка,
                    "определение вариативной функции не поддержано");
                си_добавить(ф, разобрать_блок(р));
                си_добавить(программа, ф);
            } else {
                // прототип — Konda прототипирует сам, пропускаем
                съесть_если(р, ";");
                си_освободить(ф);
            }
            return; // функция завершает объявление
        }

        // глобальная переменная
        СиУзел *о = си_узел(У_ОБЪЯВЛЕНИЕ);
        о->токен = имя; о->тип = т;
        if (съесть_если(р, "[")) { о->есть_массив = 1;
            if (тек(р)->вид == СТ_ЧИСЛО) { о->разм_массива = strtol(тек(р)->текст, nullptr, 0); съесть(р); }
            else if (!это(р, "]")) разобрать_выражение(р);
            ожидать(р, "]"); }
        if (съесть_если(р, "=")) { if (это(р, "{")) си_добавить(о, разобрать_список_иниц(р)); else си_добавить(о, разобрать_присваивание(р)); }
        си_добавить(программа, о);
        if (!съесть_если(р, ",")) break;
    }
    ожидать(р, ";");
}

СиУзел *си_разобрать(const char *исходник, size_t длина, ДинМассив *ошибки)
{
    ДинМассив ток;
    дин_массив_инициализировать(&ток, sizeof(СиТокен));
    лексировать(исходник, длина, &ток);

    Разбор р = {0};
    р.ток = (СиТокен *)ток.данные;
    р.число = ток.длина;
    р.поз = 0;
    р.ошибки = ошибки;
    дин_массив_инициализировать(&р.типдефы, sizeof(ЗаписьТипдефа));

    СиУзел *программа = си_узел(У_ПРОГРАММА);
    while (!в_конце(&р)) {
        size_t до = р.поз;
        разобрать_верхнее(&р, программа);
        if (р.поз == до) съесть(&р); // страховка от зацикливания
    }

    дин_массив_освободить(&р.типдефы);
    дин_массив_освободить(&ток);
    return программа;
}

// ─── верхний оркестратор ─────────────────────────────────────────────────────

int сиконда_конвертировать(const char *исходник, size_t длина,
                           БуферКода *вывод, ДинМассив *ошибки)
{
    СиУзел *прог = си_разобрать(исходник, длина, ошибки);
    int ок = 1;
    if (!прог) { ок = 0; }
    else {
        if (!конда_эмит(прог, вывод, ошибки)) ок = 0;
        си_освободить(прог);
    }
    if (ошибки && ошибки->длина > 0) ок = 0;
    return ок;
}
