static СиУзел *лит_из_текущего(Разбор *р)
{
    СиТокен *т = тек(р);
    СиУзел *у = nullptr;
    switch (т->вид) {
        case СТ_ЧИСЛО: у = си_узел(У_ЧИСЛО); break;
        case СТ_СТРОКА: у = си_узел(У_СТРОКА_ЛИТ); break;
        case СТ_СИМВОЛ: у = си_узел(У_СИМВОЛ_ЛИТ); break;
        case СТ_ИДЕНТ: у = си_узел(У_ИДЕНТ); break;
        default: return nullptr;
    }
    у->токен = съесть(р);
    return у;
}

static СиУзел *разобрать_список_иниц(Разбор *р)   // видит '{'
{
    СиУзел *сп = си_узел(У_СПИСОК_ИНИЦ);
    сп->токен = съесть(р); // '{'
    while (!это(р, "}") && !в_конце(р)) {
        // designated-инициализаторы (.поле=, [i]=) не поддерживаем
        if (это(р, ".") || это(р, "[")) {
            ошибка_тут(р, "designated-инициализаторы не поддержаны");
            си_освободить(сп); синхронизировать(р); return si_узел_заглушка();
        }
        if (это(р, "{")) си_добавить(сп, разобрать_список_иниц(р));
        else си_добавить(сп, разобрать_присваивание(р));
        if (!съесть_если(р, ",")) break;
    }
    ожидать(р, "}");
    return сп;
}

// заглушка, если не удалось (не используется в успешном пути)
static СиУзел *si_узел_заглушка(void) { return си_узел(У_ЧИСЛО); }

static СиУзел *разобрать_первичное(Разбор *р)
{
    СиТокен *т = тек(р);
    if (это(р, "(")) {
        // Касты и составные литералы обрабатывает разобрать_унарное; здесь «(»
        // означает группирующие скобки.
        съесть(р); // '('
        СиУзел *в = разобрать_выражение(р);
        ожидать(р, ")");
        return в;
    }
    if (текст_равен(т, "sizeof")) {
        съесть(р);
        if (это(р, "(") && pik_тип(р)) {
            съесть(р); // '('
            СиТип цт; СиУзел *инл = nullptr;
            разобрать_тип(р, &цт, &инл);
            if (инл) си_освободить(инл);
            ожидать(р, ")");
            СиУзел *у = си_узел(У_РАЗМЕР_ТИПА); у->тип = цт; return у;
        }
        СиУзел *у = си_узел(У_РАЗМЕР_ВЫРАЖ);
        си_добавить(у, разобрать_присваивание(р));
        return у;
    }
    if (это(р, "{")) return разобрать_список_иниц(р);
    СиУзел *лит = лит_из_текущего(р);
    if (лит) return лит;
    ошибка_тут(р, "неожиданный токен «%.*s» в выражении", (int)т->длина, т->текст);
    съесть(р);
    return si_узел_заглушка();
}

// заглядывание: после '(' идёт тип?
static int pik_тип(Разбор *р)
{
    if (!это(р, "(")) return 0;
    size_t сейв = р->поз;
    съесть(р);
    int рез = начинается_тип(р);
    р->поз = сейв;
    return рез;
}

static СиУзел *разобрать_унарное(Разбор *р);

static СиУзел *разобрать_постфикс(Разбор *р)
{
    СиУзел *база = разобрать_первичное(р);
    for (;;) {
        if (это(р, "(")) {
            съесть(р);
            СиУзел *в = си_узел(У_ВЫЗОВ);
            си_добавить(в, база);
            while (!это(р, ")") && !в_конце(р)) {
                си_добавить(в, разобрать_присваивание(р));
                if (!съесть_если(р, ",")) break;
            }
            ожидать(р, ")");
            база = в;
        } else if (это(р, "[")) {
            съесть(р);
            СиУзел *и = си_узел(У_ИНДЕКС);
            си_добавить(и, база);
            си_добавить(и, разобрать_выражение(р));
            ожидать(р, "]");
            база = и;
        } else if (это(р, ".") || это(р, "->")) {
            СиТокен оп = съесть(р);
            СиУзел *п = си_узел(У_ПОЛЕ);
            п->токен = оп;
            си_добавить(п, база);
            п->поле_имя = съесть(р); // имя поля
            база = п;
        } else if (это(р, "++") || это(р, "--")) {
            СиТокен оп = съесть(р);
            СиУзел *п = си_узел(У_ПОСТФИКС);
            п->токен = оп;
            си_добавить(п, база);
            база = п;
        } else break;
    }
    return база;
}

static СиУзел *разобрать_унарное(Разбор *р)
{
    if (это(р, "!") || это(р, "~") || это(р, "-") || это(р, "+") ||
        это(р, "*") || это(р, "&") || это(р, "++") || это(р, "--")) {
        СиТокен оп = съесть(р);
        СиУзел *у = си_узел(У_УНОП);
        у->токен = оп;
        си_добавить(у, разобрать_унарное(р));
        return у;
    }
    // C-каст: '(' тип ')' унарное
    if (pik_тип(р)) {
        size_t сейв = р->поз;
        съесть(р); // '('
        СиТип цт; СиУзел *инл = nullptr;
        if (разобрать_тип(р, &цт, &инл) && это(р, ")")) {
            съесть(р);
            if (инл) си_освободить(инл);
            if (это(р, "{")) {  // на самом деле составной литерал
                СиУзел *сл = си_узел(У_СОСТ_ЛИТЕРАЛ);
                сл->тип = цт;
                си_добавить(сл, разобрать_список_иниц(р));
                return сл;
            }
            СиУзел *к = си_узел(У_КАСТ);
            к->тип = цт;
            си_добавить(к, разобрать_унарное(р));
            return к;
        }
        if (инл) си_освободить(инл);
        р->поз = сейв;
    }
    return разобрать_постфикс(р);
}

static int приоритет_бинопа(Разбор *р)
{
    if (это(р, "*") || это(р, "/") || это(р, "%")) return 10;
    if (это(р, "+") || это(р, "-")) return 9;
    if (это(р, "<<") || это(р, ">>")) return 8;
    if (это(р, "<") || это(р, "<=") || это(р, ">") || это(р, ">=")) return 7;
    if (это(р, "==") || это(р, "!=")) return 6;
    if (это(р, "&")) return 5;
    if (это(р, "^")) return 4;
    if (это(р, "|")) return 3;
    if (это(р, "&&")) return 2;
    if (это(р, "||")) return 1;
    return -1;
}

static СиУзел *разобрать_бинарное(Разбор *р, int мин)
{
    СиУзел *лево = разобрать_унарное(р);
    for (;;) {
        int пр = приоритет_бинопа(р);
        if (пр < мин || пр < 0) break;
        СиТокен оп = съесть(р);
        СиУзел *право = разобрать_бинарное(р, пр + 1);
        СиУзел *б = си_узел(У_БИНОП);
        б->токен = оп;
        си_добавить(б, лево);
        си_добавить(б, право);
        лево = б;
    }
    return лево;
}

static СиУзел *разобрать_тернарник(Разбор *р)
{
    СиУзел *усл = разобрать_бинарное(р, 1);
    if (съесть_если(р, "?")) {
        СиУзел *т = си_узел(У_ТЕРНАРНИК);
        си_добавить(т, усл);
        си_добавить(т, разобрать_присваивание(р));
        ожидать(р, ":");
        си_добавить(т, разобрать_присваивание(р));
        return т;
    }
    return усл;
}

static int это_присв_оператор(Разбор *р)
{
    return это(р, "=") || это(р, "+=") || это(р, "-=") || это(р, "*=") ||
           это(р, "/=") || это(р, "%=") || это(р, "&=") || это(р, "|=") ||
           это(р, "^=") || это(р, "<<=") || это(р, ">>=");
}

static СиУзел *разобрать_присваивание(Разбор *р)
{
    СиУзел *лево = разобрать_тернарник(р);
    if (это_присв_оператор(р)) {
        СиТокен оп = съесть(р);
        СиУзел *прис = си_узел(У_ПРИСВАИВАНИЕ);
        прис->токен = оп;
        си_добавить(прис, лево);
        си_добавить(прис, разобрать_присваивание(р));
        return прис;
    }
    return лево;
}

// ─── операторы ───────────────────────────────────────────────────────────────

static СиУзел *разобрать_блок(Разбор *р);
static СиУзел *разобрать_оператор(Разбор *р);

// Разбор одного или нескольких объявлений «тип имя [= иниц] [, имя …] ;».
// Все объявители добавляются в «блок» по порядку.
static void разобрать_объявление(Разбор *р, СиУзел *блок)
{
    СиТип базовый; СиУзел *инл = nullptr;
    if (!разобрать_тип(р, &базовый, &инл)) {
        ошибка_тут(р, "ожидался тип в объявлении");
        синхронизировать(р);
        return;
    }
    if (инл) си_добавить(блок, инл);
    for (;;) {
        СиТип т = базовый;
        while (съесть_если(р, "*")) t_ук(&т);
        if (тек(р)->вид != СТ_ИДЕНТ) { ошибка_тут(р, "ожидалось имя переменной"); синхронизировать(р); return; }
        СиУзел *о = си_узел(У_ОБЪЯВЛЕНИЕ);
        о->токен = съесть(р);
        // массив
        if (съесть_если(р, "[")) {
            о->есть_массив = 1;
            if (!это(р, "]")) {
                СиТокен *рм = тек(р);
                if (рм->вид == СТ_ЧИСЛО) { о->разм_массива = strtol(рм->текст, nullptr, 0); съесть(р); }
                else разобрать_выражение(р); // размер-выражение игнорируем как значение
            }
            ожидать(р, "]");
        }
        о->тип = т;
        if (съесть_если(р, "=")) {
            if (это(р, "{")) си_добавить(о, разобрать_список_иниц(р));
            else си_добавить(о, разобрать_присваивание(р));
        }
        си_добавить(блок, о);
        if (!съесть_если(р, ",")) break;
    }
    ожидать(р, ";");
}

static void t_ук(СиТип *т) { т->указатели++; }

static СиУзел *разобрать_оператор(Разбор *р)
{
    if (это(р, "{")) return разобрать_блок(р);
    if (съесть_если(р, ";")) return nullptr; // пустой оператор

    if (текст_равен(тек(р), "goto")) {
        ошибка_тут(р, "«goto» не поддержано конвертером");
        синхронизировать(р); return nullptr;
    }
    // метка «имя:» — не поддержана
    if (тек(р)->вид == СТ_ИДЕНТ && !это_примитив(тек(р)) && текст_равен(пик(р,1), ":")
        && !начинается_тип(р)) {
        ошибка_тут(р, "метки не поддержаны");
        съесть(р); съесть(р); return nullptr;
    }

    if (текст_равен(тек(р), "if")) {
        съесть(р); ожидать(р, "(");
        СиУзел *у = си_узел(У_ЕСЛИ);
        си_добавить(у, разобрать_выражение(р));
        ожидать(р, ")");
        си_добавить(у, разобрать_оператор(р));
        if (текст_равен(тек(р), "else")) { съесть(р); си_добавить(у, разобрать_оператор(р)); }
        return у;
    }
    if (текст_равен(тек(р), "while")) {
        съесть(р); ожидать(р, "(");
        СиУзел *у = си_узел(У_ПОКА);
        си_добавить(у, разобрать_выражение(р));
        ожидать(р, ")");
        си_добавить(у, разобрать_оператор(р));
        return у;
    }
    if (текст_равен(тек(р), "do")) {
        съесть(р);
        СиУзел *у = си_узел(У_ДЕЛАЙ);
        си_добавить(у, разобрать_оператор(р));
        if (!текст_равен(тек(р), "while")) { ошибка_тут(р, "ожидалось «while» после «do»"); return у; }
        съесть(р); ожидать(р, "(");
        си_добавить(у, разобрать_выражение(р));
        ожидать(р, ")"); съесть_если(р, ";");
        return у;
    }
    if (текст_равен(тек(р), "for")) {
        съесть(р); ожидать(р, "(");
        СиУзел *у = си_узел(У_ДЛЯ);
        // инициализация
        if (это(р, ";")) { съесть(р); си_добавить(у, си_узел(У_БЛОК)); /* пусто */ }
        else if (начинается_тип(р)) {
            // объявление в for: поддержим один объявитель
            СиТип т; СиУзел *инл = nullptr; разобрать_тип(р, &т, &инл);
            if (инл) си_освободить(инл);
            while (съесть_если(р, "*")) t_ук(&т);
            СиУзел *о = си_узел(У_ОБЪЯВЛЕНИЕ); о->токен = съесть(р); о->тип = т;
            if (съесть_если(р, "=")) си_добавить(о, разобрать_присваивание(р));
            ожидать(р, ";");
            си_добавить(у, о);
        } else { си_добавить(у, разобрать_выражение(р)); ожидать(р, ";"); }
        // условие
        if (это(р, ";")) { съесть(р); си_добавить(у, си_узел(У_БЛОК)); }
        else { си_добавить(у, разобрать_выражение(р)); ожидать(р, ";"); }
        // шаг
        if (это(р, ")")) си_добавить(у, си_узел(У_БЛОК));
        else си_добавить(у, разобрать_выражение(р));
        ожидать(р, ")");
        си_добавить(у, разобрать_оператор(р));
        return у;
    }
    if (текст_равен(тек(р), "switch")) {
        съесть(р); ожидать(р, "(");
        СиУзел *у = си_узел(У_ВЫБОР);
        си_добавить(у, разобрать_выражение(р));
        ожидать(р, ")"); ожидать(р, "{");
        while (!это(р, "}") && !в_конце(р)) {
            if (текст_равен(тек(р), "case") || текст_равен(тек(р), "default")) {
                СиУзел *ветка = си_узел(У_ВЕТКА);
                if (текст_равен(тек(р), "default")) { съесть(р); ветка->токен = (СиТокен){ .вид = СТ_ИДЕНТ, .текст = "_", .длина = 1 }; ожидать(р, ":"); }
                else { съесть(р); ветка->токен = съесть(р); ожидать(р, ":"); }
                // операторы ветки до следующего case/default/}
                while (!текст_равен(тек(р), "case") && !текст_равен(тек(р), "default")
                       && !это(р, "}") && !в_конце(р)) {
                    if (текст_равен(тек(р), "break")) { съесть(р); съесть_если(р, ";"); break; }
                    СиУзел *оп = разобрать_оператор(р);
                    if (оп) си_добавить(ветка, оп);
                }
                си_добавить(у, ветка);
            } else {
                ошибка_тут(р, "ожидалось «case»/«default» в «switch»");
                синхронизировать(р);
            }
        }
        ожидать(р, "}");
        return у;
    }
    if (текст_равен(тек(р), "return")) {
        съесть(р);
        СиУзел *у = си_узел(У_ВОЗВРАТ);
        if (!это(р, ";")) си_добавить(у, разобрать_выражение(р));
        ожидать(р, ";");
        return у;
    }
    if (текст_равен(тек(р), "break"))    { съесть(р); ожидать(р, ";"); return си_узел(У_ПРЕРВАТЬ); }
    if (текст_равен(тек(р), "continue")) { съесть(р); ожидать(р, ";"); return си_узел(У_ПРОДОЛЖИТЬ); }

    // выражение-оператор
    СиУзел *в = разобрать_выражение(р);
    ожидать(р, ";");
    СиУзел *у = си_узел(У_ВЫРАЖ_ОПЕРАТОР);
    си_добавить(у, в);
    return у;
}

static СиУзел *разобрать_блок(Разбор *р)
{
    СиУзел *блок = си_узел(У_БЛОК);
    ожидать(р, "{");
    while (!это(р, "}") && !в_конце(р)) {
        if (начинается_тип(р)) {
            разобрать_объявление(р, блок);
        } else {
            СиУзел *оп = разобрать_оператор(р);
            if (оп) си_добавить(блок, оп);
        }
    }
    ожидать(р, "}");
    return блок;
}

// поля структуры/союза: видит '{'
static СиУзел *разобрать_поля_структуры(Разбор *р)
{
    СиУзел *с = си_узел(У_СТРУКТУРА);
    ожидать(р, "{");
    while (!это(р, "}") && !в_конце(р)) {
        СиТип базовый; СиУзел *инл = nullptr;
        if (!разобрать_тип(р, &базовый, &инл)) { ошибка_тут(р, "ожидался тип поля"); синхронизировать(р); continue; }
        if (инл) си_освободить(инл); // вложенные анонимные типы полей не разворачиваем
        for (;;) {
            СиТип т = базовый;
            while (съесть_если(р, "*")) t_ук(&т);
            if (тек(р)->вид != СТ_ИДЕНТ) { ошибка_тут(р, "ожидалось имя поля"); break; }
            СиУзел *поле = си_узел(У_ПАРАМЕТР);
            поле->токен = съесть(р);
            if (съесть_если(р, "[")) {
                поле->есть_массив = 1;
                if (тек(р)->вид == СТ_ЧИСЛО) { поле->разм_массива = strtol(тек(р)->текст, nullptr, 0); съесть(р); }
                else if (!это(р, "]")) разобрать_выражение(р);
                ожидать(р, "]");
            }
            if (это(р, ":")) { // битовое поле
                ошибка_тут(р, "битовые поля не поддержаны");
                съесть(р); if (тек(р)->вид == СТ_ЧИСЛО) съесть(р);
            }
            поле->тип = т;
            си_добавить(с, поле);
            if (!съесть_если(р, ",")) break;
        }
        ожидать(р, ";");
    }
    ожидать(р, "}");
    return с;
}

static СиУзел *разобрать_тело_перечисления(Разбор *р, СиТокен имя)
{
    СиУзел *е = си_узел(У_ПЕРЕЧИСЛЕНИЕ);
    е->токен = имя;
    ожидать(р, "{");
    while (!это(р, "}") && !в_конце(р)) {
        if (тек(р)->вид != СТ_ИДЕНТ) { ошибка_тут(р, "ожидался элемент перечисления"); синхронизировать(р); break; }
        СиУзел *в = си_узел(У_ВАРИАНТ);
        в->токен = съесть(р);
        if (съесть_если(р, "=")) си_добавить(в, разобрать_присваивание(р));
        си_добавить(е, в);
        if (!съесть_если(р, ",")) break;
    }
    ожидать(р, "}");
    return е;
}

