static const ЗаписьТипдефа *найти_типдеф(Разбор *р, const СиТокен *т)
{
    for (size_t i = 0; i < р->типдефы.длина; ++i) {
        ЗаписьТипдефа *з = дин_массив_получить(&р->типдефы, i);
        if (з->длина == т->длина && memcmp(з->имя, т->текст, т->длина) == 0) return з;
    }
    return nullptr;
}

static int это_квалификатор(const СиТокен *т)
{
    return текст_равен(т, "const") || текст_равен(т, "volatile") ||
           текст_равен(т, "register") || текст_равен(т, "restrict") ||
           текст_равен(т, "static") || текст_равен(т, "extern") ||
           текст_равен(т, "inline") || текст_равен(т, "auto") ||
           текст_равен(т, "_Noreturn") || текст_равен(т, "_Atomic");
}

static int это_примитив(const СиТокен *т)
{
    static const char *слова[] = {
        "void","char","short","int","long","float","double","signed","unsigned",
        "_Bool","bool","int8_t","int16_t","int32_t","int64_t",
        "uint8_t","uint16_t","uint32_t","uint64_t","size_t", nullptr
    };
    for (int i = 0; слова[i]; ++i) if (текст_равен(т, слова[i])) return 1;
    return 0;
}

// Начинается ли на текущей позиции тип (для операторов-объявлений и кастов)?
static int начинается_тип(Разбор *р)
{
    СиТокен *т = тек(р);
    if (т->вид != СТ_ИДЕНТ) return 0;
    if (это_квалификатор(т) || это_примитив(т)) return 1;
    if (текст_равен(т, "struct") || текст_равен(т, "union") || текст_равен(т, "enum")) return 1;
    if (найти_типдеф(р, т)) return 1;
    return 0;
}

static СиУзел *разобрать_поля_структуры(Разбор *р);  // вперёд
static СиУзел *разобрать_тело_перечисления(Разбор *р, СиТокен имя);

// Разбор типа. При инлайн-определении struct/union/enum с телом — кладёт
// определение в *инлайн (иначе *инлайн = nullptr). Возвращает 1 при успехе.
static int разобрать_тип(Разбор *р, СиТип *тип, СиУзел **инлайн)
{
    memset(тип, 0, sizeof(*тип));
    if (инлайн) *инлайн = nullptr;

    // ведущие квалификаторы
    while (тек(р)->вид == СТ_ИДЕНТ && это_квалификатор(тек(р))) съесть(р);

    СиТокен *т = тек(р);

    // struct / union / enum
    if (текст_равен(т, "struct") || текст_равен(т, "union") || текст_равен(т, "enum")) {
        int это_enum = текст_равен(т, "enum");
        int это_un = текст_равен(т, "union");
        съесть(р);
        СиТокен имя = { .вид = СТ_ИДЕНТ, .текст = "", .длина = 0 };
        if (тек(р)->вид == СТ_ИДЕНТ && !это_примитив(тек(р))) имя = съесть(р);
        if (это(р, "{")) {
            // инлайн-определение
            СиУзел *опр;
            if (это_enum) опр = разобрать_тело_перечисления(р, имя);
            else { опр = разобрать_поля_структуры(р); опр->токен = имя; опр->это_union = это_un; }
            if (инлайн) *инлайн = опр;
            тип->базовый = С_ИМЯ; тип->имя = имя.текст; тип->имя_длина = имя.длина;
        } else {
            тип->базовый = С_ИМЯ; тип->имя = имя.текст; тип->имя_длина = имя.длина;
        }
        while (съесть_если(р, "*")) { тип->указатели++;
            while (тек(р)->вид == СТ_ИДЕНТ && это_квалификатор(тек(р))) съесть(р); }
        return 1;
    }

    // typedef-имя
    if (т->вид == СТ_ИДЕНТ && !это_примитив(т)) {
        const ЗаписьТипдефа *з = найти_типдеф(р, т);
        if (з) {
            съесть(р);
            *тип = з->тип;
            while (съесть_если(р, "*")) { тип->указатели++;
                while (тек(р)->вид == СТ_ИДЕНТ && это_квалификатор(тек(р))) съесть(р); }
            return 1;
        }
        return 0;  // не тип
    }

    // примитивы: жадно собираем прогон ключевых слов
    int дл_long = 0, есть_short = 0, есть_char = 0, есть_int = 0, есть_double = 0,
        есть_float = 0, есть_void = 0, есть_bool = 0, есть_unsigned = 0, есть_signed = 0;
    СиБазовыйТип прямой = С_ПУСТО;
    int было = 0;
    while (тек(р)->вид == СТ_ИДЕНТ) {
        СиТокен *k = тек(р);
        if (это_квалификатор(k)) { съесть(р); continue; }
        if (текст_равен(k, "unsigned")) { есть_unsigned = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "signed"))   { есть_signed = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "void"))   { есть_void = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "char"))   { есть_char = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "short"))  { есть_short = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "int"))    { есть_int = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "long"))   { дл_long++; съесть(р); было = 1; continue; }
        if (текст_равен(k, "float"))  { есть_float = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "double")) { есть_double = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "_Bool") || текст_равен(k, "bool")) { есть_bool = 1; съесть(р); было = 1; continue; }
        if (текст_равен(k, "int8_t"))  { прямой = С_INT8;  съесть(р); было = 1; continue; }
        if (текст_равен(k, "int16_t")) { прямой = С_INT16; съесть(р); было = 1; continue; }
        if (текст_равен(k, "int32_t")) { прямой = С_INT32; съесть(р); было = 1; continue; }
        if (текст_равен(k, "int64_t")) { прямой = С_INT64; съесть(р); было = 1; continue; }
        if (текст_равен(k, "uint8_t"))  { прямой = С_UINT8;  съесть(р); было = 1; continue; }
        if (текст_равен(k, "uint16_t")) { прямой = С_UINT16; съесть(р); было = 1; continue; }
        if (текст_равен(k, "uint32_t")) { прямой = С_UINT32; съесть(р); было = 1; continue; }
        if (текст_равен(k, "uint64_t")) { прямой = С_UINT64; съесть(р); было = 1; continue; }
        if (текст_равен(k, "size_t"))   { прямой = С_SIZE;   съесть(р); было = 1; continue; }
        break;
    }
    if (!было) return 0;

    if (прямой != С_ПУСТО) тип->базовый = прямой;
    else if (есть_void) тип->базовый = С_VOID;
    else if (есть_bool) тип->базовый = С_BOOL;
    else if (есть_double) тип->базовый = С_DOUBLE;
    else if (есть_float) тип->базовый = С_FLOAT;
    else if (есть_char) тип->базовый = есть_unsigned ? С_UCHAR : (есть_signed ? С_SCHAR : С_CHAR);
    else if (есть_short) тип->базовый = С_SHORT;
    else if (дл_long >= 2) тип->базовый = С_LONGLONG;
    else if (дл_long == 1) тип->базовый = С_LONG;
    else тип->базовый = С_INT;
    (void)есть_int;
    тип->неподписанный = есть_unsigned;

    while (съесть_если(р, "*")) { тип->указатели++;
        while (тек(р)->вид == СТ_ИДЕНТ && это_квалификатор(тек(р))) съесть(р); }
    return 1;
}

