// ─── узлы дерева ─────────────────────────────────────────────────────────────

СиУзел *си_узел(СиВидУзла вид)
{
    СиУзел *у = calloc(1, sizeof(СиУзел));
    if (!у) { perror("calloc"); exit(1); }
    у->вид = вид;
    у->разм_массива = -1;
    дин_массив_инициализировать(&у->дети, sizeof(СиУзел *));
    return у;
}

void си_добавить(СиУзел *родитель, СиУзел *ребёнок)
{
    дин_массив_добавить(&родитель->дети, &ребёнок);
}

СиУзел *си_ребёнок(const СиУзел *узел, size_t индекс)
{
    if (индекс >= узел->дети.длина) return nullptr;
    return *(СиУзел **)((char *)узел->дети.данные + индекс * узел->дети.размер_элемента);
}

size_t си_детей(const СиУзел *узел)
{
    return узел->дети.длина;
}

void си_освободить(СиУзел *узел)
{
    if (!узел) return;
    for (size_t i = 0; i < узел->дети.длина; ++i) {
        си_освободить(си_ребёнок(узел, i));
    }
    дин_массив_освободить(&узел->дети);
    free(узел);
}

// ─── диагностика ─────────────────────────────────────────────────────────────

void си_добавить_ошибку(ДинМассив *ошибки, int строка, int колонка,
                        const char *формат, ...)
{
    if (!ошибки) return;
    СиДиаг д = { .ошибка = 1, .строка = строка, .колонка = колонка };
    va_list ап;
    va_start(ап, формат);
    vsnprintf(д.сообщение, sizeof(д.сообщение), формат, ап);
    va_end(ап);
    дин_массив_добавить(ошибки, &д);
}

// ─── C-лексер ────────────────────────────────────────────────────────────────

static void доб_ток(ДинМассив *ток, СиВидТокена вид, const char *текст,
                    size_t длина, int строка, int колонка)
{
    СиТокен т = { .вид = вид, .текст = текст, .длина = длина,
                  .строка = строка, .колонка = колонка };
    дин_массив_добавить(ток, &т);
}

static int начало_идент(unsigned char c) { return c == '_' || isalpha(c) || c >= 0x80; }
static int часть_идент(unsigned char c)  { return c == '_' || isalnum(c) || c >= 0x80; }

// Возвращает длину многосимвольного оператора в начале s (или 0).
static size_t длина_оператора(const char *s, size_t ост)
{
    static const char *трёх[] = { "<<=", ">>=", "...", nullptr };
    static const char *дву[] = {
        "->","++","--","<<",">>","<=",">=","==","!=","&&","||",
        "+=","-=","*=","/=","%=","&=","|=","^=", nullptr
    };
    for (int i = 0; трёх[i]; ++i)
        if (ост >= 3 && strncmp(s, трёх[i], 3) == 0) return 3;
    for (int i = 0; дву[i]; ++i)
        if (ост >= 2 && strncmp(s, дву[i], 2) == 0) return 2;
    if (strchr("+-*/%&|^~!=<>?:.,;(){}[]", s[0])) return 1;
    return 0;
}

static void лексировать(const char *ист, size_t длина, ДинМассив *ток)
{
    size_t i = 0;
    int строка = 1, колонка = 1;
    #define ПРОДВ() do { \
        if (ист[i] == '\n') { строка++; колонка = 1; } \
        else if (((unsigned char)ист[i] & 0xC0) != 0x80) { колонка++; } \
        i++; \
    } while (0)

    while (i < длина) {
        unsigned char c = (unsigned char)ист[i];
        if (c == ' ' || c == '\t' || c == '\r' || c == '\n') { ПРОДВ(); continue; }

        int тс = строка, тк = колонка;

        // Директива препроцессора — весь остаток строки одним токеном.
        if (c == '#') {
            size_t н = i;
            while (i < длина && ист[i] != '\n') {
                // продолжение строки '\' перед переводом строки
                if (ист[i] == '\\' && i + 1 < длина && ист[i + 1] == '\n') { ПРОДВ(); ПРОДВ(); continue; }
                ПРОДВ();
            }
            доб_ток(ток, СТ_ПУНКТ, ист + н, i - н, тс, тк);
            continue;
        }

        // Комментарии — вырезаем.
        if (c == '/' && i + 1 < длина && ист[i + 1] == '/') {
            while (i < длина && ист[i] != '\n') ПРОДВ();
            continue;
        }
        if (c == '/' && i + 1 < длина && ист[i + 1] == '*') {
            ПРОДВ(); ПРОДВ();
            while (i < длина && !(ист[i] == '*' && i + 1 < длина && ист[i + 1] == '/')) ПРОДВ();
            if (i < длина) { ПРОДВ(); ПРОДВ(); }
            continue;
        }

        // Строковый литерал (с возможным префиксом L/u/U/u8 не поддерживаем —
        // обычные "..." достаточно).
        if (c == '"') {
            size_t н = i; ПРОДВ();
            while (i < длина && ист[i] != '"') {
                if (ист[i] == '\\' && i + 1 < длина) ПРОДВ();
                ПРОДВ();
            }
            if (i < длина) ПРОДВ();
            доб_ток(ток, СТ_СТРОКА, ист + н, i - н, тс, тк);
            continue;
        }
        if (c == '\'') {
            size_t н = i; ПРОДВ();
            while (i < длина && ист[i] != '\'') {
                if (ист[i] == '\\' && i + 1 < длина) ПРОДВ();
                ПРОДВ();
            }
            if (i < длина) ПРОДВ();
            доб_ток(ток, СТ_СИМВОЛ, ист + н, i - н, тс, тк);
            continue;
        }

        if (начало_идент(c)) {
            size_t н = i;
            while (i < длина && часть_идент((unsigned char)ист[i])) ПРОДВ();
            доб_ток(ток, СТ_ИДЕНТ, ист + н, i - н, тс, тк);
            continue;
        }

        if (isdigit(c) || (c == '.' && i + 1 < длина && isdigit((unsigned char)ист[i + 1]))) {
            size_t н = i;
            // hex-префикс
            if (c == '0' && i + 1 < длина && (ист[i + 1] == 'x' || ист[i + 1] == 'X')) { ПРОДВ(); ПРОДВ(); }
            while (i < длина) {
                char d = ист[i];
                if (isalnum((unsigned char)d) || d == '.' || d == '_') { ПРОДВ(); }
                else if ((d == '+' || d == '-') && i > н &&
                         (ист[i - 1] == 'e' || ист[i - 1] == 'E' || ист[i - 1] == 'p' || ист[i - 1] == 'P')) { ПРОДВ(); }
                else break;
            }
            доб_ток(ток, СТ_ЧИСЛО, ист + н, i - н, тс, тк);
            continue;
        }

        size_t дл = длина_оператора(ист + i, длина - i);
        if (дл > 0) {
            доб_ток(ток, СТ_ПУНКТ, ист + i, дл, тс, тк);
            for (size_t k = 0; k < дл; ++k) ПРОДВ();
            continue;
        }

        // неизвестный символ — пропускаем как одиночную пунктуацию
        доб_ток(ток, СТ_ПУНКТ, ист + i, 1, тс, тк);
        ПРОДВ();
    }
    доб_ток(ток, СТ_КОНЕЦ, "", 0, строка, колонка);
    #undef ПРОДВ
}

