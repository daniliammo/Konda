#ifndef ИНТЕРФЕЙС_H
#define ИНТЕРФЕЙС_H

#include <stddef.h>

#include "аст.h"   // Узел, БуферКода (через транспилятор.h), ТипТокена

// ─── Konda-интерфейс библиотек (безопасный путь 2) ──────────────────────────
//
// При сборке «--библиотека» рядом с .so/.h эмитится файл «.и» с ПОЛНЫМИ
// Konda-сигнатурами экспортируемых функций (типы + владение). Потребитель,
// подключающий «#содержит "либа.h"», авто-загружает соседний «либа.и» и
// проверяет вызовы как внутрипроектные (типы, владеет/изменяемый/вывод), а не
// как непрозрачный внешний C. C-заголовок этого выразить не может.
//
// Охват v1: примитивы, срез<примитив>, указатели. Экспорт с типами
// struct/enum/типфункции пропускается (остаётся непрозрачным, fallback к .h).

#define ИНТ_МАКС_ПАРАМ 16

typedef struct {
    ТипТокена тип;      // для срез — тип элемента
    int беззнаковый;
    int указатели;
    int срез;
    int массив;
    int владеет;        // «владеет» — передача = move
    int по_ссылке;      // 0 — значение; 1 — изменяемый (ref); 2 — вывод (out)
} ИнтПараметр;

typedef struct {
    char имя[128];
    ТипТокена тип_возврата;
    int беззнаковый_возврата;
    int указатели_возврата;
    int срез_возврата;
    ИнтПараметр параметры[ИНТ_МАКС_ПАРАМ];
    size_t число_параметров;
} ИнтФункция;

typedef struct ТаблицаИнтерфейса ТаблицаИнтерфейса;

ТаблицаИнтерфейса *интерфейс_создать(void);
void               интерфейс_освободить(ТаблицаИнтерфейса *);
size_t             интерфейс_число(const ТаблицаИнтерфейса *);
const ИнтФункция  *интерфейс_получить(const ТаблицаИнтерфейса *, size_t);
// Найденные рядом с интерфейсом .so — для автолинковки потребителя.
size_t             интерфейс_число_библиотек(const ТаблицаИнтерфейса *);
const char        *интерфейс_библиотека(const ТаблицаИнтерфейса *, size_t);

// Эмиссия интерфейса библиотеки (режим --библиотека) в буфер.
void эмит_интерфейс(const Узел *корень, БуферКода *буфер);

// Сбор интерфейсов потребителя: для каждого локального «#содержит "X.h"» ищет
// соседний «X.и» (и «X.so») относительно база_путей и загружает.
void собрать_интерфейсы(const Узел *корень, const char *база_путей,
                        ТаблицаИнтерфейса *куда);

#endif /* ИНТЕРФЕЙС_H */
