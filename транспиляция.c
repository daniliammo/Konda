#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdint.h>

#include "транспилятор.h"

#define МАКС_ИМЯ 128
#define МАКС_СВЯЗЕЙ_МАССИВОВ 32
#define МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ 64

typedef struct {
    char массив[МАКС_ИМЯ];
    char длина[МАКС_ИМЯ];
} СвязьМассива;

typedef struct {
    char переменная[МАКС_ИМЯ];
    long минимальная_длина;
    int известна;
} ОграничениеДлины;

typedef struct {
    char *буфер;
    size_t длина;
    size_t емкость;
} БуферКода;

typedef struct {
    МассивТокенов *токены;
    size_t позиция;
    int ошибка;
    char сообщение[512];
    int строка_ошибки;

    БуферКода код;
    int в_точке_входа;
    СвязьМассива связи[МАКС_СВЯЗЕЙ_МАССИВОВ];
    size_t число_связей;
    ОграничениеДлины ограничения[МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ];
    size_t глубина_ограничений;
} Парсер;

static void сгенерировать_выражение(Парсер *ps);
static void сгенерировать_первичное(Парсер *ps);
static void сгенерировать_блок(Парсер *ps, int с_скобками);

static int байты_равны(const char *a, size_t la, const char *b, size_t lb)
{
    return la == lb && memcmp(a, b, la) == 0;
}

static int идентификатор_равен(const Токен *t, const char *текст)
{
    if (t->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        return 0;
    }
    size_t n = strlen(текст);
    return байты_равны(t->начало, t->длина, текст, n);
}

static int срез_равен(const char *начало, size_t длина, const char *текст)
{
    return байты_равны(начало, длина, текст, strlen(текст));
}

static void буфер_добавить(БуферКода *б, const char *текст)
{
    size_t n = strlen(текст);
    if (б->длина + n + 1 > б->емкость) {
        size_t новая = б->емкость ? б->емкость * 2 : 4096;
        while (новая < б->длина + n + 1) {
            новая *= 2;
        }
        char *p = realloc(б->буфер, новая);
        if (!p) {
            return;
        }
        б->буфер = p;
        б->емкость = новая;
    }
    memcpy(б->буфер + б->длина, текст, n);
    б->длина += n;
    б->буфер[б->длина] = '\0';
}

static void буфер_добавить_отрезок(БуферКода *б, const char *начало, size_t длина)
{
    char временный[512];
    if (длина >= sizeof(временный)) {
        return;
    }
    memcpy(временный, начало, длина);
    временный[длина] = '\0';
    буфер_добавить(б, временный);
}

static void парсер_ошибка(Парсер *ps, const Токен *t, const char *текст)
{
    if (ps->ошибка) {
        return;
    }
    ps->ошибка = 1;
    ps->строка_ошибки = t ? t->строка : 0;
    snprintf(ps->сообщение, sizeof(ps->сообщение), "%s", текст);
}

static int пропустить_служебные(Парсер *ps)
{
    while (ps->позиция < ps->токены->длина) {
        ТипТокена тип = ps->токены->данные[ps->позиция].тип;
        if (тип == ТОКЕН_КОММЕНТАРИЙ || тип == ТОКЕН_НОВАЯ_СТРОКА) {
            ps->позиция++;
            continue;
        }
        break;
    }
    return !ps->ошибка;
}

static Токен *текущий(Парсер *ps)
{
    пропустить_служебные(ps);
    if (ps->позиция >= ps->токены->длина) {
        return &ps->токены->данные[ps->токены->длина - 1];
    }
    return &ps->токены->данные[ps->позиция];
}

static int тип_текущего(Парсер *ps)
{
    return текущий(ps)->тип;
}

static int съесть(Парсер *ps, ТипТокена ожидаемый)
{
    Токен *t = текущий(ps);
    if (t->тип != ожидаемый) {
        char msg[128];
        snprintf(msg, sizeof(msg), "ожидался токен %s", имя_типа_токена(ожидаемый));
        парсер_ошибка(ps, t, msg);
        return 0;
    }
    ps->позиция++;
    return 1;
}

static void ограничения_сохранить(Парсер *ps)
{
    if (ps->глубина_ограничений >= МАКС_ГЛУБИНА_ОГРАНИЧЕНИЙ) {
        парсер_ошибка(ps, текущий(ps), "слишком глубокая вложенность ограничений");
        return;
    }
    ps->ограничения[ps->глубина_ограничений++] = (ОграничениеДлины){0};
}

static void ограничения_восстановить(Парсер *ps)
{
    if (ps->глубина_ограничений > 0) {
        ps->глубина_ограничений--;
    }
}

static void ограничение_установить(Парсер *ps, const char *переменная, size_t длина_имени, long минимальная_длина)
{
    if (ps->глубина_ограничений == 0) {
        return;
    }
    ОграничениеДлины *o = &ps->ограничения[ps->глубина_ограничений - 1];
    size_t n = длина_имени < МАКС_ИМЯ - 1 ? длина_имени : МАКС_ИМЯ - 1;
    memcpy(o->переменная, переменная, n);
    o->переменная[n] = '\0';
    o->минимальная_длина = минимальная_длина;
    o->известна = 1;
}

static long минимальная_длина_для(const Парсер *ps, const char *имя, size_t длина_имени)
{
    for (size_t i = ps->глубина_ограничений; i > 0; --i) {
        const ОграничениеДлины *o = &ps->ограничения[i - 1];
        if (o->известна && байты_равны(o->переменная, strlen(o->переменная), имя, длина_имени)) {
            return o->минимальная_длина;
        }
    }
    return 0;
}

static int известна_длина_массива(const Парсер *ps, const char *имя_массива, size_t длина_имени, long *минимальная_длина)
{
    const char *имя_длины = NULL;
    for (size_t i = 0; i < ps->число_связей; ++i) {
        if (байты_равны(ps->связи[i].массив, strlen(ps->связи[i].массив), имя_массива, длина_имени)) {
            имя_длины = ps->связи[i].длина;
            break;
        }
    }
    if (!имя_длины) {
        return 0;
    }
    long m = минимальная_длина_для(ps, имя_длины, strlen(имя_длины));
    if (m <= 0) {
        return 0;
    }
    *минимальная_длина = m;
    return 1;
}

static void связь_добавить(Парсер *ps, const char *массив, size_t lm, const char *длина, size_t ld)
{
    if (ps->число_связей >= МАКС_СВЯЗЕЙ_МАССИВОВ) {
        return;
    }
    СвязьМассива *s = &ps->связи[ps->число_связей++];
    size_t n = lm < МАКС_ИМЯ - 1 ? lm : МАКС_ИМЯ - 1;
    memcpy(s->массив, массив, n);
    s->массив[n] = '\0';
    n = ld < МАКС_ИМЯ - 1 ? ld : МАКС_ИМЯ - 1;
    memcpy(s->длина, длина, n);
    s->длина[n] = '\0';
}

static void проверить_доступ_к_массиву(Парсер *ps, const Токен *t, const char *имя, size_t длина_имени, long индекс)
{
    long минимум = 0;
    if (!известна_длина_массива(ps, имя, длина_имени, &минимум)) {
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "доступ к массиву «%.*s»: длина неизвестна на этапе компиляции (строка %d)",
                 (int)длина_имени, имя, t->строка);
        парсер_ошибка(ps, t, msg);
        return;
    }
    if (индекс < 0 || индекс >= минимум) {
        char msg[256];
        snprintf(msg, sizeof(msg),
                 "индекс %ld вне границ: при текущих условиях длина не меньше %ld (строка %d)",
                 индекс, минимум, t->строка);
        парсер_ошибка(ps, t, msg);
    }
}

static void имя_в_си(Парсер *ps, const char *начало, size_t длина, char *out, size_t out_cap)
{
    if (ps->в_точке_входа && срез_равен(начало, длина, "точка_входа")) {
        snprintf(out, out_cap, "main");
        return;
    }
    if (ps->в_точке_входа && срез_равен(начало, длина, "количество_аргументов")) {
        snprintf(out, out_cap, "argc");
        return;
    }
    if (ps->в_точке_входа && срез_равен(начало, длина, "аргументы")) {
        snprintf(out, out_cap, "argv");
        return;
    }
    size_t n = длина < out_cap - 1 ? длина : out_cap - 1;
    memcpy(out, начало, n);
    out[n] = '\0';
}

static int токен_это_тип(ТипТокена t)
{
    return t == ТОКЕН_ЦЕЛОЕ4 || t == ТОКЕН_ЦЕЛОЕ8 || t == ТОКЕН_БАЙТ
        || t == ТОКЕН_ВЕЩЕСТВЕННОЕ || t == ТОКЕН_ТИП_СИМВОЛ;
}

static int тип_токена_в_си(ТипТокена t, char *out, size_t out_cap)
{
    switch (t) {
        case ТОКЕН_ЦЕЛОЕ4:
            snprintf(out, out_cap, "int32_t");
            return 1;
        case ТОКЕН_ЦЕЛОЕ8:
            snprintf(out, out_cap, "int64_t");
            return 1;
        case ТОКЕН_БАЙТ:
            snprintf(out, out_cap, "uint8_t");
            return 1;
        case ТОКЕН_ВЕЩЕСТВЕННОЕ:
            snprintf(out, out_cap, "float");
            return 1;
        case ТОКЕН_ТИП_СИМВОЛ:
            snprintf(out, out_cap, "char");
            return 1;
        default:
            snprintf(out, out_cap, "int");
            return 0;
    }
}

static void сгенерировать_условие_если(Парсер *ps)
{
    Токен *имя = текущий(ps);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        сгенерировать_выражение(ps);
        return;
    }

    char имя_си[МАКС_ИМЯ];
    имя_в_си(ps, имя->начало, имя->длина, имя_си, sizeof(имя_си));
    ps->позиция++;

    int строгое = 0;
    int не_меньше = 0;
    if (тип_текущего(ps) == ТОКЕН_СТРЕЛКА_ВПРАВО) {
        съесть(ps, ТОКЕН_СТРЕЛКА_ВПРАВО);
        строгое = 1;
    } else if (тип_текущего(ps) == ТОКЕН_РАВНО) {
        съесть(ps, ТОКЕН_РАВНО);
        if (тип_текущего(ps) == ТОКЕН_СТРЕЛКА_ВПРАВО) {
            съесть(ps, ТОКЕН_СТРЕЛКА_ВПРАВО);
            не_меньше = 1;
        } else {
            парсер_ошибка(ps, текущий(ps), "ожидалось >= в условии");
            return;
        }
    } else {
        ps->позиция--;
        сгенерировать_выражение(ps);
        return;
    }

    Токен *число = текущий(ps);
    if (число->тип != ТОКЕН_ЧИСЛО) {
        парсер_ошибка(ps, число, "ожидалось число в условии");
        return;
    }
    long порог = strtol(число->начало, NULL, 10);

    long минимум = 0;
    if (строгое) {
        минимум = порог + 1;
    } else if (не_меньше) {
        минимум = порог;
    }
    ограничение_установить(ps, имя->начало, имя->длина, минимум);

    буфер_добавить(&ps->код, имя_си);
    if (строгое) {
        буфер_добавить(&ps->код, " > ");
    } else {
        буфер_добавить(&ps->код, " >= ");
    }
    буфер_добавить_отрезок(&ps->код, число->начало, число->длина);
    ps->позиция++;
}

static void сгенерировать_вызов(Парсер *ps)
{
    Токен *имя = текущий(ps);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        парсер_ошибка(ps, имя, "ожидалось имя функции");
        return;
    }

    char имя_си[МАКС_ИМЯ];
    имя_в_си(ps, имя->начало, имя->длина, имя_си, sizeof(имя_си));
    ps->позиция++;
    if (!съесть(ps, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        return;
    }

    int это_printf = идентификатор_равен(имя, "printf");
    if (это_printf) {
        буфер_добавить(&ps->код, "        printf(\"%s\\n\", ");
    } else {
        буфер_добавить(&ps->код, "        ");
        буфер_добавить(&ps->код, имя_си);
        буфер_добавить(&ps->код, "(");
    }

    пропустить_служебные(ps);
    if (тип_текущего(ps) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
        сгенерировать_выражение(ps);
        while (!ps->ошибка && тип_текущего(ps) == ТОКЕН_ЗАПЯТАЯ) {
            съесть(ps, ТОКЕН_ЗАПЯТАЯ);
            буфер_добавить(&ps->код, ", ");
            сгенерировать_выражение(ps);
        }
    }

    if (!съесть(ps, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
        return;
    }
    буфер_добавить(&ps->код, ")");
}

static void сгенерировать_первичное(Парсер *ps)
{
    Токен *t = текущий(ps);

    if (t->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
        char имя_си[МАКС_ИМЯ];
        имя_в_си(ps, t->начало, t->длина, имя_си, sizeof(имя_си));
        ps->позиция++;
        пропустить_служебные(ps);
        if (тип_текущего(ps) == ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            ps->позиция--;
            сгенерировать_вызов(ps);
            return;
        }
        if (тип_текущего(ps) == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
            съесть(ps, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
            Токен *инд = текущий(ps);
            if (инд->тип != ТОКЕН_ЧИСЛО) {
                парсер_ошибка(ps, инд, "индекс массива должен быть числовым литералом для проверки границ");
                return;
            }
            long idx = strtol(инд->начало, NULL, 10);
            проверить_доступ_к_массиву(ps, инд, t->начало, t->длина, idx);
            if (ps->ошибка) {
                return;
            }
            ps->позиция++;
            if (!съесть(ps, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
                return;
            }
            буфер_добавить(&ps->код, имя_си);
            буфер_добавить(&ps->код, "[");
            буфер_добавить_отрезок(&ps->код, инд->начало, инд->длина);
            буфер_добавить(&ps->код, "]");
            return;
        }
        буфер_добавить(&ps->код, имя_си);
        return;
    }

    if (t->тип == ТОКЕН_СТРОКА || t->тип == ТОКЕН_ЧИСЛО) {
        буфер_добавить_отрезок(&ps->код, t->начало, t->длина);
        ps->позиция++;
        return;
    }

    if (t->тип == ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ) {
        съесть(ps, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
        буфер_добавить(&ps->код, "(");
        сгенерировать_выражение(ps);
        if (!съесть(ps, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
            return;
        }
        буфер_добавить(&ps->код, ")");
        return;
    }

    парсер_ошибка(ps, t, "неожиданное выражение");
}

static void сгенерировать_выражение(Парсер *ps)
{
    сгенерировать_первичное(ps);
    while (!ps->ошибка) {
        Токен *op = текущий(ps);
        if (op->тип == ТОКЕН_ПЛЮС || op->тип == ТОКЕН_МИНУС || op->тип == ТОКЕН_ЗВЕЗДА
            || op->тип == ТОКЕН_КОСАЯ_ЧЕРТА || op->тип == ТОКЕН_СТРЕЛКА_ВПРАВО
            || op->тип == ТОКЕН_СТРЕЛКА_ВЛЕВО || op->тип == ТОКЕН_РАВНО) {
            буфер_добавить_отрезок(&ps->код, op->начало, op->длина);
            ps->позиция++;
            сгенерировать_первичное(ps);
            continue;
        }
        break;
    }
}

static void сгенерировать_объявление(Парсер *ps)
{
    Токен *tok_t = текущий(ps);
    if (!токен_это_тип(tok_t->тип)) {
        парсер_ошибка(ps, tok_t, "ожидался примитивный тип (целое4, целое8, байт, вещественное, символ)");
        return;
    }
    ТипТокена тип_ток = tok_t->тип;
    ps->позиция++;

    Токен *имя = текущий(ps);
    if (имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        парсер_ошибка(ps, имя, "ожидалось имя переменной");
        return;
    }

    char тип_си[64];
    char имя_си[МАКС_ИМЯ];
    тип_токена_в_си(тип_ток, тип_си, sizeof(тип_си));
    имя_в_си(ps, имя->начало, имя->длина, имя_си, sizeof(имя_си));
    ps->позиция++;

    буфер_добавить(&ps->код, "    ");
    буфер_добавить(&ps->код, тип_си);
    буфер_добавить(&ps->код, " ");
    буфер_добавить(&ps->код, имя_си);

    if (тип_текущего(ps) == ТОКЕН_РАВНО) {
        съесть(ps, ТОКЕН_РАВНО);
        буфер_добавить(&ps->код, " = ");
        сгенерировать_выражение(ps);
    }

    if (!съесть(ps, ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ)) {
        return;
    }
    буфер_добавить(&ps->код, ";\n");
}

static void сгенерировать_если(Парсер *ps)
{
    if (!съесть(ps, ТОКЕН_ЕСЛИ)) {
        return;
    }
    if (!съесть(ps, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        return;
    }

    ограничения_сохранить(ps);

    буфер_добавить(&ps->код, "    if (");
    сгенерировать_условие_если(ps);
    if (!съесть(ps, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
        ограничения_восстановить(ps);
        return;
    }
    буфер_добавить(&ps->код, ") {\n");

    сгенерировать_блок(ps, 1);

    if (тип_текущего(ps) == ТОКЕН_ИНАЧЕ) {
        съесть(ps, ТОКЕН_ИНАЧЕ);
        ограничения_восстановить(ps);
        ограничения_сохранить(ps);
        буфер_добавить(&ps->код, "    } else {\n");
        сгенерировать_блок(ps, 1);
        ограничения_восстановить(ps);
    } else {
        ограничения_восстановить(ps);
    }

    буфер_добавить(&ps->код, "    }\n");
}

static void сгенерировать_оператор(Парсер *ps)
{
    пропустить_служебные(ps);
    if (ps->ошибка) {
        return;
    }
    if (тип_текущего(ps) == ТОКЕН_КОНЕЦ_ФАЙЛА) {
        return;
    }
    if (тип_текущего(ps) == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
        return;
    }

    if (тип_текущего(ps) == ТОКЕН_ЕСЛИ) {
        сгенерировать_если(ps);
        return;
    }

    Токен *t = текущий(ps);
    if (токен_это_тип(t->тип)) {
        сгенерировать_объявление(ps);
        return;
    }

    if (t->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
        сгенерировать_вызов(ps);
        if (!ps->ошибка) {
            if (!съесть(ps, ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ)) {
                парсер_ошибка(ps, текущий(ps), "ожидалась ; после выражения");
            } else {
                буфер_добавить(&ps->код, ";\n");
            }
        }
        return;
    }

    парсер_ошибка(ps, t, "неожиданный оператор");
}

static void сгенерировать_блок(Парсер *ps, int с_скобками)
{
    if (с_скобками) {
        if (!съесть(ps, ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
            return;
        }
    }

    while (!ps->ошибка) {
        пропустить_служебные(ps);
        if (тип_текущего(ps) == ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
            || тип_текущего(ps) == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        сгенерировать_оператор(ps);
    }

    if (с_скобками) {
        съесть(ps, ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
    }
}

static void сгенерировать_препроцессор(Парсер *ps, const Токен *t)
{
    char строка[512];
    size_t n = t->длина < sizeof(строка) - 1 ? t->длина : sizeof(строка) - 1;
    memcpy(строка, t->начало, n);
    строка[n] = '\0';

    const char *содержит = strstr(строка, "содержит");
    if (содержит) {
        size_t смещение = (size_t)(содержит - строка);
        буфер_добавить_отрезок(&ps->код, строка, смещение);
        буфер_добавить(&ps->код, "include");
        буфер_добавить(&ps->код, содержит + strlen("содержит"));
    } else {
        буфер_добавить_отрезок(&ps->код, t->начало, t->длина);
    }
    буфер_добавить(&ps->код, "\n");
    ps->позиция++;
}

static void сгенерировать_функцию(Парсер *ps)
{
    Токен *tok_t = текущий(ps);
    if (!токен_это_тип(tok_t->тип)) {
        парсер_ошибка(ps, tok_t, "ожидался тип возвращаемого значения");
        return;
    }
    ТипТокена тип_возврата = tok_t->тип;
    ps->позиция++;

    Токен *имя = текущий(ps);
    int это_main = (имя->тип == ТОКЕН_ТОЧКА_ВХОДА);
    if (!это_main && имя->тип != ТОКЕН_ИДЕНТИФИКАТОР) {
        парсер_ошибка(ps, имя, "ожидалось имя функции или точка_входа");
        return;
    }

    ps->в_точке_входа = это_main;
    ps->позиция++;

    char тип_си[64];
    тип_токена_в_си(тип_возврата, тип_си, sizeof(тип_си));

    буфер_добавить(&ps->код, тип_си);
    буфер_добавить(&ps->код, " ");
    if (это_main) {
        буфер_добавить(&ps->код, "main");
    } else {
        char имя_си[МАКС_ИМЯ];
        имя_в_си(ps, имя->начало, имя->длина, имя_си, sizeof(имя_си));
        буфер_добавить(&ps->код, имя_си);
    }

    if (!съесть(ps, ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
        return;
    }
    буфер_добавить(&ps->код, "(");

    if (это_main) {
        буфер_добавить(&ps->код, "int32_t argc, char **argv");
        while (!ps->ошибка && тип_текущего(ps) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ
               && тип_текущего(ps) != ТОКЕН_КОНЕЦ_ФАЙЛА) {
            ps->позиция++;
        }
        связь_добавить(ps, "аргументы", strlen("аргументы"), "количество_аргументов", strlen("количество_аргументов"));
    } else {
        int первый = 1;
        while (!ps->ошибка && тип_текущего(ps) != ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ) {
            if (!первый) {
                буфер_добавить(&ps->код, ", ");
            }
            первый = 0;
            Токен *pt = текущий(ps);
            if (токен_это_тип(pt->тип)) {
                char tsi[64];
                тип_токена_в_си(pt->тип, tsi, sizeof(tsi));
                ps->позиция++;
                буфер_добавить(&ps->код, tsi);
                while (!ps->ошибка && (тип_текущего(ps) == ТОКЕН_ЗВЕЗДА || тип_текущего(ps) == ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ)) {
                    if (тип_текущего(ps) == ТОКЕН_ЗВЕЗДА) {
                        съесть(ps, ТОКЕН_ЗВЕЗДА);
                        буфер_добавить(&ps->код, "*");
                    } else {
                        съесть(ps, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ);
                        съесть(ps, ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ);
                        буфер_добавить(&ps->код, "[]");
                    }
                }
                буфер_добавить(&ps->код, " ");
                Токен *pn = текущий(ps);
                if (pn->тип == ТОКЕН_ИДЕНТИФИКАТОР) {
                    char nsi[МАКС_ИМЯ];
                    имя_в_си(ps, pn->начало, pn->длина, nsi, sizeof(nsi));
                    ps->позиция++;
                    буфер_добавить(&ps->код, nsi);
                }
            } else {
                ps->позиция++;
            }
            if (тип_текущего(ps) == ТОКЕН_ЗАПЯТАЯ) {
                съесть(ps, ТОКЕН_ЗАПЯТАЯ);
            }
        }
    }

    if (!съесть(ps, ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ)) {
        return;
    }
    буфер_добавить(&ps->код, ")\n{\n");

    сгенерировать_блок(ps, 1);

    буфер_добавить(&ps->код, "    return 0;\n}\n");
    ps->в_точке_входа = 0;
}

static int сгенерировать_программу(Парсер *ps)
{
    буфер_добавить(&ps->код, "/* Сгенерировано транспилятором Konda */\n");
    буфер_добавить(&ps->код, "#include <stdint.h>\n");

    while (!ps->ошибка) {
        пропустить_служебные(ps);
        Токен *t = текущий(ps);
        if (t->тип == ТОКЕН_КОНЕЦ_ФАЙЛА) {
            break;
        }
        if (t->тип == ТОКЕН_ПРЕПРОЦЕССОР) {
            сгенерировать_препроцессор(ps, t);
            continue;
        }
        if (токен_это_тип(t->тип)) {
            сгенерировать_функцию(ps);
            continue;
        }
        парсер_ошибка(ps, t, "ожидалась директива или функция");
        break;
    }

    return !ps->ошибка;
}

int транспилировать_в_си(МассивТокенов токены, const char *путь_исходника, const char *путь_выходного_си)
{
    (void)путь_исходника;
    Парсер ps = {0};
    ps.токены = &токены;

    if (!сгенерировать_программу(&ps)) {
        fprintf(stderr, "Ошибка транспиляции (строка %d): %s\n", ps.строка_ошибки, ps.сообщение);
        free(ps.код.буфер);
        return 1;
    }

    FILE *f = fopen(путь_выходного_си, "wb");
    if (!f) {
        fprintf(stderr, "Не удалось создать %s\n", путь_выходного_си);
        free(ps.код.буфер);
        return 1;
    }
    fwrite(ps.код.буфер, 1, ps.код.длина, f);
    fclose(f);
    free(ps.код.буфер);
    printf("Записан %s (%zu байт)\n", путь_выходного_си, ps.код.длина);
    return 0;
}

int скомпилировать_си(const char *путь_си, const char *путь_исполняемого)
{
    char команда[2048];
    snprintf(команда, sizeof(команда), "/bin/cc -std=c23 -Wall -Wextra -O2 -o \"%s.elf\" \"%s\"",
             путь_исполняемого, путь_си);
    printf("%s\n", команда);
    int код = system(команда);
    if (код != 0) {
        fprintf(stderr, "Ошибка компиляции (код %d)\n", код);
        return 1;
    }
    printf("Собрано: %s\n", путь_исполняемого);
    return 0;
}
