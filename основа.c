#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <errno.h>
#include <sys/stat.h>

#include "konda.h"   /* публичный API библиотеки libkonda */
#include "макросы.h" /* условная компиляция (#if/#ifdef/…) + флаги -D */

#define ПАПКА_ВЫВОДА "вывод"
#define ВЕРСИЯ_KONDA "0.1.0"

// ─── слияние программ ───────────────────────────────────────────────────────
//
// Несколько входных .конда файлов транспилируются как одна программа: каждый
// файл парсится отдельно (так линии в диагностиках остаются файл-локальными),
// затем top-level дети всех корней переносятся в общий УЗЕЛ_ПРОГРАММА.
// Семантика и кодоген работают с этим общим деревом — никаких заголовков и
// extern-деклараций не нужно, signature-pass семантики видит все функции и
// структуры сразу.
//
// Перенос: дети источника становятся детьми общего, источник «обнуляется»
// (длина = 0) и затем освобождается как пустая оболочка.
static Узел *слить_программы(Узел **корни, size_t число)
{
    Узел *общий = узел_создать(УЗЕЛ_ПРОГРАММА);
    for (size_t i = 0; i < число; ++i) {
        Узел *r = корни[i];
        if (!r) continue;
        size_t n = узел_число_детей(r);
        for (size_t j = 0; j < n; ++j) {
            узел_добавить_ребёнка(общий, узел_ребёнок(r, j));
        }
        // Дети переехали — источнику оставляем пустой массив, освободить_узел
        // не пройдётся по перенесённым указателям повторно.
        r->дети.длина = 0;
        освободить_узел(r);
        корни[i] = nullptr;
    }
    return общий;
}

// ─── вспомогательные функции для путей ──────────────────────────────────────

static int путь_без_расширения(const char *вход, char *выход, size_t cap)
{
    const char *слэш = strrchr(вход, '/');
    const char *имя = слэш ? слэш + 1 : вход;
    const char *точка = strrchr(имя, '.');
    size_t len = точка ? (size_t)(точка - имя) : strlen(имя);
    if (len + 1 >= cap) {
        return 0;
    }
    memcpy(выход, имя, len);
    выход[len] = '\0';
    return 1;
}

// ─── главная программа ─────────────────────────────────────────────────────

static void печатать_использование(const char *имя_программы)
{
    fprintf(stderr,
            "Использование: %s [--релиз] [--библиотека] <файл.конда> [файл.конда ...]\n"
            "  Несколько файлов: транспилируются как одна программа,\n"
            "  имя выходных файлов берётся от первого аргумента.\n"
            "  --релиз (--release): снять арифм. проверки (скорость уровня C);\n"
            "  по умолчанию — отладочная сборка со всеми проверками.\n"
            "  --библиотека (--lib): собрать .so + заголовок .h без точки входа.\n"
            "  --version (-v): показать версию и выйти.\n",
            имя_программы);
}

int main(int количество_аргументов, char **аргументы)
{
    // --version/-v обрабатывается раньше проверки числа аргументов: должно
    // работать само по себе, без файла.
    for (int i = 1; i < количество_аргументов; ++i) {
        if (strcmp(аргументы[i], "--version") == 0 || strcmp(аргументы[i], "-v") == 0) {
            printf("Konda %s\n", ВЕРСИЯ_KONDA);
            return 0;
        }
    }

    if (количество_аргументов < 2) {
        печатать_использование(аргументы[0]);
        return 2;
    }

    // Разбор флагов: --релиз/--release включает релизную сборку; --библиотека/
    // --lib собирает разделяемую библиотеку (.so) с заголовком (.h) вместо
    // исполняемого файла. Остальные аргументы — входные файлы .конда (порядок
    // сохраняется, имя выхода — от первого файла). Аргумент, начинающийся с
    // «--», но не совпавший ни с одним известным флагом, — вероятная опечатка:
    // отклоняем понятной ошибкой, а не пытаемся прочитать его как имя файла
    // (иначе пользователь получает малопонятное «No such file or directory»).
    int релиз = 0;
    int библиотека = 0;
    const char **входные = calloc((size_t)количество_аргументов, sizeof(*входные));
    if (!входные) {
        fprintf(stderr, "Нет памяти под список файлов\n");
        return 1;
    }
    ТаблицаМакросов макросы;
    макросы_инициализировать(&макросы);
    size_t число_файлов = 0;
    for (int i = 1; i < количество_аргументов; ++i) {
        const char *арг = аргументы[i];
        if (strcmp(арг, "--релиз") == 0 || strcmp(арг, "--release") == 0) {
            релиз = 1;
        } else if (strcmp(арг, "--дебаг") == 0 || strcmp(арг, "--debug") == 0) {
            релиз = 0;
        } else if (strcmp(арг, "--библиотека") == 0 || strcmp(арг, "--lib") == 0
                   || strcmp(арг, "--library") == 0) {
            библиотека = 1;
        } else if (арг[0] == '-' && арг[1] == 'D') {
            // Определить макрос: «-DИМЯ», «-DИМЯ=знач» или «-D ИМЯ».
            const char *деф = арг + 2;
            if (*деф == '\0' && i + 1 < количество_аргументов) деф = аргументы[++i];
            if (*деф == '\0' || !макросы_определить_аргумент(&макросы, деф)) {
                fprintf(stderr, "Неверный «-D»: ожидалось ИМЯ или ИМЯ=значение\n");
                free(входные);
                return 2;
            }
        } else if (арг[0] == '-' && арг[1] == '-') {
            fprintf(stderr, "Неизвестный флаг: %s\n", арг);
            печатать_использование(аргументы[0]);
            free(входные);
            return 2;
        } else {
            входные[число_файлов++] = арг;
        }
    }
    if (число_файлов == 0) {
        fprintf(stderr, "Не указан ни один файл .конда\n");
        free(входные);
        return 2;
    }

    // Источники, токены и AST-корни каждого файла живут до конца кодогена:
    // токены и узлы держат указатели в исходные буферы (Токен.начало).
    char            **источники = calloc(число_файлов, sizeof(*источники));
    МассивТокенов   *токены    = calloc(число_файлов, sizeof(*токены));
    Узел            **корни    = calloc(число_файлов, sizeof(*корни));
    if (!источники || !токены || !корни) {
        fprintf(stderr, "Нет памяти под массивы файлов\n");
        free(источники); free(токены); free(корни); free(входные);
        return 1;
    }

    int код = 0;
    // Объявлено до первых goto очистка (nullptr-безопасно): живёт до линковки,
    // освобождается в «очистка». Хранит сигнатуры библиотек и пути их .so.
    ТаблицаИнтерфейса *интерфейсы = nullptr;

    // ── чтение + токенизация каждого файла ─────────────────────────────────
    for (size_t i = 0; i < число_файлов; ++i) {
        const char *путь = входные[i];
        size_t размер;
        источники[i] = прочитать_файл((char *)путь, &размер);
        if (!источники[i]) {
            fprintf(stderr, "Не удалось прочитать %s\n", путь);
            код = 1;
            goto очистка;
        }
        токены[i] = токенизировать_код(размер, источники[i], 0);
        // Условная компиляция (#if/#ifdef/#ifndef/#else/#elif/#endif/#define):
        // фильтрует токены неактивных веток до разбора. Общая таблица макросов —
        // «#define» из ранних файлов видны в поздних (как единый проект).
        Диагностика мдиаг = {0};
        if (!условная_компиляция(&токены[i], &макросы, &мдиаг)) {
            fprintf(stderr, "Ошибка транспиляции %s (строка %d, колонка %d): %s\n",
                    путь, мдиаг.строка, мдиаг.колонка, мдиаг.сообщение);
            код = 1;
            goto очистка;
        }
    }

    // ── signature-pass для имён структур и перечислений: позволяет парсеру
    // файла X знать о структурах/перечислениях, объявленных в файле Y.
    // Семантика и так делает свой signature-pass для функций; для парсера
    // структуры/перечисления — частный случай из-за неоднозначности
    // «Имя п» vs обычный идентификатор.
    ИменаСтруктур *структуры_всех = имена_структур_создать();
    ИменаПеречислений *перечисления_всех = имена_перечислений_создать();
    for (size_t i = 0; i < число_файлов; ++i) {
        собрать_имена_структур(&токены[i], структуры_всех);
        собрать_имена_перечислений(&токены[i], перечисления_всех);
    }

    // ── разбор каждого файла с общим списком структур и перечислений ───────
    // Батч-режим парсера: как и семантика, разбор собирает ВСЕ синтаксические
    // ошибки файла за прогон (восстановление на границе верхнего уровня) —
    // IDE и циклы автоисправления видят весь список сразу.
    for (size_t i = 0; i < число_файлов; ++i) {
        const char *путь = входные[i];
        Диагностика диаг = {0};
        ДинМассив синт_ошибки;
        дин_массив_инициализировать(&синт_ошибки, sizeof(Диагностика));
        корни[i] = разобрать_с_именами_структур_батч(&токены[i], &диаг,
                                                  структуры_всех, перечисления_всех,
                                                  &синт_ошибки);
        if (!корни[i]) {
            for (size_t j = 0; j < синт_ошибки.длина; ++j) {
                Диагностика *д = дин_массив_получить(&синт_ошибки, j);
                fprintf(stderr,
                        "Ошибка транспиляции %s (строка %d, колонка %d): %s\n",
                        путь, д->строка, д->колонка, д->сообщение);
            }
            if (синт_ошибки.длина == 0) {
                fprintf(stderr,
                        "Ошибка транспиляции %s (строка %d, колонка %d): %s\n",
                        путь, диаг.строка, диаг.колонка, диаг.сообщение);
            }
            if (синт_ошибки.длина > 1) {
                fprintf(stderr, "Всего ошибок: %zu\n", синт_ошибки.длина);
            }
            дин_массив_освободить(&синт_ошибки);
            имена_структур_освободить(структуры_всех);
            имена_перечислений_освободить(перечисления_всех);
            код = 1;
            goto очистка;
        }
        дин_массив_освободить(&синт_ошибки);
    }
    имена_структур_освободить(структуры_всех);
    имена_перечислений_освободить(перечисления_всех);

    // ── слияние и общий проход семантики + кодогена ───────────────────────
    Узел *общий = слить_программы(корни, число_файлов);
    // Использует ли программа «поток<T>» → нужен «-pthread» при вызове cc.
    // Снимаем признак до освобождения дерева (см. ниже).
    int нужен_pthread = 0;

    // Мономорфизация обобщённых функций: шаблоны «имя<T>(…)» разворачиваются в
    // конкретные специализации по каждому вызову ещё до семантики (та работает
    // с полностью конкретным деревом). Ошибки — арность/неизвестный шаблон.
    {
        Диагностика диаг_g = {0};
        if (!мономорфизировать(общий, &диаг_g)) {
            fprintf(stderr, "Ошибка транспиляции (строка %d, колонка %d): %s\n",
                    диаг_g.строка, диаг_g.колонка, диаг_g.сообщение);
            // Признак потоков снимаем ДО освобождения дерева: линковка ниже читать
    // «общий» уже не может (это было бы use-after-free).
    нужен_pthread = программа_использует_потоки(общий);
    освободить_узел(общий);
            код = 1;
            goto очистка;
        }
    }

    // Внешние функции из подключённых заголовков: транспилятор «видит» libc и
    // прочие библиотеки, чтобы диагностировать неизвестные вызовы на уровне
    // Konda (с позицией в .конда), а не перекладывать всё на cc. Каталог для
    // «локальных.h» берём от первого входного файла.
    char база_каталог[256];
    {
        const char *слэш = strrchr(входные[0], '/');
        if (слэш) {
            size_t n = (size_t)(слэш - входные[0]);
            if (n >= sizeof(база_каталог)) n = sizeof(база_каталог) - 1;
            memcpy(база_каталог, входные[0], n);
            база_каталог[n] = '\0';
        } else {
            snprintf(база_каталог, sizeof(база_каталог), ".");
        }
    }
    ТаблицаВнешних *внешние = внешние_создать();
    if (внешние) собрать_внешние_функции(общий, база_каталог, внешние);

    // Konda-интерфейсы библиотек (безопасный путь 2): для каждого локального
    // «#содержит "либа.h"» подгружаем соседний «либа.и» — вызовы к функциям
    // библиотеки проверяются по типам и владению, а не как непрозрачный C.
    интерфейсы = интерфейс_создать();
    if (интерфейсы) собрать_интерфейсы(общий, база_каталог, интерфейсы);

    // Батч-режим: собираем ВСЕ ошибки за один проход (чтобы показать их сразу,
    // а не по одной за пересборку) и предупреждения.
    ДинМассив предупреждения, ошибки;
    дин_массив_инициализировать(&предупреждения, sizeof(Диагностика));
    дин_массив_инициализировать(&ошибки, sizeof(Диагностика));
    Диагностика диаг = {0};
    int семантика_ок = анализировать_полный_и(общий, &диаг, внешние, интерфейсы,
                                              &предупреждения, &ошибки);

    // Предупреждения (не фатальные, напр. вызов необъявленной функции) — в
    // stderr, но сборку не прерывают.
    for (size_t i = 0; i < предупреждения.длина; ++i) {
        Диагностика *п = дин_массив_получить(&предупреждения, i);
        fprintf(stderr, "Предупреждение (строка %d, колонка %d): %s\n",
                п->строка, п->колонка, п->сообщение);
    }
    // Все ошибки — в stderr (не только первая).
    for (size_t i = 0; i < ошибки.длина; ++i) {
        Диагностика *о = дин_массив_получить(&ошибки, i);
        fprintf(stderr, "Ошибка транспиляции (строка %d, колонка %d): %s\n",
                о->строка, о->колонка, о->сообщение);
    }
    if (ошибки.длина > 0)
        fprintf(stderr, "Всего ошибок: %zu\n", ошибки.длина);
    дин_массив_освободить(&предупреждения);
    дин_массив_освободить(&ошибки);
    внешние_освободить(внешние);

    if (!семантика_ок) {
        // Признак потоков снимаем ДО освобождения дерева: линковка ниже читать
    // «общий» уже не может (это было бы use-after-free).
    нужен_pthread = программа_использует_потоки(общий);
    освободить_узел(общий);
        код = 1;
        goto очистка;
    }

    // Исполняемому файлу нужна «точка_входа» (main); библиотеке — нет. Раньше
    // при её отсутствии ошибку впервые видел линкер («undefined reference to
    // `main'») — сырое сообщение от cc вместо диагностики уровня Konda.
    if (!библиотека) {
        int есть_точка_входа = 0;
        for (size_t i = 0; i < узел_число_детей(общий); ++i) {
            Узел *ф = узел_ребёнок(общий, i);
            if (ф->тип == УЗЕЛ_ФУНКЦИЯ && ф->это_main) { есть_точка_входа = 1; break; }
        }
        if (!есть_точка_входа) {
            fprintf(stderr,
                    "Ошибка: в программе нет «точка_входа» — исполняемому файлу "
                    "нужна функция «целое32 точка_входа(...)». Если это "
                    "библиотека функций без точки входа, используйте флаг "
                    "--библиотека.\n");
            // Признак потоков снимаем ДО освобождения дерева: линковка ниже читать
    // «общий» уже не может (это было бы use-after-free).
    нужен_pthread = программа_использует_потоки(общий);
    освободить_узел(общий);
            код = 1;
            goto очистка;
        }
    }

    БуферКода буфер;
    буфер_кода_инициализировать(&буфер);
    кодоген_и(общий, &буфер, релиз, интерфейсы);

    // Для библиотеки — публичный заголовок И Konda-интерфейс из того же AST
    // (до освобождения). Интерфейс несёт полные Konda-сигнатуры для потребителя.
    БуферКода буфер_h;
    буфер_кода_инициализировать(&буфер_h);
    БуферКода буфер_и;
    буфер_кода_инициализировать(&буфер_и);
    if (библиотека) {
        кодоген_заголовок(общий, &буфер_h);
        эмит_интерфейс(общий, &буфер_и);
    }
    // Признак потоков снимаем ДО освобождения дерева: линковка ниже читать
    // «общий» уже не может (это было бы use-after-free).
    нужен_pthread = программа_использует_потоки(общий);
    освободить_узел(общий);

    // ── имя выхода — от первого файла ──────────────────────────────────────
    char база[256];
    if (!путь_без_расширения(входные[0], база, sizeof(база))) {
        fprintf(stderr, "Слишком длинное имя файла\n");
        буфер_кода_освободить(&буфер);
        буфер_кода_освободить(&буфер_h);
        буфер_кода_освободить(&буфер_и);
        код = 1;
        goto очистка;
    }

    if (mkdir(ПАПКА_ВЫВОДА, 0755) != 0 && errno != EEXIST) {
        fprintf(stderr, "Не удалось создать папку %s\n", ПАПКА_ВЫВОДА);
        буфер_кода_освободить(&буфер);
        буфер_кода_освободить(&буфер_h);
        буфер_кода_освободить(&буфер_и);
        код = 1;
        goto очистка;
    }

    char путь_си[512];
    char путь_бин[512];
    snprintf(путь_си, sizeof(путь_си), "%s/%s.c", ПАПКА_ВЫВОДА, база);
    snprintf(путь_бин, sizeof(путь_бин), "%s/%s", ПАПКА_ВЫВОДА, база);

    FILE *f = fopen(путь_си, "wb");
    if (!f) {
        fprintf(stderr, "Не удалось создать %s\n", путь_си);
        буфер_кода_освободить(&буфер);
        буфер_кода_освободить(&буфер_h);
        буфер_кода_освободить(&буфер_и);
        код = 1;
        goto очистка;
    }
    const char *код_строка = буфер_кода_строка(&буфер);
    size_t код_длина = буфер.базовый.длина > 0 ? буфер.базовый.длина - 1 : 0;
    fwrite(код_строка, 1, код_длина, f);
    fclose(f);
    printf("Записан %s (%zu байт)\n", путь_си, код_длина);
    буфер_кода_освободить(&буфер);

    // Заголовок библиотеки рядом с .c.
    if (библиотека) {
        char путь_h[512];
        snprintf(путь_h, sizeof(путь_h), "%s/%s.h", ПАПКА_ВЫВОДА, база);
        FILE *fh = fopen(путь_h, "wb");
        if (!fh) {
            fprintf(stderr, "Не удалось создать %s\n", путь_h);
            буфер_кода_освободить(&буфер_h);
            код = 1;
            goto очистка;
        }
        const char *h_строка = буфер_кода_строка(&буфер_h);
        size_t h_длина = буфер_h.базовый.длина > 0 ? буфер_h.базовый.длина - 1 : 0;
        fwrite(h_строка, 1, h_длина, fh);
        fclose(fh);
        printf("Записан %s (%zu байт)\n", путь_h, h_длина);

        // Konda-интерфейс рядом с .h/.so — для безопасного пути 2.
        char путь_и[512];
        snprintf(путь_и, sizeof(путь_и), "%s/%s.и", ПАПКА_ВЫВОДА, база);
        FILE *fи = fopen(путь_и, "wb");
        if (fи) {
            const char *и_строка = буфер_кода_строка(&буфер_и);
            size_t и_длина = буфер_и.базовый.длина > 0 ? буфер_и.базовый.длина - 1 : 0;
            fwrite(и_строка, 1, и_длина, fи);
            fclose(fи);
            printf("Записан %s (%zu байт)\n", путь_и, и_длина);
        }
    }
    буфер_кода_освободить(&буфер_h);
    буфер_кода_освободить(&буфер_и);

    // Автолинковка Konda-библиотек потребителя (безопасный путь 2).
    const char *доп_so[64];
    size_t число_so = 0;
    if (!библиотека && интерфейсы) {
        size_t m = интерфейс_число_библиотек(интерфейсы);
        for (size_t i = 0; i < m && число_so < 64; ++i)
            доп_so[число_so++] = интерфейс_библиотека(интерфейсы, i);
    }
    код = скомпилировать_си(путь_си, путь_бин, релиз, библиотека,
                            база_каталог, доп_so, число_so, нужен_pthread);

очистка:
    интерфейс_освободить(интерфейсы);
    for (size_t i = 0; i < число_файлов; ++i) {
        if (корни[i]) освободить_узел(корни[i]);
        массив_токенов_освободить(&токены[i]);
        free(источники[i]);
    }
    free(корни);
    free(токены);
    free(источники);
    free(входные);
    return код;
}
