#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <errno.h>
#include <sys/stat.h>

#include "транспилятор.h"
#include "аст.h"

#define ПАПКА_ВЫВОДА "вывод"

// Новый путь через AST: токены → разбор → семантика → кодоген → запись .c.
// Включается переменной окружения KONDA_AST.
static int транспилировать_аст(МассивТокенов токены, const char *путь_выходного_си)
{
    Диагностика диаг = {0};

    Узел *дерево = разобрать(&токены, &диаг);
    if (!дерево) {
        fprintf(stderr, "Ошибка транспиляции (строка %d, колонка %d): %s\n",
                диаг.строка, диаг.колонка, диаг.сообщение);
        return 1;
    }

    if (!анализировать(дерево, &диаг)) {
        fprintf(stderr, "Ошибка транспиляции (строка %d, колонка %d): %s\n",
                диаг.строка, диаг.колонка, диаг.сообщение);
        освободить_узел(дерево);
        return 1;
    }

    БуферКода буфер;
    буфер_кода_инициализировать(&буфер);
    кодоген(дерево, &буфер);
    освободить_узел(дерево);

    FILE *f = fopen(путь_выходного_си, "wb");
    if (!f) {
        fprintf(stderr, "Не удалось создать %s\n", путь_выходного_си);
        буфер_кода_освободить(&буфер);
        return 1;
    }
    const char *код_строка = буфер_кода_строка(&буфер);
    size_t код_длина = буфер.базовый.длина > 0 ? буфер.базовый.длина - 1 : 0;
    fwrite(код_строка, 1, код_длина, f);
    fclose(f);
    буфер_кода_освободить(&буфер);
    printf("Записан %s (%zu байт)\n", путь_выходного_си, код_длина);
    return 0;
}

static int путь_без_расширения(const char *вход, char *выход, size_t cap)
{
    const char *слэш = strrchr(вход, '/');
    const char *имя = слэш ? слэш + 1 : вход;
    const char *точка = strrchr(имя, '.');
    size_t len = точка ? (size_t)(точка - имя) : strlen(имя);
    if (len + 1 >= cap) {
        return 0;
    }
    memcpy(выход, имя, len);
    выход[len] = '\0';
    return 1;
}

int main(int количество_аргументов, char **аргументы)
{
    if (количество_аргументов < 2) {
        fprintf(stderr, "Использование: %s <файл.конда>\n", аргументы[0]);
        return 2;
    }

    const char *путь_конда = аргументы[1];
    size_t размер;
    char *буфер = прочитать_файл((char *)путь_конда, &размер);
    if (!буфер) {
        fprintf(stderr, "Не удалось прочитать %s\n", путь_конда);
        return 1;
    }

    МассивТокенов токены = токенизировать_код(размер, буфер, 0);

    // printf("%s", конвертация_базовая(токены));


    char база[256];
    if (!путь_без_расширения(путь_конда, база, sizeof(база))) {
        fprintf(stderr, "Слишком длинное имя файла\n");
        очистка(&токены, буфер);
        return 1;
    }

    if (mkdir(ПАПКА_ВЫВОДА, 0755) != 0 && errno != EEXIST) {
        fprintf(stderr, "Не удалось создать папку %s\n", ПАПКА_ВЫВОДА);
        очистка(&токены, буфер);
        return 1;
    }

    char путь_си[512];
    char путь_бин[512];
    snprintf(путь_си, sizeof(путь_си), "%s/%s.c", ПАПКА_ВЫВОДА, база);
    snprintf(путь_бин, sizeof(путь_бин), "%s/%s", ПАПКА_ВЫВОДА, база);

    // По умолчанию используется путь через AST. Старый однопроходный
    // транспилятор доступен через KONDA_LEGACY=1 (будет удалён позже).
    int код;
    if (getenv("KONDA_LEGACY")) {
        код = транспилировать_в_си(токены, путь_конда, путь_си);
    } else {
        код = транспилировать_аст(токены, путь_си);
    }
    if (код == 0) {
        код = скомпилировать_си(путь_си, путь_бин);
    }

    очистка(&токены, буфер);
    return код;
}
