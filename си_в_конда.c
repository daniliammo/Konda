// СиВКонда — самостоятельный конвертер C → Konda.
//
// Отдельный бинарник: НЕ линкует libkonda и НЕ содержит транспиляции. Делает
// только синтаксический перевод .c → .конда. Все проверки Konda выполняются
// отдельной командой транспилятора над результатом:
//
//     ./Собранное/СиВКонда  файл.c              → вывод/файл.конда
//     ./Собранное/Транспилятор вывод/файл.конда → проверки Konda + C/elf

#include <errno.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <sys/stat.h>

#include "сиконда.h"   // + транспилятор.h (только ДинМассив/БуферКода)

#define ПАПКА_ВЫВОДА "вывод"
#define ВЕРСИЯ "0.1.0"

// Собственное чтение файла (не тянем ввод_вывод.o, где живёт скомпилировать_си).
static char *прочитать_весь_файл(const char *путь, size_t *размер)
{
    FILE *f = fopen(путь, "rb");
    if (!f) return nullptr;
    if (fseek(f, 0, SEEK_END) != 0) { fclose(f); return nullptr; }
    long n = ftell(f);
    if (n < 0) { fclose(f); return nullptr; }
    rewind(f);
    char *буф = malloc((size_t)n + 1);
    if (!буф) { fclose(f); return nullptr; }
    size_t прочитано = fread(буф, 1, (size_t)n, f);
    fclose(f);
    буф[прочитано] = '\0';
    if (размер) *размер = прочитано;
    return буф;
}

static int имя_без_расширения(const char *вход, char *выход, size_t cap)
{
    const char *слэш = strrchr(вход, '/');
    const char *имя = слэш ? слэш + 1 : вход;
    const char *точка = strrchr(имя, '.');
    size_t len = точка ? (size_t)(точка - имя) : strlen(имя);
    if (len + 1 >= cap) return 0;
    memcpy(выход, имя, len);
    выход[len] = '\0';
    return 1;
}

static void использование(const char *имя)
{
    fprintf(stderr,
        "Использование: %s <файл.c>\n"
        "  Переводит C в Konda: пишет вывод/<база>.конда.\n"
        "  Проверки Konda выполняет транспилятор отдельной командой:\n"
        "    Транспилятор вывод/<база>.конда\n"
        "  --version (-v): показать версию.\n", имя);
}

int main(int argc, char **argv)
{
    for (int i = 1; i < argc; ++i)
        if (strcmp(argv[i], "--version") == 0 || strcmp(argv[i], "-v") == 0) {
            printf("СиВКонда %s\n", ВЕРСИЯ);
            return 0;
        }

    const char *вход = nullptr;
    for (int i = 1; i < argc; ++i) {
        if (argv[i][0] == '-' && argv[i][1] == '-') {
            fprintf(stderr, "Неизвестный флаг: %s\n", argv[i]);
            использование(argv[0]);
            return 2;
        }
        if (вход) { fprintf(stderr, "Ожидался ровно один входной файл\n"); return 2; }
        вход = argv[i];
    }
    if (!вход) { использование(argv[0]); return 2; }

    size_t размер = 0;
    char *исходник = прочитать_весь_файл(вход, &размер);
    if (!исходник) { fprintf(stderr, "Не удалось прочитать %s\n", вход); return 1; }

    БуферКода вывод;
    буфер_кода_инициализировать(&вывод);
    ДинМассив ошибки;
    дин_массив_инициализировать(&ошибки, sizeof(СиДиаг));

    int ок = сиконда_конвертировать(исходник, размер, &вывод, &ошибки);

    for (size_t i = 0; i < ошибки.длина; ++i) {
        СиДиаг *д = дин_массив_получить(&ошибки, i);
        fprintf(stderr, "Ошибка конвертации %s (строка %d, колонка %d): %s\n",
                вход, д->строка, д->колонка, д->сообщение);
    }
    if (ошибки.длина > 0)
        fprintf(stderr, "Всего ошибок: %zu\n", ошибки.длина);

    int код = 0;
    if (!ок) {
        код = 1;
        goto очистка;
    }

    char база[256];
    if (!имя_без_расширения(вход, база, sizeof(база))) {
        fprintf(stderr, "Слишком длинное имя файла\n");
        код = 1; goto очистка;
    }
    if (mkdir(ПАПКА_ВЫВОДА, 0755) != 0 && errno != EEXIST) {
        fprintf(stderr, "Не удалось создать папку %s\n", ПАПКА_ВЫВОДА);
        код = 1; goto очистка;
    }
    char путь[512];
    snprintf(путь, sizeof(путь), "%s/%s.конда", ПАПКА_ВЫВОДА, база);
    FILE *f = fopen(путь, "wb");
    if (!f) { fprintf(stderr, "Не удалось создать %s\n", путь); код = 1; goto очистка; }
    const char *строка = буфер_кода_строка(&вывод);
    size_t длина = вывод.базовый.длина > 0 ? вывод.базовый.длина - 1 : 0;
    fwrite(строка, 1, длина, f);
    fclose(f);
    printf("Записан %s (%zu байт)\n", путь, длина);

очистка:
    дин_массив_освободить(&ошибки);
    буфер_кода_освободить(&вывод);
    free(исходник);
    return код;
}
