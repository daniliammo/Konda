#ifndef AST_H
#define AST_H

#include "транспилятор.h"

typedef enum {
    УЗЕЛ_ПРОГРАММА,      // дети: директивы (#содержит) и функции
    УЗЕЛ_ПРЕПРОЦЕССОР,   // токен: вся строка препроцессора
    УЗЕЛ_ФУНКЦИЯ,        // токен: имя; тип_данных: тип возврата; это_main; дети: [типопараметры…, параметры…, тело]
    УЗЕЛ_ТИПОПАРАМЕТР,   // токен: имя типа-параметра «T» обобщённой функции
                         // (ребёнок УЗЕЛ_ФУНКЦИЯ перед параметрами; снимается
                         // мономорфизацией)
    УЗЕЛ_ТИПОАРГУМЕНТ,   // токен/тип_данных/имя_типа: конкретный тип-аргумент на
                         // вызове «имя<тип…>(…)» (ведущий ребёнок УЗЕЛ_ВЫЗОВ)
    УЗЕЛ_ПАРАМЕТР,       // токен: имя; тип_данных: тип; указатели; массив
    УЗЕЛ_БЛОК,           // дети: операторы
    УЗЕЛ_ЕСЛИ,           // дети: [условие, тогда-блок, иначе-блок?]
    УЗЕЛ_ПОКА,           // дети: [условие, тело-блок]
    УЗЕЛ_ДЛЯ,            // дети: [инициализация, условие, шаг, тело-блок]
    УЗЕЛ_ДЛЯ_КАЖДОГО,    // foreach по срезу/массиву; токен: имя элемента;
                         // дети: [коллекция-выражение, тело-блок]
    УЗЕЛ_ВОЗВРАТ,        // дети: [выражение?]
    УЗЕЛ_ПРЕРВАТЬ,       // break — выход из ближайшего цикла
    УЗЕЛ_ПРОДОЛЖИТЬ,     // continue — следующая итерация ближайшего цикла
    УЗЕЛ_ОБЪЯВЛЕНИЕ,     // токен: имя; тип_данных: тип; дети: [инициализатор?]
    УЗЕЛ_ПРИСВАИВАНИЕ,   // дети: [цель, значение]
    УЗЕЛ_ВЫЗОВ,          // токен: имя; дети: аргументы-выражения
    УЗЕЛ_БИНОП,          // токен: оператор (различать по тексту); дети: [лево, право]
    УЗЕЛ_УНОП,           // токен: оператор; дети: [операнд]
    УЗЕЛ_ИНДЕКС,         // токен: имя массива; дети: [индекс]
    УЗЕЛ_ИДЕНТИФИКАТОР,  // токен: имя
    УЗЕЛ_ЧИСЛО,          // токен: литерал
    УЗЕЛ_СТРОКА,         // токен: литерал
    УЗЕЛ_F_СТРОКА,       // интерполированная строка; дети: чанки (УЗЕЛ_СТРОКА) и
                         // вставки (УЗЕЛ_ИНТЕРПОЛЯЦИЯ) по порядку
    УЗЕЛ_ИНТЕРПОЛЯЦИЯ,   // «\(выр)»; тип_данных/беззнаковый/указатели/срез — тип
                         // значения (для выбора спецификатора); дети: [выражение]
    УЗЕЛ_НУЛЬ,           // токен: нуль
    УЗЕЛ_СТРУКТУРА,      // токен: имя; дети: поля (УЗЕЛ_ПАРАМЕТР)
    УЗЕЛ_ПЕРЕЧИСЛЕНИЕ,   // токен: имя; дети: варианты (УЗЕЛ_ВАРИАНТ)
    УЗЕЛ_ВАРИАНТ,        // токен: имя варианта; дети: [значение-выражение?]
    УЗЕЛ_ПРИВЕДЕНИЕ,     // как<тип>(выр); тип_данных/беззнаковый/указатели; дети: [выражение]
    УЗЕЛ_ВЫБОР,          // выбор выр { … }; дети: [выражение, ветки (УЗЕЛ_ВЕТКА_ВЫБОРА)…]
    УЗЕЛ_ВЕТКА_ВЫБОРА,   // токен: имя варианта («_» — по умолчанию); дети: [блок]
    УЗЕЛ_ПОЛЕ,           // токен: имя поля; дети: [база-выражение]
    УЗЕЛ_ЛИТЕРАЛ_СТРУКТУРЫ, // дети: инициализаторы полей (УЗЕЛ_ИНИЦ_ПОЛЯ)
    УЗЕЛ_ИНИЦ_ПОЛЯ,      // токен: имя поля; дети: [значение]
    УЗЕЛ_ЛИТЕРАЛ_МАССИВА, // дети: выражения-инициализаторы в порядке элементов
    УЗЕЛ_ТИПФУНКЦИИ      // токен: имя типа-псевдонима; тип_данных/указатели/имя_типа:
                        // тип возврата; дети: параметры (УЗЕЛ_ПАРАМЕТР) — типы
                        // указателя на функцию. → C «typedef RET (*Имя)(параметры);»
} ТипУзла;

typedef struct Узел Узел;
struct Узел {
    ТипУзла тип;
    Токен токен;          // имя/оператор/литерал + строка/колонка для диагностики
    ДинМассив дети;       // элементы — Узел* (размер_элемента = sizeof(Узел*))
    ТипТокена тип_данных; // тип возврата функции / тип объявления / тип параметра
    int беззнаковый;      // префикс «неподписанный»: целоеN → uintN_t
    int это_main;         // для УЗЕЛ_ФУНКЦИЯ
    int это_союз;         // для УЗЕЛ_СТРУКТУРА: объявлено как «союз» (→ C union)
    int указатели;        // для УЗЕЛ_ПАРАМЕТР/ОБЪЯВЛЕНИЕ: число '*'
    int массив;           // УЗЕЛ_ПАРАМЕТР: наличие '[]'; УЗЕЛ_ОБЪЯВЛЕНИЕ: массив
                          // фикс. размера; УЗЕЛ_ИНДЕКС: доступ к массиву фикс.
                          // размера (флаг проставляет семантика)
    long размер_массива;  // УЗЕЛ_ОБЪЯВЛЕНИЕ: размер «тип имя[N]»
    int срез;             // тип «срез<T>» (объявление/параметр); для УЗЕЛ_ИНДЕКС:
                          // флаг доступа к срезу (проставляет семантика)
    int безопасный_индекс; // УЗЕЛ_ИНДЕКС: семантика доказала, что индекс в
                          // границах → рантайм-проверка не нужна (прямой доступ)
    int проверка_аргументов; // УЗЕЛ_ИНДЕКС: динамический индекс по «аргументы»
                          // (указатель с известной длиной = argc) → рантайм-
                          // проверка границ (безопасная обёртка над argv)
    int проверка_бинопа;  // УЗЕЛ_БИНОП: неконстантный делитель/сдвиг → рантайм-
                          // проверка (1 = деление/остаток на ноль, 2 = сдвиг вне
                          // разрядности; для сдвига разрядность в размер_массива)
    int статический;      // «статический» (C static): УЗЕЛ_ФУНКЦИЯ — внутренняя
                          // связь; УЗЕЛ_ОБЪЯВЛЕНИЕ — статическая длительность
    int владеет;          // УЗЕЛ_ПАРАМЕТР: «владеет» — параметр забирает владение
                          // срезом (функция-потребитель), передача = move
    int по_ссылке;        // УЗЕЛ_ПАРАМЕТР: 0 — обычный (по значению); 1 —
                          // «изменяемый» (ref); 2 — «вывод» (out). Кодоген
                          // передаёт такой параметр как Тип* и авто-разыменовывает
    Токен имя_типа;       // для типа-структуры (тип_данных == ТОКЕН_СТРУКТУРА):
                          // имя пользовательской структуры
    char *собственное_имя; // мономорфизация: сгенерированное имя специализации
                          // (например «максимум_целое32»). Если задано — токен
                          // указывает в этот буфер, и Узел им владеет (free при
                          // освобождении, strdup при копировании).
};

typedef struct {
    int ошибка;
    char сообщение[512];
    int строка;
    int колонка;
} Диагностика;

// Узлы и дерево
Узел *узел_создать(ТипУзла тип);
void узел_добавить_ребёнка(Узел *родитель, Узел *ребёнок);
Узел *узел_ребёнок(const Узел *узел, size_t индекс);
size_t узел_число_детей(const Узел *узел);
Узел *узел_копировать(const Узел *исходный);   // глубокая копия (мономорфизация)
void освободить_узел(Узел *узел);

// Конвейер
Узел *разобрать(МассивТокенов *токены, Диагностика *диаг);
int анализировать(Узел *корень, Диагностика *диаг);
// Расширенный анализ для проверки внешних функций и сбора предупреждений.
// внешние — таблица C-функций, увиденных через заголовки (nullptr — не
// проверять неизвестные вызовы, старое поведение). предупреждения — накопитель
// не фатальных диагностик (элементы типа Диагностика; nullptr — не собирать).
// ошибки — накопитель ВСЕХ ошибок за прогон (батч-режим для IDE/CLI: подсветить
// все сразу). nullptr — старое поведение (стоп на первой ошибке, в *диаг).
// Элементы предупреждений и ошибок — типа Диагностика. При заданном «ошибки»
// возврат 0/1 отражает пустоту списка ошибок, а не флаг *диаг.
typedef struct ТаблицаВнешних ТаблицаВнешних;
int анализировать_полный(Узел *корень, Диагностика *диаг,
                         const ТаблицаВнешних *внешние,
                         ДинМассив *предупреждения, ДинМассив *ошибки);
// релиз: 0 — отладочная сборка (все рантайм-проверки: границы срезов/массивов/
// argv, guard'ы деления/сдвига/переполнения). 1 — релиз: проверки снимаются
// (производительность уровня C); статический анализ остаётся полным.
int кодоген(Узел *корень, БуферКода *буфер, int релиз);

// Генерация публичного заголовка (.h) для режима библиотеки: типы (перечисления,
// структуры, Срез при необходимости) и прототипы НЕ-static функций. Реализации
// и рантайм-прологи остаются в .c. Делает собранную .so пригодной для вызова
// из C. Тело .c для библиотеки генерируется обычным «кодоген».
int кодоген_заголовок(Узел *корень, БуферКода *буфер);

// Multi-file: имена структур, объявленных в других файлах, чтобы парсер
// текущего файла мог распознать «Имя п» как объявление переменной
// пользовательского типа. Без этого структура из соседнего файла остаётся
// для парсера обычным идентификатором, и объявление переменной не разбирается.
typedef struct ИменаСтруктур ИменаСтруктур;
ИменаСтруктур *имена_структур_создать(void);
void           имена_структур_освободить(ИменаСтруктур *);
void           собрать_имена_структур(МассивТокенов *токены, ИменаСтруктур *);

// То же самое, но для перечислений: без этого «Имя п», где «Имя» —
// перечисление из соседнего файла, тоже не распознаётся как объявление.
typedef struct ИменаПеречислений ИменаПеречислений;
ИменаПеречислений *имена_перечислений_создать(void);
void                имена_перечислений_освободить(ИменаПеречислений *);
void                собрать_имена_перечислений(МассивТокенов *токены, ИменаПеречислений *);

Узел          *разобрать_с_именами_структур(МассивТокенов *токены,
                                             Диагностика *диаг,
                                             const ИменаСтруктур *известные_структуры,
                                             const ИменаПеречислений *известные_перечисления);

#endif
