// Конвертер C → Konda: обход C-AST и эмиссия исходника Konda в БуферКода.
// Только синтаксический перевод + структурные понижения (do-while, тернарник,
// switch). Никакой семантики — её выполняет транспилятор Konda над результатом.

#include <stdarg.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "сиконда.h"

int текст_равен_(const СиТокен *т, const char *s);  // определён ниже

typedef struct {
    БуферКода *б;
    ДинМассив *ошибки;
    int есть_ошибка;
    // Переименование параметров точки входа: Konda жёстко ожидает
    // «количество_аргументов»/«аргументы» (семантика инъектирует эти символы для
    // main), поэтому имена argc/argv из C и все их использования подменяются.
    const char *подм_имя[2];
    size_t подм_длина[2];
    const char *подм_на[2];
    int число_подмен;
} Эмиттер;

static void доб(Эмиттер *э, const char *s) { буфер_кода_добавить(э->б, s); }
static void доб_срез(Эмиттер *э, const char *s, size_t n) { буфер_кода_добавить_отрезок(э->б, s, n); }
static void доб_ток(Эмиттер *э, СиТокен т) { доб_срез(э, т.текст, т.длина); }
static void отступ(Эмиттер *э, int у) { for (int i = 0; i < у; ++i) доб(э, "    "); }

static void эм_ошибка(Эмиттер *э, СиТокен т, const char *формат, ...)
{
    э->есть_ошибка = 1;
    if (!э->ошибки) return;
    СиДиаг д = { .ошибка = 1, .строка = т.строка, .колонка = т.колонка };
    va_list ап; va_start(ап, формат);
    vsnprintf(д.сообщение, sizeof(д.сообщение), формат, ап);
    va_end(ап);
    дин_массив_добавить(э->ошибки, &д);
}

// ─── типы ────────────────────────────────────────────────────────────────────

static void эмит_тип(Эмиттер *э, СиТип т)
{
    if (т.базовый == С_ИМЯ) {
        if (т.имя_длина) доб_срез(э, т.имя, т.имя_длина);
        else доб(э, "целое32"); // безымянный — запасной вариант
    } else if (т.базовый == С_VOID && т.указатели > 0) {
        доб(э, "символ"); // void* → символ* (документированное приближение)
    } else {
        int неподп = 0;
        const char *имя = "целое32";
        switch (т.базовый) {
            case С_VOID:   имя = "ничего"; break;
            case С_BOOL:   имя = "логический"; break;
            case С_CHAR:   имя = "символ"; break;
            case С_SCHAR:  имя = "целое8"; break;
            case С_UCHAR:  имя = "байт"; break;
            case С_SHORT:  имя = "целое16"; неподп = т.неподписанный; break;
            case С_INT:    имя = "целое32"; неподп = т.неподписанный; break;
            case С_LONG:
            case С_LONGLONG: имя = "целое64"; неподп = т.неподписанный; break;
            case С_FLOAT:  имя = "вещественное"; break;
            case С_DOUBLE: имя = "вещественное64"; break;
            case С_INT8:   имя = "целое8"; break;
            case С_INT16:  имя = "целое16"; break;
            case С_INT32:  имя = "целое32"; break;
            case С_INT64:  имя = "целое64"; break;
            case С_UINT8:  имя = "байт"; break;
            case С_UINT16: имя = "целое16"; неподп = 1; break;
            case С_UINT32: имя = "целое32"; неподп = 1; break;
            case С_UINT64: имя = "целое64"; неподп = 1; break;
            case С_SIZE:   имя = "целое64"; неподп = 1; break;
            default: break;
        }
        if (неподп) доб(э, "неподписанный ");
        доб(э, имя);
    }
    for (int i = 0; i < т.указатели; ++i) доб(э, "*");
}

// ─── выражения ───────────────────────────────────────────────────────────────

static void эмит_выражение(Эмиттер *э, СиУзел *у);

// Числовой литерал: убираем C-суффиксы (u/l/f), hex → десятичное.
static void эмит_число(Эмиттер *э, СиТокен т)
{
    if (т.длина > 2 && т.текст[0] == '0' && (т.текст[1] == 'x' || т.текст[1] == 'X')) {
        unsigned long long v = strtoull(т.текст, nullptr, 16);
        char буф[32]; snprintf(буф, sizeof(буф), "%llu", v);
        доб(э, буф);
        return;
    }
    size_t n = т.длина;
    while (n > 0) {
        char c = т.текст[n - 1];
        if (c == 'u' || c == 'U' || c == 'l' || c == 'L' || c == 'f' || c == 'F') n--;
        else break;
    }
    // не срезаем суффикс у чисел с точкой, где 'f' — часть? '3.5f' → '3.5' ок.
    доб_срез(э, т.текст, n);
}

static void эмит_аргументы(Эмиттер *э, СиУзел *вызов, size_t с)
{
    доб(э, "(");
    for (size_t i = с; i < си_детей(вызов); ++i) {
        if (i > с) доб(э, ", ");
        эмит_выражение(э, си_ребёнок(вызов, i));
    }
    доб(э, ")");
}

static void эмит_выражение(Эмиттер *э, СиУзел *у)
{
    switch (у->вид) {
        case У_ЧИСЛО:      эмит_число(э, у->токен); break;
        case У_СТРОКА_ЛИТ:
        case У_СИМВОЛ_ЛИТ:  доб_ток(э, у->токен); break;
        case У_ИДЕНТ: {
            for (int i = 0; i < э->число_подмен; ++i) {
                if (у->токен.длина == э->подм_длина[i] &&
                    memcmp(у->токен.текст, э->подм_имя[i], у->токен.длина) == 0) {
                    доб(э, э->подм_на[i]);
                    return;
                }
            }
            доб_ток(э, у->токен);
            break;
        }
        case У_БИНОП:
            эмит_выражение(э, си_ребёнок(у, 0));
            доб(э, " "); доб_ток(э, у->токен); доб(э, " ");
            эмит_выражение(э, си_ребёнок(у, 1));
            break;
        case У_УНОП:
            if (у->срез_дереф) {                    // *p → p[0] (срез)
                эмит_выражение(э, си_ребёнок(у, 0));
                доб(э, "[0]");
                break;
            }
            if (текст_равен_(&у->токен, "++") || текст_равен_(&у->токен, "--"))
                эм_ошибка(э, у->токен, "«%.*s» в выражении не поддержано (только как отдельный оператор)",
                          (int)у->токен.длина, у->токен.текст);
            доб_ток(э, у->токен);
            эмит_выражение(э, си_ребёнок(у, 0));
            break;
        case У_ПОСТФИКС:
            эм_ошибка(э, у->токен, "постфиксный «%.*s» в выражении не поддержан",
                      (int)у->токен.длина, у->токен.текст);
            эмит_выражение(э, си_ребёнок(у, 0));
            break;
        case У_ВЫЗОВ:
            эмит_выражение(э, си_ребёнок(у, 0));
            эмит_аргументы(э, у, 1);
            break;
        case У_ИНДЕКС:
            эмит_выражение(э, си_ребёнок(у, 0));
            доб(э, "[");
            эмит_выражение(э, си_ребёнок(у, 1));
            доб(э, "]");
            break;
        case У_ПОЛЕ:
            эмит_выражение(э, си_ребёнок(у, 0));
            if (у->срез_стрелка) доб(э, "[0].");   // p->f → p[0].f (срез)
            else доб_ток(э, у->токен);              // «.» или «->» дословно
            доб_ток(э, у->поле_имя);
            break;
        case У_КАСТ:
            доб(э, "как<"); эмит_тип(э, у->тип); доб(э, ">(");
            эмит_выражение(э, си_ребёнок(у, 0));
            доб(э, ")");
            break;
        case У_РАЗМЕР_ТИПА:
            доб(э, "размер_обьекта("); эмит_тип(э, у->тип); доб(э, ")");
            break;
        case У_РАЗМЕР_ВЫРАЖ:
            доб(э, "размер_обьекта(");
            эмит_выражение(э, си_ребёнок(у, 0));
            доб(э, ")");
            break;
        case У_СПИСОК_ИНИЦ:
        case У_СОСТ_ЛИТЕРАЛ: {
            СиУзел *список = (у->вид == У_СОСТ_ЛИТЕРАЛ) ? си_ребёнок(у, 0) : у;
            доб(э, "{ ");
            for (size_t i = 0; i < си_детей(список); ++i) {
                if (i > 0) доб(э, ", ");
                эмит_выражение(э, си_ребёнок(список, i));
            }
            доб(э, " }");
            break;
        }
        case У_ТЕРНАРНИК:
            эм_ошибка(э, у->токен, "тернарный оператор поддержан только в присваивании и «return»");
            break;
        case У_ПРИСВАИВАНИЕ:
            эм_ошибка(э, у->токен, "присваивание внутри выражения не поддержано");
            break;
        default:
            эм_ошибка(э, у->токен, "неподдержанное выражение");
            break;
    }
}

// текст_равен объявлен в си_разбор.c; здесь своя копия под другим именем.
int текст_равен_(const СиТокен *т, const char *s)
{
    size_t n = strlen(s);
    return т->длина == n && memcmp(т->текст, s, n) == 0;
}

// ─── операторы ───────────────────────────────────────────────────────────────

static void эмит_оператор(Эмиттер *э, СиУзел *у, int ур);

// Присваивание (с раскрытием составных операторов) без отступа и перевода строки.
static void эмит_присваивание_строкой(Эмиттер *э, СиУзел *прис)
{
    СиУзел *цель = си_ребёнок(прис, 0);
    СиУзел *знач = си_ребёнок(прис, 1);
    эмит_выражение(э, цель);
    доб(э, " = ");
    if (текст_равен_(&прис->токен, "=")) {
        эмит_выражение(э, знач);
    } else {
        // a += b → a = a + b
        char оп[2] = { прис->токен.текст[0], 0 };
        эмит_выражение(э, цель);
        доб(э, " "); доб(э, оп); доб(э, " ");
        эмит_выражение(э, знач);
    }
}

// Понижение ++/-- (как оператор или шаг for): x++ → x = x + 1.
static void эмит_инкремент_строкой(Эмиттер *э, СиУзел *у)
{
    СиУзел *опнд = си_ребёнок(у, 0);
    эмит_выражение(э, опнд);
    доб(э, " = ");
    эмит_выражение(э, опнд);
    доб(э, текст_равен_(&у->токен, "++") ? " + 1" : " - 1");
}

// Выражение или присваивание/инкремент как «часть» (for-init/step) — без ; и \n.
static void эмит_часть(Эмиттер *э, СиУзел *у)
{
    if (!у) return;
    if (у->вид == У_БЛОК) return; // пусто
    if (у->вид == У_ОБЪЯВЛЕНИЕ) {
        эмит_тип(э, у->тип); доб(э, " "); доб_ток(э, у->токен);
        if (си_детей(у) > 0) { доб(э, " = "); эмит_выражение(э, си_ребёнок(у, 0)); }
        return;
    }
    if (у->вид == У_ПРИСВАИВАНИЕ) { эмит_присваивание_строкой(э, у); return; }
    if (у->вид == У_УНОП && (текст_равен_(&у->токен, "++") || текст_равен_(&у->токен, "--"))) {
        эмит_инкремент_строкой(э, у); return;
    }
    if (у->вид == У_ПОСТФИКС) { эмит_инкремент_строкой(э, у); return; }
    эмит_выражение(э, у);
}

// Тело управляющей конструкции: всегда в фигурных скобках Konda.
static void эмит_тело(Эмиттер *э, СиУзел *у, int ур)
{
    доб(э, " {\n");
    if (у->вид == У_БЛОК) {
        for (size_t i = 0; i < си_детей(у); ++i) эмит_оператор(э, си_ребёнок(у, i), ур + 1);
    } else {
        эмит_оператор(э, у, ур + 1);
    }
    отступ(э, ур); доб(э, "}");
}

static void эмит_оператор(Эмиттер *э, СиУзел *у, int ур)
{
    switch (у->вид) {
        case У_ОБЪЯВЛЕНИЕ:
            if (у->срез_выд) {   // T *p = malloc(...) → срез<T> p = выделить(N)
                СиТип элем = у->тип; элем.указатели--;
                отступ(э, ур);
                доб(э, "срез<"); эмит_тип(э, элем); доб(э, "> ");
                доб_ток(э, у->токен);
                доб(э, " = выделить(");
                if (у->счёт) эмит_выражение(э, у->счёт); else доб(э, "0");
                доб(э, ")\n");
                return;
            }
            отступ(э, ур);
            эмит_тип(э, у->тип); доб(э, " "); доб_ток(э, у->токен);
            if (у->есть_массив) {
                доб(э, "[");
                if (у->разм_массива >= 0) { char б[24]; snprintf(б, sizeof(б), "%ld", у->разм_массива); доб(э, б); }
                доб(э, "]");
            }
            if (си_детей(у) > 0) {
                СиУзел *ин = си_ребёнок(у, 0);
                if (ин->вид == У_ТЕРНАРНИК) {
                    // тип имя\n если (усл) { имя = a } иначе { имя = b }
                    доб(э, "\n"); отступ(э, ур); доб(э, "если (");
                    эмит_выражение(э, си_ребёнок(ин, 0)); доб(э, ") {\n");
                    отступ(э, ур + 1); доб_ток(э, у->токен); доб(э, " = ");
                    эмит_выражение(э, си_ребёнок(ин, 1)); доб(э, "\n");
                    отступ(э, ур); доб(э, "} иначе {\n");
                    отступ(э, ур + 1); доб_ток(э, у->токен); доб(э, " = ");
                    эмит_выражение(э, си_ребёнок(ин, 2)); доб(э, "\n");
                    отступ(э, ур); доб(э, "}\n");
                    return;
                }
                доб(э, " = "); эмит_выражение(э, ин);
            }
            доб(э, "\n");
            break;

        case У_ВЫРАЖ_ОПЕРАТОР: {
            СиУзел *в = си_ребёнок(у, 0);
            if (в->вид == У_ВЫЗОВ && в->дроп) break;  // free(срез) — autofree заменяет
            отступ(э, ур);
            if (в->вид == У_ПРИСВАИВАНИЕ) эмит_присваивание_строкой(э, в);
            else if (в->вид == У_ПОСТФИКС) эмит_инкремент_строкой(э, в);
            else if (в->вид == У_УНОП && (текст_равен_(&в->токен, "++") || текст_равен_(&в->токен, "--")))
                эмит_инкремент_строкой(э, в);
            else эмит_выражение(э, в);
            доб(э, "\n");
            break;
        }

        case У_ЕСЛИ:
            отступ(э, ур); доб(э, "если (");
            эмит_выражение(э, си_ребёнок(у, 0)); доб(э, ")");
            эмит_тело(э, си_ребёнок(у, 1), ур);
            if (си_детей(у) > 2) {
                доб(э, " иначе");
                эмит_тело(э, си_ребёнок(у, 2), ур);
            }
            доб(э, "\n");
            break;

        case У_ПОКА:
            отступ(э, ур); доб(э, "пока (");
            эмит_выражение(э, си_ребёнок(у, 0)); доб(э, ")");
            эмит_тело(э, си_ребёнок(у, 1), ур);
            доб(э, "\n");
            break;

        case У_ДЕЛАЙ: {
            // do { тело } while(усл) → пока (истина) { тело; если (!(усл)) { прервать } }
            СиУзел *тело = си_ребёнок(у, 0);
            СиУзел *усл = си_ребёнок(у, 1);
            отступ(э, ур); доб(э, "пока (истина) {\n");
            if (тело->вид == У_БЛОК)
                for (size_t i = 0; i < си_детей(тело); ++i) эмит_оператор(э, си_ребёнок(тело, i), ур + 1);
            else эмит_оператор(э, тело, ур + 1);
            отступ(э, ур + 1); доб(э, "если (!(");
            эмит_выражение(э, усл);
            доб(э, ")) {\n");
            отступ(э, ур + 2); доб(э, "прервать\n");
            отступ(э, ур + 1); доб(э, "}\n");
            отступ(э, ур); доб(э, "}\n");
            break;
        }

        case У_ДЛЯ:
            отступ(э, ур); доб(э, "для (");
            эмит_часть(э, си_ребёнок(у, 0)); доб(э, "; ");
            эмит_часть(э, си_ребёнок(у, 1)); доб(э, "; ");
            эмит_часть(э, си_ребёнок(у, 2)); доб(э, ")");
            эмит_тело(э, си_ребёнок(у, 3), ур);
            доб(э, "\n");
            break;

        case У_ВЫБОР:
            отступ(э, ур); доб(э, "выбор ");
            эмит_выражение(э, си_ребёнок(у, 0)); доб(э, " {\n");
            for (size_t i = 1; i < си_детей(у); ++i) {
                СиУзел *ветка = си_ребёнок(у, i);
                отступ(э, ур + 1); доб_ток(э, ветка->токен); доб(э, " {\n");
                for (size_t j = 0; j < си_детей(ветка); ++j)
                    эмит_оператор(э, си_ребёнок(ветка, j), ур + 2);
                отступ(э, ур + 1); доб(э, "}\n");
            }
            отступ(э, ур); доб(э, "}\n");
            break;

        case У_ВОЗВРАТ:
            отступ(э, ур);
            if (си_детей(у) > 0) {
                СиУзел *в = си_ребёнок(у, 0);
                if (в->вид == У_ТЕРНАРНИК) {
                    доб(э, "если ("); эмит_выражение(э, си_ребёнок(в, 0)); доб(э, ") {\n");
                    отступ(э, ур + 1); доб(э, "вернуть "); эмит_выражение(э, си_ребёнок(в, 1)); доб(э, "\n");
                    отступ(э, ур); доб(э, "} иначе {\n");
                    отступ(э, ур + 1); доб(э, "вернуть "); эмит_выражение(э, си_ребёнок(в, 2)); доб(э, "\n");
                    отступ(э, ур); доб(э, "}\n");
                    return;
                }
                доб(э, "вернуть "); эмит_выражение(э, в);
            } else {
                доб(э, "вернуть");
            }
            доб(э, "\n");
            break;

        case У_ПРЕРВАТЬ:    отступ(э, ур); доб(э, "прервать\n"); break;
        case У_ПРОДОЛЖИТЬ:  отступ(э, ур); доб(э, "продолжить\n"); break;
        case У_БЛОК:
            for (size_t i = 0; i < си_детей(у); ++i) эмит_оператор(э, си_ребёнок(у, i), ур);
            break;
        default:
            эм_ошибка(э, у->токен, "неподдержанный оператор");
            break;
    }
}

// ─── верхний уровень ─────────────────────────────────────────────────────────

static void эмит_препроцессор(Эмиттер *э, СиУзел *у)
{
    // «#include <...>» / «#include "..."» → «#содержит <...>»
    const char *p = у->токен.текст + 1;      // после '#'
    size_t ост = у->токен.длина - 1;
    while (ост && (*p == ' ' || *p == '\t')) { p++; ост--; }
    // пропустить "include"
    if (ост >= 7 && strncmp(p, "include", 7) == 0) { p += 7; ост -= 7; }
    while (ост && (*p == ' ' || *p == '\t')) { p++; ост--; }
    доб(э, "#содержит ");
    доб_срез(э, p, ост);
    доб(э, "\n");
}

static void эмит_структуру(Эмиттер *э, СиУзел *у)
{
    доб(э, у->это_union ? "союз " : "структура ");
    доб_ток(э, у->токен);
    доб(э, " {\n");
    for (size_t i = 0; i < си_детей(у); ++i) {
        СиУзел *поле = си_ребёнок(у, i);
        отступ(э, 1);
        эмит_тип(э, поле->тип); доб(э, " "); доб_ток(э, поле->токен);
        if (поле->есть_массив) {
            доб(э, "[");
            if (поле->разм_массива >= 0) { char б[24]; snprintf(б, sizeof(б), "%ld", поле->разм_массива); доб(э, б); }
            доб(э, "]");
        }
        доб(э, "\n");
    }
    доб(э, "}\n");
}

static void эмит_перечисление(Эмиттер *э, СиУзел *у)
{
    доб(э, "перечисление "); доб_ток(э, у->токен); доб(э, " {\n");
    for (size_t i = 0; i < си_детей(у); ++i) {
        СиУзел *в = си_ребёнок(у, i);
        отступ(э, 1); доб_ток(э, в->токен);
        if (си_детей(в) > 0) { доб(э, " = "); эмит_выражение(э, си_ребёнок(в, 0)); }
        доб(э, "\n");
    }
    доб(э, "}\n");
}

static void эмит_типфункции(Эмиттер *э, СиУзел *у)
{
    доб(э, "типфункции ");
    эмит_тип(э, у->тип); доб(э, " ");
    доб_ток(э, у->токен); доб(э, "(");
    for (size_t i = 0; i < си_детей(у); ++i) {
        if (i > 0) доб(э, ", ");
        эмит_тип(э, си_ребёнок(у, i)->тип);
    }
    доб(э, ")\n");
}

static void эмит_функцию(Эмиттер *э, СиУзел *у)
{
    эмит_тип(э, у->тип); доб(э, " ");
    if (у->это_точка_входа) доб(э, "точка_входа");
    else доб_ток(э, у->токен);
    доб(э, "(");
    static const char *ИМ_ВХОДА[2] = { "количество_аргументов", "аргументы" };
    СиУзел *тело = nullptr;
    size_t число_пар = 0;
    э->число_подмен = 0;
    for (size_t i = 0; i < си_детей(у); ++i) {
        СиУзел *р = си_ребёнок(у, i);
        if (р->вид == У_БЛОК) { тело = р; break; }
        if (число_пар > 0) доб(э, ", ");
        эмит_тип(э, р->тип); доб(э, " ");
        if (у->это_точка_входа && число_пар < 2) {
            доб(э, ИМ_ВХОДА[число_пар]);
            if (р->токен.длина) {  // подменяем использования исходного имени в теле
                э->подм_имя[э->число_подмен] = р->токен.текст;
                э->подм_длина[э->число_подмен] = р->токен.длина;
                э->подм_на[э->число_подмен] = ИМ_ВХОДА[число_пар];
                э->число_подмен++;
            }
        } else if (р->токен.длина) доб_ток(э, р->токен);
        else { char б[32]; snprintf(б, sizeof(б), "п%zu", число_пар); доб(э, б); }
        число_пар++;
    }
    доб(э, ")\n{\n");
    if (тело)
        for (size_t i = 0; i < си_детей(тело); ++i) эмит_оператор(э, си_ребёнок(тело, i), 1);
    доб(э, "}\n");
    э->число_подмен = 0;
}

int конда_эмит(СиУзел *программа, БуферКода *вывод, ДинМассив *ошибки)
{
    Эмиттер э = { .б = вывод, .ошибки = ошибки, .есть_ошибка = 0 };
    for (size_t i = 0; i < си_детей(программа); ++i) {
        СиУзел *у = си_ребёнок(программа, i);
        switch (у->вид) {
            case У_ПРЕПРОЦЕССОР: эмит_препроцессор(&э, у); break;
            case У_СТРУКТУРА:    эмит_структуру(&э, у); доб(&э, "\n"); break;
            case У_ПЕРЕЧИСЛЕНИЕ: эмит_перечисление(&э, у); доб(&э, "\n"); break;
            case У_ТИПФУНКЦИИ:   эмит_типфункции(&э, у); доб(&э, "\n"); break;
            case У_ФУНКЦИЯ:      эмит_функцию(&э, у); доб(&э, "\n"); break;
            case У_ОБЪЯВЛЕНИЕ:   эмит_оператор(&э, у, 0); break;
            default: break;
        }
    }
    return !э.есть_ошибка;
}
