/* Типы токенов */
typedef enum {
    ТОКЕН_ИДЕНТИФИКАТОР,
    ТОКЕН_ЧИСЛО,
    ТОКЕН_СТРОКА,
    ТОКЕН_СИМВОЛ,
    ТОКЕН_НЕИЗВЕСТНЫЙ_ОПЕРАТОР,
    ТОКЕН_ПУНКТУАЦИЯ,
    ТОКЕН_ПРЕПРОЦЕССОР,
    ТОКЕН_КОММЕНТАРИЙ,
    ТОКЕН_НОВАЯ_СТРОКА,
    ТОКЕН_КОНЕЦ_ФАЙЛА,
    ТОКЕН_НЕИЗВЕСТНЫЙ
} TokenType;

