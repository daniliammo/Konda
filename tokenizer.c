/* Простая кодогенерация — печать содержимого токенов */
void конвертация_базовая (TokenArray токены)
{
    char *итоговая_строка = NULL;
    size_t итоговая_длина = 0;

    for (size_t i = 0; i < токены.len; i++) {
        const Token *t = &токены.data[i];
        size_t разделитель_длина = (t->type != ТОКЕН_НОВАЯ_СТРОКА) ? 1 : 1;
        size_t новая_длина = итоговая_длина + t->len + разделитель_длина;
        char *новый_буфер = realloc(итоговая_строка, новая_длина + 1);

        if (новый_буфер == NULL) {
            free(итоговая_строка);
            fprintf(stderr, "Ошибка выделения памяти в кодогенерации\n");
            return;
        }

        итоговая_строка = новый_буфер;
        memcpy(итоговая_строка + итоговая_длина, t->start, t->len);
        итоговая_длина += t->len;

        if (t->type != ТОКЕН_НОВАЯ_СТРОКА) {
            итоговая_строка[итоговая_длина++] = ' ';
        } else {
            итоговая_строка[итоговая_длина++] = '\n';
        }
    }

    if (итоговая_строка != NULL) {
        итоговая_строка[итоговая_длина] = '\0';
        printf("%s\n", итоговая_строка);
        free(итоговая_строка);
    }
}

