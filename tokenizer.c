static const char* имя_типа_токена(TokenType t) {
    switch (t) {
        case ТОКЕН_ИДЕНТИФИКАТОР: return "Идентификатор";
        case ТОКЕН_ЧИСЛО: return "Число";
        case ТОКЕН_СТРОКА: return "Строка";
        case ТОКЕН_СИМВОЛ: return "Символ";
        case ТОКЕН_НЕИЗВЕСТНЫЙ_ОПЕРАТОР: return "Оператор";
        case ТОКЕН_ПУНКТУАЦИЯ: return "Пунктуация";
        case ТОКЕН_ПРЕПРОЦЕССОР: return "Препроцессор";
        case ТОКЕН_КОММЕНТАРИЙ: return "Комментарий";
        case ТОКЕН_НОВАЯ_СТРОКА: return "НоваяСтрока";
        case ТОКЕН_КОНЕЦ_ФАЙЛА: return "КонецФайла";
        default: return "НЕИЗВЕСТНЫЙ";
    }
}

/* Токен — zero-copy: указывает на участок входного буфера */
typedef struct {
    TokenType type;
    int line;
    int col;
    const char *start; /* указатель внутрь буфера или на статичную строку (EOF) */
    size_t len;
} Token;

/* Динамический массив токенов */
typedef struct {
    Token *data;
    size_t len;
    size_t cap;
} TokenArray;

static void массив_токенов_инициализировать(TokenArray *a) {
    a->cap = 128;
    a->len = 0;
    a->data = malloc(a->cap * sizeof(Token));
    if (!a->data) { perror("malloc"); exit(1); }
}
static void массив_токенов_освободить(TokenArray *a) {
    free(a->data); /* не освобождать a->data[i].start — zero-copy */
    a->data = NULL;
    a->len = a->cap = 0;
}
static void массив_токенов_добавить(TokenArray *a, TokenType type, int line, int col, const char *start, size_t len) {
    if (a->len + 1 > a->cap) {
        a->cap *= 2;
        a->data = realloc(a->data, a->cap * sizeof(Token));
        if (!a->data) { perror("realloc"); exit(1); }
    }
    a->data[a->len].type = type;
    a->data[a->len].line = line;
    a->data[a->len].col = col;
    a->data[a->len].start = start;
    a->data[a->len].len = len;
    a->len++;
}

/* Хелперы для распознавания идентификаторов (допускаем байты >= 0x80) */
static int является_началом_идентификатора(unsigned char c) {
    return (c == '_' || isalpha(c) || c >= 0x80);
}
static int является_частью_идентификатора(unsigned char c) {
    return (c == '_' || isalnum(c) || c >= 0x80);
}

/* Считывание всего файла в буфер */
static char* прочитать_файл(FILE *f, size_t *out_size) {
    if (fseek(f, 0, SEEK_END) == 0) {
        long len = ftell(f);
        if (len >= 0) {
            rewind(f);
            char *buf = malloc((size_t)len + 1);
            if (!buf) { perror("malloc"); exit(1); }
            size_t r = fread(buf, 1, (size_t)len, f);
            buf[r] = '\0';
            if (out_size) *out_size = r;
            return buf;
        }
    }
    /* fallback для stdin / потоков неизвестной длины */
    size_t cap = 4096, sz = 0;
    char *buf = malloc(cap);
    if (!buf) { perror("malloc"); exit(1); }
    for (;;) {
        size_t r = fread(buf + sz, 1, cap - sz, f);
        sz += r;
        if (r == 0) break;
        if (sz + 1 >= cap) {
            cap *= 2;
            buf = realloc(buf, cap);
            if (!buf) { perror("realloc"); exit(1); }
        }
    }
    buf[sz] = '\0';
    if (out_size) *out_size = sz;
    return buf;
}

/* Печать среза (start,len) с экранированием управляющих символов, в одинарных кавычках */
static void печать_экранированного_отрезка(const char *start, size_t len) {
    putchar('\'');
    for (size_t i = 0; i < len; ++i) {
        unsigned char c = (unsigned char)start[i];
        if (c == '\n') fputs("\\n", stdout);
        else if (c == '\r') fputs("\\r", stdout);
        else if (c == '\t') fputs("\\t", stdout);
        else if (c == '\\') fputs("\\\\", stdout);
        else if (c == '\'') fputs("\\'", stdout);
        else if (c >= 0x20 && c < 0x7f) putchar(c);
        else putchar(c); /* пропустить байты UTF-8 как есть */
    }
    putchar('\'');
}

/* Проверка двухсимвольного оператора */
static int является_двухсимвольным_оператором(char a, char b) {
    const char *ops[] = {
        "==","!=",">=","<=","->","++","--","&&","||",
        "+=","-=","*=","/=","%=","<<",">>","<<=",">>=",
        "&=","|=","^=","::","?:", NULL
    };
    char tmp[3] = {a, b, 0};
    for (int i = 0; ops[i]; ++i) if (strcmp(tmp, ops[i]) == 0) return 1;
    return 0;
}

/* Вспомогательные функции для продвижения позиции и обновления координат.
   Централизуют логику pos++ / col++ / обработку '\n'. */
static void продвинуть_на_1(size_t *pos, int *строка, int *колонка, const char *буфер, size_t размер) {
    if (*pos >= размер) return;
    unsigned char c = (unsigned char)буфер[*pos];
    (*pos)++;
    if (c == '\n') {
        (*строка)++;
        *колонка = 1;
    } else {
        (*колонка)++;
    }
}
static void продвинуть_на_n(size_t *pos, int *строка, int *колонка, size_t n, const char *буфер, size_t размер) {
    for (size_t i = 0; i < n; ++i) продвинуть_на_1(pos, строка, колонка, буфер, размер);
}

/* Основная функция: токенизировать код. Имена по требованию: русские. */
TokenArray токенизировать_код(size_t размер, char *буфер)
{
    size_t pos = 0;
    int строка = 1, колонка = 1;

    TokenArray массив_токенов;
    массив_токенов_инициализировать(&массив_токенов);

    static const char eof_literal[] = "EOF";

    /* Заголовок при печати */
    printf("Строка:Позиция ТИП 'СИМВОЛ В КОДЕ'\n");

    while (pos < размер) {
        unsigned char c = (unsigned char)буфер[pos];

        /* Пробельные символы:
           - пробелы/табы/возвраты — пропускаем (не эмитируем токен)
           - перевод строки — отдельный токен */
        if (c == ' ' || c == '\t' || c == '\r') {
            продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
            continue;
        }
        if (c == '\n') {
            массив_токенов_добавить(&массив_токенов, ТОКЕН_НОВАЯ_СТРОКА, строка, колонка, буфер + pos, 1);
            продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
            continue;
        }

        int tok_line = строка, tok_col = колонка;

        /* Препроцессорная директива: от '#' до конца строки (не включаем '\n') */
        if (c == '#') {
            size_t начальная_позиция = pos;
            продвинуть_на_1(&pos, &строка, &колонка, буфер, размер); /* съели '#' */
            while (pos < размер) {
                unsigned char d = (unsigned char)буфер[pos];
                if (d == '\n') break; /* оставляем '\n' для отдельного токена */
                продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_ПРЕПРОЦЕССОР, tok_line, tok_col, буфер + начальная_позиция, pos - начальная_позиция);
            continue;
        }

        /* Комментарии */
        if (c == '/' && pos + 1 < размер) {
            unsigned char d = (unsigned char)буфер[pos + 1];
            if (d == '/') {
                /* Однострочный комментарий — до конца строки (не включаем '\n') */
                size_t начальная_позиция = pos;
                продвинуть_на_n(&pos, &строка, &колонка, 2, буфер, размер); /* съесть '//' */
                while (pos < размер) {
                    unsigned char e = (unsigned char)буфер[pos];
                    if (e == '\n') break;
                    продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
                }
                массив_токенов_добавить(&массив_токенов, ТОКЕН_КОММЕНТАРИЙ, tok_line, tok_col, буфер + начальная_позиция, pos - начальная_позиция);
                continue;
            } else if (d == '*') {
                /* Блочный комментарий — включаем всё до '*/
                size_t начальная_позиция = pos;
                продвинуть_на_n(&pos, &строка, &колонка, 2, буфер, размер); /* съесть '/*' */
                int закрыт = 0;
                while (pos < размер) {
                    unsigned char e = (unsigned char)буфер[pos];
                    if (e == '*' && pos + 1 < размер && (unsigned char)буфер[pos + 1] == '/') {
                        /* включаем '*/
                        size_t конец = pos + 2;
                        массив_токенов_добавить(&массив_токенов, ТОКЕН_КОММЕНТАРИЙ, tok_line, tok_col, буфер + начальная_позиция, конец - начальная_позиция);
                        продвинуть_на_n(&pos, &строка, &колонка, 2, буфер, размер);
                        закрыт = 1;
                        break;
                    } else {
                        продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
                    }
                }
                if (!закрыт) {
                    /* Если комментарий не закрыт до конца файла — добавим текущий собранный кусок */
                    if (pos > начальная_позиция) {
                        массив_токенов_добавить(&массив_токенов, ТОКЕН_КОММЕНТАРИЙ, tok_line, tok_col, буфер + начальная_позиция, pos - начальная_позиция);
                    }
                }
                continue;
            }
        }

        /* Строковый литерал: включаем начальные/закрывающие кавычки в токен.
           Если встречается необработанный перевод строки до закрывающей кавычки —
           токен завершится до него (и перевод строки будет отдельным токеном). */
        if (c == '"') {
            size_t начальная_позиция = pos;
            продвинуть_на_1(&pos, &строка, &колонка, буфер, размер); /* съели начальную " */
            while (pos < размер) {
                unsigned char e = (unsigned char)буфер[pos];
                if (e == '\\' && pos + 1 < размер) {
                    /* пропускаем escape-последовательность (оба байта считаются частью строки) */
                    продвинуть_на_n(&pos, &строка, &колонка, 2, буфер, размер);
                } else if (e == '"') {
                    продвинуть_на_1(&pos, &строка, &колонка, буфер, размер); /* съели закрывающую " */
                    break;
                } else if (e == '\n') {
                    /* не включаем перевод строки в строковый токен — завершаем токен до '\n' */
                    break;
                } else {
                    продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
                }
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_СТРОКА, tok_line, tok_col, буфер + начальная_позиция, pos - начальная_позиция);
            continue;
        }

        /* Символьный литерал (аналогично строковому) */
        if (c == '\'') {
            size_t начальная_позиция = pos;
            продвинуть_на_1(&pos, &строка, &колонка, буфер, размер); /* съели начальную ' */
            while (pos < размер) {
                unsigned char e = (unsigned char)буфер[pos];
                if (e == '\\' && pos + 1 < размер) {
                    продвинуть_на_n(&pos, &строка, &колонка, 2, буфер, размер);
                } else if (e == '\'') {
                    продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
                    break;
                } else if (e == '\n') {
                    /* некорректный литерал — завершаем до новой строки */
                    break;
                } else {
                    продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
                }
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_СИМВОЛ, tok_line, tok_col, буфер + начальная_позиция, pos - начальная_позиция);
            continue;
        }

        /* Идентификатор */
        if (является_началом_идентификатора(c)) {
            size_t начальная_позиция = pos;
            while (pos < размер) {
                unsigned char d = (unsigned char)буфер[pos];
                if (является_частью_идентификатора(d)) продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
                else break;
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_ИДЕНТИФИКАТОР, tok_line, tok_col, буфер + начальная_позиция, pos - начальная_позиция);
            continue;
        }

        /* Число (упрощённо) */
        if (isdigit(c)) {
            size_t начальная_позиция = pos;
            int есть_точка = 0;
            while (pos < размер) {
                unsigned char d = (unsigned char)буфер[pos];
                if (d == '.' && !есть_точка) { есть_точка = 1; продвинуть_на_1(&pos, &строка, &колонка, буфер, размер); }
                else if (isdigit(d)) продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
                else break;
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_ЧИСЛО, tok_line, tok_col, буфер + начальная_позиция, pos - начальная_позиция);
            continue;
        }

        /* Двухсимвольный оператор */
        if (pos + 1 < размер && является_двухсимвольным_оператором((char)c, буфер[pos + 1])) {
            массив_токенов_добавить(&массив_токенов, ТОКЕН_НЕИЗВЕСТНЫЙ_ОПЕРАТОР, tok_line, tok_col, буфер + pos, 2);
            продвинуть_на_n(&pos, &строка, &колонка, 2, буфер, размер);
            continue;
        }

        /* Односимвольные операторы и пунктуация */
        const char *операторы = "+-*/%&|^~!=<>?:";
        const char *пунктуация = "(),;{}[].#";
        if (strchr(операторы, (char)c)) {
            массив_токенов_добавить(&массив_токенов, ТОКЕН_НЕИЗВЕСТНЫЙ_ОПЕРАТОР, tok_line, tok_col, буфер + pos, 1);
            продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
            continue;
        }
        if (strchr(пунктуация, (char)c)) {
            массив_токенов_добавить(&массив_токенов, ТОКЕН_ПУНКТУАЦИЯ, tok_line, tok_col, буфер + pos, 1);
            продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
            continue;
        }

        /* Неизвестный байт — эмитируем как отдельный токен */
        массив_токенов_добавить(&массив_токенов, ТОКЕН_НЕИЗВЕСТНЫЙ, tok_line, tok_col, буфер + pos, 1);
        продвинуть_на_1(&pos, &строка, &колонка, буфер, размер);
    }

    /* EOF — статическая строка, безопасна после освобождения буфера */
    массив_токенов_добавить(&массив_токенов, ТОКЕН_КОНЕЦ_ФАЙЛА, 0, 0, eof_literal, sizeof(eof_literal) - 1);

    /* Печать токенов (в порядке получения) */
    for (size_t i = 0; i < массив_токенов.len; ++i) {
        Token *t = &массив_токенов.data[i];
        printf("%d:%d %s ", t->line, t->col, имя_типа_токена(t->type));
        печать_экранированного_отрезка(t->start, t->len);
        putchar('\n');
    }

    return массив_токенов;
}

