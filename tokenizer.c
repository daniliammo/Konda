/* main с русскими именами аргументов */
int main(int количество_аргументов, char **аргументы) {
    if (количество_аргументов < 2) {
        fprintf(stderr, "Использование: %s <файл>\n", аргументы[0]);
        return 2;
    }

    FILE *f = fopen(аргументы[1], "rb");
    if (!f) {
        perror("fopen");
        return 2;
    }

    size_t размер;
    char *буфер = прочитать_файл(f, &размер);
    fclose(f);

    TokenArray токены = токенизировать_код(размер, буфер);

    синтаксический_анализатор(токены);

    конвертация_базовая(токены);

    /* Освобождаем ресурсы */
    очистка(&токены, буфер);

    return 0;
}
