/*
Транспилятор/tokenizer.c

Zero-copy токенизатор — токены ссылаются на срезы внутри входного буфера.
Исправлена обработка строковых литералов: нет дублирующего кода и опечаток.
Вспомогательные функции вынесены для единой логики продвижения позиции и обновления координат.

Сборка:
    cc -std=c11 -O2 -o tokenizer Транспилятор/tokenizer.c

Примечание:
- Токены — zero-copy: поле `start` указывает внутрь буфера с исходным текстом.
  Буфер должен оставаться валидным до вызова `очистка`.
- Перевод строки (`TOK_NEWLINE`) выделяется в отдельный токен.
*/

