#ifndef KONDA_H
#define KONDA_H

/*
 * Публичный API транспилятора Konda — библиотека libkonda.
 *
 * Это единственный заголовок, который подключает пользователь библиотеки:
 *
 *     #include <konda.h>
 *
 * Конвейер транспиляции (.конда → C → бинарник):
 *
 *     токенизировать_код   исходник .конда → массив токенов
 *     разобрать            токены → AST
 *     анализировать        семантика: типы, границы, области видимости
 *     кодоген              AST → C (в БуферКода)
 *     скомпилировать_си    .c → бинарник через cc
 *     очистка              освобождение токенов и исходного буфера
 *
 * Вспомогательное:
 *     прочитать_файл       чтение исходника в буфер
 *     буфер_кода_*         накопление и чтение сгенерированного C
 *     узел_* / освободить_узел   обход и освобождение AST
 *
 * Срезы и проверка границ массивов реализованы транспилятором как рантайм-
 * пролог в ГЕНЕРИРУЕМОМ C (struct Срез, срез_доступ, массив_доступ). Это часть
 * вывода, а не API библиотеки, и сюда не входит.
 *
 * Минимальный сценарий использования см. в основа.c (CLI поверх этого API).
 */

#include "транспилятор.h"   /* токены, буферы; лексер, ввод-вывод, вызов cc */
#include "аст.h"            /* AST и проходы: разобрать, анализировать, кодоген */
#include "заголовки.h"      /* сканер C-заголовков: внешние функции из #содержит */
#include "обобщения.h"      /* мономорфизация обобщённых функций «имя<T>(…)» */
#include "libkonda_ide.h"   /* высокоуровневый IDE/LSP API: konda_анализ(...) */

#endif /* KONDA_H */
