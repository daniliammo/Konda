#include <stdio.h>
#include <string.h>

#include "владение.h"

// ─── snapshot / restore ─────────────────────────────────────────────────────

void снять_состояние_владения(const Анализатор *а, СостояниеВладения *сост)
{
    сост->число = кво_символов(а);
    for (size_t i = 0; i < сост->число; ++i) {
        сост->элементы[i] = владение_символа(а, i);
    }
}

void восстановить_состояние_владения(Анализатор *а, const СостояниеВладения *сост)
{
    // Ветвь могла объявить новых символов в своих внутренних областях, но они
    // ушли вместе с выходом из области (см. выйти_из_области в семантика.c).
    // Поэтому число символов сейчас равно числу на момент snapshot — это
    // инвариант, который держится за счёт парности войти/выйти. Берём минимум
    // ради защиты от рассинхрона.
    size_t n_сейчас = кво_символов(а);
    size_t n = n_сейчас < сост->число ? n_сейчас : сост->число;
    for (size_t i = 0; i < n; ++i) {
        задать_владение_символа(а, i, сост->элементы[i]);
    }
}

// ─── merge двух ветвей в одно состояние ─────────────────────────────────────
//
// Правило (без рантайма): для каждого владеющего среза, существовавшего до
// ветвления, либо перемещён в обеих ветвях, либо ни в одной. Иначе ошибка —
// судьба free() после if была бы двусмысленной. Это компромисс между
// выразительностью и обещанием «компилятор либо доказал, либо отказал».

int слить_состояния_владения(Анализатор *а,
                              const СостояниеВладения *до_ветвления,
                              const СостояниеВладения *тогда,
                              const СостояниеВладения *иначе)
{
    size_t n = до_ветвления->число;
    for (size_t i = 0; i < n; ++i) {
        // Объект слияния — только символы, которые на момент входа в if уже
        // были владельцами среза. Заимствованные/невладеющие/неперемещённые
        // ветвление в неконсистентное состояние перевести не может: move от
        // них семантика и так запрещает.
        if (!до_ветвления->элементы[i].владеет_срезом) continue;

        int т_перем = тогда->элементы[i].перемещён;
        int и_перем = иначе->элементы[i].перемещён;
        if (т_перем == и_перем) {
            ВладениеЭлемент новое = {
                .владеет_срезом = т_перем ? 0 : 1,
                .перемещён = т_перем,
                .токен_move = т_перем ? тогда->элементы[i].токен_move : (Токен){0},
            };
            задать_владение_символа(а, i, новое);
            continue;
        }
        // Несогласованное владение: ровно в одной ветви срез перемещён.
        char msg[384];
        const Токен *где = т_перем ? &тогда->элементы[i].токен_move
                                    : &иначе->элементы[i].токен_move;
        snprintf(msg, sizeof(msg),
            "срез «%s» перемещён в одной ветви (строка %d), но жив в другой — "
            "освобождение в конце блока двусмысленно; добавьте move в обе "
            "ветви или ни в одну",
            имя_символа(а, i), где->строка);
        диагностика_владения(а, где, msg);
        return 0;
    }
    return 1;
}

// ─── sink-детектор для «вернуть» в ветви ────────────────────────────────────

int блок_завершается_возвратом(const Узел *блок)
{
    if (!блок) return 0;
    size_t n = узел_число_детей(блок);
    if (n == 0) return 0;
    const Узел *последний = узел_ребёнок((Узел *)блок, n - 1);
    return последний && последний->тип == УЗЕЛ_ВОЗВРАТ;
}
