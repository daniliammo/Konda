#include <stdio.h>
#include <string.h>

#include "владение.h"

// ─── snapshot / restore ─────────────────────────────────────────────────────

void снять_состояние_владения(const Анализатор *а, СостояниеВладения *сост)
{
    сост->число = кво_символов(а);
    for (size_t i = 0; i < сост->число; ++i) {
        сост->элементы[i] = владение_символа(а, i);
    }
}

void восстановить_состояние_владения(Анализатор *а, const СостояниеВладения *сост)
{
    // Ветвь могла объявить новых символов в своих внутренних областях, но они
    // ушли вместе с выходом из области (см. выйти_из_области в семантика.c).
    // Поэтому число символов сейчас равно числу на момент snapshot — это
    // инвариант, который держится за счёт парности войти/выйти. Берём минимум
    // ради защиты от рассинхрона.
    size_t n_сейчас = кво_символов(а);
    size_t n = n_сейчас < сост->число ? n_сейчас : сост->число;
    for (size_t i = 0; i < n; ++i) {
        задать_владение_символа(а, i, сост->элементы[i]);
    }
}

// ─── merge двух ветвей в одно состояние ─────────────────────────────────────
//
// Правило (без рантайма): для каждого владеющего среза, существовавшего до
// ветвления, либо перемещён в обеих ветвях, либо ни в одной. Иначе ошибка —
// судьба free() после if была бы двусмысленной. Это компромисс между
// выразительностью и обещанием «компилятор либо доказал, либо отказал».

int слить_состояния_владения(Анализатор *а,
                              const СостояниеВладения *до_ветвления,
                              const СостояниеВладения *тогда,
                              const СостояниеВладения *иначе)
{
    size_t n = до_ветвления->число;
    for (size_t i = 0; i < n; ++i) {
        // Объект слияния — только символы, которые на момент входа в if уже
        // были владельцами среза. Заимствованные/невладеющие/неперемещённые
        // ветвление в неконсистентное состояние перевести не может: move от
        // них семантика и так запрещает.
        if (!до_ветвления->элементы[i].владеет_срезом) continue;

        int т_перем = тогда->элементы[i].перемещён;
        int и_перем = иначе->элементы[i].перемещён;
        if (т_перем == и_перем) {
            ВладениеЭлемент новое = {
                .владеет_срезом = т_перем ? 0 : 1,
                .перемещён = т_перем,
                .токен_move = т_перем ? тогда->элементы[i].токен_move : (Токен){0},
            };
            задать_владение_символа(а, i, новое);
            continue;
        }
        // Несогласованное владение: ровно в одной ветви срез перемещён.
        char msg[384];
        const Токен *где = т_перем ? &тогда->элементы[i].токен_move
                                    : &иначе->элементы[i].токен_move;
        snprintf(msg, sizeof(msg),
            "срез «%s» перемещён в одной ветви (строка %d), но жив в другой — "
            "освобождение в конце блока двусмысленно; добавьте move в обе "
            "ветви или ни в одну",
            имя_символа(а, i), где->строка);
        диагностика_владения(а, где, msg);
        return 0;
    }
    return 1;
}

// ─── sink-детектор для «вернуть» в ветви ────────────────────────────────────

int блок_завершается_возвратом(const Узел *блок)
{
    if (!блок) return 0;
    size_t n = узел_число_детей(блок);
    if (n == 0) return 0;
    const Узел *последний = узел_ребёнок((Узел *)блок, n - 1);
    return последний && последний->тип == УЗЕЛ_ВОЗВРАТ;
}

// ─── проверка «функция возвращает значение по всем путям» ────────────────────
//
// Глубже, чем блок_завершается_возвратом (тот смотрит только тип последнего
// узла): рекурсивно доказывает, что управление не может «выпасть» из блока без
// «вернуть». Нужен для запрета молчаливого fall-through в non-void функции (UB
// в C: возврат мусора). В языке нет break/continue, поэтому бесконечный цикл
// «пока (1)» — расходящийся (управление из него не выходит), тоже терминатор.

// Литерал-условие, гарантированно истинное (для «пока (1) { … }»): числовой
// литерал, не равный нулю, либо «истина». Токен не null-terminated.
static int условие_всегда_истинно(const Узел *условие)
{
    if (!условие) return 0;
    if (условие->тип == УЗЕЛ_ЧИСЛО && условие->токен.тип == ТОКЕН_ИСТИНА) return 1;
    if (условие->тип != УЗЕЛ_ЧИСЛО) return 0;
    const char *с = условие->токен.начало;
    size_t дл = условие->токен.длина;
    // Истинно, если среди цифр есть хотя бы одна ненулевая.
    for (size_t i = 0; i < дл; ++i) {
        if (с[i] >= '1' && с[i] <= '9') return 1;
    }
    return 0;
}

// Есть ли в блоке «прервать», привязанный именно к ЭТОМУ циклу (не спускаемся
// во вложенные циклы — там break/continue связаны со своим циклом).
static int содержит_прерывание(const Узел *узел)
{
    if (!узел) return 0;
    if (узел->тип == УЗЕЛ_ПРЕРВАТЬ) return 1;
    if (узел->тип == УЗЕЛ_ПОКА || узел->тип == УЗЕЛ_ДЛЯ
        || узел->тип == УЗЕЛ_ДЛЯ_КАЖДОГО) return 0;  // чужой цикл
    for (size_t i = 0; i < узел_число_детей((Узел *)узел); ++i)
        if (содержит_прерывание(узел_ребёнок((Узел *)узел, i))) return 1;
    return 0;
}

int все_пути_возвращают(const Узел *блок)
{
    if (!блок) return 0;
    size_t n = узел_число_детей(блок);
    if (n == 0) return 0;
    const Узел *последний = узел_ребёнок((Узел *)блок, n - 1);
    if (!последний) return 0;

    switch (последний->тип) {
        case УЗЕЛ_ВОЗВРАТ:
            return 1;

        case УЗЕЛ_ЕСЛИ: {
            // Гарантия только если есть ветвь «иначе» и обе ветви возвращают.
            // дети: [условие, тогда-блок, иначе-блок?].
            if (узел_число_детей((Узел *)последний) < 3) return 0;
            const Узел *тогда = узел_ребёнок((Узел *)последний, 1);
            const Узел *иначе = узел_ребёнок((Узел *)последний, 2);
            return все_пути_возвращают(тогда) && все_пути_возвращают(иначе);
        }

        case УЗЕЛ_ВЫБОР: {
            // дети: [выражение, ветки…]. Исчерпываемость гарантирует семантика
            // отдельно; здесь — что каждая ветвь возвращает по всем путям.
            size_t к = узел_число_детей((Узел *)последний);
            if (к < 2) return 0;
            for (size_t i = 1; i < к; ++i) {
                const Узел *ветка = узел_ребёнок((Узел *)последний, i);
                if (!ветка || узел_число_детей((Узел *)ветка) == 0) return 0;
                if (!все_пути_возвращают(узел_ребёнок((Узел *)ветка, 0))) return 0;
            }
            return 1;
        }

        case УЗЕЛ_ПОКА: {
            // Бесконечный цикл «пока (1)» БЕЗ «прервать» — расходящийся:
            // управление из функции не выходит. дети: [условие, тело].
            const Узел *условие = узел_ребёнок((Узел *)последний, 0);
            const Узел *тело = узел_ребёнок((Узел *)последний, 1);
            return условие_всегда_истинно(условие) && !содержит_прерывание(тело);
        }

        default:
            return 0;
    }
}
