static int является_двухсимвольным_оператором(char а, char б) {
    const char *ops[] = {
        "==","!=",">=","<=","->","++","--","&&","||",
        "+=","-=","*=","/=","%=","<<",">>","<<=",">>=",
        "&=","|=","^=","::","?:", nullptr
    };
    const char tmp[3] = {а, б, 0};
    for (int i = 0; ops[i]; ++i) if (strcmp(tmp, ops[i]) == 0) return 1;
    return 0;
}

static ТипТокена тип_пунктуации_по_срезу(const char *начало, size_t длина) {
    if (длина >= 1) {
        switch (начало[0]) {
            case '(': return ТОКЕН_КРУГЛАЯ_СКОБКА_ОТКРЫВАЕТСЯ;
            case ')': return ТОКЕН_КРУГЛАЯ_СКОБКА_ЗАКРЫВАЕТСЯ;
            case '[': return ТОКЕН_КВАДРАТНАЯ_СКОБКА_ОТКРЫВАЕТСЯ;
            case ']': return ТОКЕН_КВАДРАТНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ;
            case '{': return ТОКЕН_ФИГУРНАЯ_СКОБКА_ОТКРЫВАЕТСЯ;
            case '}': return ТОКЕН_ФИГУРНАЯ_СКОБКА_ЗАКРЫВАЕТСЯ;
            case '.': return ТОКЕН_ТОЧКА;
            case ',': return ТОКЕН_ЗАПЯТАЯ;
            case ';': return ТОКЕН_ТОЧКА_С_ЗАПЯТОЙ;
            case '#': return ТОКЕН_РЕШЕТКА;
            default: break;
        }
    }

    return ТОКЕН_НЕИЗВЕСТНЫЙ;
}


static ТипТокена тип_оператора_по_срезу(const char *начало, size_t длина) {
    if (длина == 2) {
        if (начало[0] == '+' && начало[1] == '+') return ТОКЕН_ИНКРЕМЕНТ;
        if (начало[0] == '-' && начало[1] == '-') return ТОКЕН_ДЕКРЕМЕНТ;
    }

    if (длина >= 1) {
        switch (начало[0]) {
            case '*': return ТОКЕН_ЗВЕЗДА;
            case '/': return ТОКЕН_КОСАЯ_ЧЕРТА;
            case '+': return ТОКЕН_ПЛЮС;
            case '-': return ТОКЕН_МИНУС;
            case '=': return ТОКЕН_РАВНО;
            case '&': return ТОКЕН_АМПЕРСАНД;
            case '%': return ТОКЕН_ПРОЦЕНТ;
            case '|': return ТОКЕН_ПРЯМАЯ_ЧЕРТА;
            case '!': return ТОКЕН_ВОСКЛИЦАТЕЛЬНЫЙ_ЗНАК;
            case '<': return ТОКЕН_СТРЕЛКА_ВЛЕВО;
            case '>': return ТОКЕН_СТРЕЛКА_ВПРАВО;
            case '^': return ТОКЕН_СТРЕЛКА_ВВЕРХ;
            case '~': return ТОКЕН_ТИЛЬДА;
            default: break;
        }
    }

    return ТОКЕН_НЕИЗВЕСТНЫЙ;
}

static void продвинуть_на_1(size_t *позиция, int *строка, int *позиция_в_строке, const char *буфер, size_t размер) {
    if (*позиция >= размер) return;
    const unsigned char c = (unsigned char)буфер[*позиция];
    (*позиция)++;
    if (c == '\n') {
        (*строка)++;
        *позиция_в_строке = 1;
    } else if ((c & 0xC0) != 0x80) {
        // Продолжающие байты UTF-8 (10xxxxxx) колонку не двигают,
        // чтобы позиция_в_строке считалась по символам, а не по байтам.
        (*позиция_в_строке)++;
    }
}

static void продвинуть_на_n(size_t *позиция, int *строка, int *позиция_в_строке, size_t n, const char *буфер, size_t размер) {
    for (size_t i = 0; i < n; ++i) продвинуть_на_1(позиция, строка, позиция_в_строке, буфер, размер);
}

МассивТокенов токенизировать_код(size_t размер, char *буфер, int печатать_отладку)
{
    size_t позиция = 0;
    int строка = 1, позиция_в_строке = 1;

    МассивТокенов массив_токенов;
    массив_токенов_инициализировать(&массив_токенов);

    static const char eof_literal[] = "EOF";

    if (печатать_отладку) {
        printf("Строка:Позиция ТИП 'СИМВОЛ В КОДЕ'\n");
    }

    while (позиция < размер) {
        unsigned char c = (unsigned char)буфер[позиция];

        if (c == ' ' || c == '\t' || c == '\r') {
            продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
            continue;
        }
        if (c == '\n') {
            массив_токенов_добавить(&массив_токенов, ТОКЕН_НОВАЯ_СТРОКА, строка, позиция_в_строке, буфер + позиция, 1);
            продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
            continue;
        }

        const int tok_line = строка;
        const int tok_col = позиция_в_строке;

        if (c == '#') {
            size_t начальная_позиция = позиция;
            продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
            while (позиция < размер) {
                unsigned char d = (unsigned char)буфер[позиция];
                if (d == '\n') break;
                продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_ПРЕПРОЦЕССОР, tok_line, tok_col, буфер + начальная_позиция, позиция - начальная_позиция);
            continue;
        }

        if (c == '/' && позиция + 1 < размер) {
            unsigned char d = (unsigned char)буфер[позиция + 1];
            if (d == '/') {
                size_t начальная_позиция = позиция;
                продвинуть_на_n(&позиция, &строка, &позиция_в_строке, 2, буфер, размер);
                while (позиция < размер) {
                    unsigned char e = (unsigned char)буфер[позиция];
                    if (e == '\n') break;
                    продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
                }
                массив_токенов_добавить(&массив_токенов, ТОКЕН_КОММЕНТАРИЙ, tok_line, tok_col, буфер + начальная_позиция, позиция - начальная_позиция);
                continue;
            } else if (d == '*') {
                size_t начальная_позиция = позиция;
                продвинуть_на_n(&позиция, &строка, &позиция_в_строке, 2, буфер, размер);
                int закрыт = 0;
                while (позиция < размер) {
                    unsigned char e = (unsigned char)буфер[позиция];
                    if (e == '*' && позиция + 1 < размер && (unsigned char)буфер[позиция + 1] == '/') {
                        size_t конец = позиция + 2;
                        массив_токенов_добавить(&массив_токенов, ТОКЕН_КОММЕНТАРИЙ, tok_line, tok_col, буфер + начальная_позиция, конец - начальная_позиция);
                        продвинуть_на_n(&позиция, &строка, &позиция_в_строке, 2, буфер, размер);
                        закрыт = 1;
                        break;
                    } else {
                        продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
                    }
                }
                if (!закрыт && позиция > начальная_позиция) {
                    массив_токенов_добавить(&массив_токенов, ТОКЕН_КОММЕНТАРИЙ, tok_line, tok_col, буфер + начальная_позиция, позиция - начальная_позиция);
                }
                continue;
            }
        }

        if (c == '"') {
            size_t начальная_позиция = позиция;
            продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
            while (позиция < размер) {
                unsigned char e = (unsigned char)буфер[позиция];
                if (e == '\\' && позиция + 1 < размер) {
                    продвинуть_на_n(&позиция, &строка, &позиция_в_строке, 2, буфер, размер);
                } else if (e == '"') {
                    продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
                    break;
                } else if (e == '\n') {
                    break;
                } else {
                    продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
                }
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_СТРОКА, tok_line, tok_col, буфер + начальная_позиция, позиция - начальная_позиция);
            continue;
        }

        if (c == '\'') {
            size_t начальная_позиция = позиция;
            продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
            while (позиция < размер) {
                unsigned char e = (unsigned char)буфер[позиция];
                if (e == '\\' && позиция + 1 < размер) {
                    продвинуть_на_n(&позиция, &строка, &позиция_в_строке, 2, буфер, размер);
                } else if (e == '\'') {
                    продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
                    break;
                } else if (e == '\n') {
                    break;
                } else {
                    продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
                }
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_СИМВОЛ, tok_line, tok_col, буфер + начальная_позиция, позиция - начальная_позиция);
            continue;
        }

        if (является_началом_идентификатора(c)) {
            size_t начальная_позиция = позиция;
            while (позиция < размер) {
                unsigned char d = (unsigned char)буфер[позиция];
                if (является_частью_идентификатора(d)) продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
                else break;
            }

            const size_t длина_слова = позиция - начальная_позиция;
            ТипТокена тип_идентификатора = тип_ключевого_слова(буфер + начальная_позиция, длина_слова);

            массив_токенов_добавить(&массив_токенов, тип_идентификатора, tok_line, tok_col, буфер + начальная_позиция, позиция - начальная_позиция);
            continue;
        }

        if (isdigit(c)) {
            size_t начальная_позиция = позиция;
            int есть_точка = 0;
            while (позиция < размер) {
                unsigned char d = (unsigned char)буфер[позиция];
                if (d == '.' && !есть_точка) { есть_точка = 1; продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер); }
                else if (isdigit(d)) продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
                else break;
            }
            массив_токенов_добавить(&массив_токенов, ТОКЕН_ЧИСЛО, tok_line, tok_col, буфер + начальная_позиция, позиция - начальная_позиция);
            continue;
        }

        if (позиция + 1 < размер && является_двухсимвольным_оператором((char)c, буфер[позиция + 1])) {
            ТипТокена тип_оператора = тип_оператора_по_срезу(буфер + позиция, 2);
            массив_токенов_добавить(&массив_токенов, тип_оператора, tok_line, tok_col, буфер + позиция, 2);
            продвинуть_на_n(&позиция, &строка, &позиция_в_строке, 2, буфер, размер);
            continue;
        }

        const char *операторы = "+-*/%&|^~!=<>?:";
        const char *пунктуация = "(),;{}[].#";
        if (strchr(операторы, (char)c)) {
            ТипТокена тип_оператора = тип_оператора_по_срезу(буфер + позиция, 1);
            массив_токенов_добавить(&массив_токенов, тип_оператора, tok_line, tok_col, буфер + позиция, 1);
            продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
            continue;
        }
        if (strchr(пунктуация, (char)c)) {
            ТипТокена тип_пунктуации = тип_пунктуации_по_срезу(буфер + позиция, 1);
            массив_токенов_добавить(&массив_токенов, тип_пунктуации, tok_line, tok_col, буфер + позиция, 1);
            продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
            continue;
        }

        массив_токенов_добавить(&массив_токенов, ТОКЕН_НЕИЗВЕСТНЫЙ, tok_line, tok_col, буфер + позиция, 1);
        продвинуть_на_1(&позиция, &строка, &позиция_в_строке, буфер, размер);
    }

    массив_токенов_добавить(&массив_токенов, ТОКЕН_КОНЕЦ_ФАЙЛА, 0, 0, eof_literal, sizeof(eof_literal) - 1);

    if (печатать_отладку) {
        for (size_t i = 0; i < массив_токенов.базовый.длина; ++i) {
            Токен *t = дин_массив_получить(&массив_токенов.базовый, i);
            printf("%d:%d %s ", t->строка, t->позиция_в_строке, имя_типа_токена(t->тип));
            печать_экранированного_отрезка(t->начало, t->длина);
            putchar('\n');
        }
        printf("Всего токенов:%zu\n", массив_токенов.базовый.длина);
    }

    return массив_токенов;
}

