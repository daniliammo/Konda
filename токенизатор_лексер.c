static int является_началом_идентификатора(unsigned char c) {
    return (c == '_' || isalpha(c) || c >= 0x80);
}

static int является_частью_идентификатора(unsigned char c) {
    return (c == '_' || isalnum(c) || c >= 0x80);
}

static void печать_экранированного_отрезка(const char *начало, size_t длина) {
    putchar('\'');
    for (size_t i = 0; i < длина; ++i) {
        unsigned char c = (unsigned char)начало[i];
        if (c == '\n') fputs("\\n", stdout);
        else if (c == '\r') fputs("\\r", stdout);
        else if (c == '\t') fputs("\\t", stdout);
        else if (c == '\\') fputs("\\\\", stdout);
        else if (c == '\'') fputs("\\'", stdout);
        else if (c >= 0x20 && c < 0x7f) putchar(c);
        else putchar(c);
    }
    putchar('\'');
}

static int слово_равно(const char *начало, size_t длина, const char *образец)
{
    size_t n = strlen(образец);
    return длина == n && memcmp(начало, образец, n) == 0;
}

