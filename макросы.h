#ifndef МАКРОСЫ_H
#define МАКРОСЫ_H

#include "транспилятор.h"
#include "аст.h"   // Диагностика

// Безопасные макросы Konda: ТОЛЬКО условная компиляция (выбор веток AST по
// флагам, известным на компиляции), без функциональных макросов и без текстовой
// подстановки значений в код. Значения `#define` используются лишь в условиях
// `#if`; для именованных констант в коде есть `конст`.
//
// Директивы (C-имена и русские синонимы):
//   #if <выражение>      / #если <выражение>
//   #ifdef ИМЯ           / #опр ИМЯ
//   #ifndef ИМЯ          / #не_опр ИМЯ
//   #elif <выражение>    / #иначе_если <выражение>
//   #else                / #иначе
//   #endif               / #конец
//   #define ИМЯ [число]  / #определить ИМЯ [число]
// В выражении `#if`: целые литералы, имена макросов (значение или 0), defined(ИМЯ)
// / определён(ИМЯ), операторы ! - * / % + - < > <= >= == != && || и скобки.

#define МАКС_МАКРОСОВ 512

typedef struct {
    char имя[128];
    long значение;
} Макрос;

typedef struct {
    Макрос элементы[МАКС_МАКРОСОВ];
    size_t число;
} ТаблицаМакросов;

void макросы_инициализировать(ТаблицаМакросов *т);

// Определить макрос из аргумента CLI «-D»: «ИМЯ» или «ИМЯ=число».
// Возвращает 1 при успехе, 0 при переполнении/ошибке формата.
int макросы_определить_аргумент(ТаблицаМакросов *т, const char *дефиниция);

// Обрабатывает директивы условной компиляции над токенами файла: удаляет
// неактивные токены и сами директивы условной компиляции/`#define`. Прочие
// директивы (`#содержит` и т.п.) проходят как есть, если ветка активна.
// Таблица макросов дополняется встреченными `#define` (активных веток).
// Возвращает 1 при успехе; при ошибке (незакрытый `#if`, лишний `#endif`,
// битое выражение) заполняет *диаг и возвращает 0.
int условная_компиляция(МассивТокенов *токены, ТаблицаМакросов *макросы,
                        Диагностика *диаг);

#endif
