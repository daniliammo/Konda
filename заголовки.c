// ─── таблица внешних функций ─────────────────────────────────────────────────

struct ТаблицаВнешних {
    ВнешняяФункция *функции;
    size_t число;
    size_t емкость;
    // Уже просканированные пути заголовков — чтобы не сканировать дважды и не
    // зациклиться на взаимных #include.
    char (*пути)[1024];
    size_t число_путей;
    size_t емкость_путей;
    int всего_файлов;   // общий счётчик для ограничения глубины сканирования
};

#define МАКС_ФАЙЛОВ_ЗАГОЛОВКОВ 400
#define МАКС_ГЛУБИНА_ВКЛЮЧЕНИЙ 24

ТаблицаВнешних *внешние_создать(void)
{
    return calloc(1, sizeof(ТаблицаВнешних));
}

void внешние_освободить(ТаблицаВнешних *т)
{
    if (!т) return;
    free(т->функции);
    free(т->пути);
    free(т);
}

size_t внешние_число(const ТаблицаВнешних *т) { return т ? т->число : 0; }

const ВнешняяФункция *внешние_получить(const ТаблицаВнешних *т, size_t i)
{
    if (!т || i >= т->число) return nullptr;
    return &т->функции[i];
}

const ВнешняяФункция *найти_внешнюю(const ТаблицаВнешних *т,
                                    const char *имя, size_t длина)
{
    if (!т) return nullptr;
    for (size_t i = 0; i < т->число; ++i) {
        if (strlen(т->функции[i].имя) == длина
            && memcmp(т->функции[i].имя, имя, длина) == 0) {
            return &т->функции[i];
        }
    }
    return nullptr;
}

static void добавить_функцию(ТаблицаВнешних *т, const char *имя, size_t длина,
                             int число_параметров, int переменное)
{
    if (длина == 0 || длина >= sizeof(т->функции[0].имя)) return;
    if (найти_внешнюю(т, имя, длина)) return;  // дедуп
    if (т->число == т->емкость) {
        size_t нов = т->емкость ? т->емкость * 2 : 64;
        ВнешняяФункция *p = realloc(т->функции, нов * sizeof(*p));
        if (!p) return;
        т->функции = p;
        т->емкость = нов;
    }
    ВнешняяФункция *ф = &т->функции[т->число++];
    memcpy(ф->имя, имя, длина);
    ф->имя[длина] = '\0';
    ф->число_параметров = число_параметров;
    ф->переменное_число = переменное;
}

// Путь уже сканировали? Если нет — запоминаем и возвращаем 0 (нужно сканировать).
static int путь_виден(ТаблицаВнешних *т, const char *путь)
{
    for (size_t i = 0; i < т->число_путей; ++i)
        if (strcmp(т->пути[i], путь) == 0) return 1;
    if (т->число_путей == т->емкость_путей) {
        size_t нов = т->емкость_путей ? т->емкость_путей * 2 : 32;
        void *p = realloc(т->пути, нов * sizeof(т->пути[0]));
        if (!p) return 1;  // нет памяти — считаем «видели», чтобы не сканировать
        т->пути = p;
        т->емкость_путей = нов;
    }
    snprintf(т->пути[т->число_путей], sizeof(т->пути[0]), "%s", путь);
    т->число_путей++;
    return 0;
}

// ─── разбор прототипов ───────────────────────────────────────────────────────

static int это_имя_старт(int c) { return c == '_' || isalpha((unsigned char)c); }
static int это_имя_прод(int c) { return c == '_' || isalnum((unsigned char)c); }

// Ключевые слова C, которые могут стоять перед «(», но функциями не являются.
static int это_ключевое(const char *с, size_t n)
{
    static const char *кс[] = {
        "if", "while", "for", "switch", "return", "sizeof", "do",
        "_Static_assert", "static_assert", "defined", "typeof", "__typeof",
        "__typeof__", "__attribute__", "__attribute", "__asm", "__asm__",
        "asm", "_Alignof", "alignof", "_Generic", "__extension__",
        "__builtin_va_arg", "va_arg", nullptr
    };
    for (int i = 0; кс[i]; ++i)
        if (strlen(кс[i]) == n && memcmp(кс[i], с, n) == 0) return 1;
    return 0;
}

// Готовит копию текста для разбора прототипов: комментарии, строковые и
// символьные литералы заменяются пробелами (переводы строк сохраняются),
// чтобы «(» внутри них не путал сканер. Директивы (#…) не трогаем — их сканер
// пропускает сам. Возвращает malloc-буфер (звонящий освобождает) или NULL.
static char *подготовить_текст(const char *ист, size_t n)
{
    char *out = malloc(n + 1);
    if (!out) return nullptr;
    for (size_t i = 0; i < n; ++i) {
        char c = ист[i];
        if (c == '/' && i + 1 < n && ист[i + 1] == '*') {          // /* … */
            out[i] = ' '; out[i + 1] = ' '; i += 2;
            while (i < n && !(ист[i] == '*' && i + 1 < n && ист[i + 1] == '/')) {
                out[i] = (ист[i] == '\n') ? '\n' : ' '; i++;
            }
            if (i < n) { out[i] = ' '; if (i + 1 < n) out[i + 1] = ' '; i++; }
            continue;
        }
        if (c == '/' && i + 1 < n && ист[i + 1] == '/') {          // // …
            while (i < n && ист[i] != '\n') { out[i] = ' '; i++; }
            if (i < n) out[i] = '\n';
            continue;
        }
        if (c == '"' || c == '\'') {                                // "…" '…'
            char кавычка = c;
            out[i] = ' '; i++;
            while (i < n && ист[i] != кавычка) {
                if (ист[i] == '\\' && i + 1 < n) { out[i] = ' '; out[i + 1] = ' '; i += 2; continue; }
                out[i] = (ист[i] == '\n') ? '\n' : ' '; i++;
            }
            if (i < n) out[i] = ' ';
            continue;
        }
        out[i] = c;
    }
    out[n] = '\0';
    return out;
}

// Из строки «#include <файл>» / «#include "файл"» извлекает имя и системность.
// Возвращает 1 при успехе.
static int разобрать_include(const char *строка, char *имя, size_t cap, int *системный)
{
    const char *p = строка;
    while (*p == ' ' || *p == '\t') p++;
    if (*p != '#') return 0;
    p++;
    while (*p == ' ' || *p == '\t') p++;
    if (strncmp(p, "include", 7) != 0) return 0;
    p += 7;
    while (*p == ' ' || *p == '\t') p++;
    char откр = *p, закр;
    if (откр == '<') { закр = '>'; *системный = 1; }
    else if (откр == '"') { закр = '"'; *системный = 0; }
    else return 0;
    p++;
    size_t k = 0;
    while (*p && *p != закр && *p != '\n' && k + 1 < cap) имя[k++] = *p++;
    имя[k] = '\0';
    return k > 0;
}

static int файл_существует(const char *путь)
{
    struct stat st;
    return stat(путь, &st) == 0 && S_ISREG(st.st_mode);
}

// Системные каталоги для «<…>»-заголовков. /usr/include покрывает libc.
static const char *СИСТ_КАТАЛОГИ[] = {
    "/usr/include", "/usr/local/include", nullptr
};

// Разрешает имя заголовка в полный путь. база_путей — каталог исходника (для
// «локальных.h»). Возвращает 1 и пишет путь при успехе.
static int разрешить_заголовок(const char *имя, int системный,
                               const char *база_путей, char *путь, size_t cap)
{
    if (!системный && база_путей && база_путей[0]) {
        snprintf(путь, cap, "%s/%s", база_путей, имя);
        if (файл_существует(путь)) return 1;
    }
    for (int i = 0; СИСТ_КАТАЛОГИ[i]; ++i) {
        snprintf(путь, cap, "%s/%s", СИСТ_КАТАЛОГИ[i], имя);
        if (файл_существует(путь)) return 1;
    }
    return 0;
}

// Каталог, содержащий файл (для разрешения относительных вложенных include).
static void каталог_файла(const char *путь, char *out, size_t cap)
{
    const char *слэш = strrchr(путь, '/');
    if (!слэш) { snprintf(out, cap, "."); return; }
    size_t n = (size_t)(слэш - путь);
    if (n >= cap) n = cap - 1;
    memcpy(out, путь, n);
    out[n] = '\0';
}

static void сканировать_файл(ТаблицаВнешних *т, const char *путь,
                             int глубина);

// Сканирует уже прочитанный текст заголовка: (1) извлекает прототипы функций,
// (2) следует вложенным #include.
static void сканировать_текст(ТаблицаВнешних *т, const char *ист, size_t n,
                              const char *свой_каталог, int глубина)
{
    // (1) Вложенные #include — по сырым строкам (до чистки, чтобы "имя.h"
    //     не было стёрто как строковый литерал).
    if (глубина < МАКС_ГЛУБИНА_ВКЛЮЧЕНИЙ) {
        const char *стр = ист;
        while (стр < ист + n) {
            const char *конец = memchr(стр, '\n', (size_t)(ист + n - стр));
            size_t длина_стр = конец ? (size_t)(конец - стр) : (size_t)(ист + n - стр);
            // Быстрый фильтр: строка-директива include.
            const char *q = стр;
            while (q < стр + длина_стр && (*q == ' ' || *q == '\t')) q++;
            if (q < стр + длина_стр && *q == '#') {
                char буфер_стр[1024];
                size_t к = длина_стр < sizeof(буфер_стр) - 1 ? длина_стр : sizeof(буфер_стр) - 1;
                memcpy(буфер_стр, стр, к); буфер_стр[к] = '\0';
                char имя[512]; int системный = 0;
                if (разобрать_include(буфер_стр, имя, sizeof(имя), &системный)) {
                    char путь[1024];
                    if (разрешить_заголовок(имя, системный, свой_каталог,
                                            путь, sizeof(путь))) {
                        сканировать_файл(т, путь, глубина + 1);
                    }
                }
            }
            if (!конец) break;
            стр = конец + 1;
        }
    }

    // (2) Прототипы функций — по очищенной копии.
    char *текст = подготовить_текст(ист, n);
    if (!текст) return;
    int глуб_скобок = 0;  // {…} — тело функции/структуры; прототипы на 0
    size_t i = 0;
    while (i < n) {
        char c = текст[i];
        if (c == '#') {  // директива — пропускаем всю (учёт продолжения «\»)
            while (i < n && текст[i] != '\n') {
                if (текст[i] == '\\' && i + 1 < n && текст[i + 1] == '\n') i++;
                i++;
            }
            continue;
        }
        if (c == '{') { глуб_скобок++; i++; continue; }
        if (c == '}') { if (глуб_скобок > 0) глуб_скобок--; i++; continue; }
        if (это_имя_старт((unsigned char)c)) {
            size_t старт = i;
            while (i < n && это_имя_прод((unsigned char)текст[i])) i++;
            size_t длина_имени = i - старт;
            size_t j = i;
            while (j < n && (текст[j] == ' ' || текст[j] == '\t' || текст[j] == '\n')) j++;
            if (j < n && текст[j] == '(' && глуб_скобок == 0
                && !это_ключевое(текст + старт, длина_имени)) {
                // Кандидат-функция: разбираем группу скобок.
                size_t p = j + 1;
                int уровень = 1;
                int запятые = 0, переменное = 0;
                size_t содерж_старт = p, содерж_конец = p;
                int есть_содерж = 0;
                while (p < n && уровень > 0) {
                    char d = текст[p];
                    if (d == '(') уровень++;
                    else if (d == ')') { уровень--; if (уровень == 0) { содерж_конец = p; break; } }
                    else if (d == ',' && уровень == 1) запятые++;
                    else if (d == '.' && p + 2 < n && текст[p+1] == '.' && текст[p+2] == '.'
                             && уровень == 1) переменное = 1;
                    if (уровень >= 1 && d != ' ' && d != '\t' && d != '\n') есть_содерж = 1;
                    p++;
                }
                // Число параметров (эвристика; для вариативных — фиксированные).
                int число;
                if (!есть_содерж) {
                    число = -1;  // «()» — неизвестно
                } else {
                    // «(void)» → 0 параметров.
                    const char *s = текст + содерж_старт;
                    size_t дл = содерж_конец > содерж_старт ? содерж_конец - содерж_старт : 0;
                    while (дл && (*s == ' ' || *s == '\t' || *s == '\n')) { s++; дл--; }
                    while (дл && (s[дл-1] == ' ' || s[дл-1] == '\t' || s[дл-1] == '\n')) дл--;
                    if (дл == 4 && memcmp(s, "void", 4) == 0) число = 0;
                    else if (переменное) число = запятые;      // фикс. до «...»
                    else число = запятые + 1;
                }
                добавить_функцию(т, текст + старт, длина_имени, число, переменное);
                i = p;  // за закрывающей «)»
                continue;
            }
            continue;  // идентификатор без «(» — идём дальше
        }
        i++;
    }
    free(текст);
}

static void сканировать_файл(ТаблицаВнешних *т, const char *путь, int глубина)
{
    if (глубина >= МАКС_ГЛУБИНА_ВКЛЮЧЕНИЙ) return;
    if (т->всего_файлов >= МАКС_ФАЙЛОВ_ЗАГОЛОВКОВ) return;
    if (путь_виден(т, путь)) return;
    т->всего_файлов++;

    FILE *f = fopen(путь, "rb");
    if (!f) return;
    if (fseek(f, 0, SEEK_END) != 0) { fclose(f); return; }
    long размер = ftell(f);
    if (размер < 0 || fseek(f, 0, SEEK_SET) != 0) { fclose(f); return; }
    char *буфер = malloc((size_t)размер + 1);
    if (!буфер) { fclose(f); return; }
    size_t прочитано = fread(буфер, 1, (size_t)размер, f);
    fclose(f);
    буфер[прочитано] = '\0';

    char свой_каталог[1024];
    каталог_файла(путь, свой_каталог, sizeof(свой_каталог));
    сканировать_текст(т, буфер, прочитано, свой_каталог, глубина);
    free(буфер);
}

void собрать_внешние_функции(const Узел *корень, const char *база_путей,
                             ТаблицаВнешних *куда)
{
    if (!корень || !куда) return;
    for (size_t i = 0; i < узел_число_детей(корень); ++i) {
        const Узел *узел = узел_ребёнок(корень, i);
        if (!узел || узел->тип != УЗЕЛ_ПРЕПРОЦЕССОР) continue;
        // Токен препроцессора хранит всю строку «#содержит <файл>».
        char строка[1024];
        size_t дл = узел->токен.длина < sizeof(строка) - 1
            ? узел->токен.длина : sizeof(строка) - 1;
        memcpy(строка, узел->токен.начало, дл);
        строка[дл] = '\0';
        // «содержит» → «include» уже не сделано (это делает кодоген); здесь
        // работаем с исходной формой: заменим «содержит» на «include» для
        // общего разборщика include-директив.
        char нормализ[1024];
        char *содержит = strstr(строка, "содержит");
        if (содержит) {
            size_t смещ = (size_t)(содержит - строка);
            snprintf(нормализ, sizeof(нормализ), "%.*sinclude%s",
                     (int)смещ, строка, содержит + strlen("содержит"));
        } else {
            snprintf(нормализ, sizeof(нормализ), "%s", строка);
        }
        char имя[512]; int системный = 0;
        if (!разобрать_include(нормализ, имя, sizeof(имя), &системный)) continue;
        char путь[1024];
        if (разрешить_заголовок(имя, системный, база_путей, путь, sizeof(путь)))
            сканировать_файл(куда, путь, 0);
    }
}
